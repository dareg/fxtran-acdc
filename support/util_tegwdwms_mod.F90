MODULE UTIL_TEGWDWMS_MOD

USE YOEGWDWMS, ONLY : TEGWDWMS

INTERFACE SAVE
MODULE PROCEDURE SAVE_TEGWDWMS
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TEGWDWMS
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TEGWDWMS (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEGWDWMS), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%LREGWWMS
END SUBROUTINE
SUBROUTINE LOAD_TEGWDWMS (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEGWDWMS), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%LREGWWMS
END SUBROUTINE




END MODULE
