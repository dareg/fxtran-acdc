SUBROUTINE COPY_MODEL_PHYSICS_AEROSOL_TYPE (YD, LDCREATED)
USE UTIL_TEAERLID_MOD
USE UTIL_TEAERMAP_MOD
USE UTIL_TEAERSNK_MOD
USE UTIL_TEAERSRC_MOD
USE UTIL_TEAERVOL_MOD
USE UTIL_TEDBUG_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_AEROSOL_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED
LOGICAL :: LLCREATED

LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$acc enter data create (YD)
  !$acc update device (YD)
ENDIF
CALL COPY (YD%YREAERLID, LDCREATED=.TRUE.)

CALL COPY (YD%YREAERMAP, LDCREATED=.TRUE.)

CALL COPY (YD%YREAERSNK, LDCREATED=.TRUE.)

CALL COPY (YD%YREAERSRC, LDCREATED=.TRUE.)

CALL COPY (YD%YREAERVOL, LDCREATED=.TRUE.)

CALL COPY (YD%YREDBUG, LDCREATED=.TRUE.)

END SUBROUTINE



