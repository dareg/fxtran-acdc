WRITE (KLUN) LREFILV
IF (LREFILV) THEN
  WRITE (KLUN) LBOUND (YD%REFILV)
  WRITE (KLUN) UBOUND (YD%REFILV)
  WRITE (KLUN) YD%REFILV
ENDIF
LREFILD = ALLOCATED (YD%REFILD)
WRITE (KLUN) LREFILD
IF (LREFILD) THEN
  WRITE (KLUN) LBOUND (YD%REFILD)
  WRITE (KLUN) UBOUND (YD%REFILD)
  WRITE (KLUN) YD%REFILD
