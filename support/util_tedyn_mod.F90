LOGICAL :: LRDIDIVE, LRDIGFLE, LRDIPDE, LRDISPE, LRDITE, LRDIVDE, LRDIVORE, LRDSDIVE, LRDSVDE, LRDSVORE, LREFILD, LREFILV
LRDIVORE = ALLOCATED (YD%RDIVORE)
WRITE (KLUN) LRDIVORE
