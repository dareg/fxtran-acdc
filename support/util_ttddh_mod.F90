MODULE UTIL_TTDDH_MOD

USE YOMTDDH, ONLY : TTDDH

INTERFACE SAVE
MODULE PROCEDURE SAVE_TTDDH
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TTDDH
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TTDDH (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TTDDH), INTENT (IN), TARGET :: YD
LOGICAL :: LCFLDNAMES2D, LCFLDNAMES3D, LCFLDTYPES2D, LCFLDTYPES3D, LHDCS0, LHDCS1, LHDCVB0, LHDCVB1, LPDDHFSVI
