  WRITE (KLUN) LBOUND (YD%HDCVB0)
  WRITE (KLUN) UBOUND (YD%HDCVB0)
  WRITE (KLUN) YD%HDCVB0
ENDIF
LHDCVB1 = ALLOCATED (YD%HDCVB1)
WRITE (KLUN) LHDCVB1
IF (LHDCVB1) THEN
  WRITE (KLUN) LBOUND (YD%HDCVB1)
  WRITE (KLUN) UBOUND (YD%HDCVB1)
  WRITE (KLUN) YD%HDCVB1
ENDIF
LHDCS0 = ALLOCATED (YD%HDCS0)
WRITE (KLUN) LHDCS0
IF (LHDCS0) THEN
  WRITE (KLUN) LBOUND (YD%HDCS0)
  WRITE (KLUN) UBOUND (YD%HDCS0)
  WRITE (KLUN) YD%HDCS0
ENDIF
LHDCS1 = ALLOCATED (YD%HDCS1)
WRITE (KLUN) LHDCS1
IF (LHDCS1) THEN
  WRITE (KLUN) LBOUND (YD%HDCS1)
  WRITE (KLUN) UBOUND (YD%HDCS1)
  WRITE (KLUN) YD%HDCS1
ENDIF
LPDDHFSVI = ALLOCATED (YD%PDDHFSVI)
WRITE (KLUN) LPDDHFSVI
IF (LPDDHFSVI) THEN
  WRITE (KLUN) LBOUND (YD%PDDHFSVI)
  WRITE (KLUN) UBOUND (YD%PDDHFSVI)
  WRITE (KLUN) YD%PDDHFSVI
ENDIF
LCFLDNAMES3D = ALLOCATED (YD%CFLDNAMES3D)
WRITE (KLUN) LCFLDNAMES3D
IF (LCFLDNAMES3D) THEN
  WRITE (KLUN) LBOUND (YD%CFLDNAMES3D)
  WRITE (KLUN) UBOUND (YD%CFLDNAMES3D)
  WRITE (KLUN) YD%CFLDNAMES3D
ENDIF
LCFLDTYPES3D = ALLOCATED (YD%CFLDTYPES3D)
WRITE (KLUN) LCFLDTYPES3D
IF (LCFLDTYPES3D) THEN
  WRITE (KLUN) LBOUND (YD%CFLDTYPES3D)
  WRITE (KLUN) UBOUND (YD%CFLDTYPES3D)
  WRITE (KLUN) YD%CFLDTYPES3D
ENDIF
LCFLDNAMES2D = ALLOCATED (YD%CFLDNAMES2D)
WRITE (KLUN) LCFLDNAMES2D
IF (LCFLDNAMES2D) THEN
  WRITE (KLUN) LBOUND (YD%CFLDNAMES2D)
  WRITE (KLUN) UBOUND (YD%CFLDNAMES2D)
  WRITE (KLUN) YD%CFLDNAMES2D
ENDIF
LCFLDTYPES2D = ALLOCATED (YD%CFLDTYPES2D)
WRITE (KLUN) LCFLDTYPES2D
IF (LCFLDTYPES2D) THEN
  WRITE (KLUN) LBOUND (YD%CFLDTYPES2D)
  WRITE (KLUN) UBOUND (YD%CFLDTYPES2D)
  WRITE (KLUN) YD%CFLDTYPES2D
ENDIF
END SUBROUTINE
SUBROUTINE LOAD_TTDDH (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
