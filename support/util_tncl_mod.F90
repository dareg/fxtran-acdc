MODULE UTIL_TNCL_MOD

USE YOMNCL, ONLY : TNCL

INTERFACE SAVE
MODULE PROCEDURE SAVE_TNCL
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TNCL
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TNCL (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TNCL), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%LNCLIN
WRITE (KLUN) YD%LREGCL
END SUBROUTINE
SUBROUTINE LOAD_TNCL (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TNCL), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%LNCLIN
READ (KLUN) YD%LREGCL
END SUBROUTINE




END MODULE
