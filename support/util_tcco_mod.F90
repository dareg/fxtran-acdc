MODULE UTIL_TCCO_MOD

USE INTDYNSL_MOD, ONLY : TCCO

INTERFACE SAVE
MODULE PROCEDURE SAVE_TCCO
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TCCO
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TCCO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCCO), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%M_RLON
WRITE (KLUN) YD%M_RLAT
WRITE (KLUN) YD%M_RQX
WRITE (KLUN) YD%M_RQY
WRITE (KLUN) YD%NDIM
END SUBROUTINE

SUBROUTINE LOAD_TCCO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCCO), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%M_RLON
READ (KLUN) YD%M_RLAT
READ (KLUN) YD%M_RQX
READ (KLUN) YD%M_RQY
READ (KLUN) YD%NDIM
END SUBROUTINE






END MODULE
