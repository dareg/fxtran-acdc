MODULE UTIL_TEAERMAP_MOD

USE YOEAERMAP, ONLY : TEAERMAP

INTERFACE SAVE
MODULE PROCEDURE SAVE_TEAERMAP
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TEAERMAP
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TEAERMAP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERMAP), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%NDUSRCP
WRITE (KLUN) YD%RDDUAER
WRITE (KLUN) YD%RDUSRCP
END SUBROUTINE

SUBROUTINE LOAD_TEAERMAP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERMAP), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%NDUSRCP
READ (KLUN) YD%RDDUAER
READ (KLUN) YD%RDUSRCP
END SUBROUTINE






END MODULE
