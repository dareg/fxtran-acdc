WRITE (KLUN) LRW
IF (LRW) THEN
  WRITE (KLUN) LBOUND (YD%RW)
  WRITE (KLUN) UBOUND (YD%RW)
  WRITE (KLUN) YD%RW
ENDIF
LRMU = ALLOCATED (YD%RMU)
WRITE (KLUN) LRMU
IF (LRMU) THEN
  WRITE (KLUN) LBOUND (YD%RMU)
  WRITE (KLUN) UBOUND (YD%RMU)
  WRITE (KLUN) YD%RMU
ENDIF
