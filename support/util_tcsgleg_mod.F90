  WRITE (KLUN) LBOUND (YD%R1QM2)
  WRITE (KLUN) UBOUND (YD%R1QM2)
  WRITE (KLUN) YD%R1QM2
ENDIF
LRACTHE = ALLOCATED (YD%RACTHE)
WRITE (KLUN) LRACTHE
IF (LRACTHE) THEN
  WRITE (KLUN) LBOUND (YD%RACTHE)
  WRITE (KLUN) UBOUND (YD%RACTHE)
  WRITE (KLUN) YD%RACTHE
ENDIF
LRLATIG = ALLOCATED (YD%RLATIG)
