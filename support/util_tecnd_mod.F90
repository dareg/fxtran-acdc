MODULE UTIL_TECND_MOD

USE YOECND, ONLY : TECND

INTERFACE SAVE
MODULE PROCEDURE SAVE_TECND
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TECND
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TECND (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECND), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%REPFLM
WRITE (KLUN) YD%REPQMI
END SUBROUTINE

SUBROUTINE LOAD_TECND (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECND), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%REPFLM
READ (KLUN) YD%REPQMI
END SUBROUTINE






END MODULE
