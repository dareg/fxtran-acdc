MODULE UTIL_TCOU_MOD

USE YOMCOU, ONLY : TCOU

INTERFACE SAVE
MODULE PROCEDURE SAVE_TCOU
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TCOU
END INTERFACE




CONTAINS

SUBROUTINE SAVE_TCOU (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOU), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%NCULF
WRITE (KLUN) YD%NPIAT
WRITE (KLUN) YD%NCULMR
WRITE (KLUN) YD%NCULMW
END SUBROUTINE
SUBROUTINE LOAD_TCOU (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOU), INTENT (OUT), TARGET :: YD

READ (KLUN) YD%NCULF
READ (KLUN) YD%NPIAT
READ (KLUN) YD%NCULMR
READ (KLUN) YD%NCULMW
END SUBROUTINE




END MODULE
