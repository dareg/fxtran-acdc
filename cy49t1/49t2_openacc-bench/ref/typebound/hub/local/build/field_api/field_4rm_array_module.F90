
MODULE FIELD_4RM_ARRAY_MODULE

!$ACDC methods --methods-list=crc64,size,host,copy,wipe,save,load --field-api --field-api-class cpg

USE FIELD_4RM_MODULE
USE FIELD_4RM_FACTORY_MODULE

USE PARKIND1, ONLY : JPRM, JPRD, JPIM, JPLM

IMPLICIT NONE

PRIVATE

TYPE FIELD_4RM_ARRAY
  CLASS (FIELD_4RM), POINTER :: F_P => NULL ()
  REAL(KIND=JPRM), POINTER, CONTIGUOUS :: P_FIELD (:,:,:,:) => NULL()
  REAL(KIND=JPRM), POINTER :: P (:,:,:) => NULL()
CONTAINS
  GENERIC :: INIT => INIT_FIELD_4RM_ARRAY_WRAPPER, INIT_FIELD_4RM_ARRAY_OWNER
  PROCEDURE, PRIVATE :: INIT_FIELD_4RM_ARRAY_WRAPPER
  PROCEDURE, PRIVATE :: INIT_FIELD_4RM_ARRAY_OWNER
  PROCEDURE :: RESIZE => RESIZE_FIELD_4RM_ARRAY
  PROCEDURE :: FINAL => FINAL_FIELD_4RM_ARRAY
  PROCEDURE :: UPDATE_VIEW => UPDATE_VIEW_FIELD_4RM_ARRAY
PROCEDURE :: COPY => COPY_FIELD_4RM_ARRAY
PROCEDURE :: CRC64 => CRC64_FIELD_4RM_ARRAY
PROCEDURE :: HOST => HOST_FIELD_4RM_ARRAY
PROCEDURE :: LOAD => LOAD_FIELD_4RM_ARRAY
PROCEDURE :: SAVE => SAVE_FIELD_4RM_ARRAY
PROCEDURE :: SIZE => SIZE_FIELD_4RM_ARRAY
PROCEDURE :: WIPE => WIPE_FIELD_4RM_ARRAY
END TYPE

PUBLIC :: FIELD_4RM_ARRAY


INTERFACE

MODULE SUBROUTINE COPY_FIELD_4RM_ARRAY (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_FIELD_4RM_ARRAY (SELF, KLUN, CDPATH)
USE FIELD_UTIL_MODULE
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_FIELD_4RM_ARRAY (SELF)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_FIELD_4RM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_FIELD_4RM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_FIELD_4RM_ARRAY (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_FIELD_4RM_ARRAY (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_4RM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE INIT_FIELD_4RM_ARRAY_WRAPPER (SELF, P, LBOUNDS)

CLASS (FIELD_4RM_ARRAY) :: SELF
REAL(KIND=JPRM), INTENT (IN), TARGET :: P (:,:,:,:)
INTEGER (KIND=JPIM), INTENT (IN), OPTIONAL :: LBOUNDS (4)

IF(SIZE(P) > 0) CALL FIELD_NEW (SELF%F_P, DATA=P, PERSISTENT=.TRUE., LBOUNDS=LBOUNDS)

END SUBROUTINE

SUBROUTINE INIT_FIELD_4RM_ARRAY_OWNER (SELF, PERSISTENT, UBOUNDS, LBOUNDS)

CLASS (FIELD_4RM_ARRAY) :: SELF
INTEGER (KIND=JPIM), INTENT (IN) :: UBOUNDS (4)
INTEGER (KIND=JPIM), INTENT (IN), OPTIONAL :: LBOUNDS (4)
LOGICAL,             INTENT (IN), OPTIONAL :: PERSISTENT

CALL FIELD_NEW (SELF%F_P, UBOUNDS=UBOUNDS, PERSISTENT=PERSISTENT, LBOUNDS=LBOUNDS)

END SUBROUTINE

SUBROUTINE RESIZE_FIELD_4RM_ARRAY (SELF, UBOUNDS, LBOUNDS, PERSISTENT)

CLASS (FIELD_4RM_ARRAY) :: SELF
INTEGER (KIND=JPIM), INTENT (IN) :: UBOUNDS (4)
INTEGER (KIND=JPIM), INTENT (IN), OPTIONAL :: LBOUNDS (4)
LOGICAL,             INTENT (IN), OPTIONAL :: PERSISTENT

CALL FIELD_RESIZE (SELF%F_P, UBOUNDS=UBOUNDS, PERSISTENT=PERSISTENT, LBOUNDS=LBOUNDS)

END SUBROUTINE

SUBROUTINE UPDATE_VIEW_FIELD_4RM_ARRAY (SELF, BLOCK_INDEX)
CLASS (FIELD_4RM_ARRAY) :: SELF
INTEGER (KIND=JPIM), INTENT (IN) :: BLOCK_INDEX

IF (ASSOCIATED (SELF%F_P)) THEN
  SELF%P => SELF%F_P%GET_VIEW (BLOCK_INDEX)
ENDIF

END SUBROUTINE

SUBROUTINE FINAL_FIELD_4RM_ARRAY (SELF)
CLASS (FIELD_4RM_ARRAY) :: SELF

IF (ASSOCIATED (SELF%F_P)) THEN
  CALL FIELD_DELETE (SELF%F_P)
  SELF%P_FIELD => NULL ()
  SELF%P => NULL ()
ENDIF

END SUBROUTINE


END MODULE
