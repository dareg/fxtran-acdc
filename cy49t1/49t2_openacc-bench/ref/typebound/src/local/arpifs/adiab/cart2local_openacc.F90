SUBROUTINE CART2LOCAL_OPENACC (KFLEV, KPROMA, KST, KEND, PGEMU&
&, PGSQM2, PGECLO, PGESLO, PX, PY, PZ, PU, PV, YDSTACK)
!$ACC ROUTINE (CART2LOCAL_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

#include "stack.h"
USE STACK_MOD
USE ABOR1_ACC_MOD

IMPLICIT NONE

INTEGER (KIND=JPIM), INTENT (IN)::KEND
INTEGER (KIND=JPIM), INTENT (IN)::KST
INTEGER (KIND=JPIM), INTENT (IN)::KPROMA
INTEGER (KIND=JPIM), INTENT (IN)::KFLEV
REAL (KIND=JPRB), INTENT (IN)::PGESLO (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGECLO (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGSQM2 (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PGEMU (KPROMA)
REAL (KIND=JPRB), INTENT (IN)::PX (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (IN)::PY (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (IN)::PZ (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (OUT)::PU (KPROMA, KFLEV)
REAL (KIND=JPRB), INTENT (OUT)::PV (KPROMA, KFLEV)
TYPE(STACK), INTENT (IN) :: YDSTACK
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPRB)::ZY
REAL (KIND=JPRB)::ZX


JROF = KST



DO JLEV=1, KFLEV
  
  ZX=PX (JROF, JLEV)
  ZY=PY (JROF, JLEV)
  PU (JROF, JLEV)=ZY*PGECLO (JROF)-ZX*PGESLO (JROF)
  PV (JROF, JLEV)=-ZX*PGECLO (JROF)*PGEMU (JROF)-ZY*PGESLO&
  & (JROF)*PGEMU (JROF)+PZ (JROF, JLEV)*PGSQM2 (JROF)
  
ENDDO


ENDSUBROUTINE CART2LOCAL_OPENACC
