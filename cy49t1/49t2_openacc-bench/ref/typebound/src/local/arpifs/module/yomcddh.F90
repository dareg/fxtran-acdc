MODULE YOMCDDH

!$ACDC methods 


IMPLICIT NONE

SAVE

!       ----------------------------------------------------------------
!*     ** *YOMCDDH* CHARACTER ARRAYS FOR DDH SURFACE VARIABLE NAMES
!       ----------------------------------------------------------------

TYPE :: TCDDH
CHARACTER,ALLOCATABLE :: CADHTLS(:)*12
CHARACTER,ALLOCATABLE :: CADHTSS(:)*12
CHARACTER,ALLOCATABLE :: CADHTTS(:)*12
CHARACTER,ALLOCATABLE :: CADHTIS(:)*12
CHARACTER,ALLOCATABLE :: CADHSSS(:)*12
CHARACTER,ALLOCATABLE :: CADHIIS(:)*12
CHARACTER,ALLOCATABLE :: CADHWLS(:)*12
CHARACTER,ALLOCATABLE :: CADHFSS(:)*12
CONTAINS
PROCEDURE :: COPY => COPY_TCDDH
PROCEDURE :: CRC64 => CRC64_TCDDH
PROCEDURE :: HOST => HOST_TCDDH
PROCEDURE :: LOAD => LOAD_TCDDH
PROCEDURE :: SAVE => SAVE_TCDDH
PROCEDURE :: SIZE => SIZE_TCDDH
PROCEDURE :: WIPE => WIPE_TCDDH
END TYPE TCDDH

!!TYPE(TCDDH), POINTER :: YRCDDH => NULL()

!     C.Jakob           E.C.M.W.F.     15/ 5/00

!     NAME        TYPE       DESCRIPTION
!     ----        ----       -----------

!     *CADHTLS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES
!                            IN TILE ARRAY 
!     *CADHTSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES
!                            IN SNOW ENERGY BUDGET ARRAY 
!     *CADHTTS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SOIL ENERGY BUDGET ARRAY 
!     *CADHTIS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SEA-ICE ENERGY BUDGET ARRAY 
!     *CADHSSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SNOW WATER BUDGET ARRAY 
!     *CADHIIS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN INTERCEPTION LAYER WATER BUDGET ARRAY 
!     *CADHWLS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN SOIL WATER BUDGET ARRAY 
!     *CADHFSS*   CHARACTER  NAMES OF DDH VARIABLES AND FLUXES 
!                            IN FREE-STYLE SINGLE LEVEL ARRAY 

INTERFACE

MODULE SUBROUTINE COPY_TCDDH (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCDDH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TCDDH (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TCDDH), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TCDDH (SELF)

IMPLICIT NONE
CLASS (TCDDH), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TCDDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TCDDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TCDDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TCDDH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TCDDH (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TCDDH),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TCDDH (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TCDDH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMCDDH
