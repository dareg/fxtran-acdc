MODULE MODEL_ATMOS_OCEAN_COUPLING_MOD

!$ACDC methods 

  USE YOMMCC, ONLY : TMCC
  USE YOMCOM, ONLY : TCOM
  USE YOMCOU, ONLY : TCOU
  IMPLICIT NONE

  TYPE MODEL_ATMOS_OCEAN_COUPLING_TYPE

    TYPE(TMCC)  :: YRMCC
    TYPE(TCOM)  :: YRCOM
    TYPE(TCOU)  :: YRCOU

    CONTAINS

    PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
    
  PROCEDURE :: COPY => COPY_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: CRC64 => CRC64_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: HOST => HOST_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: LOAD => LOAD_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: SAVE => SAVE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: SIZE => SIZE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
PROCEDURE :: WIPE => WIPE_MODEL_ATMOS_OCEAN_COUPLING_TYPE
END TYPE MODEL_ATMOS_OCEAN_COUPLING_TYPE

  !---------------------------------------------------------------------

  INTERFACE

MODULE SUBROUTINE COPY_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_MODEL_ATMOS_OCEAN_COUPLING_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_ATMOS_OCEAN_COUPLING_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 

  SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(MODEL_ATMOS_OCEAN_COUPLING_TYPE), INTENT(IN) :: SELF
  INTEGER                               , INTENT(IN) :: KDEPTH
  INTEGER                               , INTENT(IN) :: KOUTNO

  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH) // 'model%yrml_aoc : '
  CALL SELF%YRMCC%PRINT(KDEPTH+2,KOUTNO)
  CALL SELF%YRCOM%PRINT(KDEPTH+2,KOUTNO)
  CALL SELF%YRCOU%PRINT(KDEPTH+2,KOUTNO)
  
  END SUBROUTINE PRINT_CONFIGURATION

END MODULE MODEL_ATMOS_OCEAN_COUPLING_MOD
