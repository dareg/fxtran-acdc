SUBMODULE (MODEL_GENERAL_CONF_MOD) MODEL_GENERAL_CONF_MOD_MODEL_GENERAL_CONF_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE LOAD_MODEL_GENERAL_CONF_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_GENERAL_CONF_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LGEOM
READ (KLUN) LGEOM
IF (LGEOM) THEN
  ALLOCATE (SELF%GEOM)
  CALL SELF%GEOM%LOAD (KLUN)
ELSE
  NULLIFY (SELF%GEOM)
ENDIF
CALL SELF%YRDIMF%LOAD (KLUN)
CALL SELF%YGFL%LOAD (KLUN)
CALL SELF%YRRIP%LOAD (KLUN)
CALL SELF%YRDIMECV%LOAD (KLUN)
CALL SELF%YRMODERR%LOAD (KLUN)
CALL SELF%YRSPPT_CONFIG%LOAD (KLUN)
CALL SELF%YRSPP_CONFIG%LOAD (KLUN)
END SUBROUTINE


END SUBMODULE
