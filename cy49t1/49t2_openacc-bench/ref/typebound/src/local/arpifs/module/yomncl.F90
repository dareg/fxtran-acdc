MODULE YOMNCL

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB

IMPLICIT NONE

SAVE

! ------ CLOUD CHARACTERISTICS FOR SIMPLIFIED SCHEME

! LNCLIN   : .TRUE. IF (A,L,I) grid-point upper air fields to be read
!            on input for new cloud scheme of the linearized model
! LREGCL   : .TRUE. if the regularization in the cloud scheme is used

TYPE :: TNCL
LOGICAL :: LNCLIN
LOGICAL :: LREGCL
!----------------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: COPY => COPY_TNCL
PROCEDURE :: CRC64 => CRC64_TNCL
PROCEDURE :: HOST => HOST_TNCL
PROCEDURE :: LOAD => LOAD_TNCL
PROCEDURE :: SAVE => SAVE_TNCL
PROCEDURE :: SIZE => SIZE_TNCL
PROCEDURE :: WIPE => WIPE_TNCL
END TYPE TNCL
!============================================================================

!!TYPE(TNCL), POINTER :: YRNCL => NULL()

!     ------------------------------------------------------------------
INTERFACE

MODULE SUBROUTINE COPY_TNCL (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TNCL), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TNCL (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TNCL), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TNCL (SELF)

IMPLICIT NONE
CLASS (TNCL), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TNCL (SELF, KLUN)

IMPLICIT NONE
CLASS (TNCL), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TNCL (SELF, KLUN)

IMPLICIT NONE
CLASS (TNCL), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TNCL (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TNCL),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TNCL (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TNCL), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
IMPLICIT NONE
CLASS(TNCL), INTENT(IN) :: SELF
INTEGER    , INTENT(IN) :: KDEPTH
INTEGER    , INTENT(IN) :: KOUTNO

INTEGER :: IDEPTHLOC

IDEPTHLOC = KDEPTH+2

WRITE(KOUTNO,*) REPEAT(' ',KDEPTH   ) // 'model%yrml_phy_slin%yrncl : '
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LNCLIN = ', SELF%LNCLIN
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LREGCL = ', SELF%LREGCL

END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOMNCL
