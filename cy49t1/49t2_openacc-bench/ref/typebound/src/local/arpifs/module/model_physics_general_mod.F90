MODULE MODEL_PHYSICS_GENERAL_MOD

!$ACDC methods 

  USE YOMDPHY , ONLY : TDPHY
  USE YOMSLPHY, ONLY : TSLPHY
  USE YOEVDF,   ONLY : TVDF
  IMPLICIT NONE
  
  TYPE MODEL_PHYSICS_GENERAL_TYPE

    TYPE(TDPHY)  :: YRDPHY   !! dimensions
    TYPE(TSLPHY) :: YRSLPHY  !! physics & dynamics interfacing
    TYPE(TVDF)   :: YRVDF
  
  CONTAINS
  
    PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 

  PROCEDURE :: COPY => COPY_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: CRC64 => CRC64_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: HOST => HOST_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: LOAD => LOAD_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: SAVE => SAVE_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: SIZE => SIZE_MODEL_PHYSICS_GENERAL_TYPE
PROCEDURE :: WIPE => WIPE_MODEL_PHYSICS_GENERAL_TYPE
END TYPE MODEL_PHYSICS_GENERAL_TYPE

  !---------------------------------------------------------------------

  INTERFACE

MODULE SUBROUTINE COPY_MODEL_PHYSICS_GENERAL_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_MODEL_PHYSICS_GENERAL_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_MODEL_PHYSICS_GENERAL_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_MODEL_PHYSICS_GENERAL_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_MODEL_PHYSICS_GENERAL_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 

  SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(MODEL_PHYSICS_GENERAL_TYPE), INTENT(IN) :: SELF
  INTEGER                          , INTENT(IN) :: KDEPTH
  INTEGER                          , INTENT(IN) :: KOUTNO

  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH) // 'model%yrml_phy_g : '
  CALL SELF%YRDPHY%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRSLPHY%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRVDF%PRINT(KDEPTH+2, KOUTNO)

  END SUBROUTINE PRINT_CONFIGURATION

END MODULE MODEL_PHYSICS_GENERAL_MOD
