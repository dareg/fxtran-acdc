SUBMODULE (YOMVERT) YOMVERT_TVERTICAL_GEOM_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE LOAD_TVERTICAL_GEOM (SELF, KLUN)

IMPLICIT NONE
CLASS (TVERTICAL_GEOM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

READ (KLUN) SELF%LNONHYD_GEOM
CALL SELF%YRVAB%LOAD (KLUN)
CALL SELF%YRVETA%LOAD (KLUN)
CALL SELF%YRVFE%LOAD (KLUN)
CALL SELF%YRCVER%LOAD (KLUN)
END SUBROUTINE


END SUBMODULE
