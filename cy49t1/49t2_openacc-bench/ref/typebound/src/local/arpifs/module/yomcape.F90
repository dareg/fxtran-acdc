USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TCAPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TCAPE (SELF, KLUN)

IMPLICIT NONE
CLASS (TCAPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

