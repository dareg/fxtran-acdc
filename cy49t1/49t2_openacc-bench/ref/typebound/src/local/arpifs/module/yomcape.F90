IMPLICIT NONE
CLASS (TCAPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TCAPE (SELF)

IMPLICIT NONE
CLASS (TCAPE), TARGET :: SELF
END SUBROUTINE

