MODULE YOMVSPLIP

!$ACDC methods 


USE PARKIND1 , ONLY : JPRB
IMPLICIT NONE

SAVE

! * Variables related to use of spline cubic vertical interpolations.
! RVSPTRI,RVSPC      : are used to re-profile the field to be interpolated
!                      in routine VSPLTRANS.
! RFVV: is used in the computation of the vertical weights.

TYPE TVSPLIP
REAL(KIND=JPRB),ALLOCATABLE :: RVSPTRI(:,:)
REAL(KIND=JPRB),ALLOCATABLE :: RVSPC(:)
REAL(KIND=JPRB),ALLOCATABLE :: RFVV(:,:,:)
CONTAINS
PROCEDURE :: COPY => COPY_TVSPLIP
PROCEDURE :: CRC64 => CRC64_TVSPLIP
PROCEDURE :: HOST => HOST_TVSPLIP
PROCEDURE :: LOAD => LOAD_TVSPLIP
PROCEDURE :: SAVE => SAVE_TVSPLIP
PROCEDURE :: SIZE => SIZE_TVSPLIP
PROCEDURE :: WIPE => WIPE_TVSPLIP
END TYPE TVSPLIP

INTERFACE

MODULE SUBROUTINE COPY_TVSPLIP (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TVSPLIP (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TVSPLIP), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TVSPLIP (SELF)

IMPLICIT NONE
CLASS (TVSPLIP), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TVSPLIP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TVSPLIP (SELF, KLUN)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TVSPLIP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TVSPLIP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TVSPLIP (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TVSPLIP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMVSPLIP
