SUBMODULE (MODEL_GENERAL_CONF_MOD) MODEL_GENERAL_CONF_MOD_MODEL_GENERAL_CONF_TYPE_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE SAVE_MODEL_GENERAL_CONF_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_GENERAL_CONF_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LGEOM
LGEOM = ASSOCIATED (SELF%GEOM)
WRITE (KLUN) LGEOM
IF (LGEOM) THEN
  CALL SELF%GEOM%SAVE (KLUN)
ENDIF
CALL SELF%YRDIMF%SAVE (KLUN)
CALL SELF%YGFL%SAVE (KLUN)
CALL SELF%YRRIP%SAVE (KLUN)
CALL SELF%YRDIMECV%SAVE (KLUN)
CALL SELF%YRMODERR%SAVE (KLUN)
CALL SELF%YRSPPT_CONFIG%SAVE (KLUN)
CALL SELF%YRSPP_CONFIG%SAVE (KLUN)
END SUBROUTINE

END SUBMODULE
