MODULE YOMGPDDH

!$ACDC methods 


USE PARKIND1  ,ONLY : JPIM     ,JPRB

IMPLICIT NONE

SAVE

!     ------------------------------------------------------------------

!*    GP Buffer used in Horizontal Diffusion (HD):
!     and Semi-Implicit (SI) DDH tendencies.

! -   ARRAYS

TYPE :: TGPDDH
REAL(KIND=JPRB),ALLOCATABLE:: GMVTNDHD_DDH(:,:,:,:) !
REAL(KIND=JPRB),ALLOCATABLE:: GFLTNDHD_DDH(:,:,:,:) !
REAL(KIND=JPRB),ALLOCATABLE:: GMVTNDSI_DDH(:,:,:,:) !
REAL(KIND=JPRB),ALLOCATABLE:: GMVTNDSL_DDH(:,:,:,:) !
REAL(KIND=JPRB),ALLOCATABLE:: GFLTNDSL_DDH(:,:,:,:) !
CONTAINS
PROCEDURE :: COPY => COPY_TGPDDH
PROCEDURE :: CRC64 => CRC64_TGPDDH
PROCEDURE :: HOST => HOST_TGPDDH
PROCEDURE :: LOAD => LOAD_TGPDDH
PROCEDURE :: SAVE => SAVE_TGPDDH
PROCEDURE :: SIZE => SIZE_TGPDDH
PROCEDURE :: WIPE => WIPE_TGPDDH
END TYPE TGPDDH

!!TYPE(TGPDDH), POINTER :: YRGPDDH => NULL()

!     ------------------------------------------------------------------
INTERFACE

MODULE SUBROUTINE COPY_TGPDDH (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TGPDDH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TGPDDH (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TGPDDH), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TGPDDH (SELF)

IMPLICIT NONE
CLASS (TGPDDH), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TGPDDH (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TGPDDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TGPDDH (SELF, KLUN)

IMPLICIT NONE
CLASS (TGPDDH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TGPDDH (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TGPDDH),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TGPDDH (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TGPDDH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMGPDDH
