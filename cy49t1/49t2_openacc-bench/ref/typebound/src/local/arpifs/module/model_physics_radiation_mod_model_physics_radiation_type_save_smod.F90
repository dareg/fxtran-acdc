SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE SAVE_MODEL_PHYSICS_RADIATION_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYRAD_GSGEOM
CALL SELF%YRERAD%SAVE (KLUN)
CALL SELF%YRESWRT%SAVE (KLUN)
CALL SELF%YREOVLP%SAVE (KLUN)
CALL SELF%YRENEUR%SAVE (KLUN)
CALL SELF%YRELWRAD%SAVE (KLUN)
CALL SELF%YREAERD%SAVE (KLUN)
CALL SELF%YREAERATM%SAVE (KLUN)
CALL SELF%YREUVRAD%SAVE (KLUN)
CALL SELF%YRERDI%SAVE (KLUN)
CALL SELF%YREMCICA%SAVE (KLUN)
CALL SELF%YRRCOEF%SAVE (KLUN)
CALL SELF%YRTRC%SAVE (KLUN)
CALL SELF%RADGRID%SAVE (KLUN)
LYRAD_GSGEOM = ASSOCIATED (SELF%YRAD_GSGEOM)
WRITE (KLUN) LYRAD_GSGEOM
IF (LYRAD_GSGEOM) THEN
  WRITE (KLUN) LBOUND (SELF%YRAD_GSGEOM)
  WRITE (KLUN) UBOUND (SELF%YRAD_GSGEOM)
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    CALL SELF%YRAD_GSGEOM (J1)%SAVE (KLUN)
  ENDDO
ENDIF
CALL SELF%YRERIP%SAVE (KLUN)
CALL SELF%YRADIATION%SAVE (KLUN)
CALL SELF%YRRI%SAVE (KLUN)
CALL SELF%YRRO%SAVE (KLUN)
END SUBROUTINE

END SUBMODULE
