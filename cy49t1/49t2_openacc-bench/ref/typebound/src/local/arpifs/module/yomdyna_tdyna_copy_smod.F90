SUBMODULE (YOMDYNA) YOMDYNA_TDYNA_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE COPY_TDYNA (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TDYNA), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

CALL SELF%YYTTND%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YYTGMVT95%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YYTGFLT95%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

END SUBMODULE
