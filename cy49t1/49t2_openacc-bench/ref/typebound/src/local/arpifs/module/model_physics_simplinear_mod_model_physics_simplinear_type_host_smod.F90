MODULE SUBROUTINE HOST_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LGPHIST
CALL SELF%YREPHLI%HOST ()

CALL SELF%YRCUMFS%HOST ()

CALL SELF%YREGWDWMS%HOST ()

CALL SELF%YRECUMF2%HOST ()

CALL SELF%YRPHLC%HOST ()

CALL SELF%YRPHNC%HOST ()

CALL SELF%YRNCL%HOST ()

CALL SELF%YRSRFTLAD%HOST ()

LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL SELF%GPHIST (J1)%HOST ()
  ENDDO
ENDIF

END SUBROUTINE

END SUBMODULE
