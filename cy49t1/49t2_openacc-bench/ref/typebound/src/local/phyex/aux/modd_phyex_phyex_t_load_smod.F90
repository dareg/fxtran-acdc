SUBMODULE (MODD_PHYEX) MODD_PHYEX_PHYEX_T_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE LOAD_PHYEX_T (SELF, KLUN)

IMPLICIT NONE
CLASS (PHYEX_T), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL SELF%CST%LOAD (KLUN)
CALL SELF%PARAM_ICEN%LOAD (KLUN)
CALL SELF%RAIN_ICE_DESCRN%LOAD (KLUN)
CALL SELF%RAIN_ICE_PARAMN%LOAD (KLUN)
CALL SELF%CLOUDPARN%LOAD (KLUN)
CALL SELF%PARAM_MFSHALLN%LOAD (KLUN)
CALL SELF%CSTURB%LOAD (KLUN)
CALL SELF%TURBN%LOAD (KLUN)
CALL SELF%NEBN%LOAD (KLUN)
CALL SELF%PARAM_LIMA%LOAD (KLUN)
CALL SELF%PARAM_LIMA_WARM%LOAD (KLUN)
CALL SELF%PARAM_LIMA_COLD%LOAD (KLUN)
CALL SELF%PARAM_LIMA_MIXED%LOAD (KLUN)
CALL SELF%TNSV%LOAD (KLUN)
CALL SELF%MISC%LOAD (KLUN)
END SUBROUTINE


END SUBMODULE
