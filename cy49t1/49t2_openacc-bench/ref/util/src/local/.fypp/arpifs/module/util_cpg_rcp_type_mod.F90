  CALL HOST_FIELD_3RB (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP) THEN
  CALL HOST_FIELD_3RB (SELF%F_KAP)
ENDIF

END SUBROUTINE

     



