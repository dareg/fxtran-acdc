SUBROUTINE COPY_CPG_RCP_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_RCP_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CP, LF_DATA, LF_KAP, LF_R, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC ENTER DATA CREATE (SELF%ZVIEW)
  !$ACC UPDATE DEVICE (SELF%ZVIEW)
  !$ACC ENTER DATA ATTACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC ENTER DATA CREATE (SELF%ZDATA)
  !$ACC UPDATE DEVICE (SELF%ZDATA)
  !$ACC ENTER DATA ATTACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_DATA)
  !$ACC ENTER DATA ATTACH (SELF%F_DATA)
ENDIF

LF_CP = ASSOCIATED (SELF%F_CP)
IF (LF_CP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_CP)
  !$ACC ENTER DATA ATTACH (SELF%F_CP)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_R)
  !$ACC ENTER DATA ATTACH (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_KAP)
  !$ACC ENTER DATA ATTACH (SELF%F_KAP)
ENDIF

END SUBROUTINE

