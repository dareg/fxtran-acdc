FUNCTION SIZE_MODEL_PHYSICS_AEROSOL_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TEAERLID_MOD
USE UTIL_TEAERMAP_MOD
USE UTIL_TEAERSNK_MOD
USE UTIL_TEAERSRC_MOD
USE UTIL_TEAERVOL_MOD
USE UTIL_TEDBUG_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_AEROSOL_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TEAERLID (SELF%YREAERLID, CLPATH//'%YREAERLID', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREAERLID'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEAERMAP (SELF%YREAERMAP, CLPATH//'%YREAERMAP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREAERMAP'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEAERSNK (SELF%YREAERSNK, CLPATH//'%YREAERSNK', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREAERSNK'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEAERSRC (SELF%YREAERSRC, CLPATH//'%YREAERSRC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREAERSRC'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEAERVOL (SELF%YREAERVOL, CLPATH//'%YREAERVOL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREAERVOL'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEDBUG (SELF%YREDBUG, CLPATH//'%YREDBUG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREDBUG'
ENDIF
END FUNCTION

