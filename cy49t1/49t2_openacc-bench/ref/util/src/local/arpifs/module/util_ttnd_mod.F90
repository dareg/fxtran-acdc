SUBROUTINE CRC64_TTND (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TTND), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDU), CDPATH//'%M_TNDU'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDV), CDPATH//'%M_TNDV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDU_NOC), CDPATH//'%M_TNDU_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDV_NOC), CDPATH//'%M_TNDV_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDT), CDPATH//'%M_TNDT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDPD), CDPATH//'%M_TNDPD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDVD), CDPATH//'%M_TNDVD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_TNDGW), CDPATH//'%M_TNDGW'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

