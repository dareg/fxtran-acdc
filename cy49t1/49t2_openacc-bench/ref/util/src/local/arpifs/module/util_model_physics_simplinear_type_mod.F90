CLIND = ''
CALL CRC64_TNCL (SELF%YRNCL, KLUN, CDPATH//'%YRNCL'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TSRFTLAD (SELF%YRSRFTLAD, KLUN, CDPATH//'%YRSRFTLAD'//TRIM(CLIND))

