FUNCTION SIZE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
INTEGER :: J1
LOGICAL :: LGPHIST

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TEPHLI (SELF%YREPHLI, CLPATH//'%YREPHLI', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREPHLI'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCUMFS (SELF%YRCUMFS, CLPATH//'%YRCUMFS', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCUMFS'
ENDIF
JSIZE = 0
ISIZE = SIZE_TEGWDWMS (SELF%YREGWDWMS, CLPATH//'%YREGWDWMS', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREGWDWMS'
ENDIF
JSIZE = 0
ISIZE = SIZE_TECUMF2 (SELF%YRECUMF2, CLPATH//'%YRECUMF2', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRECUMF2'
ENDIF
JSIZE = 0
ISIZE = SIZE_TPHLC (SELF%YRPHLC, CLPATH//'%YRPHLC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPHLC'
ENDIF
JSIZE = 0
ISIZE = SIZE_TPHNC (SELF%YRPHNC, CLPATH//'%YRPHNC', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPHNC'
ENDIF
JSIZE = 0
ISIZE = SIZE_TNCL (SELF%YRNCL, CLPATH//'%YRNCL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRNCL'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSRFTLAD (SELF%YRSRFTLAD, CLPATH//'%YRSRFTLAD', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSRFTLAD'
ENDIF
LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  JSIZE = 0
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    ISIZE = SIZE_SPHYS_HIST_TYPE (SELF%GPHIST (J1), CLPATH//'%GPHIST', .FALSE.)
    JSIZE = JSIZE + ISIZE
    KSIZE = KSIZE + ISIZE
  ENDDO
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%GPHIST'
  ENDIF
ENDIF
END FUNCTION

