SUBROUTINE SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LGPHIST
CALL SAVE_TEPHLI (SELF%YREPHLI, KLUN)
CALL SAVE_TCUMFS (SELF%YRCUMFS, KLUN)
CALL SAVE_TEGWDWMS (SELF%YREGWDWMS, KLUN)
CALL SAVE_TECUMF2 (SELF%YRECUMF2, KLUN)
CALL SAVE_TPHLC (SELF%YRPHLC, KLUN)
CALL SAVE_TPHNC (SELF%YRPHNC, KLUN)
CALL SAVE_TNCL (SELF%YRNCL, KLUN)
CALL SAVE_TSRFTLAD (SELF%YRSRFTLAD, KLUN)
LGPHIST = ASSOCIATED (SELF%GPHIST)
WRITE (KLUN) LGPHIST
IF (LGPHIST) THEN
  WRITE (KLUN) LBOUND (SELF%GPHIST)
  WRITE (KLUN) UBOUND (SELF%GPHIST)
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL SAVE_SPHYS_HIST_TYPE (SELF%GPHIST (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD

IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGPHIST
CALL LOAD_TEPHLI (SELF%YREPHLI, KLUN)
CALL LOAD_TCUMFS (SELF%YRCUMFS, KLUN)
CALL LOAD_TEGWDWMS (SELF%YREGWDWMS, KLUN)
CALL LOAD_TECUMF2 (SELF%YRECUMF2, KLUN)
CALL LOAD_TPHLC (SELF%YRPHLC, KLUN)
CALL LOAD_TPHNC (SELF%YRPHNC, KLUN)
CALL LOAD_TNCL (SELF%YRNCL, KLUN)
CALL LOAD_TSRFTLAD (SELF%YRSRFTLAD, KLUN)
READ (KLUN) LGPHIST
IF (LGPHIST) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%GPHIST (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL LOAD_SPHYS_HIST_TYPE (SELF%GPHIST (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%GPHIST)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_SPHYS_HIST_TYPE_MOD
USE UTIL_TCUMFS_MOD
USE UTIL_TECUMF2_MOD
USE UTIL_TEGWDWMS_MOD
USE UTIL_TEPHLI_MOD
USE UTIL_TNCL_MOD
USE UTIL_TPHLC_MOD
USE UTIL_TPHNC_MOD
USE UTIL_TSRFTLAD_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LGPHIST

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL COPY_TEPHLI (SELF%YREPHLI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCUMFS (SELF%YRCUMFS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEGWDWMS (SELF%YREGWDWMS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TECUMF2 (SELF%YRECUMF2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TPHLC (SELF%YRPHLC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TPHNC (SELF%YRPHNC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TNCL (SELF%YRNCL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSRFTLAD (SELF%YRSRFTLAD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGPHIST = ASSOCIATED (SELF%GPHIST)
IF (LGPHIST) THEN
  !$ACC ENTER DATA CREATE (SELF%GPHIST)
  !$ACC UPDATE DEVICE (SELF%GPHIST)
  DO J1 = LBOUND (SELF%GPHIST, 1), UBOUND (SELF%GPHIST, 1)
    CALL COPY_SPHYS_HIST_TYPE (SELF%GPHIST (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%GPHIST)
ENDIF

END SUBROUTINE

