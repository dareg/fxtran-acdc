SUBROUTINE CRC64_TCCO (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TCCO), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_RLON), CDPATH//'%M_RLON'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_RLAT), CDPATH//'%M_RLAT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_RQX), CDPATH//'%M_RQX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_RQY), CDPATH//'%M_RQY'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

SUBROUTINE WIPE_TCCO (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TCCO), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION SIZE_TCCO (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCCO),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%M_RLON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_RLON'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_RLAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_RLAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_RQX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_RQX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%M_RQY)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%M_RQY'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (SELF%NDIM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NDIM'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

