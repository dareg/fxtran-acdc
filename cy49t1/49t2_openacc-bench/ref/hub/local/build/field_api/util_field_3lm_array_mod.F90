MODULE UTIL_FIELD_3LM_ARRAY_MOD

USE FIELD_3LM_ARRAY_MODULE, ONLY : FIELD_3LM_ARRAY

INTERFACE SAVE
MODULE PROCEDURE SAVE_FIELD_3LM_ARRAY
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_FIELD_3LM_ARRAY
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_FIELD_3LM_ARRAY
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_FIELD_3LM_ARRAY
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_FIELD_3LM_ARRAY
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_FIELD_3LM_ARRAY
END INTERFACE

INTERFACE SIZE
MODULE PROCEDURE SIZE_FIELD_3LM_ARRAY
END INTERFACE


CONTAINS

SUBROUTINE SAVE_FIELD_3LM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
WRITE (KLUN) LF_P
IF (LF_P) THEN
  CALL SAVE_FIELD_3LM (SELF%F_P, KLUN)
ENDIF
LP_FIELD = ASSOCIATED (SELF%P_FIELD)
WRITE (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  WRITE (KLUN) LBOUND (SELF%P_FIELD)
  WRITE (KLUN) UBOUND (SELF%P_FIELD)
  WRITE (KLUN) SELF%P_FIELD
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_FIELD_3LM_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL3(3), IU3(3)
LOGICAL :: LF_P, LP_FIELD
READ (KLUN) LF_P
IF (LF_P) THEN
  CALL LOAD_FIELD_3LM (SELF%F_P, KLUN)
ELSE
  NULLIFY (SELF%F_P)
ENDIF
READ (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (SELF%P_FIELD (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  READ (KLUN) SELF%P_FIELD
ELSE
  NULLIFY (SELF%P_FIELD)
ENDIF
NULLIFY (SELF%P)
END SUBROUTINE


SUBROUTINE COPY_FIELD_3LM_ARRAY (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LP_FIELD

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3LM (SELF%F_P)
  !$ACC ENTER DATA ATTACH (SELF%F_P)
ENDIF

LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  !$ACC ENTER DATA CREATE (SELF%P_FIELD)
  !$ACC UPDATE DEVICE (SELF%P_FIELD)
  !$ACC ENTER DATA ATTACH (SELF%P_FIELD)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_FIELD_3LM_ARRAY (SELF)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), TARGET :: SELF
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_3LM (SELF%F_P)
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_FIELD_3LM_ARRAY (SELF, KLUN, CDPATH)
USE FIELD_UTIL_MODULE
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CLIND = ''
  WRITE (KLUN, '(Z16.16," ",A)') SELF%F_P%CRC64 (), CDPATH//'%F_P'//TRIM(CLIND)
ENDIF

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%P_FIELD), CDPATH//'%P_FIELD'

END SUBROUTINE

SUBROUTINE WIPE_FIELD_3LM_ARRAY (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LP_FIELD

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3LM (SELF%F_P)
ENDIF

LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  !$ACC EXIT DATA DETACH (SELF%P_FIELD)
  !$ACC EXIT DATA DETACH (SELF%P_FIELD)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION SIZE_FIELD_3LM_ARRAY (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (FIELD_3LM_ARRAY),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LF_P, LP_FIELD

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LP_FIELD = ASSOCIATED (SELF%P_FIELD)
IF (LP_FIELD) THEN
  ISIZE = KIND (SELF%P_FIELD) * SIZE (SELF%P_FIELD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%P_FIELD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

END MODULE
