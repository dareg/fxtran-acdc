SUBROUTINE WIPE_MODEL_PHYSICS_STOCHAST_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TPERTPAR_MOD
USE UTIL_TSTOPH_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_STOCHAST_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_TPERTPAR (SELF%YRPERTPAR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TSTOPH (SELF%YRSTOPH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION SIZE_MODEL_PHYSICS_STOCHAST_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TPERTPAR_MOD
USE UTIL_TSTOPH_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_STOCHAST_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TPERTPAR (SELF%YRPERTPAR, CLPATH//'%YRPERTPAR', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPERTPAR'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSTOPH (SELF%YRSTOPH, CLPATH//'%YRSTOPH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSTOPH'
ENDIF
END FUNCTION

