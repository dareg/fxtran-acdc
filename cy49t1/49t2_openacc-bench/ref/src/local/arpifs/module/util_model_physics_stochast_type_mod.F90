SUBROUTINE COPY_MODEL_PHYSICS_STOCHAST_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TPERTPAR_MOD
USE UTIL_TSTOPH_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_STOCHAST_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL COPY_TPERTPAR (SELF%YRPERTPAR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSTOPH (SELF%YRSTOPH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

