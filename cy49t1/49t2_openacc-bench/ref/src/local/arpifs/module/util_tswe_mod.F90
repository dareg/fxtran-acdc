SUBROUTINE CRC64_TSWE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TSWE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ALPHASWE), CDPATH//'%ALPHASWE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%GMUCENSWE), CDPATH//'%GMUCENSWE'

END SUBROUTINE

SUBROUTINE WIPE_TSWE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TSWE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

