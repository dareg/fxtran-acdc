FUNCTION SIZE_MODEL_PHYSICS_GENERAL_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TDPHY_MOD
USE UTIL_TSLPHY_MOD
USE UTIL_TVDF_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_GENERAL_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TDPHY (SELF%YRDPHY, CLPATH//'%YRDPHY', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDPHY'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSLPHY (SELF%YRSLPHY, CLPATH//'%YRSLPHY', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSLPHY'
ENDIF
JSIZE = 0
ISIZE = SIZE_TVDF (SELF%YRVDF, CLPATH//'%YRVDF', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVDF'
ENDIF
END FUNCTION

