MODULE UTIL_SURFACE_VARIABLE_GROUP_LAKEB_MOD

USE SURFACE_VARIABLES_MOD, ONLY : SURFACE_VARIABLE_GROUP_LAKEB



INTERFACE COPY
MODULE PROCEDURE COPY_SURFACE_VARIABLE_GROUP_LAKEB
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_SURFACE_VARIABLE_GROUP_LAKEB
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_SURFACE_VARIABLE_GROUP_LAKEB
END INTERFACE



CONTAINS





SUBROUTINE COPY_SURFACE_VARIABLE_GROUP_LAKEB (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_LAKEB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL COPY_VARIABLE_2RB (SELF%VLICT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLMLT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLTLT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLBLT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLSHF, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLICD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (SELF%VLMLD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_GROUP)
  !$ACC ENTER DATA ATTACH (SELF%F_GROUP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_SURFACE_VARIABLE_GROUP_LAKEB (SELF)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD

IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_LAKEB), TARGET :: SELF
LOGICAL :: LF_GROUP
CALL HOST_VARIABLE_2RB (SELF%VLICT)

CALL HOST_VARIABLE_2RB (SELF%VLMLT)

CALL HOST_VARIABLE_2RB (SELF%VLTLT)

CALL HOST_VARIABLE_2RB (SELF%VLBLT)

CALL HOST_VARIABLE_2RB (SELF%VLSHF)

CALL HOST_VARIABLE_2RB (SELF%VLICD)

CALL HOST_VARIABLE_2RB (SELF%VLMLD)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_SURFACE_VARIABLE_GROUP_LAKEB (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_LAKEB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_VARIABLE_2RB (SELF%VLICT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLMLT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLTLT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLBLT, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLSHF, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLICD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (SELF%VLMLD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
