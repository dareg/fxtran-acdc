SUBROUTINE COPY_CPG_HWIND_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_HWIND_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_DATA, LF_UH, LF_VH, LF_WWI, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC ENTER DATA CREATE (SELF%ZVIEW)
  !$ACC UPDATE DEVICE (SELF%ZVIEW)
  !$ACC ENTER DATA ATTACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC ENTER DATA CREATE (SELF%ZDATA)
  !$ACC UPDATE DEVICE (SELF%ZDATA)
  !$ACC ENTER DATA ATTACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_DATA)
  !$ACC ENTER DATA ATTACH (SELF%F_DATA)
ENDIF

LF_UH = ASSOCIATED (SELF%F_UH)
IF (LF_UH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_UH)
  !$ACC ENTER DATA ATTACH (SELF%F_UH)
ENDIF

LF_VH = ASSOCIATED (SELF%F_VH)
IF (LF_VH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_VH)
  !$ACC ENTER DATA ATTACH (SELF%F_VH)
ENDIF

LF_WWI = ASSOCIATED (SELF%F_WWI)
IF (LF_WWI .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_WWI)
  !$ACC ENTER DATA ATTACH (SELF%F_WWI)
ENDIF

END SUBROUTINE

