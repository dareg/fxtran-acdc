SUBROUTINE CUCTRACER_OPENACC (YDCST, YDCUMFS, YDECUMF2, YDECUMF, KIDIA, KFDIA, KLON&
&, KLEV, KTRAC, KCTOP, KDTOP, KTYPE, LDCUM, LDDRAF, PTSPHY, PAPH, PAP, PMFU, PMFD,&
& PMFUO, PMFDO, PUDRATE, PDDRATE, PDMFUP, PDMFDP, PCEN, PTENC, PSCAV, YDSTACK)
!$ACC ROUTINE (CUCTRACER_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMCST,ONLY:TCST
USE YOECUMF,ONLY:TECUMF
USE YOECUMF2,ONLY:TECUMF2
USE YOMCUMFS,ONLY:TCUMFS
#include "stack.h"
USE STACK_MOD
USE ABOR1_ACC_MOD

IMPLICIT NONE

TYPE (TCST), INTENT (IN)::YDCST
TYPE (TCUMFS), INTENT (IN)::YDCUMFS
TYPE (TECUMF), INTENT (IN)::YDECUMF
TYPE (TECUMF2), INTENT (IN)::YDECUMF2
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
INTEGER (KIND=JPIM), INTENT (IN)::KTRAC
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KCTOP (KLON)
INTEGER (KIND=JPIM), INTENT (IN)::KDTOP (KLON)
INTEGER (KIND=JPIM), INTENT (IN)::KTYPE (KLON)
LOGICAL, INTENT (IN)::LDCUM (KLON)
LOGICAL, INTENT (IN)::LDDRAF (KLON)
REAL (KIND=JPRB), INTENT (IN)::PTSPHY
REAL (KIND=JPRB), INTENT (IN)::PAPH (KLON, KLEV+1)
REAL (KIND=JPRB), INTENT (IN)::PAP (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PMFU (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PMFD (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PMFUO (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PMFDO (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PUDRATE (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDDRATE (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDMFUP (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDMFDP (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PCEN (KLON, KLEV, KTRAC)
REAL (KIND=JPRB), INTENT (IN)::PSCAV (KTRAC)
TYPE(STACK), INTENT (IN) :: YDSTACK
TYPE(STACK) :: YLSTACK
REAL (KIND=JPRB), INTENT (INOUT)::PTENC (KLON, KLEV, KTRAC)
INTEGER (KIND=JPIM)::JN
INTEGER (KIND=JPIM)::JL
INTEGER (KIND=JPIM)::JK
INTEGER (KIND=JPIM)::IM
INTEGER (KIND=JPIM)::IK
REAL (KIND=JPRB)::ZADVW 
REAL (KIND=JPRB)::ZC
REAL (KIND=JPRB)::ZTSPHY
REAL (KIND=JPRB)::ZPOSI
REAL (KIND=JPRB)::ZERATE
REAL (KIND=JPRB)::ZIMP
REAL (KIND=JPRB)::ZMFA
REAL (KIND=JPRB)::ZZP
REAL (KIND=JPRB)::ZRMFSOLCT
REAL (KIND=JPRB)::ZRMFCMIN
temp (REAL (KIND=JPRB), ZMFC, (KLON, KLEV, KTRAC))
temp (REAL (KIND=JPRB), ZTENC, (KLON, KLEV, KTRAC))
temp (REAL (KIND=JPRB), ZCD, (KLON, KLEV, KTRAC))
temp (REAL (KIND=JPRB), ZCU, (KLON, KLEV, KTRAC))
temp (REAL (KIND=JPRB), ZCEN, (KLON, KLEV, KTRAC))
temp (REAL (KIND=JPRB), ZR1, (KLON, KLEV))
temp (REAL (KIND=JPRB), ZB, (KLON, KLEV))
temp (REAL (KIND=JPRB), ZDP, (KLON, KLEV))
temp (LOGICAL, LLCUMBAS, (KLON, KLEV))
temp (LOGICAL, LLCUMASK, (KLON, KLEV))

#include "cubidiag_openacc.intfb.h"
INTEGER (KIND=JPIM) :: JLON

YLSTACK = YDSTACK



IF (KIND (ZMFC) == 8) THEN
    alloc8 (ZMFC)
ELSEIF (KIND (ZMFC) == 4) THEN
    alloc4 (ZMFC)
ELSE
    STOP 1
ENDIF


IF (KIND (ZTENC) == 8) THEN
    alloc8 (ZTENC)
ELSEIF (KIND (ZTENC) == 4) THEN
    alloc4 (ZTENC)
ELSE
    STOP 1
ENDIF


IF (KIND (ZCD) == 8) THEN
    alloc8 (ZCD)
ELSEIF (KIND (ZCD) == 4) THEN
    alloc4 (ZCD)
ELSE
    STOP 1
ENDIF


IF (KIND (ZCU) == 8) THEN
    alloc8 (ZCU)
ELSEIF (KIND (ZCU) == 4) THEN
    alloc4 (ZCU)
ELSE
    STOP 1
ENDIF


IF (KIND (ZCEN) == 8) THEN
    alloc8 (ZCEN)
ELSEIF (KIND (ZCEN) == 4) THEN
    alloc4 (ZCEN)
ELSE
    STOP 1
ENDIF


IF (KIND (ZR1) == 8) THEN
    alloc8 (ZR1)
ELSEIF (KIND (ZR1) == 4) THEN
    alloc4 (ZR1)
ELSE
    STOP 1
ENDIF


IF (KIND (ZB) == 8) THEN
    alloc8 (ZB)
ELSEIF (KIND (ZB) == 4) THEN
    alloc4 (ZB)
ELSE
    STOP 1
ENDIF


IF (KIND (ZDP) == 8) THEN
    alloc8 (ZDP)
ELSEIF (KIND (ZDP) == 4) THEN
    alloc4 (ZDP)
ELSE
    STOP 1
ENDIF


IF (KIND (LLCUMBAS) == 8) THEN
    alloc8 (LLCUMBAS)
ELSEIF (KIND (LLCUMBAS) == 4) THEN
    alloc4 (LLCUMBAS)
ELSE
    STOP 1
ENDIF


IF (KIND (LLCUMASK) == 8) THEN
    alloc8 (LLCUMASK)
ELSEIF (KIND (LLCUMASK) == 4) THEN
    alloc4 (LLCUMASK)
ELSE
    STOP 1
ENDIF


JLON = KIDIA





IF (YDCUMFS%LECUMFS) THEN
  ZRMFSOLCT=YDECUMF2%RMFSOLCT2
  ZRMFCMIN=YDECUMF2%RMFCMIN2
ELSE
  ZRMFSOLCT=YDECUMF%RMFSOLCT
  ZRMFCMIN=YDECUMF%RMFCMIN
ENDIF

ZIMP=1.0_JPRB-ZRMFSOLCT
ZTSPHY=1.0_JPRB/PTSPHY

DO JL=KIDIA, KFDIA
  ZADVW =0.0_JPRB

  IF (KTYPE (JLON)==1)  THEN
      ZADVW =YDECUMF%RMFADVW
  ENDIF

ENDDO


DO JK=2, KLEV

  DO JL=KIDIA, KFDIA
    LLCUMASK (JLON, JK)=.FALSE.

    IF (LDCUM (JLON)) THEN
      ZDP (JLON, JK)=YDCST%RG/(PAPH (JLON, JK+1)-PAPH (JLON, JK))

      IF (JK>=KCTOP (JLON)-1) THEN
        LLCUMASK (JLON, JK)=.TRUE.
      ENDIF

    ENDIF

  ENDDO

ENDDO


DO JN=1, KTRAC

  DO JK=2, KLEV
    IK=JK-1

    DO JL=KIDIA, KFDIA
      ZCEN (JLON, JK, JN)=PCEN (JLON, JK, JN)
      ZCD (JLON, JK, JN)=PCEN (JLON, IK, JN)
      ZCU (JLON, JK, JN)=PCEN (JLON, IK, JN)
      ZMFC (JLON, JK, JN)=0.0_JPRB
      ZTENC (JLON, JK, JN)=0.0_JPRB
    ENDDO

  ENDDO


  DO JL=KIDIA, KFDIA
    ZCU (JLON, KLEV, JN)=PCEN (JLON, KLEV, JN)
  ENDDO


  DO JK=KLEV-1, 3,-1
    IK=JK+1

    DO JL=KIDIA, KFDIA

      IF (LLCUMASK (JLON, JK)) THEN
        ZERATE=PMFU (JLON, JK)-PMFU (JLON, IK)+PUDRATE (JLON, JK)
        ZMFA=1.0_JPRB/MAX (ZRMFCMIN, PMFU (JLON, JK))

        IF (JK>=KCTOP (JLON)) THEN
          ZCU (JLON, JK, JN)=(PMFU (JLON, IK)*ZCU (JLON, IK, JN)+ZERATE*PCEN (JLON, JK, JN&
          &)-(PUDRATE (JLON, JK)+PDMFUP (JLON, JK)*PSCAV (JN))*ZCU (JLON, IK, JN))*ZMFA
        ENDIF

      ENDIF

    ENDDO

  ENDDO


  DO JK=3, KLEV
    IK=JK-1

    DO JL=KIDIA, KFDIA

      IF (LDDRAF (JLON).AND.JK==KDTOP (JLON)) THEN
        ZCD (JLON, JK, JN)=0.1_JPRB*ZCU (JLON, JK, JN)+0.9_JPRB*PCEN (JLON, IK, JN)
      ELSEIF (LDDRAF (JLON).AND.JK>KDTOP (JLON)) THEN
        ZERATE=-PMFD (JLON, JK)+PMFD (JLON, IK)+PDDRATE (JLON, JK)
        ZMFA=1._JPRB/MIN (-ZRMFCMIN, PMFD (JLON, JK))
        ZCD (JLON, JK, JN)=(PMFD (JLON, IK)*ZCD (JLON, IK, JN)-ZERATE*PCEN (JLON, IK, JN&
        &)+(PDDRATE (JLON, JK)+PDMFDP (JLON, JK)*PSCAV (JN))*ZCD (JLON, IK, JN))*ZMFA
      ENDIF

    ENDDO

  ENDDO

  JK=KLEV
  IK=JK-1

  DO JL=KIDIA, KFDIA

    IF (LDDRAF (JLON)) THEN
      ZPOSI=-ZDP (JLON, JK)*(PMFU (JLON, JK)*ZCU (JLON, JK, JN)+PMFD (JLON, JK)*ZCD&
      & (JLON, JK, JN)-(PMFU (JLON, JK)+PMFD (JLON, JK))*PCEN (JLON, IK, JN))

      IF (PCEN (JLON, JK, JN)+ZPOSI*PTSPHY<0.0_JPRB) THEN
        ZMFA=1._JPRB/MIN (-ZRMFCMIN, PMFD (JLON, JK))
        ZCD (JLON, JK, JN)=((PMFU (JLON, JK)+PMFD (JLON, JK))*PCEN (JLON, IK, JN)-PMFU (JLON&
        &, JK)*ZCU (JLON, JK, JN)+PCEN (JLON, JK, JN)/(PTSPHY*ZDP (JLON, JK)))*ZMFA
      ENDIF

    ENDIF

  ENDDO

ENDDO


DO JN=1, KTRAC

  DO JK=2, KLEV
    IK=JK-1

    DO JL=KIDIA, KFDIA

      IF (LLCUMASK (JLON, JK)) THEN
        ZMFA=PMFU (JLON, JK)+PMFD (JLON, JK)
        ZMFC (JLON, JK, JN)=PMFU (JLON, JK)*ZCU (JLON, JK, JN)+PMFD (JLON&
        &, JK)*ZCD (JLON, JK, JN)-ZIMP*ZMFA*ZCEN (JLON, IK, JN)
      ENDIF

    ENDDO

  ENDDO


  DO JK=2, KLEV-1
    IK=JK+1

    DO JL=KIDIA, KFDIA

      IF (LLCUMASK (JLON, JK)) THEN
        ZTENC (JLON, JK, JN)=ZDP (JLON, JK)*(ZMFC (JLON, IK, JN)-ZMFC (JLON, JK, JN))
      ENDIF

    ENDDO

  ENDDO

  JK=KLEV

  DO JL=KIDIA, KFDIA

    IF (LDCUM (JLON)) THEN
      ZTENC (JLON, JK, JN)=-ZDP (JLON, JK)*ZMFC (JLON, JK, JN)
    ENDIF

  ENDDO

ENDDO


IF (ZRMFSOLCT==0.0_JPRB) THEN

  DO JN=1, KTRAC

    DO JK=2, KLEV

      DO JL=KIDIA, KFDIA

        IF (LLCUMASK (JLON, JK)) THEN
          PTENC (JLON, JK, JN)=PTENC (JLON, JK, JN)+ZTENC (JLON, JK, JN)
        ENDIF

      ENDDO

    ENDDO

  ENDDO

ELSE
  LLCUMBAS (JLON,:)=.FALSE.
  ZB (JLON,:)=1._JPRB

  DO JN=1, KTRAC

    DO JK=2, KLEV
      IK=JK+1
      IM=JK-1

      DO JL=KIDIA, KFDIA
        LLCUMBAS (JLON, JK)=LLCUMASK (JLON, JK)

        IF (LLCUMBAS (JLON, JK)) THEN
          ZZP=ZRMFSOLCT*ZDP (JLON, JK)*PTSPHY
          ZMFC (JLON, JK, JN)=-ZZP*(PMFU (JLON, JK)+PMFD (JLON, JK))

          IF (JK<KLEV) THEN
            ZB (JLON, JK)=1.0_JPRB+ZZP*(PMFU (JLON, IK)+PMFD (JLON, IK))
          ELSE
            ZB (JLON, JK)=1.0_JPRB
          ENDIF

          ZZP=YDCST%RG*(PMFUO (JLON, JK)+YDECUMF%RMFADVWDD*PMFDO (JLON&
          &, JK))/(PAP (JLON, JK)-PAP (JLON, IM))*PTSPHY*ZADVW 
          ZC=ZZP*(PCEN (JLON, IM, JN)-PCEN (JLON, JK, JN))
          ZTENC (JLON, JK, JN)=ZTENC (JLON, JK, JN)*PTSPHY+PCEN (JLON, JK, JN)-ZC
        ENDIF

      ENDDO

    ENDDO

    CALL CUBIDIAG_OPENACC (KIDIA, KFDIA, KLON, KLEV, KCTOP, LLCUMBAS&
    &, ZMFC (:,:, JN), ZB, ZTENC (:,:, JN), ZR1, YDSTACK=YLSTACK)

    DO JK=2, KLEV

      DO JL=KIDIA, KFDIA

        IF (LLCUMBAS (JLON, JK)) THEN
          PTENC (JLON, JK, JN)=PTENC (JLON, JK, JN)+(ZR1 (JLON, JK)-PCEN (JLON, JK, JN))*ZTSPHY
        ENDIF

      ENDDO

    ENDDO

  ENDDO

ENDIF




ENDSUBROUTINE CUCTRACER_OPENACC
