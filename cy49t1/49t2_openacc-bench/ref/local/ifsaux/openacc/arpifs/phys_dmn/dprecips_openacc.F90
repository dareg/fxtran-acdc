SUBROUTINE DPRECIPS_OPENACC (YDCST, YDPRECIPS, KIDIA, KFDIA, KLON, KLEV, POROG, PCLSTPW&
&, PDIAGH, PAPHIFM, PDZZ, PTPW, PQCM, PFPLSL, PFPLSN, PFPLSG, PDPRECIPS, YDSTACK)
!$ACC ROUTINE (DPRECIPS_OPENACC) SEQ
USE PARKIND1,ONLY:JPIM, JPRB

USE YOMDPRECIPS,ONLY:TDPRECIPS
USE YOMCST,ONLY:TCST
#include "stack.h"
USE STACK_MOD
USE ABOR1_ACC_MOD

IMPLICIT NONE

TYPE (TCST), INTENT (IN)::YDCST
TYPE (TDPRECIPS), INTENT (IN), TARGET::YDPRECIPS
INTEGER (KIND=JPIM), INTENT (IN)::KIDIA
INTEGER (KIND=JPIM), INTENT (IN)::KFDIA
INTEGER (KIND=JPIM), INTENT (IN)::KLON
INTEGER (KIND=JPIM), INTENT (IN)::KLEV
REAL (KIND=JPRB), INTENT (IN)::POROG (KLON)
REAL (KIND=JPRB), INTENT (IN)::PCLSTPW (KLON)
REAL (KIND=JPRB), INTENT (IN)::PDIAGH (KLON)
REAL (KIND=JPRB), INTENT (IN)::PAPHIFM (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PDZZ (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PTPW (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PQCM (KLON, KLEV)
REAL (KIND=JPRB), INTENT (IN)::PFPLSL (KLON)
REAL (KIND=JPRB), INTENT (IN)::PFPLSN (KLON)
REAL (KIND=JPRB), INTENT (IN)::PFPLSG (KLON)
REAL (KIND=JPRB), INTENT (OUT)::PDPRECIPS (KLON)
TYPE(STACK), INTENT (IN) :: YDSTACK
TYPE(STACK) :: YLSTACK
INTEGER (KIND=JPIM)::INDI2
INTEGER (KIND=JPIM)::INDI1
INTEGER (KIND=JPIM)::INDIH
INTEGER (KIND=JPIM)::JLEVB
INTEGER (KIND=JPIM)::JLEVH
INTEGER (KIND=JPIM)::JL
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
INTEGER (KIND=JPIM)::I2 
INTEGER (KIND=JPIM)::I1 
INTEGER (KIND=JPIM)::JJLEVM1 
INTEGER (KIND=JPIM)::JLEVM1 
INTEGER (KIND=JPIM)::ITOP 
REAL (KIND=JPRB)::ZINVG
REAL (KIND=JPRB)::ZALPHA
REAL (KIND=JPRB)::ZZ
REAL (KIND=JPRB)::ZPROFNEG 
REAL (KIND=JPRB)::ZLIQ 
REAL (KIND=JPRB)::ZRATIO 
temp (REAL (KIND=JPRB), ZHF, (KLON, KLEV))
REAL (KIND=JPRB)::ZHEIGHT 
REAL (KIND=JPRB)::ZTOT 
REAL (KIND=JPRB)::ZICE 
REAL (KIND=JPRB)::ZDCLWC 
REAL (KIND=JPRB)::ZANEG 
REAL (KIND=JPRB)::ZAPOS 


YLSTACK = YDSTACK



IF (KIND (ZHF) == 8) THEN
    alloc8 (ZHF)
ELSEIF (KIND (ZHF) == 4) THEN
    alloc4 (ZHF)
ELSE
    STOP 1
ENDIF


JL = KIDIA



ZINVG=1._JPRB/YDCST%RG

DO JLON=KIDIA, KFDIA
  ZLIQ =MAX (PFPLSL (JL), 0._JPRB)
  ZICE =MAX (PFPLSN (JL), 0._JPRB)+MAX (PFPLSG (JL), 0._JPRB)
  ZTOT =ZLIQ +ZICE 
  ZTOT =MAX (ZTOT , 1.E-8_JPRB)
  ZRATIO =ZLIQ /ZTOT 
  ZHEIGHT =POROG (JL)*ZINVG
ENDDO

ITOP =1._JPIM
I1 =1._JPIM
I2 =1._JPIM
ZPROFNEG =0._JPRB
ZANEG =0._JPRB
ZAPOS =0._JPRB

DO JLEV=KLEV, 1,-1

  DO JLON=KIDIA, KFDIA
    ZHF (JL, JLEV)=PAPHIFM (JL, JLEV)*ZINVG
    INDIH=INT (MAX (0._JPRB, SIGN (1._JPRB, ZHF (JL, JLEV)-YDPRECIPS%RHTOP-ZHEIGHT )))
    ITOP =MAX (ITOP , JLEV*INDIH)
    I1 =MAX (I1 , ITOP , JLEV*INT (MAX (0._JPRB, SIGN (1._JPRB, PTPW (JL, JLEV)-YDCST%RTT))))
    I2 =MIN (I1 , MAX (I2 , ITOP , JLEV*INT (MAX (0._JPRB, SIGN (1._JPRB, YDCST%RTT-PTPW (JL, JLEV))))))
    ZPROFNEG =ZPROFNEG +(1-INDIH)*MAX (0._JPRB, SIGN (1._JPRB, PTPW (JL, JLEV)-YDCST%RTT))
    INDI1=MAX (0, SIGN (1, JLEV-MIN (I1 +1, KLEV)))
    ZANEG =ZANEG +INDI1*PDZZ (JL, JLEV)*(YDCST%RTT-PTPW (JL, JLEV))
    INDI2=MAX (0, SIGN (1, JLEV-MIN (I2 +1, I1 )))*MAX (0, SIGN (1, I1 -JLEV))
    ZAPOS =ZAPOS +INDI2*PDZZ (JL, JLEV)*(PTPW (JL, JLEV)-YDCST%RTT)
  ENDDO

ENDDO

JLEVM1 =0

DO JLEV=KLEV, 1,-1

  DO JLON=KIDIA, KFDIA
    ZZ=ZHF (JL, JLEV)-YDPRECIPS%HDCLWC-ZHEIGHT 
    JJLEVM1 =MAX (0._JPRB, SIGN (1._JPRB, ZZ))*JLEV
    JLEVM1 =MAX (JJLEVM1 , JLEVM1 )
  ENDDO

ENDDO


DO JLON=KIDIA, KFDIA
  JLEVH=JLEVM1 
  JLEVB=MIN (JLEVM1 +1, KLEV)

  IF (JLEVH==JLEVB) THEN
    ZDCLWC =PQCM (JL, JLEV)
  ELSE
    ZALPHA=(ZHF (JL, JLEVH)-YDPRECIPS%HDCLWC-ZHEIGHT )/(ZHF (JL, JLEVH)-ZHF (JL, JLEVB))
    ZDCLWC =PQCM (JL, JLEVB)*ZALPHA+PQCM (JL, JLEVH)*(1-ZALPHA)
  ENDIF

ENDDO


DO JLON=KIDIA, KFDIA

  IF (ZTOT >=YDPRECIPS%RPRECSEUIL) THEN

    IF (PDIAGH (JL)>=YDPRECIPS%RDHAIL1) THEN

      IF (PDIAGH (JL)>=YDPRECIPS%RDHAIL2) THEN
        PDPRECIPS (JL)=10._JPRB
      ELSE
        PDPRECIPS (JL)=9._JPRB
      ENDIF

    ELSEIF (PCLSTPW (JL)<YDPRECIPS%RTPW) THEN

      IF (ZPROFNEG ==0._JPRB) THEN

        IF (ZDCLWC >YDPRECIPS%RDCLWC) THEN
          PDPRECIPS (JL)=12._JPRB
        ELSEIF (PCLSTPW (JL)<=YDPRECIPS%RTPW-2) THEN
          PDPRECIPS (JL)=5._JPRB
        ELSE
          PDPRECIPS (JL)=6._JPRB
        ENDIF

      ELSEIF (ZAPOS >=YDPRECIPS%RAWARM) THEN

        IF (ZANEG <YDPRECIPS%RACOLD) THEN
          PDPRECIPS (JL)=3._JPRB
        ELSE
          PDPRECIPS (JL)=8._JPRB
        ENDIF

      ELSEIF (ZAPOS >=YDPRECIPS%RAWARM2) THEN

        IF (ZANEG <YDPRECIPS%RACOLD2) THEN
          PDPRECIPS (JL)=7._JPRB
        ELSE
          PDPRECIPS (JL)=8._JPRB
        ENDIF

      ELSE
        PDPRECIPS (JL)=6._JPRB
      ENDIF

    ELSEIF (ZRATIO >YDPRECIPS%RDSEUIL4) THEN

      IF (ZLIQ >YDPRECIPS%RDSEUIL5) THEN
        PDPRECIPS (JL)=1._JPRB
      ELSE
        PDPRECIPS (JL)=11._JPRB
      ENDIF

    ELSEIF (ZRATIO >YDPRECIPS%RDSEUIL3) THEN
      PDPRECIPS (JL)=7._JPRB
    ELSEIF (ZRATIO >YDPRECIPS%RDSEUIL2) THEN
      PDPRECIPS (JL)=193._JPRB
    ELSEIF (ZRATIO >YDPRECIPS%RDSEUIL1) THEN
      PDPRECIPS (JL)=6._JPRB
    ELSE
      PDPRECIPS (JL)=5._JPRB
    ENDIF

  ELSE
    PDPRECIPS (JL)=0._JPRB
  ENDIF

ENDDO



ENDSUBROUTINE DPRECIPS_OPENACC
