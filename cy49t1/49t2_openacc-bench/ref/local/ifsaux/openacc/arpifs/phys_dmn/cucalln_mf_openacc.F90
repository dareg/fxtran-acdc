INTEGER (KIND=JPIM)::JL
INTEGER (KIND=JPIM)::JK
INTEGER (KIND=JPIM)::IFLAG
REAL (KIND=JPRB)::ZEPS
REAL (KIND=JPRB)::ZGDPH
REAL (KIND=JPRB)::ZCP
LOGICAL::LLTDKMF

#include "cuccdia_openacc.intfb.h"
#include "cumastrn_openacc.intfb.h"
#include "satur_openacc.intfb.h"
#include "fcttre.func.h"
#include "fcttrm.func.h"
INTEGER (KIND=JPIM) :: JLON

YLSTACK = YDSTACK



IF (KIND (ZRAIN) == 8) THEN
    alloc8 (ZRAIN)
ELSEIF (KIND (ZRAIN) == 4) THEN
    alloc4 (ZRAIN)
ELSE
    STOP 1
ENDIF


IF (KIND (ZQSAT) == 8) THEN
    alloc8 (ZQSAT)
ELSEIF (KIND (ZQSAT) == 4) THEN
    alloc4 (ZQSAT)
ELSE
    STOP 1
ENDIF


IF (KIND (ZQU) == 8) THEN
    alloc8 (ZQU)
ELSEIF (KIND (ZQU) == 4) THEN
    alloc4 (ZQU)
ELSE
    STOP 1
ENDIF


IF (KIND (ZTU) == 8) THEN
    alloc8 (ZTU)
ELSEIF (KIND (ZTU) == 4) THEN
    alloc4 (ZTU)
ELSE
    STOP 1
ENDIF


IF (KIND (ZVP1) == 8) THEN
    alloc8 (ZVP1)
ELSEIF (KIND (ZVP1) == 4) THEN
    alloc4 (ZVP1)
