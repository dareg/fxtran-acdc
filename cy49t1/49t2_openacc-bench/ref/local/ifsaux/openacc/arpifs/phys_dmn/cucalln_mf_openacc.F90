    alloc4 (ZCP1)
ELSE
    STOP 1
ENDIF


IF (KIND (ZENTHS) == 8) THEN
    alloc8 (ZENTHS)
ELSEIF (KIND (ZENTHS) == 4) THEN
    alloc4 (ZENTHS)
ELSE
    STOP 1
ENDIF


IF (KIND (ZENTHD) == 8) THEN
    alloc8 (ZENTHD)
ELSEIF (KIND (ZENTHD) == 4) THEN
    alloc4 (ZENTHD)
ELSE
    STOP 1
ENDIF


IF (KIND (ZUEA) == 8) THEN
    alloc8 (ZUEA)
ELSEIF (KIND (ZUEA) == 4) THEN
    alloc4 (ZUEA)
