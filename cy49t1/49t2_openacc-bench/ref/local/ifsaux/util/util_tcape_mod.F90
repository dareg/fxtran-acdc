SUBROUTINE SAVE_TCAPE (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCAPE), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%NCAPEITER
WRITE (KLUN) YD%NETAPES
WRITE (KLUN) YD%NCAPEPSD
WRITE (KLUN) YD%GCAPERET
WRITE (KLUN) YD%GCAPEPSD
WRITE (KLUN) YD%GMISCINV
WRITE (KLUN) YD%GCAPEMIN
WRITE (KLUN) YD%GCINMAX
WRITE (KLUN) YD%LADAE
WRITE (KLUN) YD%LMCAPEA
END SUBROUTINE

SUBROUTINE LOAD_TCAPE (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCAPE), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) YD%NCAPEITER
READ (KLUN) YD%NETAPES
READ (KLUN) YD%NCAPEPSD
READ (KLUN) ZTMP0
YD%GCAPERET = ZTMP0
READ (KLUN) ZTMP0
YD%GCAPEPSD = ZTMP0
READ (KLUN) ZTMP0
YD%GMISCINV = ZTMP0
READ (KLUN) ZTMP0
YD%GCAPEMIN = ZTMP0
READ (KLUN) ZTMP0
YD%GCINMAX = ZTMP0
READ (KLUN) YD%LADAE
READ (KLUN) YD%LMCAPEA
END SUBROUTINE


