SUBROUTINE CRC64_TCAPE (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TCAPE), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NCAPEITER), CDPATH//'%NCAPEITER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NETAPES), CDPATH//'%NETAPES'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NCAPEPSD), CDPATH//'%NCAPEPSD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%GCAPERET), CDPATH//'%GCAPERET'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%GCAPEPSD), CDPATH//'%GCAPEPSD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%GMISCINV), CDPATH//'%GMISCINV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%GCAPEMIN), CDPATH//'%GCAPEMIN'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%GCINMAX), CDPATH//'%GCINMAX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LADAE), CDPATH//'%LADAE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LMCAPEA), CDPATH//'%LMCAPEA'

END SUBROUTINE

