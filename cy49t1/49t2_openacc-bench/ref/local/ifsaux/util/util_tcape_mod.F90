INTEGER*8 FUNCTION SIZE_TCAPE (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCAPE),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NCAPEITER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCAPEITER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NETAPES)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NETAPES'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NCAPEPSD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCAPEPSD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%GCAPERET)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPERET'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%GCAPEPSD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPEPSD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%GMISCINV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GMISCINV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%GCAPEMIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCAPEMIN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%GCINMAX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%GCINMAX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LADAE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADAE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LMCAPEA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LMCAPEA'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

