  NULLIFY (YD%YT)
ENDIF
READ (KLUN) LYW
IF (LYW) THEN
  ALLOCATE (YD%YW)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YW)
ELSE
  NULLIFY (YD%YW)
ENDIF
READ (KLUN) LYFC
IF (LYFC) THEN
  ALLOCATE (YD%YFC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YFC)
ELSE
  NULLIFY (YD%YFC)
ENDIF
READ (KLUN) LYIC
IF (LYIC) THEN
  ALLOCATE (YD%YIC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YIC)
ELSE
  NULLIFY (YD%YIC)
ENDIF
READ (KLUN) LYFP1
IF (LYFP1) THEN
  ALLOCATE (YD%YFP1)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YFP1)
ELSE
  NULLIFY (YD%YFP1)
