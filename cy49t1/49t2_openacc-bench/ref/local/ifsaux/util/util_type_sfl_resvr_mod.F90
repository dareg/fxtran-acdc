SUBROUTINE LOAD_TYPE_SFL_RESVR (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_RESVR), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYFC, LYFP1, LYIC, LYRR, LYT, LYW
