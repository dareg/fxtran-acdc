  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YT)
ENDIF
LYW = ASSOCIATED (YD%YW)
WRITE (KLUN) LYW
IF (LYW) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YW)
ENDIF
LYFC = ASSOCIATED (YD%YFC)
WRITE (KLUN) LYFC
IF (LYFC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YFC)
ENDIF
LYIC = ASSOCIATED (YD%YIC)
WRITE (KLUN) LYIC
IF (LYIC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YIC)
ENDIF
LYFP1 = ASSOCIATED (YD%YFP1)
WRITE (KLUN) LYFP1
IF (LYFP1) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YFP1)
ENDIF
LYRR = ASSOCIATED (YD%YRR)
WRITE (KLUN) LYRR
IF (LYRR) THEN
  WRITE (KLUN) LBOUND (YD%YRR)
  WRITE (KLUN) UBOUND (YD%YRR)
  DO J1 = LBOUND (YD%YRR, 1), UBOUND (YD%YRR, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YRR (J1))
  ENDDO
