INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_RESVR), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYFC, LYFP1, LYIC, LYRR, LYT, LYW
LYT = ASSOCIATED (YD%YT)
