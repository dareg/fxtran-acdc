SUBROUTINE LOAD_TYPE_SURF_MTL_3D (KLUN, YD)
USE PARKIND1, ONLY : JPRD
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, LOAD_TYPE_SURF_MTL

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SURF_MTL_3D), INTENT (OUT), TARGET :: YD
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCNAME, LIGRBCODE, LNREQIN, LREFVALI
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
YLSUPER => YD
CALL LOAD_TYPE_SURF_MTL (KLUN, YLSUPER)
READ (KLUN) LIGRBCODE
IF (LIGRBCODE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%IGRBCODE (IL1(1):IU1(1)))
  READ (KLUN) YD%IGRBCODE
ENDIF
READ (KLUN) LCNAME
IF (LCNAME) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%CNAME (IL1(1):IU1(1)))
  READ (KLUN) YD%CNAME
ENDIF
READ (KLUN) LREFVALI
IF (LREFVALI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%REFVALI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%REFVALI,1):UBOUND(YD%REFVALI,1)))
  READ (KLUN) ZTMP1
  YD%REFVALI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LNREQIN
IF (LNREQIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%NREQIN (IL1(1):IU1(1)))
  READ (KLUN) YD%NREQIN
ENDIF
END SUBROUTINE


