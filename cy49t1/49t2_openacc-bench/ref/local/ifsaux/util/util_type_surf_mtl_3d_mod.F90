SUBROUTINE LOAD_TYPE_SURF_MTL_3D (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, LOAD_TYPE_SURF_MTL

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_3D), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCNAME, LIGRBCODE, LNREQIN, LREFVALI
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
YLSUPER => SELF
CALL LOAD_TYPE_SURF_MTL (YLSUPER, KLUN)
READ (KLUN) LIGRBCODE
IF (LIGRBCODE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%IGRBCODE (IL1(1):IU1(1)))
  READ (KLUN) SELF%IGRBCODE
ENDIF
READ (KLUN) LCNAME
IF (LCNAME) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%CNAME (IL1(1):IU1(1)))
  READ (KLUN) SELF%CNAME
ENDIF
READ (KLUN) LREFVALI
IF (LREFVALI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%REFVALI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(SELF%REFVALI,1):UBOUND(SELF%REFVALI,1)))
  READ (KLUN) ZTMP1
  SELF%REFVALI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LNREQIN
IF (LNREQIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%NREQIN (IL1(1):IU1(1)))
  READ (KLUN) SELF%NREQIN
ENDIF
END SUBROUTINE


