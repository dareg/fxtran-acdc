MODULE UTIL_SURFACE_VARIABLE_GROUP_VCLIV_MOD

USE SURFACE_VARIABLES_MOD, ONLY : SURFACE_VARIABLE_GROUP_VCLIV



INTERFACE COPY
MODULE PROCEDURE COPY_SURFACE_VARIABLE_GROUP_VCLIV
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_SURFACE_VARIABLE_GROUP_VCLIV
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_SURFACE_VARIABLE_GROUP_VCLIV
END INTERFACE



CONTAINS





SUBROUTINE COPY_SURFACE_VARIABLE_GROUP_VCLIV (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIV), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_VARIABLE_2RB (YD%VARG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VSAB, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VD2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VIVEG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VRSMIN, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VLAI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VHV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VZ0H, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VALS, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VALV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_GROUP)
  !$ACC ENTER DATA ATTACH (YD%F_GROUP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_SURFACE_VARIABLE_GROUP_VCLIV (YD)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD

IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIV), TARGET :: YD
LOGICAL :: LF_GROUP
CALL HOST_VARIABLE_2RB (YD%VARG)

CALL HOST_VARIABLE_2RB (YD%VSAB)

CALL HOST_VARIABLE_2RB (YD%VD2)

CALL HOST_VARIABLE_2RB (YD%VIVEG)

CALL HOST_VARIABLE_2RB (YD%VRSMIN)

CALL HOST_VARIABLE_2RB (YD%VLAI)

CALL HOST_VARIABLE_2RB (YD%VHV)

CALL HOST_VARIABLE_2RB (YD%VZ0H)

CALL HOST_VARIABLE_2RB (YD%VALS)

CALL HOST_VARIABLE_2RB (YD%VALV)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (YD%F_GROUP)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_SURFACE_VARIABLE_GROUP_VCLIV (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIV), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_VARIABLE_2RB (YD%VARG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VSAB, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VD2, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VIVEG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VRSMIN, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VLAI, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VHV, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VZ0H, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VALS, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VALV, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
