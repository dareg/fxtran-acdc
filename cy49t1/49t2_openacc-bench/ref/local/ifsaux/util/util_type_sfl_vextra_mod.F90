MODULE UTIL_TYPE_SFL_VEXTRA_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_VEXTRA

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_VEXTRA
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_VEXTRA
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_VEXTRA
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_VEXTRA
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_VEXTRA
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_VEXTRA
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_VEXTRA (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VEXTRA), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYXA
LYXA = ASSOCIATED (YD%YXA)
WRITE (KLUN) LYXA
IF (LYXA) THEN
  WRITE (KLUN) LBOUND (YD%YXA)
  WRITE (KLUN) UBOUND (YD%YXA)
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YXA (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VEXTRA (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VEXTRA), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYXA
READ (KLUN) LYXA
IF (LYXA) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YXA (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YXA (J1))
  ENDDO
ELSE
  NULLIFY (YD%YXA)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VEXTRA (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VEXTRA), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYXA

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYXA = ASSOCIATED (YD%YXA)
IF (LYXA) THEN
  !$ACC ENTER DATA CREATE (YD%YXA)
  !$ACC UPDATE DEVICE (YD%YXA)
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YXA (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YXA)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_VEXTRA (YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VEXTRA), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYXA
LYXA = ASSOCIATED (YD%YXA)
IF (LYXA) THEN
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    CALL HOST_TYPE_SURF_MTL_3D (YD%YXA (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_VEXTRA (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VEXTRA), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYXA
LYXA = ASSOCIATED (YD%YXA)
IF (LYXA) THEN
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YXA (J1), KLUN, CDPATH//'%YXA'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_VEXTRA (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VEXTRA), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYXA

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYXA = ASSOCIATED (YD%YXA)
IF (LYXA) THEN
  !$ACC EXIT DATA DETACH (YD%YXA)
  DO J1 = LBOUND (YD%YXA, 1), UBOUND (YD%YXA, 1)
    CALL WIPE_TYPE_SURF_MTL_3D (YD%YXA (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YXA)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
