INTEGER*8 FUNCTION SIZE_TVERTICAL_GEOM (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCVER_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVETA_MOD
USE UTIL_TVFE_MOD
IMPLICIT NONE
TYPE (TVERTICAL_GEOM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%LNONHYD_GEOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LNONHYD_GEOM'
ENDIF
KSIZE = KSIZE + ISIZE
JSIZE = 0
ISIZE = SIZE_TVAB (YD%YRVAB, CLPATH//'%YRVAB', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVAB'
ENDIF
JSIZE = 0
ISIZE = SIZE_TVETA (YD%YRVETA, CLPATH//'%YRVETA', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVETA'
ENDIF
JSIZE = 0
ISIZE = SIZE_TVFE (YD%YRVFE, CLPATH//'%YRVFE', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVFE'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCVER (YD%YRCVER, CLPATH//'%YRCVER', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCVER'
ENDIF
END FUNCTION

