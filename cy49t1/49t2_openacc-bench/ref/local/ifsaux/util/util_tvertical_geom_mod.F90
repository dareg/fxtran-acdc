SUBROUTINE CRC64_TVERTICAL_GEOM (YD, KLUN, CDPATH)
USE UTIL_TCVER_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVETA_MOD
USE UTIL_TVFE_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TVERTICAL_GEOM), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LNONHYD_GEOM), CDPATH//'%LNONHYD_GEOM'

CLIND = ''
CALL CRC64_TVAB (YD%YRVAB, KLUN, CDPATH//'%YRVAB'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TVETA (YD%YRVETA, KLUN, CDPATH//'%YRVETA'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TVFE (YD%YRVFE, KLUN, CDPATH//'%YRVFE'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TCVER (YD%YRCVER, KLUN, CDPATH//'%YRCVER'//TRIM(CLIND))

END SUBROUTINE

