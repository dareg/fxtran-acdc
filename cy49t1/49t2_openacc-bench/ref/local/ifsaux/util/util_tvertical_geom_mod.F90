SUBROUTINE COPY_TVERTICAL_GEOM (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TCVER_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVETA_MOD
USE UTIL_TVFE_MOD
IMPLICIT NONE
TYPE (TVERTICAL_GEOM), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF

CALL COPY_TVAB (YD%YRVAB, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVETA (YD%YRVETA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVFE (YD%YRVFE, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCVER (YD%YRCVER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

