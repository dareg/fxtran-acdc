WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LRDUST), CDPATH//'%LRDUST'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LRCO2), CDPATH//'%LRCO2'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LINITCHEM), CDPATH//'%LINITCHEM'

