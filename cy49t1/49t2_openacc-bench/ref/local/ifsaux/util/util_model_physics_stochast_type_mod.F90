SUBROUTINE COPY_MODEL_PHYSICS_STOCHAST_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TPERTPAR_MOD
USE UTIL_TSTOPH_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_STOCHAST_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_TPERTPAR (YD%YRPERTPAR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSTOPH (YD%YRSTOPH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

