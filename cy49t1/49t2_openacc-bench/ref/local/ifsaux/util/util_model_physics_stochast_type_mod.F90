CALL HOST_TSTOPH (YD%YRSTOPH)

END SUBROUTINE

     

SUBROUTINE CRC64_MODEL_PHYSICS_STOCHAST_TYPE (YD, KLUN, CDPATH)
USE UTIL_TPERTPAR_MOD
USE UTIL_TSTOPH_MOD
