SUBROUTINE CRC64_TCOMPO (YD, KLUN, CDPATH)
USE UTIL_TCOMPO_EMIS_AUX_MOD
USE UTIL_TCOMPO_EMIS_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TCOMPO), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCHEM_DIA), CDPATH//'%LCHEM_DIA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RCHEM_DIA_PERIOD), CDPATH//'%RCHEM_DIA_PERIOD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCHEM_DDFLX), CDPATH//'%LCHEM_DDFLX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCOMPO_DDFLX_DIR), CDPATH//'%LCOMPO_DDFLX_DIR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCHEM_TROPO), CDPATH//'%LCHEM_TROPO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAEREQSAM4CLIM), CDPATH//'%LAEREQSAM4CLIM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAEROSFC), CDPATH//'%LAEROSFC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCOMPO_DCDD), CDPATH//'%LCOMPO_DCDD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAERNITRATE), CDPATH//'%LAERNITRATE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAERRESUSPENSION), CDPATH//'%LAERRESUSPENSION'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAERSOA), CDPATH//'%LAERSOA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAERSOA_COUPLED), CDPATH//'%LAERSOA_COUPLED'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KGHG_CHEMTEND_CH4), CDPATH//'%KGHG_CHEMTEND_CH4'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LAERNUCL), CDPATH//'%LAERNUCL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%AERO_SCHEME), CDPATH//'%AERO_SCHEME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NEMIS2D_DESC), CDPATH//'%NEMIS2D_DESC'

LYEMIS2D_DESC = ALLOCATED (YD%YEMIS2D_DESC)
IF (LYEMIS2D_DESC) THEN
  DO J1 = LBOUND (YD%YEMIS2D_DESC, 1), UBOUND (YD%YEMIS2D_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TCOMPO_EMIS (YD%YEMIS2D_DESC (J1), KLUN, CDPATH//'%YEMIS2D_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NEMIS3D_DESC), CDPATH//'%NEMIS3D_DESC'

LYEMIS3D_DESC = ALLOCATED (YD%YEMIS3D_DESC)
IF (LYEMIS3D_DESC) THEN
  DO J1 = LBOUND (YD%YEMIS3D_DESC, 1), UBOUND (YD%YEMIS3D_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TCOMPO_EMIS (YD%YEMIS3D_DESC (J1), KLUN, CDPATH//'%YEMIS3D_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

LYEMIS2DAUX_DESC = ALLOCATED (YD%YEMIS2DAUX_DESC)
IF (LYEMIS2DAUX_DESC) THEN
  DO J1 = LBOUND (YD%YEMIS2DAUX_DESC, 1), UBOUND (YD%YEMIS2DAUX_DESC, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TCOMPO_EMIS_AUX (YD%YEMIS2DAUX_DESC (J1), KLUN, CDPATH//'%YEMIS2DAUX_DESC'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

