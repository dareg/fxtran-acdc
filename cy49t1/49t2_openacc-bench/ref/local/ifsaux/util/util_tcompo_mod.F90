SUBROUTINE LOAD_TCOMPO (KLUN, YD)
USE UTIL_TCOMPO_EMIS_AUX_MOD
USE UTIL_TCOMPO_EMIS_MOD
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOMPO), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYEMIS2DAUX_DESC, LYEMIS2D_DESC, LYEMIS3D_DESC
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) YD%LCHEM_DIA
READ (KLUN) ZTMP0
YD%RCHEM_DIA_PERIOD = ZTMP0
READ (KLUN) YD%LCHEM_DDFLX
READ (KLUN) YD%LCOMPO_DDFLX_DIR
READ (KLUN) YD%LCHEM_TROPO
READ (KLUN) YD%LAEREQSAM4CLIM
READ (KLUN) YD%LAEROSFC
READ (KLUN) YD%LCOMPO_DCDD
READ (KLUN) YD%LAERNITRATE
READ (KLUN) YD%LAERRESUSPENSION
READ (KLUN) YD%LAERSOA
READ (KLUN) YD%LAERSOA_COUPLED
READ (KLUN) YD%KGHG_CHEMTEND_CH4
READ (KLUN) YD%LAERNUCL
READ (KLUN) YD%AERO_SCHEME
READ (KLUN) YD%NEMIS2D_DESC
READ (KLUN) LYEMIS2D_DESC
IF (LYEMIS2D_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YEMIS2D_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YEMIS2D_DESC, 1), UBOUND (YD%YEMIS2D_DESC, 1)
    CALL LOAD_TCOMPO_EMIS (KLUN, YD%YEMIS2D_DESC (J1))
  ENDDO
ENDIF
READ (KLUN) YD%NEMIS3D_DESC
READ (KLUN) LYEMIS3D_DESC
IF (LYEMIS3D_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YEMIS3D_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YEMIS3D_DESC, 1), UBOUND (YD%YEMIS3D_DESC, 1)
    CALL LOAD_TCOMPO_EMIS (KLUN, YD%YEMIS3D_DESC (J1))
  ENDDO
ENDIF
READ (KLUN) LYEMIS2DAUX_DESC
IF (LYEMIS2DAUX_DESC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YEMIS2DAUX_DESC (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YEMIS2DAUX_DESC, 1), UBOUND (YD%YEMIS2DAUX_DESC, 1)
    CALL LOAD_TCOMPO_EMIS_AUX (KLUN, YD%YEMIS2DAUX_DESC (J1))
  ENDDO
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TCOMPO (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TCOMPO_EMIS_AUX_MOD
