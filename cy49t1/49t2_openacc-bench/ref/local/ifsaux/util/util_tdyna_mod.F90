CALL COPY_TGMVT (YD%YYTGMVT95, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TGFLT (YD%YYTGFLT95, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

