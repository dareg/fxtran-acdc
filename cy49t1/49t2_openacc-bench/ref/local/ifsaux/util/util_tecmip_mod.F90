SUBROUTINE LOAD_TECMIP (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECMIP), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL3(3), IU3(3), IL4(4), IU4(4)
LOGICAL :: LRLATCLI, LRLONCLI, LROZT1, LRPROC1, LRSINC1, LZOZCL
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) YD%NRCP
READ (KLUN) YD%NO3CMIP
READ (KLUN) YD%NGHGCMIP
READ (KLUN) YD%NCMIPFIXYR
READ (KLUN) YD%NCURRYR
READ (KLUN) YD%GHGDATADIR
READ (KLUN) YD%CSOLARDATA
READ (KLUN) YD%CO3DATADIR
READ (KLUN) YD%CO3DATAFIL
READ (KLUN) LZOZCL
IF (LZOZCL) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (YD%ZOZCL (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(YD%ZOZCL,1):UBOUND(YD%ZOZCL,1), LBOUND(YD%ZOZCL,2):UBOUND(YD%ZOZCL,2), LBOUND(YD%ZOZCL,3):UBOUND(YD%ZOZCL,3), LBOUND(YD%ZOZCL,4):UBOUND(YD%ZOZCL,4)))
  READ (KLUN) ZTMP4
  YD%ZOZCL = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LRSINC1
IF (LRSINC1) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RSINC1 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RSINC1,1):UBOUND(YD%RSINC1,1)))
  READ (KLUN) ZTMP1
  YD%RSINC1 = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRPROC1
IF (LRPROC1) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RPROC1 (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RPROC1,1):UBOUND(YD%RPROC1,1)))
  READ (KLUN) ZTMP1
  YD%RPROC1 = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLATCLI
IF (LRLATCLI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLATCLI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RLATCLI,1):UBOUND(YD%RLATCLI,1)))
  READ (KLUN) ZTMP1
  YD%RLATCLI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRLONCLI
IF (LRLONCLI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RLONCLI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RLONCLI,1):UBOUND(YD%RLONCLI,1)))
  READ (KLUN) ZTMP1
  YD%RLONCLI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LROZT1
IF (LROZT1) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%ROZT1 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%ROZT1,1):UBOUND(YD%ROZT1,1), LBOUND(YD%ROZT1,2):UBOUND(YD%ROZT1,2), LBOUND(YD%ROZT1,3):UBOUND(YD%ROZT1,3)))
  READ (KLUN) ZTMP3
  YD%ROZT1 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE


