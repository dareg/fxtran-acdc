SUBROUTINE CRC64_TYPE_SFL_VCLIP (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIP), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYTPC, LYVP, LYWPC
LYTPC = ASSOCIATED (YD%YTPC)
IF (LYTPC) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YTPC, KLUN, CDPATH//'%YTPC'//TRIM(CLIND))
ENDIF

LYWPC = ASSOCIATED (YD%YWPC)
IF (LYWPC) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YWPC, KLUN, CDPATH//'%YWPC'//TRIM(CLIND))
ENDIF

LYVP = ASSOCIATED (YD%YVP)
IF (LYVP) THEN
  DO J1 = LBOUND (YD%YVP, 1), UBOUND (YD%YVP, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (YD%YVP (J1), KLUN, CDPATH//'%YVP'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

