  !$ACC UPDATE DEVICE (SELF%YTPC)
  CALL COPY_TYPE_SURF_MTL_2D (SELF%YTPC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YTPC)
