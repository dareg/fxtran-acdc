LYWPC = ASSOCIATED (SELF%YWPC)
IF (LYWPC) THEN
  !$ACC ENTER DATA CREATE (SELF%YWPC)
  !$ACC UPDATE DEVICE (SELF%YWPC)
  CALL COPY_TYPE_SURF_MTL_2D (SELF%YWPC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YWPC)
ENDIF

LYVP = ASSOCIATED (SELF%YVP)
IF (LYVP) THEN
  !$ACC ENTER DATA CREATE (SELF%YVP)
  !$ACC UPDATE DEVICE (SELF%YVP)
  DO J1 = LBOUND (SELF%YVP, 1), UBOUND (SELF%YVP, 1)
    CALL COPY_TYPE_SURF_MTL_2D (SELF%YVP (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YVP)
