SUBROUTINE SAVE_TYPE_SFL_VCLIP (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VCLIP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYTPC, LYVP, LYWPC
LYTPC = ASSOCIATED (SELF%YTPC)
WRITE (KLUN) LYTPC
IF (LYTPC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YTPC, KLUN)
ENDIF
LYWPC = ASSOCIATED (SELF%YWPC)
WRITE (KLUN) LYWPC
IF (LYWPC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YWPC, KLUN)
ENDIF
LYVP = ASSOCIATED (SELF%YVP)
WRITE (KLUN) LYVP
IF (LYVP) THEN
  WRITE (KLUN) LBOUND (SELF%YVP)
  WRITE (KLUN) UBOUND (SELF%YVP)
  DO J1 = LBOUND (SELF%YVP, 1), UBOUND (SELF%YVP, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (SELF%YVP (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VCLIP (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYTPC, LYVP, LYWPC
READ (KLUN) LYTPC
IF (LYTPC) THEN
  ALLOCATE (SELF%YTPC)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YTPC, KLUN)
ELSE
  NULLIFY (SELF%YTPC)
ENDIF
READ (KLUN) LYWPC
IF (LYWPC) THEN
  ALLOCATE (SELF%YWPC)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YWPC, KLUN)
ELSE
  NULLIFY (SELF%YWPC)
ENDIF
READ (KLUN) LYVP
IF (LYVP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YVP (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YVP, 1), UBOUND (SELF%YVP, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVP (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YVP)
ENDIF
END SUBROUTINE


