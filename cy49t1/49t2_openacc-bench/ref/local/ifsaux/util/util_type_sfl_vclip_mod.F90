SUBROUTINE SAVE_TYPE_SFL_VCLIP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VCLIP), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYTPC, LYVP, LYWPC
LYTPC = ASSOCIATED (YD%YTPC)
WRITE (KLUN) LYTPC
IF (LYTPC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YTPC)
ENDIF
LYWPC = ASSOCIATED (YD%YWPC)
WRITE (KLUN) LYWPC
IF (LYWPC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YWPC)
ENDIF
LYVP = ASSOCIATED (YD%YVP)
WRITE (KLUN) LYVP
IF (LYVP) THEN
  WRITE (KLUN) LBOUND (YD%YVP)
  WRITE (KLUN) UBOUND (YD%YVP)
  DO J1 = LBOUND (YD%YVP, 1), UBOUND (YD%YVP, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YVP (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VCLIP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VCLIP), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYTPC, LYVP, LYWPC
READ (KLUN) LYTPC
IF (LYTPC) THEN
  ALLOCATE (YD%YTPC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YTPC)
ELSE
  NULLIFY (YD%YTPC)
ENDIF
READ (KLUN) LYWPC
IF (LYWPC) THEN
  ALLOCATE (YD%YWPC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YWPC)
ELSE
  NULLIFY (YD%YWPC)
ENDIF
READ (KLUN) LYVP
IF (LYVP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YVP (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YVP, 1), UBOUND (YD%YVP, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVP (J1))
  ENDDO
ELSE
  NULLIFY (YD%YVP)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VCLIP (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VCLIP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYTPC, LYVP, LYWPC

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYTPC = ASSOCIATED (YD%YTPC)
IF (LYTPC) THEN
  !$ACC ENTER DATA CREATE (YD%YTPC)
  !$ACC UPDATE DEVICE (YD%YTPC)
  CALL COPY_TYPE_SURF_MTL_2D (YD%YTPC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YTPC)
ENDIF

LYWPC = ASSOCIATED (YD%YWPC)
IF (LYWPC) THEN
  !$ACC ENTER DATA CREATE (YD%YWPC)
  !$ACC UPDATE DEVICE (YD%YWPC)
  CALL COPY_TYPE_SURF_MTL_2D (YD%YWPC, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YWPC)
ENDIF

LYVP = ASSOCIATED (YD%YVP)
IF (LYVP) THEN
  !$ACC ENTER DATA CREATE (YD%YVP)
  !$ACC UPDATE DEVICE (YD%YVP)
  DO J1 = LBOUND (YD%YVP, 1), UBOUND (YD%YVP, 1)
    CALL COPY_TYPE_SURF_MTL_2D (YD%YVP (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YVP)
ENDIF

END SUBROUTINE

