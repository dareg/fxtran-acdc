SUBROUTINE CRC64_TEGSL (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TEGSL), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RIPORE), CDPATH//'%RIPORE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RJPORE), CDPATH//'%RJPORE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%REGPK), CDPATH//'%REGPK'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RNOBILE), CDPATH//'%RNOBILE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RHSUD), CDPATH//'%RHSUD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RMERCAT), CDPATH//'%RMERCAT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RLON0R), CDPATH//'%RLON0R'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RELONC), CDPATH//'%RELONC'

