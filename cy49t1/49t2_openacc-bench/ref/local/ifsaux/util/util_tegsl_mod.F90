INTEGER*8 FUNCTION SIZE_TEGSL (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TEGSL),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%RIPORE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RIPORE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RJPORE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RJPORE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%REGPK)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REGPK'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RNOBILE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RNOBILE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RHSUD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RHSUD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RMERCAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RMERCAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RLON0R)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLON0R'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RELONC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RELONC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RELATC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RELATC'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

