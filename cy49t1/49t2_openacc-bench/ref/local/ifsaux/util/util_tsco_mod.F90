SUBROUTINE SAVE_TSCO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSCO), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%M_COSCO
WRITE (KLUN) YD%M_SINCO
WRITE (KLUN) YD%M_SINLA
WRITE (KLUN) YD%M_COPHI
WRITE (KLUN) YD%NDIM
END SUBROUTINE

SUBROUTINE LOAD_TSCO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
