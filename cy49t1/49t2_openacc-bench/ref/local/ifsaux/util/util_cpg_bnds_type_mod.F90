SUBROUTINE CRC64_CPG_BNDS_TYPE (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (CPG_BNDS_TYPE), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KIDIA), CDPATH//'%KIDIA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KFDIA), CDPATH//'%KFDIA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KFDIE), CDPATH//'%KFDIE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KIDIE), CDPATH//'%KIDIE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KBL), CDPATH//'%KBL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KSTGLO), CDPATH//'%KSTGLO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%KLON), CDPATH//'%KLON'

