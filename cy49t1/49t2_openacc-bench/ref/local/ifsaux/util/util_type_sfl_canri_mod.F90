MODULE UTIL_TYPE_SFL_CANRI_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_CANRI

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_CANRI
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_CANRI
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_CANRI
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_CANRI
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_CANRI
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_CANRI
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_CANRI (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_CANRI), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYCI
LYCI = ASSOCIATED (YD%YCI)
WRITE (KLUN) LYCI
IF (LYCI) THEN
  WRITE (KLUN) LBOUND (YD%YCI)
  WRITE (KLUN) UBOUND (YD%YCI)
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YCI (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_CANRI (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_CANRI), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYCI
READ (KLUN) LYCI
IF (LYCI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YCI (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YCI (J1))
  ENDDO
ELSE
  NULLIFY (YD%YCI)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_CANRI (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_CANRI), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYCI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYCI = ASSOCIATED (YD%YCI)
IF (LYCI) THEN
  !$ACC ENTER DATA CREATE (YD%YCI)
  !$ACC UPDATE DEVICE (YD%YCI)
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    CALL COPY_TYPE_SURF_MTL_2D (YD%YCI (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YCI)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_CANRI (YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_CANRI), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYCI
LYCI = ASSOCIATED (YD%YCI)
IF (LYCI) THEN
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    CALL HOST_TYPE_SURF_MTL_2D (YD%YCI (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_CANRI (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_CANRI), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYCI
LYCI = ASSOCIATED (YD%YCI)
IF (LYCI) THEN
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (YD%YCI (J1), KLUN, CDPATH//'%YCI'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_CANRI (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_CANRI), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYCI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYCI = ASSOCIATED (YD%YCI)
IF (LYCI) THEN
  !$ACC EXIT DATA DETACH (YD%YCI)
  DO J1 = LBOUND (YD%YCI, 1), UBOUND (YD%YCI, 1)
    CALL WIPE_TYPE_SURF_MTL_2D (YD%YCI (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YCI)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
