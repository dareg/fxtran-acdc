MODULE UTIL_TYPE_SURF_MTL_2D_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SURF_MTL_2D

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SURF_MTL_2D
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SURF_MTL_2D
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SURF_MTL_2D (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, SAVE_TYPE_SURF_MTL
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: YD
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => YD
CALL SAVE_TYPE_SURF_MTL (KLUN, YLSUPER)
WRITE (KLUN) YD%IGRBCODE
WRITE (KLUN) YD%CNAME
WRITE (KLUN) YD%REFVALI
WRITE (KLUN) YD%NREQIN
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SURF_MTL_2D (KLUN, YD)
USE PARKIND1, ONLY : JPRD
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, LOAD_TYPE_SURF_MTL

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SURF_MTL_2D), INTENT (OUT), TARGET :: YD
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
REAL(KIND=JPRD) :: ZTMP0
YLSUPER => YD
CALL LOAD_TYPE_SURF_MTL (KLUN, YLSUPER)
READ (KLUN) YD%IGRBCODE
READ (KLUN) YD%CNAME
READ (KLUN) ZTMP0
YD%REFVALI = ZTMP0
READ (KLUN) YD%NREQIN
END SUBROUTINE


SUBROUTINE COPY_TYPE_SURF_MTL_2D (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, COPY_TYPE_SURF_MTL
IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
YLSUPER => YD
CALL COPY_TYPE_SURF_MTL (YLSUPER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)




END SUBROUTINE

SUBROUTINE HOST_TYPE_SURF_MTL_2D (YD)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, HOST_TYPE_SURF_MTL

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), TARGET :: YD
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => YD
CALL HOST_TYPE_SURF_MTL (YLSUPER)




END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SURF_MTL_2D (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, CRC64_TYPE_SURF_MTL
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => YD
CALL CRC64_TYPE_SURF_MTL (YLSUPER, KLUN, CDPATH)
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%REFVALI), CDPATH//'%REFVALI'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NREQIN), CDPATH//'%NREQIN'

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SURF_MTL_2D (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, WIPE_TYPE_SURF_MTL
IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
YLSUPER => YD
CALL WIPE_TYPE_SURF_MTL (YLSUPER, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)




LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



