MODULE UTIL_TYPE_SURF_MTL_2D_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SURF_MTL_2D

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SURF_MTL_2D
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SURF_MTL_2D
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SURF_MTL_2D
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SURF_MTL_2D (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, SAVE_TYPE_SURF_MTL
IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => SELF
CALL SAVE_TYPE_SURF_MTL (YLSUPER, KLUN)
WRITE (KLUN) SELF%IGRBCODE
WRITE (KLUN) SELF%CNAME
WRITE (KLUN) SELF%REFVALI
WRITE (KLUN) SELF%NREQIN
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SURF_MTL_2D (SELF, KLUN)
USE PARKIND1, ONLY : JPRD
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, LOAD_TYPE_SURF_MTL

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
REAL(KIND=JPRD) :: ZTMP0
YLSUPER => SELF
CALL LOAD_TYPE_SURF_MTL (YLSUPER, KLUN)
READ (KLUN) SELF%IGRBCODE
READ (KLUN) SELF%CNAME
READ (KLUN) ZTMP0
SELF%REFVALI = ZTMP0
READ (KLUN) SELF%NREQIN
END SUBROUTINE


SUBROUTINE COPY_TYPE_SURF_MTL_2D (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, COPY_TYPE_SURF_MTL
IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
YLSUPER => SELF
CALL COPY_TYPE_SURF_MTL (YLSUPER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_TYPE_SURF_MTL_2D (SELF)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, HOST_TYPE_SURF_MTL

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), TARGET :: SELF
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => SELF
CALL HOST_TYPE_SURF_MTL (YLSUPER)

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SURF_MTL_2D (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, CRC64_TYPE_SURF_MTL
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER
YLSUPER => SELF
CALL CRC64_TYPE_SURF_MTL (YLSUPER, KLUN, CDPATH)
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REFVALI), CDPATH//'%REFVALI'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NREQIN), CDPATH//'%NREQIN'

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SURF_MTL_2D (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_MOD, ONLY : TYPE_SURF_MTL, WIPE_TYPE_SURF_MTL
IMPLICIT NONE
TYPE (TYPE_SURF_MTL_2D), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
CLASS (TYPE_SURF_MTL), POINTER :: YLSUPER

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
YLSUPER => SELF
CALL WIPE_TYPE_SURF_MTL (YLSUPER, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
