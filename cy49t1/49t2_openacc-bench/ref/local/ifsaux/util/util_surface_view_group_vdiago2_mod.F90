SUBROUTINE COPY_SURFACE_VIEW_GROUP_VDIAGO2 (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_SURFACE_VARIABLE_GROUP_VDIAGO2_MOD
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VDIAGO2), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP, LF_OCDEP, LF_USTRC, LF_VSTRC, LVARIABLE_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LVARIABLE_GROUP = ASSOCIATED (YD%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC ENTER DATA CREATE (YD%VARIABLE_GROUP)
  !$ACC UPDATE DEVICE (YD%VARIABLE_GROUP)
  CALL COPY_SURFACE_VARIABLE_GROUP_VDIAGO2 (YD%VARIABLE_GROUP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%VARIABLE_GROUP)
ENDIF

LF_OCDEP = ASSOCIATED (YD%F_OCDEP)
IF (LF_OCDEP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_OCDEP)
  !$ACC ENTER DATA ATTACH (YD%F_OCDEP)
ENDIF

LF_USTRC = ASSOCIATED (YD%F_USTRC)
IF (LF_USTRC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_USTRC)
  !$ACC ENTER DATA ATTACH (YD%F_USTRC)
ENDIF

LF_VSTRC = ASSOCIATED (YD%F_VSTRC)
IF (LF_VSTRC .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_VSTRC)
  !$ACC ENTER DATA ATTACH (YD%F_VSTRC)
ENDIF

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_GROUP)
  !$ACC ENTER DATA ATTACH (YD%F_GROUP)
ENDIF

END SUBROUTINE

