LF_VSTRC = ASSOCIATED (YD%F_VSTRC)
IF (LF_VSTRC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_VSTRC)
ENDIF

LF_GROUP = ASSOCIATED (YD%F_GROUP)
