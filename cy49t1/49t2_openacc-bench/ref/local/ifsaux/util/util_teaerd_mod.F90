SUBROUTINE LOAD_TEAERD (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERD), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LCVDAED, LCVDAEL, LCVDAES, LCVDAEU
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LCVDAES
IF (LCVDAES) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%CVDAES (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%CVDAES,1):UBOUND(YD%CVDAES,1)))
  READ (KLUN) ZTMP1
  YD%CVDAES = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAEL
IF (LCVDAEL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%CVDAEL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%CVDAEL,1):UBOUND(YD%CVDAEL,1)))
  READ (KLUN) ZTMP1
  YD%CVDAEL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAEU
IF (LCVDAEU) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%CVDAEU (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%CVDAEU,1):UBOUND(YD%CVDAEU,1)))
  READ (KLUN) ZTMP1
  YD%CVDAEU = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCVDAED
IF (LCVDAED) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%CVDAED (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%CVDAED,1):UBOUND(YD%CVDAED,1)))
  READ (KLUN) ZTMP1
  YD%CVDAED = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
ALLOCATE (ZTMP1(LBOUND(YD%RAESC,1):UBOUND(YD%RAESC,1)))
READ (KLUN) ZTMP1
YD%RAESC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAESS,1):UBOUND(YD%RAESS,1)))
READ (KLUN) ZTMP1
YD%RAESS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAELC,1):UBOUND(YD%RAELC,1)))
READ (KLUN) ZTMP1
YD%RAELC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAELS,1):UBOUND(YD%RAELS,1)))
READ (KLUN) ZTMP1
YD%RAELS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAEUC,1):UBOUND(YD%RAEUC,1)))
READ (KLUN) ZTMP1
YD%RAEUC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAEUS,1):UBOUND(YD%RAEUS,1)))
READ (KLUN) ZTMP1
YD%RAEUS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAEDC,1):UBOUND(YD%RAEDC,1)))
READ (KLUN) ZTMP1
YD%RAEDC = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RAEDS,1):UBOUND(YD%RAEDS,1)))
READ (KLUN) ZTMP1
YD%RAEDS = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%RCAEOPS = ZTMP0
READ (KLUN) ZTMP0
YD%RCAEOPL = ZTMP0
READ (KLUN) ZTMP0
YD%RCAEOPU = ZTMP0
READ (KLUN) ZTMP0
YD%RCAEOPD = ZTMP0
READ (KLUN) ZTMP0
YD%RCTRBGA = ZTMP0
READ (KLUN) ZTMP0
YD%RCVOBGA = ZTMP0
READ (KLUN) ZTMP0
YD%RCSTBGA = ZTMP0
READ (KLUN) ZTMP0
YD%RCTRPT = ZTMP0
ALLOCATE (ZTMP1(LBOUND(YD%RCAEADK,1):UBOUND(YD%RCAEADK,1)))
READ (KLUN) ZTMP1
YD%RCAEADK = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%RCAEADM = ZTMP0
READ (KLUN) ZTMP0
YD%RCAEROS = ZTMP0
END SUBROUTINE


