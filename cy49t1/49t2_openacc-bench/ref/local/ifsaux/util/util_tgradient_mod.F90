MODULE UTIL_TGRADIENT_MOD

USE YOMDGRADIENT, ONLY : TGRADIENT

INTERFACE SAVE
MODULE PROCEDURE SAVE_TGRADIENT
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TGRADIENT
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TGRADIENT
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TGRADIENT
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TGRADIENT
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TGRADIENT
END INTERFACE

INTERFACE SIZE
MODULE PROCEDURE SIZE_TGRADIENT
END INTERFACE


CONTAINS

SUBROUTINE SAVE_TGRADIENT (KLUN, YD)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TGRADIENT), INTENT (IN), TARGET :: YD

CALL SAVE_TGRADIENT_PTR (KLUN, YD%YPTR)
END SUBROUTINE

SUBROUTINE LOAD_TGRADIENT (KLUN, YD)
USE UTIL_TGRADIENT_PTR_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TGRADIENT), INTENT (OUT), TARGET :: YD

CALL LOAD_TGRADIENT_PTR (KLUN, YD%YPTR)
END SUBROUTINE


SUBROUTINE COPY_TGRADIENT (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_TGRADIENT_PTR (YD%YPTR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_TGRADIENT (YD)
USE UTIL_TGRADIENT_PTR_MOD

IMPLICIT NONE
TYPE (TGRADIENT), TARGET :: YD

CALL HOST_TGRADIENT_PTR (YD%YPTR)

END SUBROUTINE

     

SUBROUTINE CRC64_TGRADIENT (YD, KLUN, CDPATH)
USE UTIL_TGRADIENT_PTR_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TGRADIENT), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

CLIND = ''
CALL CRC64_TGRADIENT_PTR (YD%YPTR, KLUN, CDPATH//'%YPTR'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE WIPE_TGRADIENT (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_TGRADIENT_PTR (YD%YPTR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_TGRADIENT (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TGRADIENT_PTR_MOD
IMPLICIT NONE
TYPE (TGRADIENT),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TGRADIENT_PTR (YD%YPTR, CLPATH//'%YPTR', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YPTR'
ENDIF
END FUNCTION

END MODULE
