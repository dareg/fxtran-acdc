SUBROUTINE CRC64_TTND (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TTND), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDU), CDPATH//'%M_TNDU'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDV), CDPATH//'%M_TNDV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDU_NOC), CDPATH//'%M_TNDU_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDV_NOC), CDPATH//'%M_TNDV_NOC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDT), CDPATH//'%M_TNDT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDPD), CDPATH//'%M_TNDPD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDVD), CDPATH//'%M_TNDVD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_TNDGW), CDPATH//'%M_TNDGW'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

