SUBROUTINE SAVE_TYPE_SFL_VDIAGO2 (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VDIAGO2), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYOCDEP, LYUSTRC, LYV2, LYVSTRC
LYOCDEP = ASSOCIATED (YD%YOCDEP)
WRITE (KLUN) LYOCDEP
IF (LYOCDEP) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YOCDEP)
ENDIF
LYUSTRC = ASSOCIATED (YD%YUSTRC)
WRITE (KLUN) LYUSTRC
IF (LYUSTRC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YUSTRC)
ENDIF
LYVSTRC = ASSOCIATED (YD%YVSTRC)
WRITE (KLUN) LYVSTRC
IF (LYVSTRC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YVSTRC)
ENDIF
LYV2 = ASSOCIATED (YD%YV2)
WRITE (KLUN) LYV2
IF (LYV2) THEN
  WRITE (KLUN) LBOUND (YD%YV2)
  WRITE (KLUN) UBOUND (YD%YV2)
  DO J1 = LBOUND (YD%YV2, 1), UBOUND (YD%YV2, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YV2 (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VDIAGO2 (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VDIAGO2), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYOCDEP, LYUSTRC, LYV2, LYVSTRC
READ (KLUN) LYOCDEP
IF (LYOCDEP) THEN
  ALLOCATE (YD%YOCDEP)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YOCDEP)
ELSE
  NULLIFY (YD%YOCDEP)
ENDIF
READ (KLUN) LYUSTRC
IF (LYUSTRC) THEN
  ALLOCATE (YD%YUSTRC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YUSTRC)
ELSE
  NULLIFY (YD%YUSTRC)
ENDIF
READ (KLUN) LYVSTRC
IF (LYVSTRC) THEN
  ALLOCATE (YD%YVSTRC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVSTRC)
ELSE
  NULLIFY (YD%YVSTRC)
ENDIF
READ (KLUN) LYV2
IF (LYV2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YV2 (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YV2, 1), UBOUND (YD%YV2, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YV2 (J1))
  ENDDO
ELSE
  NULLIFY (YD%YV2)
ENDIF
END SUBROUTINE


