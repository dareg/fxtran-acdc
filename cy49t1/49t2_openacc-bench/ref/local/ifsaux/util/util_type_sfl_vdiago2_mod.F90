SUBROUTINE CRC64_TYPE_SFL_VDIAGO2 (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VDIAGO2), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYOCDEP, LYUSTRC, LYV2, LYVSTRC
LYOCDEP = ASSOCIATED (YD%YOCDEP)
IF (LYOCDEP) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YOCDEP, KLUN, CDPATH//'%YOCDEP'//TRIM(CLIND))
ENDIF

LYUSTRC = ASSOCIATED (YD%YUSTRC)
IF (LYUSTRC) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YUSTRC, KLUN, CDPATH//'%YUSTRC'//TRIM(CLIND))
ENDIF

LYVSTRC = ASSOCIATED (YD%YVSTRC)
IF (LYVSTRC) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YVSTRC, KLUN, CDPATH//'%YVSTRC'//TRIM(CLIND))
ENDIF

LYV2 = ASSOCIATED (YD%YV2)
IF (LYV2) THEN
  DO J1 = LBOUND (YD%YV2, 1), UBOUND (YD%YV2, 1)
