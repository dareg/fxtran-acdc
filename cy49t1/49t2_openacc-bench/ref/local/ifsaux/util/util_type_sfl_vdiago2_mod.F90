SUBROUTINE SAVE_TYPE_SFL_VDIAGO2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VDIAGO2), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYOCDEP, LYUSTRC, LYV2, LYVSTRC
LYOCDEP = ASSOCIATED (SELF%YOCDEP)
WRITE (KLUN) LYOCDEP
IF (LYOCDEP) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YOCDEP, KLUN)
ENDIF
LYUSTRC = ASSOCIATED (SELF%YUSTRC)
WRITE (KLUN) LYUSTRC
IF (LYUSTRC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YUSTRC, KLUN)
ENDIF
LYVSTRC = ASSOCIATED (SELF%YVSTRC)
WRITE (KLUN) LYVSTRC
IF (LYVSTRC) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YVSTRC, KLUN)
ENDIF
LYV2 = ASSOCIATED (SELF%YV2)
WRITE (KLUN) LYV2
IF (LYV2) THEN
  WRITE (KLUN) LBOUND (SELF%YV2)
  WRITE (KLUN) UBOUND (SELF%YV2)
  DO J1 = LBOUND (SELF%YV2, 1), UBOUND (SELF%YV2, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (SELF%YV2 (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VDIAGO2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VDIAGO2), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYOCDEP, LYUSTRC, LYV2, LYVSTRC
READ (KLUN) LYOCDEP
IF (LYOCDEP) THEN
  ALLOCATE (SELF%YOCDEP)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YOCDEP, KLUN)
ELSE
  NULLIFY (SELF%YOCDEP)
ENDIF
READ (KLUN) LYUSTRC
IF (LYUSTRC) THEN
  ALLOCATE (SELF%YUSTRC)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YUSTRC, KLUN)
ELSE
  NULLIFY (SELF%YUSTRC)
ENDIF
READ (KLUN) LYVSTRC
IF (LYVSTRC) THEN
  ALLOCATE (SELF%YVSTRC)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVSTRC, KLUN)
ELSE
  NULLIFY (SELF%YVSTRC)
ENDIF
READ (KLUN) LYV2
IF (LYV2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YV2 (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YV2, 1), UBOUND (SELF%YV2, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YV2 (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YV2)
ENDIF
END SUBROUTINE


