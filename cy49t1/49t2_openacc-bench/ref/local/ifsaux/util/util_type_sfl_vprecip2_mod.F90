SUBROUTINE CRC64_TYPE_SFL_VPRECIP2 (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2
LYPRECIP2 = ASSOCIATED (SELF%YPRECIP2)
IF (LYPRECIP2) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YPRECIP2, KLUN, CDPATH//'%YPRECIP2'//TRIM(CLIND))
ENDIF

LYXP2 = ASSOCIATED (SELF%YXP2)
IF (LYXP2) THEN
  DO J1 = LBOUND (SELF%YXP2, 1), UBOUND (SELF%YXP2, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (SELF%YXP2 (J1), KLUN, CDPATH//'%YXP2'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

