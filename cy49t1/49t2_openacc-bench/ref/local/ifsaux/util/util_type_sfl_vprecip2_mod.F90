IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYPRECIP2 = ASSOCIATED (YD%YPRECIP2)
IF (LYPRECIP2) THEN
  !$ACC EXIT DATA DETACH (YD%YPRECIP2)
  CALL WIPE_TYPE_SURF_MTL_3D (YD%YPRECIP2, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC EXIT DATA DETACH (YD%YPRECIP2)
ENDIF

LYXP2 = ASSOCIATED (YD%YXP2)
IF (LYXP2) THEN
  !$ACC EXIT DATA DETACH (YD%YXP2)
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    CALL WIPE_TYPE_SURF_MTL_3D (YD%YXP2 (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YXP2)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



