LYPRECIP2 = ASSOCIATED (YD%YPRECIP2)
IF (LYPRECIP2) THEN
  CALL HOST_TYPE_SURF_MTL_3D (YD%YPRECIP2)
ENDIF

LYXP2 = ASSOCIATED (YD%YXP2)
IF (LYXP2) THEN
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    CALL HOST_TYPE_SURF_MTL_3D (YD%YXP2 (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_VPRECIP2 (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2
LYPRECIP2 = ASSOCIATED (YD%YPRECIP2)
IF (LYPRECIP2) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YPRECIP2, KLUN, CDPATH//'%YPRECIP2'//TRIM(CLIND))
ENDIF

LYXP2 = ASSOCIATED (YD%YXP2)
IF (LYXP2) THEN
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YXP2 (J1), KLUN, CDPATH//'%YXP2'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_VPRECIP2 (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
