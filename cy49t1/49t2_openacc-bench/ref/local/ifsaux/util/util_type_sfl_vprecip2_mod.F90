CONTAINS

SUBROUTINE SAVE_TYPE_SFL_VPRECIP2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2
LYPRECIP2 = ASSOCIATED (SELF%YPRECIP2)
WRITE (KLUN) LYPRECIP2
IF (LYPRECIP2) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YPRECIP2, KLUN)
ENDIF
LYXP2 = ASSOCIATED (SELF%YXP2)
WRITE (KLUN) LYXP2
IF (LYXP2) THEN
  WRITE (KLUN) LBOUND (SELF%YXP2)
  WRITE (KLUN) UBOUND (SELF%YXP2)
  DO J1 = LBOUND (SELF%YXP2, 1), UBOUND (SELF%YXP2, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (SELF%YXP2 (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VPRECIP2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYPRECIP2, LYXP2
READ (KLUN) LYPRECIP2
IF (LYPRECIP2) THEN
  ALLOCATE (SELF%YPRECIP2)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YPRECIP2, KLUN)
ELSE
  NULLIFY (SELF%YPRECIP2)
ENDIF
READ (KLUN) LYXP2
IF (LYXP2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YXP2 (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YXP2, 1), UBOUND (SELF%YXP2, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (SELF%YXP2 (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YXP2)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VPRECIP2 (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LYPRECIP2 = ASSOCIATED (SELF%YPRECIP2)
IF (LYPRECIP2) THEN
  !$ACC ENTER DATA CREATE (SELF%YPRECIP2)
  !$ACC UPDATE DEVICE (SELF%YPRECIP2)
  CALL COPY_TYPE_SURF_MTL_3D (SELF%YPRECIP2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YPRECIP2)
ENDIF

LYXP2 = ASSOCIATED (SELF%YXP2)
IF (LYXP2) THEN
  !$ACC ENTER DATA CREATE (SELF%YXP2)
  !$ACC UPDATE DEVICE (SELF%YXP2)
  DO J1 = LBOUND (SELF%YXP2, 1), UBOUND (SELF%YXP2, 1)
    CALL COPY_TYPE_SURF_MTL_3D (SELF%YXP2 (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YXP2)
ENDIF

END SUBROUTINE

