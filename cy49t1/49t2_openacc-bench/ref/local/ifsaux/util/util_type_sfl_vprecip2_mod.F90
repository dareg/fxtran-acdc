MODULE UTIL_TYPE_SFL_VPRECIP2_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_VPRECIP2

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_VPRECIP2
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_VPRECIP2
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_VPRECIP2
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_VPRECIP2
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_VPRECIP2
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_VPRECIP2
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_VPRECIP2 (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VPRECIP2), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2
LYPRECIP2 = ASSOCIATED (YD%YPRECIP2)
WRITE (KLUN) LYPRECIP2
IF (LYPRECIP2) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YPRECIP2)
ENDIF
LYXP2 = ASSOCIATED (YD%YXP2)
WRITE (KLUN) LYXP2
IF (LYXP2) THEN
  WRITE (KLUN) LBOUND (YD%YXP2)
  WRITE (KLUN) UBOUND (YD%YXP2)
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YXP2 (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VPRECIP2 (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VPRECIP2), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYPRECIP2, LYXP2
READ (KLUN) LYPRECIP2
IF (LYPRECIP2) THEN
  ALLOCATE (YD%YPRECIP2)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YPRECIP2)
ELSE
  NULLIFY (YD%YPRECIP2)
ENDIF
READ (KLUN) LYXP2
IF (LYXP2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YXP2 (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YXP2 (J1))
  ENDDO
ELSE
  NULLIFY (YD%YXP2)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VPRECIP2 (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP2), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYPRECIP2, LYXP2

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYPRECIP2 = ASSOCIATED (YD%YPRECIP2)
IF (LYPRECIP2) THEN
  !$ACC ENTER DATA CREATE (YD%YPRECIP2)
  !$ACC UPDATE DEVICE (YD%YPRECIP2)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YPRECIP2, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YPRECIP2)
ENDIF

LYXP2 = ASSOCIATED (YD%YXP2)
IF (LYXP2) THEN
  !$ACC ENTER DATA CREATE (YD%YXP2)
  !$ACC UPDATE DEVICE (YD%YXP2)
  DO J1 = LBOUND (YD%YXP2, 1), UBOUND (YD%YXP2, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YXP2 (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YXP2)
ENDIF

END SUBROUTINE

