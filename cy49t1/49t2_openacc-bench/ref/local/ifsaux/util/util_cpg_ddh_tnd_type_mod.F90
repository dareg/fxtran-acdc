MODULE UTIL_CPG_DDH_TND_TYPE_MOD

USE CPG_DDH_TND_TYPE_MOD, ONLY : CPG_DDH_TND_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_DDH_TND_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_DDH_TND_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_DDH_TND_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_DDH_TND_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_CPG_TNDSI_DDH_TYPE (YD%YSI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_TNDHD_DDH_TYPE (YD%YHD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_TNDSL_DDH_TYPE (YD%YSL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_CPG_DDH_TND_TYPE (YD)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD

IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), TARGET :: YD

CALL HOST_CPG_TNDSI_DDH_TYPE (YD%YSI)

CALL HOST_CPG_TNDHD_DDH_TYPE (YD%YHD)

CALL HOST_CPG_TNDSL_DDH_TYPE (YD%YSL)

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_DDH_TND_TYPE (YD, LDDELETED, LDFIELDAPI)
USE UTIL_CPG_TNDHD_DDH_TYPE_MOD
USE UTIL_CPG_TNDSI_DDH_TYPE_MOD
USE UTIL_CPG_TNDSL_DDH_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_DDH_TND_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_CPG_TNDSI_DDH_TYPE (YD%YSI, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_TNDHD_DDH_TYPE (YD%YHD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_TNDSL_DDH_TYPE (YD%YSL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
