END SUBROUTINE

SUBROUTINE LOAD_TVSPLIP (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TVSPLIP), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LRFVV, LRVSPC, LRVSPTRI
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LRVSPTRI
IF (LRVSPTRI) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RVSPTRI (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RVSPTRI,1):UBOUND(YD%RVSPTRI,1), LBOUND(YD%RVSPTRI,2):UBOUND(YD%RVSPTRI,2)))
  READ (KLUN) ZTMP2
  YD%RVSPTRI = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LRVSPC
IF (LRVSPC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RVSPC (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RVSPC,1):UBOUND(YD%RVSPC,1)))
  READ (KLUN) ZTMP1
  YD%RVSPC = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LRFVV
IF (LRFVV) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RFVV (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RFVV,1):UBOUND(YD%RFVV,1), LBOUND(YD%RFVV,2):UBOUND(YD%RFVV,2), LBOUND(YD%RFVV,3):UBOUND(YD%RFVV,3)))
  READ (KLUN) ZTMP3
  YD%RFVV = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE


