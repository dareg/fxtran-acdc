INTEGER*8 FUNCTION SIZE_TVSPLIP (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TVSPLIP),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRFVV, LRVSPC, LRVSPTRI

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRVSPTRI = ALLOCATED (YD%RVSPTRI)
IF (LRVSPTRI) THEN
  ISIZE = KIND (YD%RVSPTRI) * SIZE (YD%RVSPTRI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RVSPTRI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRVSPC = ALLOCATED (YD%RVSPC)
IF (LRVSPC) THEN
  ISIZE = KIND (YD%RVSPC) * SIZE (YD%RVSPC)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RVSPC'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRFVV = ALLOCATED (YD%RFVV)
IF (LRFVV) THEN
  ISIZE = KIND (YD%RFVV) * SIZE (YD%RFVV)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RFVV'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

