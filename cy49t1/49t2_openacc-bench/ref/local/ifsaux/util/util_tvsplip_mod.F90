SUBROUTINE SAVE_TVSPLIP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TVSPLIP), INTENT (IN), TARGET :: YD
LOGICAL :: LRFVV, LRVSPC, LRVSPTRI
LRVSPTRI = ALLOCATED (YD%RVSPTRI)
WRITE (KLUN) LRVSPTRI
IF (LRVSPTRI) THEN
  WRITE (KLUN) LBOUND (YD%RVSPTRI)
  WRITE (KLUN) UBOUND (YD%RVSPTRI)
