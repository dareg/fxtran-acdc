SUBROUTINE SAVE_TERIP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TERIP), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%RCODECM
WRITE (KLUN) YD%RSIDECM
WRITE (KLUN) YD%RCOVSRM
WRITE (KLUN) YD%RSIVSRM
WRITE (KLUN) YD%RSOVRM
WRITE (KLUN) YD%RWSOVRM
END SUBROUTINE

SUBROUTINE LOAD_TERIP (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TERIP), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) ZTMP0
YD%RCODECM = ZTMP0
READ (KLUN) ZTMP0
YD%RSIDECM = ZTMP0
READ (KLUN) ZTMP0
YD%RCOVSRM = ZTMP0
READ (KLUN) ZTMP0
YD%RSIVSRM = ZTMP0
READ (KLUN) ZTMP0
YD%RSOVRM = ZTMP0
READ (KLUN) ZTMP0
YD%RWSOVRM = ZTMP0
END SUBROUTINE


