SUBROUTINE CRC64_TERIP (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TERIP), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RCODECM), CDPATH//'%RCODECM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RSIDECM), CDPATH//'%RSIDECM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RCOVSRM), CDPATH//'%RCOVSRM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RSIVSRM), CDPATH//'%RSIVSRM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RSOVRM), CDPATH//'%RSOVRM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RWSOVRM), CDPATH//'%RWSOVRM'

END SUBROUTINE

SUBROUTINE WIPE_TERIP (YD, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TERIP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF






LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_TERIP (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TERIP),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%RCODECM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RCODECM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RSIDECM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSIDECM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RCOVSRM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RCOVSRM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RSIVSRM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSIVSRM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RSOVRM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSOVRM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RWSOVRM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RWSOVRM'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

