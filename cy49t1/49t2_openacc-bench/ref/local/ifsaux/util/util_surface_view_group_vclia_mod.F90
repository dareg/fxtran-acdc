SUBROUTINE COPY_SURFACE_VIEW_GROUP_VCLIA (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_SURFACE_VARIABLE_GROUP_VCLIA_MOD
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIA), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_DES, LF_GROUP, LF_LAN, LF_SEA, LF_SOO, LF_SUL, LF_VOL, LVARIABLE_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LVARIABLE_GROUP = ASSOCIATED (YD%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC ENTER DATA CREATE (YD%VARIABLE_GROUP)
  !$ACC UPDATE DEVICE (YD%VARIABLE_GROUP)
  CALL COPY_SURFACE_VARIABLE_GROUP_VCLIA (YD%VARIABLE_GROUP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%VARIABLE_GROUP)
ENDIF

LF_SEA = ASSOCIATED (YD%F_SEA)
IF (LF_SEA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_SEA)
  !$ACC ENTER DATA ATTACH (YD%F_SEA)
ENDIF

LF_LAN = ASSOCIATED (YD%F_LAN)
IF (LF_LAN .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_LAN)
  !$ACC ENTER DATA ATTACH (YD%F_LAN)
ENDIF

LF_SOO = ASSOCIATED (YD%F_SOO)
IF (LF_SOO .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_SOO)
  !$ACC ENTER DATA ATTACH (YD%F_SOO)
ENDIF

LF_DES = ASSOCIATED (YD%F_DES)
IF (LF_DES .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_DES)
  !$ACC ENTER DATA ATTACH (YD%F_DES)
ENDIF

LF_SUL = ASSOCIATED (YD%F_SUL)
IF (LF_SUL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_SUL)
  !$ACC ENTER DATA ATTACH (YD%F_SUL)
ENDIF

LF_VOL = ASSOCIATED (YD%F_VOL)
IF (LF_VOL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_VOL)
  !$ACC ENTER DATA ATTACH (YD%F_VOL)
ENDIF

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_GROUP)
  !$ACC ENTER DATA ATTACH (YD%F_GROUP)
ENDIF

END SUBROUTINE

