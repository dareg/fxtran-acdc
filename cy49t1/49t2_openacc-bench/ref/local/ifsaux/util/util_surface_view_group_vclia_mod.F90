  CALL WIPE_FIELD_2RB (YD%F_LAN)
ENDIF

LF_SOO = ASSOCIATED (YD%F_SOO)
IF (LF_SOO .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_SOO)
ENDIF

LF_DES = ASSOCIATED (YD%F_DES)
IF (LF_DES .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_DES)
ENDIF

LF_SUL = ASSOCIATED (YD%F_SUL)
IF (LF_SUL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_SUL)
ENDIF

LF_VOL = ASSOCIATED (YD%F_VOL)
IF (LF_VOL .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_VOL)
ENDIF

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



