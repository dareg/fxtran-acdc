SUBROUTINE CRC64_TEMCICA (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TEMCICA), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%XCW), CDPATH//'%XCW'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%XCW_D), CDPATH//'%XCW_D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NMCI1), CDPATH//'%NMCI1'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NMCI2), CDPATH//'%NMCI2'

END SUBROUTINE

