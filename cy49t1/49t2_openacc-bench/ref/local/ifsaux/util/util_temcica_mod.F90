SUBROUTINE SAVE_TEMCICA (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEMCICA), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%XCW
WRITE (KLUN) YD%XCW_D
WRITE (KLUN) YD%NMCI1
WRITE (KLUN) YD%NMCI2
END SUBROUTINE

SUBROUTINE LOAD_TEMCICA (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEMCICA), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
ALLOCATE (ZTMP2(LBOUND(YD%XCW,1):UBOUND(YD%XCW,1), LBOUND(YD%XCW,2):UBOUND(YD%XCW,2)))
READ (KLUN) ZTMP2
YD%XCW = ZTMP2
DEALLOCATE (ZTMP2)
READ (KLUN) YD%XCW_D
READ (KLUN) YD%NMCI1
READ (KLUN) YD%NMCI2
END SUBROUTINE


