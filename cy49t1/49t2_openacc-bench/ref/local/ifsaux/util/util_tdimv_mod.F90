INTEGER*8 FUNCTION SIZE_TDIMV (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TDIMV),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NFLEVG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLEVL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLEVLMX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEVLMX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLSUR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSUR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLSUL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSUL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLSA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLSA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFLEN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFLEN'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NIOLEVG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NIOLEVG'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

