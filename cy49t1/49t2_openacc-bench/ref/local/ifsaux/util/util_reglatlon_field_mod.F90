INTEGER*8 FUNCTION SIZE_REGLATLON_FIELD (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (REGLATLON_FIELD),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LPFLD, LPLAT, LPLON, LPSIN

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NLAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLON'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%DLAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DLAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%DLON)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%DLON'
ENDIF
KSIZE = KSIZE + ISIZE
LPFLD = ASSOCIATED (YD%PFLD)
IF (LPFLD) THEN
  ISIZE = KIND (YD%PFLD) * SIZE (YD%PFLD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PFLD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPLAT = ASSOCIATED (YD%PLAT)
IF (LPLAT) THEN
  ISIZE = KIND (YD%PLAT) * SIZE (YD%PLAT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLAT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPSIN = ASSOCIATED (YD%PSIN)
IF (LPSIN) THEN
  ISIZE = KIND (YD%PSIN) * SIZE (YD%PSIN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PSIN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LPLON = ASSOCIATED (YD%PLON)
IF (LPLON) THEN
  ISIZE = KIND (YD%PLON) * SIZE (YD%PLON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%PLON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

