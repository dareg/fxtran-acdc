SUBROUTINE LOAD_REGLATLON_FIELD (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (REGLATLON_FIELD), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LPFLD, LPLAT, LPLON, LPSIN
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%NLAT
READ (KLUN) YD%NLON
READ (KLUN) ZTMP0
YD%DLAT = ZTMP0
READ (KLUN) ZTMP0
YD%DLON = ZTMP0
READ (KLUN) LPFLD
IF (LPFLD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%PFLD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%PFLD,1):UBOUND(YD%PFLD,1), LBOUND(YD%PFLD,2):UBOUND(YD%PFLD,2)))
  READ (KLUN) ZTMP2
  YD%PFLD = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (YD%PFLD)
ENDIF
READ (KLUN) LPLAT
IF (LPLAT) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%PLAT (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%PLAT,1):UBOUND(YD%PLAT,1)))
  READ (KLUN) ZTMP1
  YD%PLAT = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%PLAT)
ENDIF
READ (KLUN) LPSIN
IF (LPSIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%PSIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%PSIN,1):UBOUND(YD%PSIN,1)))
  READ (KLUN) ZTMP1
  YD%PSIN = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%PSIN)
ENDIF
READ (KLUN) LPLON
IF (LPLON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%PLON (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%PLON,1):UBOUND(YD%PLON,1)))
  READ (KLUN) ZTMP1
  YD%PLON = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%PLON)
ENDIF
END SUBROUTINE


