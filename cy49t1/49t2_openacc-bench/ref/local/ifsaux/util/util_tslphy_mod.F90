SUBROUTINE SAVE_TSLPHY (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSLPHY), INTENT (IN), TARGET :: YD
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5
LRSLWX = ALLOCATED (YD%RSLWX)
WRITE (KLUN) LRSLWX
IF (LRSLWX) THEN
  WRITE (KLUN) LBOUND (YD%RSLWX)
  WRITE (KLUN) UBOUND (YD%RSLWX)
  WRITE (KLUN) YD%RSLWX
ENDIF
WRITE (KLUN) YD%NVTEND
LSAVTEND = ALLOCATED (YD%SAVTEND)
WRITE (KLUN) LSAVTEND
IF (LSAVTEND) THEN
  WRITE (KLUN) LBOUND (YD%SAVTEND)
  WRITE (KLUN) UBOUND (YD%SAVTEND)
  WRITE (KLUN) YD%SAVTEND
ENDIF
LSAVTEND5 = ALLOCATED (YD%SAVTEND5)
WRITE (KLUN) LSAVTEND5
IF (LSAVTEND5) THEN
  WRITE (KLUN) LBOUND (YD%SAVTEND5)
  WRITE (KLUN) UBOUND (YD%SAVTEND5)
  WRITE (KLUN) YD%SAVTEND5
ENDIF
WRITE (KLUN) YD%MU_SAVTEND
WRITE (KLUN) YD%MV_SAVTEND
WRITE (KLUN) YD%MT_SAVTEND
