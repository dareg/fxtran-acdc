SUBROUTINE LOAD_TSLPHY (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSLPHY), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL4(4), IU4(4)
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP4 (:,:,:,:)
READ (KLUN) LRSLWX
IF (LRSLWX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RSLWX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RSLWX,1):UBOUND(YD%RSLWX,1)))
  READ (KLUN) ZTMP1
  YD%RSLWX = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) YD%NVTEND
READ (KLUN) LSAVTEND
IF (LSAVTEND) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (YD%SAVTEND (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(YD%SAVTEND,1):UBOUND(YD%SAVTEND,1), LBOUND(YD%SAVTEND,2):UBOUND(YD%SAVTEND,2), LBOUND(YD%SAVTEND,3):UBOUND(YD%SAVTEND,3), LBOUND(YD%SAVTEND,4):UBOUND(YD%SAVTEND,4)))
  READ (KLUN) ZTMP4
  YD%SAVTEND = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) LSAVTEND5
IF (LSAVTEND5) THEN
  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (YD%SAVTEND5 (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(YD%SAVTEND5,1):UBOUND(YD%SAVTEND5,1), LBOUND(YD%SAVTEND5,2):UBOUND(YD%SAVTEND5,2), LBOUND(YD%SAVTEND5,3):UBOUND(YD%SAVTEND5,3), LBOUND(YD%SAVTEND5,4):UBOUND(YD%SAVTEND5,4)))
  READ (KLUN) ZTMP4
  YD%SAVTEND5 = ZTMP4
  DEALLOCATE (ZTMP4)
ENDIF
READ (KLUN) YD%MU_SAVTEND
READ (KLUN) YD%MV_SAVTEND
READ (KLUN) YD%MT_SAVTEND
END SUBROUTINE


