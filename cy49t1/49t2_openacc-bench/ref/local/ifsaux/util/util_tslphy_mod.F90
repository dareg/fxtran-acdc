INTEGER*8 FUNCTION SIZE_TSLPHY (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSLPHY),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRSLWX, LSAVTEND, LSAVTEND5

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRSLWX = ALLOCATED (YD%RSLWX)
IF (LRSLWX) THEN
  ISIZE = KIND (YD%RSLWX) * SIZE (YD%RSLWX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSLWX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%NVTEND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVTEND'
ENDIF
KSIZE = KSIZE + ISIZE
LSAVTEND = ALLOCATED (YD%SAVTEND)
IF (LSAVTEND) THEN
  ISIZE = KIND (YD%SAVTEND) * SIZE (YD%SAVTEND)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SAVTEND'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSAVTEND5 = ALLOCATED (YD%SAVTEND5)
IF (LSAVTEND5) THEN
  ISIZE = KIND (YD%SAVTEND5) * SIZE (YD%SAVTEND5)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SAVTEND5'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%MU_SAVTEND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MU_SAVTEND'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%MV_SAVTEND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MV_SAVTEND'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%MT_SAVTEND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%MT_SAVTEND'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

