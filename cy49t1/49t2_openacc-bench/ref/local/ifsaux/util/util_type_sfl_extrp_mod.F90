MODULE UTIL_TYPE_SFL_EXTRP_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_EXTRP

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_EXTRP
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_EXTRP
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_EXTRP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (YD%YEP)
WRITE (KLUN) LYEP
IF (LYEP) THEN
  WRITE (KLUN) LBOUND (YD%YEP)
  WRITE (KLUN) UBOUND (YD%YEP)
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YEP (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_EXTRP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_EXTRP), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYEP
READ (KLUN) LYEP
IF (LYEP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YEP (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YEP (J1))
  ENDDO
ELSE
  NULLIFY (YD%YEP)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_EXTRP (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYEP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYEP = ASSOCIATED (YD%YEP)
IF (LYEP) THEN
  !$ACC ENTER DATA CREATE (YD%YEP)
  !$ACC UPDATE DEVICE (YD%YEP)
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YEP (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YEP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_EXTRP (YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (YD%YEP)
IF (LYEP) THEN
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    CALL HOST_TYPE_SURF_MTL_3D (YD%YEP (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_EXTRP (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (YD%YEP)
IF (LYEP) THEN
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YEP (J1), KLUN, CDPATH//'%YEP'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_EXTRP (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYEP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYEP = ASSOCIATED (YD%YEP)
IF (LYEP) THEN
  !$ACC EXIT DATA DETACH (YD%YEP)
  DO J1 = LBOUND (YD%YEP, 1), UBOUND (YD%YEP, 1)
    CALL WIPE_TYPE_SURF_MTL_3D (YD%YEP (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YEP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
