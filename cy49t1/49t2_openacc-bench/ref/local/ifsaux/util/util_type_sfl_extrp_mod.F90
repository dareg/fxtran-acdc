MODULE UTIL_TYPE_SFL_EXTRP_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_EXTRP

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_EXTRP
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_EXTRP
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_EXTRP
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_EXTRP (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (SELF%YEP)
WRITE (KLUN) LYEP
IF (LYEP) THEN
  WRITE (KLUN) LBOUND (SELF%YEP)
  WRITE (KLUN) UBOUND (SELF%YEP)
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (SELF%YEP (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_EXTRP (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYEP
READ (KLUN) LYEP
IF (LYEP) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YEP (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (SELF%YEP (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YEP)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_EXTRP (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYEP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LYEP = ASSOCIATED (SELF%YEP)
IF (LYEP) THEN
  !$ACC ENTER DATA CREATE (SELF%YEP)
  !$ACC UPDATE DEVICE (SELF%YEP)
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    CALL COPY_TYPE_SURF_MTL_3D (SELF%YEP (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YEP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_EXTRP (SELF)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (SELF%YEP)
IF (LYEP) THEN
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    CALL HOST_TYPE_SURF_MTL_3D (SELF%YEP (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_EXTRP (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYEP
LYEP = ASSOCIATED (SELF%YEP)
IF (LYEP) THEN
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (SELF%YEP (J1), KLUN, CDPATH//'%YEP'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_EXTRP (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_EXTRP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYEP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYEP = ASSOCIATED (SELF%YEP)
IF (LYEP) THEN
  !$ACC EXIT DATA DETACH (SELF%YEP)
  DO J1 = LBOUND (SELF%YEP, 1), UBOUND (SELF%YEP, 1)
    CALL WIPE_TYPE_SURF_MTL_3D (SELF%YEP (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (SELF%YEP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
