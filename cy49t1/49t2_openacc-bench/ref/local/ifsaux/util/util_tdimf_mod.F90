INTEGER*8 FUNCTION SIZE_TDIMF (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TDIMF),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LNGRBSP2, LNGRBSP3

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NFTHER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFTHER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NF3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NF3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFD2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFD2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NFC2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NFC2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NS3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NS2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NS1D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NS1D'
ENDIF
KSIZE = KSIZE + ISIZE
LNGRBSP3 = ALLOCATED (YD%NGRBSP3)
IF (LNGRBSP3) THEN
  ISIZE = KIND (YD%NGRBSP3) * SIZE (YD%NGRBSP3)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGRBSP3'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNGRBSP2 = ALLOCATED (YD%NGRBSP2)
IF (LNGRBSP2) THEN
  ISIZE = KIND (YD%NGRBSP2) * SIZE (YD%NGRBSP2)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGRBSP2'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%LVOR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LVOR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LADER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LUVDER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LUVDER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSPT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSPT'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

