SUBROUTINE LOAD_TECVGRIB (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECVGRIB), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL
READ (KLUN) LMECVGRB_1D
IF (LMECVGRB_1D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MECVGRB_1D (IL1(1):IU1(1)))
  READ (KLUN) YD%MECVGRB_1D
ENDIF
READ (KLUN) LMECVGRB_2D
