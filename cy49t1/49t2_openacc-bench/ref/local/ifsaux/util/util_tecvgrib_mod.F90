INTEGER*8 FUNCTION SIZE_TECVGRIB (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TECVGRIB),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMECVGRB_1D, LMECVGRB_2D, LMECVGRB_3D, LMECVGRB_ALL

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LMECVGRB_1D = ALLOCATED (YD%MECVGRB_1D)
IF (LMECVGRB_1D) THEN
  ISIZE = KIND (YD%MECVGRB_1D) * SIZE (YD%MECVGRB_1D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_1D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_2D = ALLOCATED (YD%MECVGRB_2D)
IF (LMECVGRB_2D) THEN
  ISIZE = KIND (YD%MECVGRB_2D) * SIZE (YD%MECVGRB_2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_3D = ALLOCATED (YD%MECVGRB_3D)
IF (LMECVGRB_3D) THEN
  ISIZE = KIND (YD%MECVGRB_3D) * SIZE (YD%MECVGRB_3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_3D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LMECVGRB_ALL = ALLOCATED (YD%MECVGRB_ALL)
IF (LMECVGRB_ALL) THEN
  ISIZE = KIND (YD%MECVGRB_ALL) * SIZE (YD%MECVGRB_ALL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MECVGRB_ALL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

