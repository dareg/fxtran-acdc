  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MECVGRB_3D (IL1(1):IU1(1)))
  READ (KLUN) YD%MECVGRB_3D
ENDIF
READ (KLUN) LMECVGRB_ALL
IF (LMECVGRB_ALL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MECVGRB_ALL (IL1(1):IU1(1)))
  READ (KLUN) YD%MECVGRB_ALL
ENDIF
END SUBROUTINE


