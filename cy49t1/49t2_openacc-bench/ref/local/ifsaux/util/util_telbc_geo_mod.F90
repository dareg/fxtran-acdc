INTEGER*8 FUNCTION SIZE_TELBC_GEO (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TELBC_GEO),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMPTRCPLBLK, LNIND_LEN, LNIND_LIST

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LNIND_LIST = ALLOCATED (YD%NIND_LIST)
IF (LNIND_LIST) THEN
  ISIZE = KIND (YD%NIND_LIST) * SIZE (YD%NIND_LIST)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NIND_LIST'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNIND_LEN = ALLOCATED (YD%NIND_LEN)
IF (LNIND_LEN) THEN
  ISIZE = KIND (YD%NIND_LEN) * SIZE (YD%NIND_LEN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NIND_LEN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%NCPLBLKS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NCPLBLKS'
ENDIF
KSIZE = KSIZE + ISIZE
LMPTRCPLBLK = ALLOCATED (YD%MPTRCPLBLK)
IF (LMPTRCPLBLK) THEN
  ISIZE = KIND (YD%MPTRCPLBLK) * SIZE (YD%MPTRCPLBLK)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MPTRCPLBLK'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

