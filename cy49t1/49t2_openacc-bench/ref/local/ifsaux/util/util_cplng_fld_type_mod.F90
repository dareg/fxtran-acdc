INTEGER*8 FUNCTION SIZE_CPLNG_FLD_TYPE (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (CPLNG_FLD_TYPE),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LD, LID

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LID = ALLOCATED (YD%ID)
IF (LID) THEN
  ISIZE = KIND (YD%ID) * SIZE (YD%ID)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ID'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%NAME) * LEN (YD%NAME)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NAME'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TYPE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TYPE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%INOUT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%INOUT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%STAGE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%STAGE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NUM_LVL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUM_LVL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NUM_CAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUM_CAT'
ENDIF
KSIZE = KSIZE + ISIZE
LD = ALLOCATED (YD%D)
IF (LD) THEN
  ISIZE = KIND (YD%D) * SIZE (YD%D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

