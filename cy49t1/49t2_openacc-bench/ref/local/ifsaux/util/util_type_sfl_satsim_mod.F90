SUBROUTINE SAVE_TYPE_SFL_SATSIM (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_SATSIM), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYCLBT, LYCSBT, LYSM
LYCLBT = ASSOCIATED (YD%YCLBT)
WRITE (KLUN) LYCLBT
IF (LYCLBT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YCLBT)
ENDIF
LYCSBT = ASSOCIATED (YD%YCSBT)
WRITE (KLUN) LYCSBT
IF (LYCSBT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YCSBT)
ENDIF
LYSM = ASSOCIATED (YD%YSM)
WRITE (KLUN) LYSM
IF (LYSM) THEN
  WRITE (KLUN) LBOUND (YD%YSM)
  WRITE (KLUN) UBOUND (YD%YSM)
  DO J1 = LBOUND (YD%YSM, 1), UBOUND (YD%YSM, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YSM (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_SATSIM (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_SATSIM), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYCLBT, LYCSBT, LYSM
READ (KLUN) LYCLBT
IF (LYCLBT) THEN
  ALLOCATE (YD%YCLBT)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YCLBT)
ELSE
  NULLIFY (YD%YCLBT)
ENDIF
READ (KLUN) LYCSBT
IF (LYCSBT) THEN
  ALLOCATE (YD%YCSBT)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YCSBT)
ELSE
  NULLIFY (YD%YCSBT)
ENDIF
READ (KLUN) LYSM
IF (LYSM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YSM (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YSM, 1), UBOUND (YD%YSM, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YSM (J1))
  ENDDO
ELSE
  NULLIFY (YD%YSM)
ENDIF
END SUBROUTINE


