SUBROUTINE SAVE_TYPE_SFL_SATSIM (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_SATSIM), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYCLBT, LYCSBT, LYSM
LYCLBT = ASSOCIATED (SELF%YCLBT)
WRITE (KLUN) LYCLBT
IF (LYCLBT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YCLBT, KLUN)
ENDIF
LYCSBT = ASSOCIATED (SELF%YCSBT)
WRITE (KLUN) LYCSBT
IF (LYCSBT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YCSBT, KLUN)
ENDIF
LYSM = ASSOCIATED (SELF%YSM)
WRITE (KLUN) LYSM
IF (LYSM) THEN
  WRITE (KLUN) LBOUND (SELF%YSM)
  WRITE (KLUN) UBOUND (SELF%YSM)
  DO J1 = LBOUND (SELF%YSM, 1), UBOUND (SELF%YSM, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (SELF%YSM (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_SATSIM (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_SATSIM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYCLBT, LYCSBT, LYSM
READ (KLUN) LYCLBT
IF (LYCLBT) THEN
  ALLOCATE (SELF%YCLBT)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YCLBT, KLUN)
ELSE
  NULLIFY (SELF%YCLBT)
ENDIF
READ (KLUN) LYCSBT
IF (LYCSBT) THEN
  ALLOCATE (SELF%YCSBT)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YCSBT, KLUN)
ELSE
  NULLIFY (SELF%YCSBT)
ENDIF
READ (KLUN) LYSM
IF (LYSM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YSM (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YSM, 1), UBOUND (SELF%YSM, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (SELF%YSM (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YSM)
ENDIF
END SUBROUTINE


