IF (LYCLBT) THEN
  !$ACC ENTER DATA CREATE (YD%YCLBT)
  !$ACC UPDATE DEVICE (YD%YCLBT)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YCLBT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YCLBT)
ENDIF

LYCSBT = ASSOCIATED (YD%YCSBT)
IF (LYCSBT) THEN
  !$ACC ENTER DATA CREATE (YD%YCSBT)
  !$ACC UPDATE DEVICE (YD%YCSBT)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YCSBT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YCSBT)
ENDIF

LYSM = ASSOCIATED (YD%YSM)
IF (LYSM) THEN
  !$ACC ENTER DATA CREATE (YD%YSM)
  !$ACC UPDATE DEVICE (YD%YSM)
  DO J1 = LBOUND (YD%YSM, 1), UBOUND (YD%YSM, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YSM (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YSM)
ENDIF

END SUBROUTINE

