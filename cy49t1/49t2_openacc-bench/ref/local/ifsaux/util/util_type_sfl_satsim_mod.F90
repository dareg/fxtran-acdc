SUBROUTINE CRC64_TYPE_SFL_SATSIM (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_SATSIM), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYCLBT, LYCSBT, LYSM
LYCLBT = ASSOCIATED (YD%YCLBT)
IF (LYCLBT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YCLBT, KLUN, CDPATH//'%YCLBT'//TRIM(CLIND))
ENDIF

LYCSBT = ASSOCIATED (YD%YCSBT)
IF (LYCSBT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YCSBT, KLUN, CDPATH//'%YCSBT'//TRIM(CLIND))
ENDIF

LYSM = ASSOCIATED (YD%YSM)
IF (LYSM) THEN
  DO J1 = LBOUND (YD%YSM, 1), UBOUND (YD%YSM, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YSM (J1), KLUN, CDPATH//'%YSM'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

