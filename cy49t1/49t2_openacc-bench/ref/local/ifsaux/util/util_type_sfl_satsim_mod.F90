SUBROUTINE CRC64_TYPE_SFL_SATSIM (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_SATSIM), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYCLBT, LYCSBT, LYSM
LYCLBT = ASSOCIATED (SELF%YCLBT)
IF (LYCLBT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YCLBT, KLUN, CDPATH//'%YCLBT'//TRIM(CLIND))
ENDIF

LYCSBT = ASSOCIATED (SELF%YCSBT)
IF (LYCSBT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YCSBT, KLUN, CDPATH//'%YCSBT'//TRIM(CLIND))
ENDIF

LYSM = ASSOCIATED (SELF%YSM)
IF (LYSM) THEN
  DO J1 = LBOUND (SELF%YSM, 1), UBOUND (SELF%YSM, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (SELF%YSM (J1), KLUN, CDPATH//'%YSM'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

