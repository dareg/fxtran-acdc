SUBROUTINE SAVE_TPADDH (SELF, KLUN)

IMPLICIT NONE
TYPE (TPADDH), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LNGLALIST, LNGPUMASK
LNGLALIST = ALLOCATED (SELF%NGLALIST)
WRITE (KLUN) LNGLALIST
IF (LNGLALIST) THEN
  WRITE (KLUN) LBOUND (SELF%NGLALIST)
  WRITE (KLUN) UBOUND (SELF%NGLALIST)
  WRITE (KLUN) SELF%NGLALIST
ENDIF
LNGPUMASK = ALLOCATED (SELF%NGPUMASK)
WRITE (KLUN) LNGPUMASK
IF (LNGPUMASK) THEN
  WRITE (KLUN) LBOUND (SELF%NGPUMASK)
  WRITE (KLUN) UBOUND (SELF%NGPUMASK)
  WRITE (KLUN) SELF%NGPUMASK
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TPADDH (SELF, KLUN)

IMPLICIT NONE
TYPE (TPADDH), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LNGLALIST, LNGPUMASK
READ (KLUN) LNGLALIST
IF (LNGLALIST) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NGLALIST (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NGLALIST
ENDIF
READ (KLUN) LNGPUMASK
IF (LNGPUMASK) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (SELF%NGPUMASK (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) SELF%NGPUMASK
ENDIF
END SUBROUTINE


