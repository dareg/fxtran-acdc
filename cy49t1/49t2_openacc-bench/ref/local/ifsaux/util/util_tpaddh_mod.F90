INTEGER*8 FUNCTION SIZE_TPADDH (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TPADDH),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LNGLALIST, LNGPUMASK

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LNGLALIST = ALLOCATED (YD%NGLALIST)
IF (LNGLALIST) THEN
  ISIZE = KIND (YD%NGLALIST) * SIZE (YD%NGLALIST)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGLALIST'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNGPUMASK = ALLOCATED (YD%NGPUMASK)
IF (LNGPUMASK) THEN
  ISIZE = KIND (YD%NGPUMASK) * SIZE (YD%NGPUMASK)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NGPUMASK'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

