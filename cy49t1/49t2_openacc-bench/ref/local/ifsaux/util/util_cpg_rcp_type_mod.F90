  CALL HOST_FIELD_3RB (YD%F_R)
ENDIF


LF_KAP = ASSOCIATED (YD%F_KAP)
IF (LF_KAP) THEN
  CALL HOST_FIELD_3RB (YD%F_KAP)
ENDIF

END SUBROUTINE

     



