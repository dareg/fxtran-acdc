IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (YD%F_DATA)
ENDIF


LF_CP = ASSOCIATED (YD%F_CP)
IF (LF_CP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_CP)
ENDIF


LF_R = ASSOCIATED (YD%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_R)
ENDIF


LF_KAP = ASSOCIATED (YD%F_KAP)
IF (LF_KAP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_KAP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



