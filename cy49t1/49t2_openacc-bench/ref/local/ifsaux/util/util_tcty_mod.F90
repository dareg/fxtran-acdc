SUBROUTINE SAVE_TCTY (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCTY), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%M_EVEL
WRITE (KLUN) YD%M_VVEL
WRITE (KLUN) YD%M_PSDIV
WRITE (KLUN) YD%M_PSDVBC
WRITE (KLUN) YD%M_DIVDP
WRITE (KLUN) YD%NDIM
END SUBROUTINE

SUBROUTINE LOAD_TCTY (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
