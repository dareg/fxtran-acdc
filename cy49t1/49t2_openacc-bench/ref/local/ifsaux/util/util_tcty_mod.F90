SUBROUTINE CRC64_TCTY (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TCTY), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_EVEL), CDPATH//'%M_EVEL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_VVEL), CDPATH//'%M_VVEL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_PSDIV), CDPATH//'%M_PSDIV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_PSDVBC), CDPATH//'%M_PSDVBC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%M_DIVDP), CDPATH//'%M_DIVDP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

