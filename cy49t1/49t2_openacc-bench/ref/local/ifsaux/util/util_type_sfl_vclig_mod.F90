  ALLOCATE (YD%YCHSS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YCHSS)
ELSE
  NULLIFY (YD%YCHSS)
ENDIF
READ (KLUN) LYEVAP
IF (LYEVAP) THEN
  ALLOCATE (YD%YEVAP)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YEVAP)
