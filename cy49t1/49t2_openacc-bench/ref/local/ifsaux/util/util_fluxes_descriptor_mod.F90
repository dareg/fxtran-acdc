INTEGER*8 FUNCTION SIZE_FLUXES_DESCRIPTOR (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (FLUXES_DESCRIPTOR),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%CLPREF) * SIZE (YD%CLPREF) * LEN (YD%CLPREF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLPREF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CLSUFF) * LEN (YD%CLSUFF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLSUFF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%INUMLEV) * SIZE (YD%INUMLEV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%INUMLEV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%ILEV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ILEV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%IPARITY)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IPARITY'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LACTIVE)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LACTIVE'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%ILEVBOT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ILEVBOT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%IOFFSET)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IOFFSET'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

