SUBROUTINE COPY_CPG_HWIND_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_HWIND_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_DATA, LF_UH, LF_VH, LF_WWI, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF

LZVIEW = ASSOCIATED (YD%ZVIEW)
IF (LZVIEW) THEN
  !$ACC ENTER DATA CREATE (YD%ZVIEW)
  !$ACC UPDATE DEVICE (YD%ZVIEW)
  !$ACC ENTER DATA ATTACH (YD%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (YD%ZDATA)
IF (LZDATA) THEN
  !$ACC ENTER DATA CREATE (YD%ZDATA)
  !$ACC UPDATE DEVICE (YD%ZDATA)
  !$ACC ENTER DATA ATTACH (YD%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (YD%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (YD%F_DATA)
  !$ACC ENTER DATA ATTACH (YD%F_DATA)
ENDIF


LF_UH = ASSOCIATED (YD%F_UH)
IF (LF_UH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_UH)
  !$ACC ENTER DATA ATTACH (YD%F_UH)
ENDIF


LF_VH = ASSOCIATED (YD%F_VH)
IF (LF_VH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_VH)
  !$ACC ENTER DATA ATTACH (YD%F_VH)
ENDIF


LF_WWI = ASSOCIATED (YD%F_WWI)
IF (LF_WWI .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_WWI)
  !$ACC ENTER DATA ATTACH (YD%F_WWI)
ENDIF

END SUBROUTINE

