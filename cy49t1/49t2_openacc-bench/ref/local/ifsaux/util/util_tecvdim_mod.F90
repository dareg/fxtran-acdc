INTEGER*8 FUNCTION SIZE_TECVDIM (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TECVDIM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%LECV_1D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LECV_1D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LECV_2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LECV_2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LECV_3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LECV_3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECV_1D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV_1D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECVSP_2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECVSP_2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECVGP_2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECVGP_2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECV_2D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV_2D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECVSP_3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECVSP_3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECVGP_3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECVGP_3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECV_3D)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV_3D'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NECV_ALL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV_ALL'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

