SUBROUTINE CRC64_TECVDIM (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TECVDIM), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LECV_1D), CDPATH//'%LECV_1D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LECV_2D), CDPATH//'%LECV_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LECV_3D), CDPATH//'%LECV_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECV_1D), CDPATH//'%NECV_1D'

