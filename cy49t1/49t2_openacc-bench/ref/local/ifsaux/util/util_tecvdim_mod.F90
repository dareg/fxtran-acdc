WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECVGP_2D), CDPATH//'%NECVGP_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECV_2D), CDPATH//'%NECV_2D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECVSP_3D), CDPATH//'%NECVSP_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECVGP_3D), CDPATH//'%NECVGP_3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECV_3D), CDPATH//'%NECV_3D'

