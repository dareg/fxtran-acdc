MODULE UTIL_FIELD_2IM_ARRAY_MOD

USE FIELD_2IM_ARRAY_MODULE, ONLY : FIELD_2IM_ARRAY




INTERFACE HOST
MODULE PROCEDURE HOST_FIELD_2IM_ARRAY
END INTERFACE

     




CONTAINS







SUBROUTINE HOST_FIELD_2IM_ARRAY (SELF)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (FIELD_2IM_ARRAY), TARGET :: SELF
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_2IM (SELF%F_P)
ENDIF

END SUBROUTINE

     







END MODULE
