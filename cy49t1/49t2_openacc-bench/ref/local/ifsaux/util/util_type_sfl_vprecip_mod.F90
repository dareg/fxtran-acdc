MODULE UTIL_TYPE_SFL_VPRECIP_MOD

USE SURFACE_FIELDS_MIX, ONLY : TYPE_SFL_VPRECIP

INTERFACE SAVE
MODULE PROCEDURE SAVE_TYPE_SFL_VPRECIP
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TYPE_SFL_VPRECIP
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TYPE_SFL_VPRECIP
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TYPE_SFL_VPRECIP
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TYPE_SFL_VPRECIP
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TYPE_SFL_VPRECIP
END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_VPRECIP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VPRECIP), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYPRECIP, LYXP1
LYPRECIP = ASSOCIATED (YD%YPRECIP)
WRITE (KLUN) LYPRECIP
IF (LYPRECIP) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YPRECIP)
ENDIF
LYXP1 = ASSOCIATED (YD%YXP1)
WRITE (KLUN) LYXP1
IF (LYXP1) THEN
  WRITE (KLUN) LBOUND (YD%YXP1)
  WRITE (KLUN) UBOUND (YD%YXP1)
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YXP1 (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VPRECIP (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VPRECIP), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYPRECIP, LYXP1
READ (KLUN) LYPRECIP
IF (LYPRECIP) THEN
  ALLOCATE (YD%YPRECIP)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YPRECIP)
ELSE
  NULLIFY (YD%YPRECIP)
ENDIF
READ (KLUN) LYXP1
IF (LYXP1) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YXP1 (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YXP1 (J1))
  ENDDO
ELSE
  NULLIFY (YD%YXP1)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VPRECIP (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYPRECIP, LYXP1

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYPRECIP = ASSOCIATED (YD%YPRECIP)
IF (LYPRECIP) THEN
  !$ACC ENTER DATA CREATE (YD%YPRECIP)
  !$ACC UPDATE DEVICE (YD%YPRECIP)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YPRECIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YPRECIP)
ENDIF

LYXP1 = ASSOCIATED (YD%YXP1)
IF (LYXP1) THEN
  !$ACC ENTER DATA CREATE (YD%YXP1)
  !$ACC UPDATE DEVICE (YD%YXP1)
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YXP1 (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YXP1)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_VPRECIP (YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYPRECIP, LYXP1
LYPRECIP = ASSOCIATED (YD%YPRECIP)
IF (LYPRECIP) THEN
  CALL HOST_TYPE_SURF_MTL_3D (YD%YPRECIP)
ENDIF

LYXP1 = ASSOCIATED (YD%YXP1)
IF (LYXP1) THEN
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    CALL HOST_TYPE_SURF_MTL_3D (YD%YXP1 (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_VPRECIP (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYPRECIP, LYXP1
LYPRECIP = ASSOCIATED (YD%YPRECIP)
IF (LYPRECIP) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YPRECIP, KLUN, CDPATH//'%YPRECIP'//TRIM(CLIND))
ENDIF

LYXP1 = ASSOCIATED (YD%YXP1)
IF (LYXP1) THEN
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YXP1 (J1), KLUN, CDPATH//'%YXP1'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TYPE_SFL_VPRECIP (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VPRECIP), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYPRECIP, LYXP1

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LYPRECIP = ASSOCIATED (YD%YPRECIP)
IF (LYPRECIP) THEN
  !$ACC EXIT DATA DETACH (YD%YPRECIP)
  CALL WIPE_TYPE_SURF_MTL_3D (YD%YPRECIP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC EXIT DATA DETACH (YD%YPRECIP)
ENDIF

LYXP1 = ASSOCIATED (YD%YXP1)
IF (LYXP1) THEN
  !$ACC EXIT DATA DETACH (YD%YXP1)
  DO J1 = LBOUND (YD%YXP1, 1), UBOUND (YD%YXP1, 1)
    CALL WIPE_TYPE_SURF_MTL_3D (YD%YXP1 (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YXP1)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



