INTEGER*8 FUNCTION SIZE_TOROG (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TOROG),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LOROG = ALLOCATED (YD%OROG)
IF (LOROG) THEN
  ISIZE = KIND (YD%OROG) * SIZE (YD%OROG)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROG'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGL = ALLOCATED (YD%OROGL)
IF (LOROGL) THEN
  ISIZE = KIND (YD%OROGL) * SIZE (YD%OROGL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGM = ALLOCATED (YD%OROGM)
IF (LOROGM) THEN
  ISIZE = KIND (YD%OROGM) * SIZE (YD%OROGM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGLL = ALLOCATED (YD%OROGLL)
IF (LOROGLL) THEN
  ISIZE = KIND (YD%OROGLL) * SIZE (YD%OROGLL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGLL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGMM = ALLOCATED (YD%OROGMM)
IF (LOROGMM) THEN
  ISIZE = KIND (YD%OROGMM) * SIZE (YD%OROGMM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OROGMM'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LOROGLM = ALLOCATED (YD%OROGLM)
IF (LOROGLM) THEN
