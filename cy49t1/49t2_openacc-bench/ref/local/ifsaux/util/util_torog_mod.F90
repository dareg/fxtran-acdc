SUBROUTINE LOAD_TOROG (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TOROG), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LOROG, LOROGL, LOROGLL, LOROGLM, LOROGM, LOROGMM
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LOROG
IF (LOROG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROG (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROG,1):UBOUND(YD%OROG,1)))
  READ (KLUN) ZTMP1
  YD%OROG = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGL
IF (LOROGL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROGL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROGL,1):UBOUND(YD%OROGL,1)))
  READ (KLUN) ZTMP1
  YD%OROGL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGM
IF (LOROGM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROGM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROGM,1):UBOUND(YD%OROGM,1)))
  READ (KLUN) ZTMP1
  YD%OROGM = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGLL
IF (LOROGLL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROGLL (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROGLL,1):UBOUND(YD%OROGLL,1)))
  READ (KLUN) ZTMP1
  YD%OROGLL = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGMM
IF (LOROGMM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROGMM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROGMM,1):UBOUND(YD%OROGMM,1)))
  READ (KLUN) ZTMP1
  YD%OROGMM = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LOROGLM
IF (LOROGLM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OROGLM (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OROGLM,1):UBOUND(YD%OROGLM,1)))
  READ (KLUN) ZTMP1
