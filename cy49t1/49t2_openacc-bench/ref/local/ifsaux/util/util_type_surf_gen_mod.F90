SUBROUTINE CRC64_TYPE_SURF_GEN (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SURF_GEN), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NUMFLDS), CDPATH//'%NUMFLDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NDIM), CDPATH//'%NDIM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NLEVS), CDPATH//'%NLEVS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IPTR), CDPATH//'%IPTR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IPTR5), CDPATH//'%IPTR5'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NDIM5), CDPATH//'%NDIM5'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NOFFTRAJ), CDPATH//'%NOFFTRAJ'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NOFFTRAJ_CST), CDPATH//'%NOFFTRAJ_CST'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CGRPNAME), CDPATH//'%CGRPNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%L3D), CDPATH//'%L3D'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LMTL), CDPATH//'%LMTL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%FINALISED), CDPATH//'%FINALISED'

END SUBROUTINE

