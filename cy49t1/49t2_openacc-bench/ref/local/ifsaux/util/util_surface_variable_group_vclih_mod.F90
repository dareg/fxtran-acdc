INTERFACE COPY
MODULE PROCEDURE COPY_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_SURFACE_VARIABLE_GROUP_VCLIH
END INTERFACE



CONTAINS





SUBROUTINE COPY_SURFACE_VARIABLE_GROUP_VCLIH (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_VARIABLE_2RB (YD%VTCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VSCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VBCCH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VPBLH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VSPSH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VQSH, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VPCL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VPSL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VPCN, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VPSN, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_2RB (YD%VEVA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_GROUP)
  !$ACC ENTER DATA ATTACH (YD%F_GROUP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_SURFACE_VARIABLE_GROUP_VCLIH (YD)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD

IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), TARGET :: YD
LOGICAL :: LF_GROUP
CALL HOST_VARIABLE_2RB (YD%VTCCH)

CALL HOST_VARIABLE_2RB (YD%VSCCH)

CALL HOST_VARIABLE_2RB (YD%VBCCH)

CALL HOST_VARIABLE_2RB (YD%VPBLH)

CALL HOST_VARIABLE_2RB (YD%VSPSH)

CALL HOST_VARIABLE_2RB (YD%VQSH)

CALL HOST_VARIABLE_2RB (YD%VPCL)

CALL HOST_VARIABLE_2RB (YD%VPSL)

CALL HOST_VARIABLE_2RB (YD%VPCN)

CALL HOST_VARIABLE_2RB (YD%VPSN)

CALL HOST_VARIABLE_2RB (YD%VEVA)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (YD%F_GROUP)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_SURFACE_VARIABLE_GROUP_VCLIH (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_VARIABLE_2RB_MOD
IMPLICIT NONE
TYPE (SURFACE_VARIABLE_GROUP_VCLIH), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_VARIABLE_2RB (YD%VTCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VSCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VBCCH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VPBLH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VSPSH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VQSH, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VPCL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VPSL, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VPCN, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VPSN, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_VARIABLE_2RB (YD%VEVA, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LF_GROUP = ASSOCIATED (YD%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



