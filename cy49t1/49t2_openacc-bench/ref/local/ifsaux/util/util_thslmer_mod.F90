INTEGER*8 FUNCTION SIZE_THSLMER (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (THSLMER),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LR3DTW, LRIPI, LRSLD, LRSLDW

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRIPI = ALLOCATED (YD%RIPI)
IF (LRIPI) THEN
  ISIZE = KIND (YD%RIPI) * SIZE (YD%RIPI)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RIPI'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSLD = ALLOCATED (YD%RSLD)
IF (LRSLD) THEN
  ISIZE = KIND (YD%RSLD) * SIZE (YD%RSLD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSLD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSLDW = ALLOCATED (YD%RSLDW)
IF (LRSLDW) THEN
  ISIZE = KIND (YD%RSLDW) * SIZE (YD%RSLDW)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSLDW'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LR3DTW = ALLOCATED (YD%R3DTW)
IF (LR3DTW) THEN
  ISIZE = KIND (YD%R3DTW) * SIZE (YD%R3DTW)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%R3DTW'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

