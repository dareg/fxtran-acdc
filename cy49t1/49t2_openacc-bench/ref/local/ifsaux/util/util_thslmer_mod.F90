SUBROUTINE LOAD_THSLMER (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (THSLMER), INTENT (OUT), TARGET :: YD
INTEGER :: IL2(2), IU2(2), IL3(3), IU3(3)
LOGICAL :: LR3DTW, LRIPI, LRSLD, LRSLDW
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LRIPI
IF (LRIPI) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RIPI (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RIPI
ENDIF
READ (KLUN) LRSLD
IF (LRSLD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RSLD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RSLD,1):UBOUND(YD%RSLD,1), LBOUND(YD%RSLD,2):UBOUND(YD%RSLD,2)))
  READ (KLUN) ZTMP2
  YD%RSLD = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LRSLDW
IF (LRSLDW) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%RSLDW (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%RSLDW,1):UBOUND(YD%RSLDW,1), LBOUND(YD%RSLDW,2):UBOUND(YD%RSLDW,2), LBOUND(YD%RSLDW,3):UBOUND(YD%RSLDW,3)))
  READ (KLUN) ZTMP3
  YD%RSLDW = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LR3DTW
IF (LR3DTW) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%R3DTW (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%R3DTW,1):UBOUND(YD%R3DTW,1), LBOUND(YD%R3DTW,2):UBOUND(YD%R3DTW,2), LBOUND(YD%R3DTW,3):UBOUND(YD%R3DTW,3)))
  READ (KLUN) ZTMP3
  YD%R3DTW = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
END SUBROUTINE


