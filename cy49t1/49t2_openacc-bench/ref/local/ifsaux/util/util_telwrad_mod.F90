INTEGER*8 FUNCTION SIZE_TELWRAD (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TELWRAD),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NLEVLWC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLEVLWC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLWFR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLWFR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLOOPLW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLOOPLW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLOOPLWO)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLOOPLWO'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NPROMALW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NPROMALW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLASTLW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLASTLW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LOPTLWPR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LOPTLWPR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LWLCLHR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWLCLHR'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

