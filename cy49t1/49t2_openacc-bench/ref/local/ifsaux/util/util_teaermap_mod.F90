SUBROUTINE CRC64_TEAERMAP (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TEAERMAP), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NDUSRCP), CDPATH//'%NDUSRCP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RDDUAER), CDPATH//'%RDDUAER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RDUSRCP), CDPATH//'%RDUSRCP'

END SUBROUTINE

