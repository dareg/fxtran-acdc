SUBROUTINE SAVE_TEAERMAP (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERMAP), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%NDUSRCP
WRITE (KLUN) YD%RDDUAER
WRITE (KLUN) YD%RDUSRCP
END SUBROUTINE

SUBROUTINE LOAD_TEAERMAP (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERMAP), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%NDUSRCP
ALLOCATE (ZTMP1(LBOUND(YD%RDDUAER,1):UBOUND(YD%RDDUAER,1)))
READ (KLUN) ZTMP1
YD%RDDUAER = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP2(LBOUND(YD%RDUSRCP,1):UBOUND(YD%RDUSRCP,1), LBOUND(YD%RDUSRCP,2):UBOUND(YD%RDUSRCP,2)))
READ (KLUN) ZTMP2
YD%RDUSRCP = ZTMP2
DEALLOCATE (ZTMP2)
END SUBROUTINE


