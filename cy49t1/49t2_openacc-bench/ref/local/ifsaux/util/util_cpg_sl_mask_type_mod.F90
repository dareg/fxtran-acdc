MODULE UTIL_CPG_SL_MASK_TYPE_MOD

USE CPG_SL_MASK_TYPE_MOD, ONLY : CPG_SL_MASK_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_SL_MASK_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_SL_MASK_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_SL_MASK_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_SL_MASK_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL_MASK_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_1IM (YD%F_P)
  !$ACC ENTER DATA ATTACH (YD%F_P)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_CPG_SL_MASK_TYPE (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (CPG_SL_MASK_TYPE), TARGET :: YD
LOGICAL :: LF_P
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_1IM (YD%F_P)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_SL_MASK_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL_MASK_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_1IM (YD%F_P)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
