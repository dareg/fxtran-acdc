SUBROUTINE CRC64_TYPE_SFL_OML (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_OML), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
LYTO = ASSOCIATED (YD%YTO)
IF (LYTO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YTO, KLUN, CDPATH//'%YTO'//TRIM(CLIND))
ENDIF

LYSO = ASSOCIATED (YD%YSO)
IF (LYSO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YSO, KLUN, CDPATH//'%YSO'//TRIM(CLIND))
ENDIF

LYUO = ASSOCIATED (YD%YUO)
IF (LYUO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YUO, KLUN, CDPATH//'%YUO'//TRIM(CLIND))
ENDIF

LYVO = ASSOCIATED (YD%YVO)
IF (LYVO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YVO, KLUN, CDPATH//'%YVO'//TRIM(CLIND))
ENDIF

LYOM = ASSOCIATED (YD%YOM)
IF (LYOM) THEN
  DO J1 = LBOUND (YD%YOM, 1), UBOUND (YD%YOM, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YOM (J1), KLUN, CDPATH//'%YOM'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

