SUBROUTINE CRC64_TYPE_SFL_OML (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_OML), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
LYTO = ASSOCIATED (SELF%YTO)
IF (LYTO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YTO, KLUN, CDPATH//'%YTO'//TRIM(CLIND))
ENDIF

LYSO = ASSOCIATED (SELF%YSO)
IF (LYSO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YSO, KLUN, CDPATH//'%YSO'//TRIM(CLIND))
ENDIF

LYUO = ASSOCIATED (SELF%YUO)
IF (LYUO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YUO, KLUN, CDPATH//'%YUO'//TRIM(CLIND))
ENDIF

LYVO = ASSOCIATED (SELF%YVO)
IF (LYVO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YVO, KLUN, CDPATH//'%YVO'//TRIM(CLIND))
ENDIF

LYOM = ASSOCIATED (SELF%YOM)
IF (LYOM) THEN
  DO J1 = LBOUND (SELF%YOM, 1), UBOUND (SELF%YOM, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (SELF%YOM (J1), KLUN, CDPATH//'%YOM'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

