SUBROUTINE SAVE_TYPE_SFL_OML (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_OML), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
LYTO = ASSOCIATED (YD%YTO)
WRITE (KLUN) LYTO
IF (LYTO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YTO)
ENDIF
LYSO = ASSOCIATED (YD%YSO)
WRITE (KLUN) LYSO
IF (LYSO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YSO)
ENDIF
LYUO = ASSOCIATED (YD%YUO)
WRITE (KLUN) LYUO
IF (LYUO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YUO)
ENDIF
LYVO = ASSOCIATED (YD%YVO)
WRITE (KLUN) LYVO
IF (LYVO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YVO)
ENDIF
LYOM = ASSOCIATED (YD%YOM)
WRITE (KLUN) LYOM
IF (LYOM) THEN
  WRITE (KLUN) LBOUND (YD%YOM)
  WRITE (KLUN) UBOUND (YD%YOM)
  DO J1 = LBOUND (YD%YOM, 1), UBOUND (YD%YOM, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YOM (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_OML (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_OML), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
READ (KLUN) LYTO
IF (LYTO) THEN
  ALLOCATE (YD%YTO)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YTO)
ELSE
  NULLIFY (YD%YTO)
ENDIF
READ (KLUN) LYSO
IF (LYSO) THEN
  ALLOCATE (YD%YSO)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YSO)
ELSE
  NULLIFY (YD%YSO)
ENDIF
READ (KLUN) LYUO
IF (LYUO) THEN
  ALLOCATE (YD%YUO)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YUO)
ELSE
  NULLIFY (YD%YUO)
ENDIF
READ (KLUN) LYVO
IF (LYVO) THEN
  ALLOCATE (YD%YVO)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YVO)
ELSE
  NULLIFY (YD%YVO)
ENDIF
READ (KLUN) LYOM
IF (LYOM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YOM (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YOM, 1), UBOUND (YD%YOM, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YOM (J1))
  ENDDO
ELSE
  NULLIFY (YD%YOM)
ENDIF
END SUBROUTINE


