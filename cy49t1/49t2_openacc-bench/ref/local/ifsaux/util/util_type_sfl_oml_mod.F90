SUBROUTINE SAVE_TYPE_SFL_OML (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_OML), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
LYTO = ASSOCIATED (SELF%YTO)
WRITE (KLUN) LYTO
IF (LYTO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YTO, KLUN)
ENDIF
LYSO = ASSOCIATED (SELF%YSO)
WRITE (KLUN) LYSO
IF (LYSO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YSO, KLUN)
ENDIF
LYUO = ASSOCIATED (SELF%YUO)
WRITE (KLUN) LYUO
IF (LYUO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YUO, KLUN)
ENDIF
LYVO = ASSOCIATED (SELF%YVO)
WRITE (KLUN) LYVO
IF (LYVO) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YVO, KLUN)
ENDIF
LYOM = ASSOCIATED (SELF%YOM)
WRITE (KLUN) LYOM
IF (LYOM) THEN
  WRITE (KLUN) LBOUND (SELF%YOM)
  WRITE (KLUN) UBOUND (SELF%YOM)
  DO J1 = LBOUND (SELF%YOM, 1), UBOUND (SELF%YOM, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (SELF%YOM (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_OML (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_OML), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYOM, LYSO, LYTO, LYUO, LYVO
READ (KLUN) LYTO
IF (LYTO) THEN
  ALLOCATE (SELF%YTO)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YTO, KLUN)
ELSE
  NULLIFY (SELF%YTO)
ENDIF
READ (KLUN) LYSO
IF (LYSO) THEN
  ALLOCATE (SELF%YSO)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YSO, KLUN)
ELSE
  NULLIFY (SELF%YSO)
ENDIF
READ (KLUN) LYUO
IF (LYUO) THEN
  ALLOCATE (SELF%YUO)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YUO, KLUN)
ELSE
  NULLIFY (SELF%YUO)
ENDIF
READ (KLUN) LYVO
IF (LYVO) THEN
  ALLOCATE (SELF%YVO)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YVO, KLUN)
ELSE
  NULLIFY (SELF%YVO)
ENDIF
READ (KLUN) LYOM
IF (LYOM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YOM (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YOM, 1), UBOUND (SELF%YOM, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (SELF%YOM (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YOM)
ENDIF
END SUBROUTINE


