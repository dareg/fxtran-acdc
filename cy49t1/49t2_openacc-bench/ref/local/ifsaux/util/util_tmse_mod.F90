SUBROUTINE CRC64_TMSE (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TMSE), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LPGDFWR), CDPATH//'%LPGDFWR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LHISFWR), CDPATH//'%LHISFWR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LFTZERO), CDPATH//'%LFTZERO'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NSURFEXCTLMAX), CDPATH//'%NSURFEXCTLMAX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NSURFEXCTL), CDPATH//'%NSURFEXCTL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NSTEP_DUMP_STATE), CDPATH//'%NSTEP_DUMP_STATE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CPREFIX_SFX1), CDPATH//'%CPREFIX_SFX1'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CPREFIX_SFX2), CDPATH//'%CPREFIX_SFX2'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%XZSEPS), CDPATH//'%XZSEPS'

END SUBROUTINE

