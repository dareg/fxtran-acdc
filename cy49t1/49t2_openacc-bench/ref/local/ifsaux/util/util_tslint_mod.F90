MODULE UTIL_TSLINT_MOD

USE YOMSLINT, ONLY : TSLINT

INTERFACE SAVE
MODULE PROCEDURE SAVE_TSLINT
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TSLINT
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TSLINT
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TSLINT
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TSLINT
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TSLINT
END INTERFACE

INTERFACE SIZE
MODULE PROCEDURE SIZE_TSLINT
END INTERFACE


CONTAINS

SUBROUTINE SAVE_TSLINT (SELF, KLUN)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL SAVE_TVSPLIP (SELF%YRVSPLIP, KLUN)
CALL SAVE_TVSLETA (SELF%YRVSLETA, KLUN)
CALL SAVE_THSLMER (SELF%YRHSLMER, KLUN)
END SUBROUTINE

SUBROUTINE LOAD_TSLINT (SELF, KLUN)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD

IMPLICIT NONE
TYPE (TSLINT), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL LOAD_TVSPLIP (SELF%YRVSPLIP, KLUN)
CALL LOAD_TVSLETA (SELF%YRVSLETA, KLUN)
CALL LOAD_THSLMER (SELF%YRHSLMER, KLUN)
END SUBROUTINE


SUBROUTINE COPY_TSLINT (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL COPY_TVSPLIP (SELF%YRVSPLIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVSLETA (SELF%YRVSLETA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_THSLMER (SELF%YRHSLMER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_TSLINT (SELF)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD

IMPLICIT NONE
TYPE (TSLINT), TARGET :: SELF

CALL HOST_TVSPLIP (SELF%YRVSPLIP)

CALL HOST_TVSLETA (SELF%YRVSLETA)

CALL HOST_THSLMER (SELF%YRHSLMER)

END SUBROUTINE

     

SUBROUTINE CRC64_TSLINT (SELF, KLUN, CDPATH)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TSLINT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
CLIND = ''
CALL CRC64_TVSPLIP (SELF%YRVSPLIP, KLUN, CDPATH//'%YRVSPLIP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TVSLETA (SELF%YRVSLETA, KLUN, CDPATH//'%YRVSLETA'//TRIM(CLIND))

CLIND = ''
CALL CRC64_THSLMER (SELF%YRHSLMER, KLUN, CDPATH//'%YRHSLMER'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE WIPE_TSLINT (SELF, LDDELETED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_TVSPLIP (SELF%YRVSPLIP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TVSLETA (SELF%YRVSLETA, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_THSLMER (SELF%YRHSLMER, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

FUNCTION SIZE_TSLINT (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TVSPLIP (SELF%YRVSPLIP, CLPATH//'%YRVSPLIP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSPLIP'
ENDIF
JSIZE = 0
ISIZE = SIZE_TVSLETA (SELF%YRVSLETA, CLPATH//'%YRVSLETA', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSLETA'
ENDIF
JSIZE = 0
ISIZE = SIZE_THSLMER (SELF%YRHSLMER, CLPATH//'%YRHSLMER', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRHSLMER'
ENDIF
END FUNCTION

END MODULE
