MODULE UTIL_TSLINT_MOD

USE YOMSLINT, ONLY : TSLINT

INTERFACE SAVE
MODULE PROCEDURE SAVE_TSLINT
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TSLINT
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TSLINT
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TSLINT
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TSLINT
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TSLINT
END INTERFACE

INTERFACE SIZE
MODULE PROCEDURE SIZE_TSLINT
END INTERFACE


CONTAINS

SUBROUTINE SAVE_TSLINT (KLUN, YD)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSLINT), INTENT (IN), TARGET :: YD

CALL SAVE_TVSPLIP (KLUN, YD%YRVSPLIP)
CALL SAVE_TVSLETA (KLUN, YD%YRVSLETA)
CALL SAVE_THSLMER (KLUN, YD%YRHSLMER)
END SUBROUTINE

SUBROUTINE LOAD_TSLINT (KLUN, YD)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSLINT), INTENT (OUT), TARGET :: YD

CALL LOAD_TVSPLIP (KLUN, YD%YRVSPLIP)
CALL LOAD_TVSLETA (KLUN, YD%YRVSLETA)
CALL LOAD_THSLMER (KLUN, YD%YRHSLMER)
END SUBROUTINE


SUBROUTINE COPY_TSLINT (YD, LDCREATED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_TVSPLIP (YD%YRVSPLIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVSLETA (YD%YRVSLETA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_THSLMER (YD%YRHSLMER, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_TSLINT (YD)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD

IMPLICIT NONE
TYPE (TSLINT), TARGET :: YD

CALL HOST_TVSPLIP (YD%YRVSPLIP)

CALL HOST_TVSLETA (YD%YRVSLETA)

CALL HOST_THSLMER (YD%YRHSLMER)

END SUBROUTINE

     

SUBROUTINE CRC64_TSLINT (YD, KLUN, CDPATH)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TSLINT), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

CLIND = ''
CALL CRC64_TVSPLIP (YD%YRVSPLIP, KLUN, CDPATH//'%YRVSPLIP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TVSLETA (YD%YRVSLETA, KLUN, CDPATH//'%YRVSLETA'//TRIM(CLIND))

CLIND = ''
CALL CRC64_THSLMER (YD%YRHSLMER, KLUN, CDPATH//'%YRHSLMER'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE WIPE_TSLINT (YD, LDDELETED, LDFIELDAPI)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_TVSPLIP (YD%YRVSPLIP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TVSLETA (YD%YRVSLETA, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_THSLMER (YD%YRHSLMER, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_TSLINT (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_THSLMER_MOD
USE UTIL_TVSLETA_MOD
USE UTIL_TVSPLIP_MOD
IMPLICIT NONE
TYPE (TSLINT),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TVSPLIP (YD%YRVSPLIP, CLPATH//'%YRVSPLIP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSPLIP'
ENDIF
JSIZE = 0
ISIZE = SIZE_TVSLETA (YD%YRVSLETA, CLPATH//'%YRVSLETA', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVSLETA'
ENDIF
JSIZE = 0
ISIZE = SIZE_THSLMER (YD%YRHSLMER, CLPATH//'%YRHSLMER', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRHSLMER'
ENDIF
END FUNCTION

END MODULE
