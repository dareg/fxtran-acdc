MODULE UTIL_CPG_DDH_TYPE_MOD

USE CPG_TYPE_MOD, ONLY : CPG_DDH_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_DDH_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_DDH_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_DDH_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_DDH_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_DDH_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_AUX2D, LF_AUX3D, LF_AUXSM, LF_DDHI, LF_DHCV

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_AUX3D = ASSOCIATED (YD%F_AUX3D)
IF (LF_AUX3D .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (YD%F_AUX3D)
  !$ACC ENTER DATA ATTACH (YD%F_AUX3D)
ENDIF

LF_AUX2D = ASSOCIATED (YD%F_AUX2D)
IF (LF_AUX2D .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_AUX2D)
  !$ACC ENTER DATA ATTACH (YD%F_AUX2D)
ENDIF

LF_DHCV = ASSOCIATED (YD%F_DHCV)
IF (LF_DHCV .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (YD%F_DHCV)
  !$ACC ENTER DATA ATTACH (YD%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (YD%F_AUXSM)
IF (LF_AUXSM .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_5RB (YD%F_AUXSM)
  !$ACC ENTER DATA ATTACH (YD%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (YD%F_DDHI)
IF (LF_DDHI .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2IM (YD%F_DDHI)
  !$ACC ENTER DATA ATTACH (YD%F_DDHI)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_CPG_DDH_TYPE (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (CPG_DDH_TYPE), TARGET :: YD
LOGICAL :: LF_AUX2D, LF_AUX3D, LF_AUXSM, LF_DDHI, LF_DHCV
LF_AUX3D = ASSOCIATED (YD%F_AUX3D)
IF (LF_AUX3D) THEN
  CALL HOST_FIELD_4RB (YD%F_AUX3D)
ENDIF

LF_AUX2D = ASSOCIATED (YD%F_AUX2D)
IF (LF_AUX2D) THEN
  CALL HOST_FIELD_3RB (YD%F_AUX2D)
ENDIF

LF_DHCV = ASSOCIATED (YD%F_DHCV)
IF (LF_DHCV) THEN
  CALL HOST_FIELD_4RB (YD%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (YD%F_AUXSM)
IF (LF_AUXSM) THEN
  CALL HOST_FIELD_5RB (YD%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (YD%F_DDHI)
IF (LF_DDHI) THEN
  CALL HOST_FIELD_2IM (YD%F_DDHI)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_DDH_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_DDH_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_AUX2D, LF_AUX3D, LF_AUXSM, LF_DDHI, LF_DHCV

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_AUX3D = ASSOCIATED (YD%F_AUX3D)
IF (LF_AUX3D .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (YD%F_AUX3D)
ENDIF

LF_AUX2D = ASSOCIATED (YD%F_AUX2D)
IF (LF_AUX2D .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_AUX2D)
ENDIF

LF_DHCV = ASSOCIATED (YD%F_DHCV)
IF (LF_DHCV .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (YD%F_DHCV)
ENDIF

LF_AUXSM = ASSOCIATED (YD%F_AUXSM)
IF (LF_AUXSM .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_5RB (YD%F_AUXSM)
ENDIF

LF_DDHI = ASSOCIATED (YD%F_DDHI)
IF (LF_DDHI .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2IM (YD%F_DDHI)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
