JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSDDH (YD%YRSDDH, CLPATH//'%YRSDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TTDDH (YD%YRTDDH, CLPATH//'%YRTDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRTDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TGPDDH (YD%YRGPDDH, CLPATH//'%YRGPDDH', .FALSE.)
