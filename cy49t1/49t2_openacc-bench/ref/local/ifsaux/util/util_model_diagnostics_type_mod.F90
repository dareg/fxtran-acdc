INTEGER*8 FUNCTION SIZE_MODEL_DIAGNOSTICS_TYPE (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCDDH_MOD
USE UTIL_TGPDDH_MOD
USE UTIL_TLDDH_MOD
USE UTIL_TMDDH_MOD
USE UTIL_TPADDH_MOD
USE UTIL_TSDDH_MOD
USE UTIL_TSPDDH_MOD
USE UTIL_TTDDH_MOD
