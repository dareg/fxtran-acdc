SUBROUTINE LOAD_TYPE_SFL_CLS (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_CLS), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYCL, LYHUCLS, LYNUCLS, LYNVCLS, LYSICLS, LYTCLS, LYUCLS, LYVCLS
READ (KLUN) LYTCLS
IF (LYTCLS) THEN
  ALLOCATE (YD%YTCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YTCLS)
ELSE
  NULLIFY (YD%YTCLS)
ENDIF
READ (KLUN) LYHUCLS
IF (LYHUCLS) THEN
  ALLOCATE (YD%YHUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YHUCLS)
ELSE
  NULLIFY (YD%YHUCLS)
ENDIF
READ (KLUN) LYUCLS
IF (LYUCLS) THEN
  ALLOCATE (YD%YUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YUCLS)
ELSE
  NULLIFY (YD%YUCLS)
ENDIF
READ (KLUN) LYVCLS
IF (LYVCLS) THEN
  ALLOCATE (YD%YVCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVCLS)
ELSE
  NULLIFY (YD%YVCLS)
ENDIF
READ (KLUN) LYNUCLS
IF (LYNUCLS) THEN
  ALLOCATE (YD%YNUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YNUCLS)
ELSE
  NULLIFY (YD%YNUCLS)
ENDIF
READ (KLUN) LYNVCLS
IF (LYNVCLS) THEN
  ALLOCATE (YD%YNVCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YNVCLS)
ELSE
  NULLIFY (YD%YNVCLS)
ENDIF
READ (KLUN) LYSICLS
IF (LYSICLS) THEN
  ALLOCATE (YD%YSICLS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YSICLS)
ELSE
  NULLIFY (YD%YSICLS)
ENDIF
READ (KLUN) LYCL
IF (LYCL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YCL (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YCL, 1), UBOUND (YD%YCL, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YCL (J1))
  ENDDO
ELSE
  NULLIFY (YD%YCL)
ENDIF
END SUBROUTINE


