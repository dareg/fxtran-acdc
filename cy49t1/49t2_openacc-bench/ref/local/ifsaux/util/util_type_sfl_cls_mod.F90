SUBROUTINE LOAD_TYPE_SFL_CLS (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_CLS), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYCL, LYHUCLS, LYNUCLS, LYNVCLS, LYSICLS, LYTCLS, LYUCLS, LYVCLS
READ (KLUN) LYTCLS
IF (LYTCLS) THEN
  ALLOCATE (SELF%YTCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YTCLS, KLUN)
ELSE
  NULLIFY (SELF%YTCLS)
ENDIF
READ (KLUN) LYHUCLS
IF (LYHUCLS) THEN
  ALLOCATE (SELF%YHUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YHUCLS, KLUN)
ELSE
  NULLIFY (SELF%YHUCLS)
ENDIF
READ (KLUN) LYUCLS
IF (LYUCLS) THEN
  ALLOCATE (SELF%YUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YUCLS, KLUN)
ELSE
  NULLIFY (SELF%YUCLS)
ENDIF
READ (KLUN) LYVCLS
IF (LYVCLS) THEN
  ALLOCATE (SELF%YVCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVCLS, KLUN)
ELSE
  NULLIFY (SELF%YVCLS)
ENDIF
READ (KLUN) LYNUCLS
IF (LYNUCLS) THEN
  ALLOCATE (SELF%YNUCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YNUCLS, KLUN)
ELSE
  NULLIFY (SELF%YNUCLS)
ENDIF
READ (KLUN) LYNVCLS
IF (LYNVCLS) THEN
  ALLOCATE (SELF%YNVCLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YNVCLS, KLUN)
ELSE
  NULLIFY (SELF%YNVCLS)
ENDIF
READ (KLUN) LYSICLS
IF (LYSICLS) THEN
  ALLOCATE (SELF%YSICLS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YSICLS, KLUN)
ELSE
  NULLIFY (SELF%YSICLS)
ENDIF
READ (KLUN) LYCL
IF (LYCL) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YCL (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YCL, 1), UBOUND (SELF%YCL, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YCL (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YCL)
ENDIF
END SUBROUTINE


