SUBROUTINE LOAD_TEAERVOL (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERVOL), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%NAERVOLC
READ (KLUN) YD%NAERVOLE
READ (KLUN) YD%NINTERPT
READ (KLUN) YD%NTVOLC
READ (KLUN) YD%NVOLERUP
READ (KLUN) YD%NVOLHOMO
READ (KLUN) YD%NVOLOPTP
READ (KLUN) YD%NVOLERUZ
READ (KLUN) YD%NVOLDATS
READ (KLUN) YD%NVOLDATE
ALLOCATE (ZTMP2(LBOUND(YD%RAERVOLC,1):UBOUND(YD%RAERVOLC,1), LBOUND(YD%RAERVOLC,2):UBOUND(YD%RAERVOLC,2)))
READ (KLUN) ZTMP2
YD%RAERVOLC = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP2(LBOUND(YD%RAERVOLE,1):UBOUND(YD%RAERVOLE,1), LBOUND(YD%RAERVOLE,2):UBOUND(YD%RAERVOLE,2)))
READ (KLUN) ZTMP2
YD%RAERVOLE = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP1(LBOUND(YD%RVOLERUZ,1):UBOUND(YD%RVOLERUZ,1)))
READ (KLUN) ZTMP1
YD%RVOLERUZ = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%RVSEDVOL = ZTMP0
READ (KLUN) ZTMP0
YD%RVDEPVOL = ZTMP0
READ (KLUN) ZTMP0
YD%RWDEPVOL = ZTMP0
END SUBROUTINE


