MODULE UTIL_MF_PHYS_STATE_GSP_SG_TYPE_MOD

USE MF_PHYS_BASE_STATE_TYPE_MOD, ONLY : MF_PHYS_STATE_GSP_SG_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_MF_PHYS_STATE_GSP_SG_TYPE
END INTERFACE


     

INTERFACE WIPE
MODULE PROCEDURE WIPE_MF_PHYS_STATE_GSP_SG_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_MF_PHYS_STATE_GSP_SG_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SG_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_A, LF_F, LF_R

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_F = ASSOCIATED (YD%F_F)
IF (LF_F .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_F)
  !$ACC ENTER DATA ATTACH (YD%F_F)
ENDIF

LF_A = ASSOCIATED (YD%F_A)
IF (LF_A .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_A)
  !$ACC ENTER DATA ATTACH (YD%F_A)
ENDIF

LF_R = ASSOCIATED (YD%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_R)
  !$ACC ENTER DATA ATTACH (YD%F_R)
ENDIF

END SUBROUTINE



     



SUBROUTINE WIPE_MF_PHYS_STATE_GSP_SG_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_STATE_GSP_SG_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_A, LF_F, LF_R

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_F = ASSOCIATED (YD%F_F)
IF (LF_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_F)
ENDIF

LF_A = ASSOCIATED (YD%F_A)
IF (LF_A .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_A)
ENDIF

LF_R = ASSOCIATED (YD%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_R)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
