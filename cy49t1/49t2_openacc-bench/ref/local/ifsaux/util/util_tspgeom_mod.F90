SUBROUTINE SAVE_TSPGEOM (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSPGEOM), INTENT (IN), TARGET :: YD
LOGICAL :: LGMR, LSCGMAP
LGMR = ALLOCATED (YD%GMR)
WRITE (KLUN) LGMR
IF (LGMR) THEN
  WRITE (KLUN) LBOUND (YD%GMR)
  WRITE (KLUN) UBOUND (YD%GMR)
  WRITE (KLUN) YD%GMR
ENDIF
LSCGMAP = ALLOCATED (YD%SCGMAP)
WRITE (KLUN) LSCGMAP
IF (LSCGMAP) THEN
  WRITE (KLUN) LBOUND (YD%SCGMAP)
  WRITE (KLUN) UBOUND (YD%SCGMAP)
  WRITE (KLUN) YD%SCGMAP
ENDIF
WRITE (KLUN) YD%ESCGMAP
END SUBROUTINE

SUBROUTINE LOAD_TSPGEOM (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSPGEOM), INTENT (OUT), TARGET :: YD
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LGMR, LSCGMAP
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LGMR
IF (LGMR) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%GMR (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%GMR,1):UBOUND(YD%GMR,1), LBOUND(YD%GMR,2):UBOUND(YD%GMR,2)))
  READ (KLUN) ZTMP2
  YD%GMR = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LSCGMAP
IF (LSCGMAP) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%SCGMAP (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%SCGMAP,1):UBOUND(YD%SCGMAP,1), LBOUND(YD%SCGMAP,2):UBOUND(YD%SCGMAP,2)))
  READ (KLUN) ZTMP2
  YD%SCGMAP = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
ALLOCATE (ZTMP1(LBOUND(YD%ESCGMAP,1):UBOUND(YD%ESCGMAP,1)))
READ (KLUN) ZTMP1
YD%ESCGMAP = ZTMP1
DEALLOCATE (ZTMP1)
END SUBROUTINE


