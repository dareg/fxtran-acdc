INTEGER*8 FUNCTION SIZE_TSPGEOM (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSPGEOM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGMR, LSCGMAP

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGMR = ALLOCATED (YD%GMR)
IF (LGMR) THEN
  ISIZE = KIND (YD%GMR) * SIZE (YD%GMR)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GMR'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSCGMAP = ALLOCATED (YD%SCGMAP)
IF (LSCGMAP) THEN
  ISIZE = KIND (YD%SCGMAP) * SIZE (YD%SCGMAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SCGMAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%ESCGMAP) * SIZE (YD%ESCGMAP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ESCGMAP'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

