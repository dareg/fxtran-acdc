SUBROUTINE CRC64_TYPE_SFL_VCLIK (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIK), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYUDGRO, LYVK
LYUDGRO = ASSOCIATED (SELF%YUDGRO)
IF (LYUDGRO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YUDGRO, KLUN, CDPATH//'%YUDGRO'//TRIM(CLIND))
ENDIF

LYVK = ASSOCIATED (SELF%YVK)
IF (LYVK) THEN
  DO J1 = LBOUND (SELF%YVK, 1), UBOUND (SELF%YVK, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (SELF%YVK (J1), KLUN, CDPATH//'%YVK'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

