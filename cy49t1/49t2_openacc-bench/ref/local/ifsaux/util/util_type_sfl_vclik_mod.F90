SUBROUTINE SAVE_TYPE_SFL_VCLIK (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VCLIK), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYUDGRO, LYVK
LYUDGRO = ASSOCIATED (SELF%YUDGRO)
WRITE (KLUN) LYUDGRO
IF (LYUDGRO) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YUDGRO, KLUN)
ENDIF
LYVK = ASSOCIATED (SELF%YVK)
WRITE (KLUN) LYVK
IF (LYVK) THEN
  WRITE (KLUN) LBOUND (SELF%YVK)
  WRITE (KLUN) UBOUND (SELF%YVK)
  DO J1 = LBOUND (SELF%YVK, 1), UBOUND (SELF%YVK, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (SELF%YVK (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VCLIK (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIK), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYUDGRO, LYVK
READ (KLUN) LYUDGRO
IF (LYUDGRO) THEN
  ALLOCATE (SELF%YUDGRO)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YUDGRO, KLUN)
ELSE
  NULLIFY (SELF%YUDGRO)
ENDIF
READ (KLUN) LYVK
IF (LYVK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YVK (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YVK, 1), UBOUND (SELF%YVK, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVK (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YVK)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_VCLIK (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VCLIK), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYUDGRO, LYVK

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LYUDGRO = ASSOCIATED (SELF%YUDGRO)
IF (LYUDGRO) THEN
  !$ACC ENTER DATA CREATE (SELF%YUDGRO)
  !$ACC UPDATE DEVICE (SELF%YUDGRO)
  CALL COPY_TYPE_SURF_MTL_2D (SELF%YUDGRO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YUDGRO)
ENDIF

LYVK = ASSOCIATED (SELF%YVK)
IF (LYVK) THEN
  !$ACC ENTER DATA CREATE (SELF%YVK)
  !$ACC UPDATE DEVICE (SELF%YVK)
  DO J1 = LBOUND (SELF%YVK, 1), UBOUND (SELF%YVK, 1)
    CALL COPY_TYPE_SURF_MTL_2D (SELF%YVK (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YVK)
ENDIF

END SUBROUTINE

