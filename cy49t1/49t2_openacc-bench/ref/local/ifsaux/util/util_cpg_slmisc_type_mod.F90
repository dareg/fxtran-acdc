MODULE UTIL_CPG_SLMISC_TYPE_MOD

USE CPG_SLMISC_TYPE_MOD, ONLY : CPG_SLMISC_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_SLMISC_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_SLMISC_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_SLMISC_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_SLMISC_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_SLHDA = ASSOCIATED (YD%F_SLHDA)
IF (LF_SLHDA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_SLHDA)
  !$ACC ENTER DATA ATTACH (YD%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (YD%F_SLHDD0)
IF (LF_SLHDD0 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_SLHDD0)
  !$ACC ENTER DATA ATTACH (YD%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (YD%F_ISETTLOFF)
IF (LF_ISETTLOFF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3IM (YD%F_ISETTLOFF)
  !$ACC ENTER DATA ATTACH (YD%F_ISETTLOFF)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_CPG_SLMISC_TYPE (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), TARGET :: YD
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0
LF_SLHDA = ASSOCIATED (YD%F_SLHDA)
IF (LF_SLHDA) THEN
  CALL HOST_FIELD_2RB (YD%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (YD%F_SLHDD0)
IF (LF_SLHDD0) THEN
  CALL HOST_FIELD_2RB (YD%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (YD%F_ISETTLOFF)
IF (LF_ISETTLOFF) THEN
  CALL HOST_FIELD_3IM (YD%F_ISETTLOFF)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_SLMISC_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SLMISC_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ISETTLOFF, LF_SLHDA, LF_SLHDD0

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_SLHDA = ASSOCIATED (YD%F_SLHDA)
IF (LF_SLHDA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_SLHDA)
ENDIF

LF_SLHDD0 = ASSOCIATED (YD%F_SLHDD0)
IF (LF_SLHDD0 .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_SLHDD0)
ENDIF

LF_ISETTLOFF = ASSOCIATED (YD%F_ISETTLOFF)
IF (LF_ISETTLOFF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3IM (YD%F_ISETTLOFF)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
