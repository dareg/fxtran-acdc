SUBROUTINE SAVE_TYPE_SFL_VCLIA (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_VCLIA), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
LYSEA = ASSOCIATED (SELF%YSEA)
WRITE (KLUN) LYSEA
IF (LYSEA) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YSEA, KLUN)
ENDIF
LYLAN = ASSOCIATED (SELF%YLAN)
WRITE (KLUN) LYLAN
IF (LYLAN) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YLAN, KLUN)
ENDIF
LYSOO = ASSOCIATED (SELF%YSOO)
WRITE (KLUN) LYSOO
IF (LYSOO) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YSOO, KLUN)
ENDIF
LYDES = ASSOCIATED (SELF%YDES)
WRITE (KLUN) LYDES
IF (LYDES) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YDES, KLUN)
ENDIF
LYSUL = ASSOCIATED (SELF%YSUL)
WRITE (KLUN) LYSUL
IF (LYSUL) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YSUL, KLUN)
ENDIF
LYVOL = ASSOCIATED (SELF%YVOL)
WRITE (KLUN) LYVOL
IF (LYVOL) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (SELF%YVOL, KLUN)
ENDIF
LYVA = ASSOCIATED (SELF%YVA)
WRITE (KLUN) LYVA
IF (LYVA) THEN
  WRITE (KLUN) LBOUND (SELF%YVA)
  WRITE (KLUN) UBOUND (SELF%YVA)
  DO J1 = LBOUND (SELF%YVA, 1), UBOUND (SELF%YVA, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (SELF%YVA (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VCLIA (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIA), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
READ (KLUN) LYSEA
IF (LYSEA) THEN
  ALLOCATE (SELF%YSEA)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YSEA, KLUN)
ELSE
  NULLIFY (SELF%YSEA)
ENDIF
READ (KLUN) LYLAN
IF (LYLAN) THEN
  ALLOCATE (SELF%YLAN)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YLAN, KLUN)
ELSE
  NULLIFY (SELF%YLAN)
ENDIF
READ (KLUN) LYSOO
IF (LYSOO) THEN
  ALLOCATE (SELF%YSOO)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YSOO, KLUN)
ELSE
  NULLIFY (SELF%YSOO)
ENDIF
READ (KLUN) LYDES
IF (LYDES) THEN
  ALLOCATE (SELF%YDES)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YDES, KLUN)
ELSE
  NULLIFY (SELF%YDES)
ENDIF
READ (KLUN) LYSUL
IF (LYSUL) THEN
  ALLOCATE (SELF%YSUL)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YSUL, KLUN)
ELSE
  NULLIFY (SELF%YSUL)
ENDIF
READ (KLUN) LYVOL
IF (LYVOL) THEN
  ALLOCATE (SELF%YVOL)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVOL, KLUN)
ELSE
  NULLIFY (SELF%YVOL)
ENDIF
READ (KLUN) LYVA
IF (LYVA) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YVA (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YVA, 1), UBOUND (SELF%YVA, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVA (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YVA)
ENDIF
END SUBROUTINE


