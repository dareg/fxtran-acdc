SUBROUTINE CRC64_TYPE_SFL_VCLIA (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIA), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
LYSEA = ASSOCIATED (SELF%YSEA)
IF (LYSEA) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YSEA, KLUN, CDPATH//'%YSEA'//TRIM(CLIND))
ENDIF

LYLAN = ASSOCIATED (SELF%YLAN)
IF (LYLAN) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YLAN, KLUN, CDPATH//'%YLAN'//TRIM(CLIND))
ENDIF

LYSOO = ASSOCIATED (SELF%YSOO)
IF (LYSOO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YSOO, KLUN, CDPATH//'%YSOO'//TRIM(CLIND))
ENDIF

LYDES = ASSOCIATED (SELF%YDES)
IF (LYDES) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YDES, KLUN, CDPATH//'%YDES'//TRIM(CLIND))
ENDIF

LYSUL = ASSOCIATED (SELF%YSUL)
IF (LYSUL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YSUL, KLUN, CDPATH//'%YSUL'//TRIM(CLIND))
ENDIF

LYVOL = ASSOCIATED (SELF%YVOL)
IF (LYVOL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (SELF%YVOL, KLUN, CDPATH//'%YVOL'//TRIM(CLIND))
ENDIF

LYVA = ASSOCIATED (SELF%YVA)
IF (LYVA) THEN
  DO J1 = LBOUND (SELF%YVA, 1), UBOUND (SELF%YVA, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (SELF%YVA (J1), KLUN, CDPATH//'%YVA'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

