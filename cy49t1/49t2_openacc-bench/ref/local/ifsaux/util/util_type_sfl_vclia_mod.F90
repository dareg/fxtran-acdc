SUBROUTINE CRC64_TYPE_SFL_VCLIA (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIA), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
LYSEA = ASSOCIATED (YD%YSEA)
IF (LYSEA) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YSEA, KLUN, CDPATH//'%YSEA'//TRIM(CLIND))
ENDIF

LYLAN = ASSOCIATED (YD%YLAN)
IF (LYLAN) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YLAN, KLUN, CDPATH//'%YLAN'//TRIM(CLIND))
ENDIF

LYSOO = ASSOCIATED (YD%YSOO)
IF (LYSOO) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YSOO, KLUN, CDPATH//'%YSOO'//TRIM(CLIND))
ENDIF

LYDES = ASSOCIATED (YD%YDES)
IF (LYDES) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YDES, KLUN, CDPATH//'%YDES'//TRIM(CLIND))
ENDIF

LYSUL = ASSOCIATED (YD%YSUL)
IF (LYSUL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YSUL, KLUN, CDPATH//'%YSUL'//TRIM(CLIND))
ENDIF

LYVOL = ASSOCIATED (YD%YVOL)
IF (LYVOL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_2D (YD%YVOL, KLUN, CDPATH//'%YVOL'//TRIM(CLIND))
ENDIF

LYVA = ASSOCIATED (YD%YVA)
IF (LYVA) THEN
  DO J1 = LBOUND (YD%YVA, 1), UBOUND (YD%YVA, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (YD%YVA (J1), KLUN, CDPATH//'%YVA'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

