SUBROUTINE SAVE_TYPE_SFL_VCLIA (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VCLIA), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
LYSEA = ASSOCIATED (YD%YSEA)
WRITE (KLUN) LYSEA
IF (LYSEA) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YSEA)
ENDIF
LYLAN = ASSOCIATED (YD%YLAN)
WRITE (KLUN) LYLAN
IF (LYLAN) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YLAN)
ENDIF
LYSOO = ASSOCIATED (YD%YSOO)
WRITE (KLUN) LYSOO
IF (LYSOO) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YSOO)
ENDIF
LYDES = ASSOCIATED (YD%YDES)
WRITE (KLUN) LYDES
IF (LYDES) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YDES)
ENDIF
LYSUL = ASSOCIATED (YD%YSUL)
WRITE (KLUN) LYSUL
IF (LYSUL) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YSUL)
ENDIF
LYVOL = ASSOCIATED (YD%YVOL)
WRITE (KLUN) LYVOL
IF (LYVOL) THEN
  CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YVOL)
ENDIF
LYVA = ASSOCIATED (YD%YVA)
WRITE (KLUN) LYVA
IF (LYVA) THEN
  WRITE (KLUN) LBOUND (YD%YVA)
  WRITE (KLUN) UBOUND (YD%YVA)
  DO J1 = LBOUND (YD%YVA, 1), UBOUND (YD%YVA, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (KLUN, YD%YVA (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_VCLIA (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VCLIA), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYDES, LYLAN, LYSEA, LYSOO, LYSUL, LYVA, LYVOL
READ (KLUN) LYSEA
IF (LYSEA) THEN
  ALLOCATE (YD%YSEA)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YSEA)
ELSE
  NULLIFY (YD%YSEA)
ENDIF
READ (KLUN) LYLAN
IF (LYLAN) THEN
  ALLOCATE (YD%YLAN)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YLAN)
ELSE
  NULLIFY (YD%YLAN)
ENDIF
READ (KLUN) LYSOO
IF (LYSOO) THEN
  ALLOCATE (YD%YSOO)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YSOO)
ELSE
  NULLIFY (YD%YSOO)
ENDIF
READ (KLUN) LYDES
IF (LYDES) THEN
  ALLOCATE (YD%YDES)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YDES)
ELSE
  NULLIFY (YD%YDES)
ENDIF
READ (KLUN) LYSUL
IF (LYSUL) THEN
  ALLOCATE (YD%YSUL)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YSUL)
ELSE
  NULLIFY (YD%YSUL)
ENDIF
READ (KLUN) LYVOL
IF (LYVOL) THEN
  ALLOCATE (YD%YVOL)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVOL)
ELSE
  NULLIFY (YD%YVOL)
ENDIF
READ (KLUN) LYVA
IF (LYVA) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YVA (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YVA, 1), UBOUND (YD%YVA, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVA (J1))
  ENDDO
ELSE
  NULLIFY (YD%YVA)
ENDIF
END SUBROUTINE


