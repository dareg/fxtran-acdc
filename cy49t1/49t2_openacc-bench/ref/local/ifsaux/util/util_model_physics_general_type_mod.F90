SUBROUTINE COPY_MODEL_PHYSICS_GENERAL_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TDPHY_MOD
USE UTIL_TSLPHY_MOD
USE UTIL_TVDF_MOD
IMPLICIT NONE
TYPE (MODEL_PHYSICS_GENERAL_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_TDPHY (YD%YRDPHY, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSLPHY (YD%YRSLPHY, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVDF (YD%YRVDF, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

