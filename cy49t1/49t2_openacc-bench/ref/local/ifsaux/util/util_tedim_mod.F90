INTEGER*8 FUNCTION SIZE_TEDIM (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TEDIM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LNISMAX, LNISNAX

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NSECPLG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSECPLG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NBZONG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBZONG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NBZONL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBZONL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NBZONU)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBZONU'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NNOEXTZG)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NNOEXTZG'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NNOEXTZL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NNOEXTZL'
ENDIF
KSIZE = KSIZE + ISIZE
LNISMAX = ASSOCIATED (YD%NISMAX)
IF (LNISMAX) THEN
  ISIZE = KIND (YD%NISMAX) * SIZE (YD%NISMAX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NISMAX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNISNAX = ASSOCIATED (YD%NISNAX)
IF (LNISNAX) THEN
  ISIZE = KIND (YD%NISNAX) * SIZE (YD%NISNAX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NISNAX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%LBIPINCI)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LBIPINCI'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NBIPINCIX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBIPINCIX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NBIPINCIY)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NBIPINCIY'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NEDOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NEDOM'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

