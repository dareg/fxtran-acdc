SUBROUTINE SAVE_TEOZOC (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEOZOC), INTENT (IN), TARGET :: YD
LOGICAL :: LZOZCL, LZOZCLAQUA
LZOZCL = ALLOCATED (YD%ZOZCL)
WRITE (KLUN) LZOZCL
IF (LZOZCL) THEN
  WRITE (KLUN) LBOUND (YD%ZOZCL)
  WRITE (KLUN) UBOUND (YD%ZOZCL)
  WRITE (KLUN) YD%ZOZCL
ENDIF
LZOZCLAQUA = ALLOCATED (YD%ZOZCLAQUA)
WRITE (KLUN) LZOZCLAQUA
IF (LZOZCLAQUA) THEN
  WRITE (KLUN) LBOUND (YD%ZOZCLAQUA)
  WRITE (KLUN) UBOUND (YD%ZOZCLAQUA)
  WRITE (KLUN) YD%ZOZCLAQUA
ENDIF
