INTEGER*8 FUNCTION SIZE_TEOZOC (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TEOZOC),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LZOZCL, LZOZCLAQUA

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LZOZCL = ALLOCATED (YD%ZOZCL)
IF (LZOZCL) THEN
  ISIZE = KIND (YD%ZOZCL) * SIZE (YD%ZOZCL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ZOZCL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LZOZCLAQUA = ALLOCATED (YD%ZOZCLAQUA)
IF (LZOZCLAQUA) THEN
  ISIZE = KIND (YD%ZOZCLAQUA) * SIZE (YD%ZOZCLAQUA)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%ZOZCLAQUA'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%COZQC) * SIZE (YD%COZQC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZQC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%COZQS) * SIZE (YD%COZQS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZQS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%COZHC) * SIZE (YD%COZHC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZHC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%COZHS) * SIZE (YD%COZHS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%COZHS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RSINC) * SIZE (YD%RSINC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RSINC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%ROZT) * SIZE (YD%ROZT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%ROZT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RPROC) * SIZE (YD%RPROC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RPROC'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

