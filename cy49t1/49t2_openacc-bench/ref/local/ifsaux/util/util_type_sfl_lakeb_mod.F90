  ALLOCATE (YD%YLBLT)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YLBLT)
ELSE
  NULLIFY (YD%YLBLT)
ENDIF
READ (KLUN) LYLSHF
IF (LYLSHF) THEN
  ALLOCATE (YD%YLSHF)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YLSHF)
