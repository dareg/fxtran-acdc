  ALLOCATE (YD%YSL (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YSL, 1), UBOUND (YD%YSL, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YSL (J1))
  ENDDO
ELSE
