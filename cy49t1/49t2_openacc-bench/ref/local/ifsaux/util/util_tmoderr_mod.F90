SUBROUTINE SAVE_TMODERR (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TMODERR), INTENT (IN), TARGET :: YD
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
WRITE (KLUN) YD%LSCALERR
WRITE (KLUN) YD%LTAPERERR
WRITE (KLUN) YD%NTYPE_MODERR
WRITE (KLUN) YD%NSTEP_MODERR
WRITE (KLUN) YD%NORDER_MODERR
WRITE (KLUN) YD%NPRTMODERR
WRITE (KLUN) YD%TAPERERR_INI
WRITE (KLUN) YD%TAPERERR_END
WRITE (KLUN) YD%TSTEP_ERR
LMSTEPERR = ALLOCATED (YD%MSTEPERR)
WRITE (KLUN) LMSTEPERR
IF (LMSTEPERR) THEN
  WRITE (KLUN) LBOUND (YD%MSTEPERR)
  WRITE (KLUN) UBOUND (YD%MSTEPERR)
  WRITE (KLUN) YD%MSTEPERR
ENDIF
LTSTEPERR = ALLOCATED (YD%TSTEPERR)
WRITE (KLUN) LTSTEPERR
IF (LTSTEPERR) THEN
  WRITE (KLUN) LBOUND (YD%TSTEPERR)
  WRITE (KLUN) UBOUND (YD%TSTEPERR)
  WRITE (KLUN) YD%TSTEPERR
ENDIF
LFSTEPERR = ALLOCATED (YD%FSTEPERR)
WRITE (KLUN) LFSTEPERR
IF (LFSTEPERR) THEN
  WRITE (KLUN) LBOUND (YD%FSTEPERR)
  WRITE (KLUN) UBOUND (YD%FSTEPERR)
  WRITE (KLUN) YD%FSTEPERR
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TMODERR (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TMODERR), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%LSCALERR
READ (KLUN) YD%LTAPERERR
READ (KLUN) YD%NTYPE_MODERR
READ (KLUN) YD%NSTEP_MODERR
READ (KLUN) YD%NORDER_MODERR
READ (KLUN) YD%NPRTMODERR
READ (KLUN) YD%TAPERERR_INI
READ (KLUN) YD%TAPERERR_END
READ (KLUN) ZTMP0
YD%TSTEP_ERR = ZTMP0
READ (KLUN) LMSTEPERR
IF (LMSTEPERR) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%MSTEPERR (IL1(1):IU1(1)))
  READ (KLUN) YD%MSTEPERR
ENDIF
READ (KLUN) LTSTEPERR
IF (LTSTEPERR) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%TSTEPERR (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%TSTEPERR,1):UBOUND(YD%TSTEPERR,1)))
  READ (KLUN) ZTMP1
  YD%TSTEPERR = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LFSTEPERR
IF (LFSTEPERR) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%FSTEPERR (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%FSTEPERR,1):UBOUND(YD%FSTEPERR,1), LBOUND(YD%FSTEPERR,2):UBOUND(YD%FSTEPERR,2)))
  READ (KLUN) ZTMP2
  YD%FSTEPERR = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE


