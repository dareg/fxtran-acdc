INTEGER*8 FUNCTION SIZE_TMODERR (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TMODERR),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LFSTEPERR, LMSTEPERR, LTSTEPERR

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%LSCALERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSCALERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LTAPERERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LTAPERERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NTYPE_MODERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NTYPE_MODERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NSTEP_MODERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSTEP_MODERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NORDER_MODERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NORDER_MODERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NPRTMODERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NPRTMODERR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TAPERERR_INI)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TAPERERR_INI'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TAPERERR_END)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TAPERERR_END'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TSTEP_ERR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TSTEP_ERR'
ENDIF
KSIZE = KSIZE + ISIZE
LMSTEPERR = ALLOCATED (YD%MSTEPERR)
IF (LMSTEPERR) THEN
  ISIZE = KIND (YD%MSTEPERR) * SIZE (YD%MSTEPERR)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MSTEPERR'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTSTEPERR = ALLOCATED (YD%TSTEPERR)
IF (LTSTEPERR) THEN
  ISIZE = KIND (YD%TSTEPERR) * SIZE (YD%TSTEPERR)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TSTEPERR'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LFSTEPERR = ALLOCATED (YD%FSTEPERR)
IF (LFSTEPERR) THEN
  ISIZE = KIND (YD%FSTEPERR) * SIZE (YD%FSTEPERR)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%FSTEPERR'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

