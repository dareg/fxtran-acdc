SUBROUTINE SAVE_TSWE (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSWE), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%ALPHASWE
WRITE (KLUN) YD%GMUCENSWE
END SUBROUTINE

SUBROUTINE LOAD_TSWE (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TSWE), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) ZTMP0
YD%ALPHASWE = ZTMP0
READ (KLUN) ZTMP0
YD%GMUCENSWE = ZTMP0
END SUBROUTINE


SUBROUTINE COPY_TSWE (YD, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TSWE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF


END SUBROUTINE

SUBROUTINE HOST_TSWE (YD)

IMPLICIT NONE
