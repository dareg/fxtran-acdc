  READ (KLUN) ZTMP3
  YD%HDCS0 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LHDCS1
IF (LHDCS1) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%HDCS1 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%HDCS1,1):UBOUND(YD%HDCS1,1), LBOUND(YD%HDCS1,2):UBOUND(YD%HDCS1,2), LBOUND(YD%HDCS1,3):UBOUND(YD%HDCS1,3)))
  READ (KLUN) ZTMP3
  YD%HDCS1 = ZTMP3
  DEALLOCATE (ZTMP3)
ENDIF
READ (KLUN) LPDDHFSVI
IF (LPDDHFSVI) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%PDDHFSVI (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%PDDHFSVI,1):UBOUND(YD%PDDHFSVI,1)))
  READ (KLUN) ZTMP1
  YD%PDDHFSVI = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LCFLDNAMES3D
IF (LCFLDNAMES3D) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
