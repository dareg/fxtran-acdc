LOGICAL :: LCFLDNAMES2D, LCFLDNAMES3D, LCFLDTYPES2D, LCFLDTYPES3D, LHDCS0, LHDCS1, LHDCVB0, LHDCVB1, LPDDHFSVI
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP3 (:,:,:)
READ (KLUN) LHDCVB0
IF (LHDCVB0) THEN
  READ (KLUN) IL3
  READ (KLUN) IU3
  ALLOCATE (YD%HDCVB0 (IL3(1):IU3(1), IL3(2):IU3(2), IL3(3):IU3(3)))
  ALLOCATE (ZTMP3(LBOUND(YD%HDCVB0,1):UBOUND(YD%HDCVB0,1), LBOUND(YD%HDCVB0,2):UBOUND(YD%HDCVB0,2), LBOUND(YD%HDCVB0,3):UBOUND(YD%HDCVB0,3)))
