SUBROUTINE LOAD_TPHYDS (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TPHYDS), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%JPVXP
READ (KLUN) YD%JPVXP2
READ (KLUN) YD%JPCXP
READ (KLUN) YD%JPVEXTR
READ (KLUN) YD%JPVEXTRDI
READ (KLUN) YD%JPVXTR2
READ (KLUN) YD%JPVPRECIP
READ (KLUN) YD%JPVPRECIP2
READ (KLUN) YD%CEXTRP
READ (KLUN) YD%CXTRP2
READ (KLUN) YD%CVEXTRA
READ (KLUN) YD%CVEXRAD
READ (KLUN) YD%CVEXTRDI
READ (KLUN) YD%CVEXTR2
READ (KLUN) YD%CVPRECIP
READ (KLUN) YD%CVPRECIP2
READ (KLUN) YD%NEXTRPGB
READ (KLUN) YD%NXTRP2GB
READ (KLUN) YD%NVEXTRAGB
READ (KLUN) YD%NVEXRADGB
READ (KLUN) YD%NVEXTR2GB
READ (KLUN) YD%NVEXTRDIGB
READ (KLUN) YD%NVPRECIPGB
READ (KLUN) YD%NVPRECIPGB2
READ (KLUN) YD%NSFLUX
READ (KLUN) YD%NSFORC
READ (KLUN) YD%NPPVCLIX
ALLOCATE (ZTMP2(LBOUND(YD%EXTRPDF,1):UBOUND(YD%EXTRPDF,1), LBOUND(YD%EXTRPDF,2):UBOUND(YD%EXTRPDF,2)))
READ (KLUN) ZTMP2
YD%EXTRPDF = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP1(LBOUND(YD%XTRP2DF,1):UBOUND(YD%XTRP2DF,1)))
READ (KLUN) ZTMP1
YD%XTRP2DF = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VEXTRADF,1):UBOUND(YD%VEXTRADF,1)))
READ (KLUN) ZTMP1
YD%VEXTRADF = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VEXRADDF,1):UBOUND(YD%VEXRADDF,1)))
READ (KLUN) ZTMP1
YD%VEXRADDF = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VEXTR2DF,1):UBOUND(YD%VEXTR2DF,1)))
READ (KLUN) ZTMP1
YD%VEXTR2DF = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VEXTRDI,1):UBOUND(YD%VEXTRDI,1)))
READ (KLUN) ZTMP1
YD%VEXTRDI = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VPRECIP,1):UBOUND(YD%VPRECIP,1)))
READ (KLUN) ZTMP1
YD%VPRECIP = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%VPRECIP2,1):UBOUND(YD%VPRECIP2,1)))
READ (KLUN) ZTMP1
YD%VPRECIP2 = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) YD%LREQIN_VEXTRDI
READ (KLUN) YD%LREQOUT_VEXTRDI
END SUBROUTINE


