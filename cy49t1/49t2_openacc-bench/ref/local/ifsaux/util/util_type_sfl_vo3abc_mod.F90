SUBROUTINE LOAD_TYPE_SFL_VO3ABC (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VO3ABC), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYA, LYB, LYC, LYVC
READ (KLUN) LYA
IF (LYA) THEN
  ALLOCATE (YD%YA)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YA)
ELSE
  NULLIFY (YD%YA)
ENDIF
READ (KLUN) LYB
IF (LYB) THEN
  ALLOCATE (YD%YB)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YB)
ELSE
  NULLIFY (YD%YB)
ENDIF
READ (KLUN) LYC
IF (LYC) THEN
  ALLOCATE (YD%YC)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YC)
ELSE
  NULLIFY (YD%YC)
ENDIF
READ (KLUN) LYVC
IF (LYVC) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YVC (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YVC, 1), UBOUND (YD%YVC, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVC (J1))
  ENDDO
ELSE
  NULLIFY (YD%YVC)
ENDIF
END SUBROUTINE


