SUBROUTINE LOAD_TEAERLID (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERLID), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%NAELID
READ (KLUN) YD%NWLID
READ (KLUN) YD%NAERLISI
READ (KLUN) YD%JWLID
ALLOCATE (ZTMP1(LBOUND(YD%RWLID,1):UBOUND(YD%RWLID,1)))
READ (KLUN) ZTMP1
YD%RWLID = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RLICO2,1):UBOUND(YD%RLICO2,1)))
READ (KLUN) ZTMP1
YD%RLICO2 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RLIO2,1):UBOUND(YD%RLIO2,1)))
READ (KLUN) ZTMP1
YD%RLIO2 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP2(LBOUND(YD%RLINO2,1):UBOUND(YD%RLINO2,1), LBOUND(YD%RLINO2,2):UBOUND(YD%RLINO2,2)))
READ (KLUN) ZTMP2
YD%RLINO2 = ZTMP2
DEALLOCATE (ZTMP2)
ALLOCATE (ZTMP2(LBOUND(YD%RLIO3,1):UBOUND(YD%RLIO3,1), LBOUND(YD%RLIO3,2):UBOUND(YD%RLIO3,2)))
READ (KLUN) ZTMP2
YD%RLIO3 = ZTMP2
DEALLOCATE (ZTMP2)
READ (KLUN) ZTMP0
YD%RLICLS = ZTMP0
READ (KLUN) ZTMP0
YD%RLIDELT = ZTMP0
READ (KLUN) ZTMP0
YD%RLINS = ZTMP0
READ (KLUN) ZTMP0
YD%RLIPREF = ZTMP0
READ (KLUN) ZTMP0
YD%RLITREF = ZTMP0
READ (KLUN) ZTMP0
YD%RLIT0 = ZTMP0
READ (KLUN) ZTMP0
YD%RLIDDZ = ZTMP0
END SUBROUTINE


