SUBROUTINE CRC64_TECND (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TECND), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REPFLM), CDPATH//'%REPFLM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%REPQMI), CDPATH//'%REPQMI'

END SUBROUTINE

SUBROUTINE WIPE_TECND (SELF, LDDELETED, LDFIELDAPI)

