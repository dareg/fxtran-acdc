SUBROUTINE SAVE_TECND (SELF, KLUN)

IMPLICIT NONE
TYPE (TECND), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%REPFLM
WRITE (KLUN) SELF%REPQMI
END SUBROUTINE

SUBROUTINE LOAD_TECND (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
TYPE (TECND), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) ZTMP0
SELF%REPFLM = ZTMP0
READ (KLUN) ZTMP0
SELF%REPQMI = ZTMP0
END SUBROUTINE


