SUBROUTINE COPY_CPG_TNDSI_DDH_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_VARIABLE_3RB_MOD
IMPLICIT NONE
TYPE (CPG_TNDSI_DDH_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZDATA_GMV, LZVIEW_GMV

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LZDATA_GMV = ASSOCIATED (SELF%ZDATA_GMV)
IF (LZDATA_GMV) THEN
  !$ACC ENTER DATA CREATE (SELF%ZDATA_GMV)
  !$ACC UPDATE DEVICE (SELF%ZDATA_GMV)
  !$ACC ENTER DATA ATTACH (SELF%ZDATA_GMV)
ENDIF

LZVIEW_GMV = ASSOCIATED (SELF%ZVIEW_GMV)
IF (LZVIEW_GMV) THEN
  !$ACC ENTER DATA CREATE (SELF%ZVIEW_GMV)
  !$ACC UPDATE DEVICE (SELF%ZVIEW_GMV)
  !$ACC ENTER DATA ATTACH (SELF%ZVIEW_GMV)
ENDIF

CALL COPY_VARIABLE_3RB (SELF%U, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%V, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%T, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%PD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%VD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_CPG_TNDSI_DDH_TYPE (SELF)
USE UTIL_VARIABLE_3RB_MOD

IMPLICIT NONE
TYPE (CPG_TNDSI_DDH_TYPE), TARGET :: SELF
