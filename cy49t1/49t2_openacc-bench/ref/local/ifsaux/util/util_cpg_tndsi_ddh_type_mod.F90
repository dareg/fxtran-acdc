SUBROUTINE COPY_CPG_TNDSI_DDH_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_VARIABLE_3RB_MOD
IMPLICIT NONE
TYPE (CPG_TNDSI_DDH_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LZDATA_GMV, LZVIEW_GMV

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LZDATA_GMV = ASSOCIATED (YD%ZDATA_GMV)
IF (LZDATA_GMV) THEN
  !$ACC ENTER DATA CREATE (YD%ZDATA_GMV)
  !$ACC UPDATE DEVICE (YD%ZDATA_GMV)
  !$ACC ENTER DATA ATTACH (YD%ZDATA_GMV)
ENDIF

LZVIEW_GMV = ASSOCIATED (YD%ZVIEW_GMV)
IF (LZVIEW_GMV) THEN
  !$ACC ENTER DATA CREATE (YD%ZVIEW_GMV)
  !$ACC UPDATE DEVICE (YD%ZVIEW_GMV)
  !$ACC ENTER DATA ATTACH (YD%ZVIEW_GMV)
ENDIF

CALL COPY_VARIABLE_3RB (YD%U, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (YD%V, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (YD%T, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (YD%PD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (YD%VD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_CPG_TNDSI_DDH_TYPE (YD)
USE UTIL_VARIABLE_3RB_MOD

IMPLICIT NONE
