MODULE UTIL_CPG_SL1B_2D_TYPE_MOD

USE CPG_SL1_TYPE_MOD, ONLY : CPG_SL1B_2D_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_SL1B_2D_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_SL1B_2D_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_SL1B_2D_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_SL1B_2D_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL1B_2D_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P)
  !$ACC ENTER DATA ATTACH (YD%F_P)
ENDIF

LF_P_F = ASSOCIATED (YD%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P_F)
  !$ACC ENTER DATA ATTACH (YD%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (YD%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P_SPF)
  !$ACC ENTER DATA ATTACH (YD%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (YD%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P_SP)
  !$ACC ENTER DATA ATTACH (YD%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (YD%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P_P)
  !$ACC ENTER DATA ATTACH (YD%F_P_P)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_CPG_SL1B_2D_TYPE (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (CPG_SL1B_2D_TYPE), TARGET :: YD
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_2RB (YD%F_P)
ENDIF

LF_P_F = ASSOCIATED (YD%F_P_F)
IF (LF_P_F) THEN
  CALL HOST_FIELD_2RB (YD%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (YD%F_P_SPF)
IF (LF_P_SPF) THEN
  CALL HOST_FIELD_2RB (YD%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (YD%F_P_SP)
IF (LF_P_SP) THEN
  CALL HOST_FIELD_2RB (YD%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (YD%F_P_P)
IF (LF_P_P) THEN
  CALL HOST_FIELD_2RB (YD%F_P_P)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_SL1B_2D_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL1B_2D_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P, LF_P_F, LF_P_P, LF_P_SP, LF_P_SPF

