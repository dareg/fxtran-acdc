IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P)
ENDIF

LF_P_F = ASSOCIATED (YD%F_P_F)
IF (LF_P_F .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P_F)
ENDIF

LF_P_SPF = ASSOCIATED (YD%F_P_SPF)
IF (LF_P_SPF .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P_SPF)
ENDIF

LF_P_SP = ASSOCIATED (YD%F_P_SP)
IF (LF_P_SP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P_SP)
ENDIF

LF_P_P = ASSOCIATED (YD%F_P_P)
IF (LF_P_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P_P)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



