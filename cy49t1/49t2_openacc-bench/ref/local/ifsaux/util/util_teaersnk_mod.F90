ALLOCATE (ZTMP1(LBOUND(YD%RSSGROWTH_RHTAB15,1):UBOUND(YD%RSSGROWTH_RHTAB15,1)))
READ (KLUN) ZTMP1
YD%RSSGROWTH_RHTAB15 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RSSDENS_RHTAB15,1):UBOUND(YD%RSSDENS_RHTAB15,1)))
READ (KLUN) ZTMP1
YD%RSSDENS_RHTAB15 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RMMD_DD,1):UBOUND(YD%RMMD_DD,1)))
READ (KLUN) ZTMP1
YD%RMMD_DD = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RRHO_DD,1):UBOUND(YD%RRHO_DD,1)))
READ (KLUN) ZTMP1
YD%RRHO_DD = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RMMD_NI,1):UBOUND(YD%RMMD_NI,1)))
READ (KLUN) ZTMP1
YD%RMMD_NI = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RRHO_NI,1):UBOUND(YD%RRHO_NI,1)))
READ (KLUN) ZTMP1
YD%RRHO_NI = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RMMD_SS,1):UBOUND(YD%RMMD_SS,1)))
READ (KLUN) ZTMP1
YD%RMMD_SS = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RRHO_SS,1):UBOUND(YD%RRHO_SS,1)))
READ (KLUN) ZTMP1
YD%RRHO_SS = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%RHO_WAT = ZTMP0
READ (KLUN) ZTMP0
YD%RHO_ICE = ZTMP0
READ (KLUN) ZTMP0
YD%RHAMAKER = ZTMP0
READ (KLUN) ZTMP0
YD%RSO2CV1 = ZTMP0
READ (KLUN) ZTMP0
YD%RSO2CV2 = ZTMP0
READ (KLUN) ZTMP0
YD%RSUCV1 = ZTMP0
READ (KLUN) ZTMP0
YD%RSUCV2 = ZTMP0
READ (KLUN) ZTMP0
YD%RVSO2CV1 = ZTMP0
READ (KLUN) ZTMP0
YD%RVSO2CV2 = ZTMP0
END SUBROUTINE


