SUBROUTINE LOAD_TEAERSNK (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TEAERSNK), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%NDRYDEPVEL_DYN
READ (KLUN) YD%NDRYDEP
READ (KLUN) ZTMP0
YD%R_R = ZTMP0
READ (KLUN) ZTMP0
YD%R_S = ZTMP0
ALLOCATE (ZTMP1(LBOUND(YD%RAERTS,1):UBOUND(YD%RAERTS,1)))
READ (KLUN) ZTMP1
YD%RAERTS = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%RFRAER = ZTMP0
ALLOCATE (ZTMP1(LBOUND(YD%RRHTAB,1):UBOUND(YD%RRHTAB,1)))
READ (KLUN) ZTMP1
YD%RRHTAB = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RRHTAB15,1):UBOUND(YD%RRHTAB15,1)))
READ (KLUN) ZTMP1
YD%RRHTAB15 = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RSSGROWTH_RHTAB,1):UBOUND(YD%RSSGROWTH_RHTAB,1)))
READ (KLUN) ZTMP1
YD%RSSGROWTH_RHTAB = ZTMP1
DEALLOCATE (ZTMP1)
ALLOCATE (ZTMP1(LBOUND(YD%RSSDENS_RHTAB,1):UBOUND(YD%RSSDENS_RHTAB,1)))
READ (KLUN) ZTMP1
YD%RSSDENS_RHTAB = ZTMP1
