SUBROUTINE CRC64_VARIABLE_BASE (YD, KLUN, CDPATH)
USE UTIL_TYPE_GFL_COMP_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (VARIABLE_BASE), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NAME), CDPATH//'%NAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LACTIVE), CDPATH//'%LACTIVE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LT1), CDPATH//'%LT1'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LT9), CDPATH//'%LT9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LPH9), CDPATH//'%LPH9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LDL), CDPATH//'%LDL'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LDM), CDPATH//'%LDM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LDL9), CDPATH//'%LDL9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LDM9), CDPATH//'%LDM9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LPC_PH), CDPATH//'%LPC_PH'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LADV), CDPATH//'%LADV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LGP), CDPATH//'%LGP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LWATER), CDPATH//'%LWATER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LTHERMACT), CDPATH//'%LTHERMACT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LCDERS), CDPATH//'%LCDERS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RCP), CDPATH//'%RCP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%R), CDPATH//'%R'

CLIND = ''
CALL CRC64_TYPE_GFL_COMP (YD%YCOMP, KLUN, CDPATH//'%YCOMP'//TRIM(CLIND))

END SUBROUTINE

