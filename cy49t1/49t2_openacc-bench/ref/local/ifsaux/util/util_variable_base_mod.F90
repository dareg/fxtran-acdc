ISIZE = KIND (YD%LT9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LT9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LPH9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LPH9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LDL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LDM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LDL9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDL9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LDM9)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LDM9'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LPC_PH)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LPC_PH'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LADV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LADV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LGP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LGP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LWATER)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWATER'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LTHERMACT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LTHERMACT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LCDERS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LCDERS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RCP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RCP'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%R)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%R'
ENDIF
KSIZE = KSIZE + ISIZE
