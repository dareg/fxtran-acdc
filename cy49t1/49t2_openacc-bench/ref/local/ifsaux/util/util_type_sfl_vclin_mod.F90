  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YVN (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YVN, 1), UBOUND (SELF%YVN, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YVN (J1), KLUN)
  ENDDO
