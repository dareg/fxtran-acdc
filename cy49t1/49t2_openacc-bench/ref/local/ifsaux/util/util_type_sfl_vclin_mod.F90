SUBROUTINE LOAD_TYPE_SFL_VCLIN (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_VCLIN), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYACCPR, LYACCPR5, LYACPR, LYBAS, LYTOP, LYVN
READ (KLUN) LYTOP
IF (LYTOP) THEN
  ALLOCATE (YD%YTOP)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YTOP)
ELSE
  NULLIFY (YD%YTOP)
ENDIF
READ (KLUN) LYBAS
IF (LYBAS) THEN
  ALLOCATE (YD%YBAS)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YBAS)
ELSE
  NULLIFY (YD%YBAS)
ENDIF
READ (KLUN) LYACPR
IF (LYACPR) THEN
  ALLOCATE (YD%YACPR)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YACPR)
ELSE
  NULLIFY (YD%YACPR)
ENDIF
READ (KLUN) LYACCPR
IF (LYACCPR) THEN
  ALLOCATE (YD%YACCPR)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YACCPR)
ELSE
  NULLIFY (YD%YACCPR)
ENDIF
READ (KLUN) LYACCPR5
IF (LYACCPR5) THEN
  ALLOCATE (YD%YACCPR5)
  CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YACCPR5)
ELSE
  NULLIFY (YD%YACCPR5)
ENDIF
READ (KLUN) LYVN
IF (LYVN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YVN (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YVN, 1), UBOUND (YD%YVN, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (KLUN, YD%YVN (J1))
  ENDDO
ELSE
  NULLIFY (YD%YVN)
ENDIF
END SUBROUTINE


