SUBROUTINE LOAD_TYPE_SFL_VCLIN (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_VCLIN), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYACCPR, LYACCPR5, LYACPR, LYBAS, LYTOP, LYVN
READ (KLUN) LYTOP
IF (LYTOP) THEN
  ALLOCATE (SELF%YTOP)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YTOP, KLUN)
ELSE
  NULLIFY (SELF%YTOP)
ENDIF
READ (KLUN) LYBAS
IF (LYBAS) THEN
  ALLOCATE (SELF%YBAS)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YBAS, KLUN)
ELSE
  NULLIFY (SELF%YBAS)
ENDIF
READ (KLUN) LYACPR
IF (LYACPR) THEN
  ALLOCATE (SELF%YACPR)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YACPR, KLUN)
ELSE
  NULLIFY (SELF%YACPR)
ENDIF
READ (KLUN) LYACCPR
IF (LYACCPR) THEN
  ALLOCATE (SELF%YACCPR)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YACCPR, KLUN)
ELSE
  NULLIFY (SELF%YACCPR)
ENDIF
READ (KLUN) LYACCPR5
IF (LYACCPR5) THEN
  ALLOCATE (SELF%YACCPR5)
  CALL LOAD_TYPE_SURF_MTL_2D (SELF%YACCPR5, KLUN)
ELSE
  NULLIFY (SELF%YACCPR5)
ENDIF
READ (KLUN) LYVN
