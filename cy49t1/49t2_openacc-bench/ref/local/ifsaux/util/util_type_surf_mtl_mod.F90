SUBROUTINE CRC64_TYPE_SURF_MTL (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SURF_MTL), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%MP), CDPATH//'%MP'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%MP0), CDPATH//'%MP0'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%MP9), CDPATH//'%MP9'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%MP1), CDPATH//'%MP1'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ITRAJ), CDPATH//'%ITRAJ'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IBUGFINDER), CDPATH//'%IBUGFINDER'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSET), CDPATH//'%LSET'

END SUBROUTINE

