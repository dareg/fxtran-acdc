INTEGER*8 FUNCTION SIZE_TVDF (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TVDF),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%RLAM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RLAM'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RVDIFTS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RVDIFTS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LWDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LWDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%REPS1WDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REPS1WDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%REPS2WDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REPS2WDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RETAWDS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RETAWDS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RTOFDALPHA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RTOFDALPHA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%REISTHSC)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%REISTHSC'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NSUBST)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NSUBST'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RFAC_TWO_COEF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RFAC_TWO_COEF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RZC_H)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RZC_H'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%RZL_INF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%RZL_INF'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

