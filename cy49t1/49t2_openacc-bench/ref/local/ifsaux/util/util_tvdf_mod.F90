SUBROUTINE CRC64_TVDF (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TVDF), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RLAM), CDPATH//'%RLAM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RVDIFTS), CDPATH//'%RVDIFTS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LWDS), CDPATH//'%LWDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%REPS1WDS), CDPATH//'%REPS1WDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%REPS2WDS), CDPATH//'%REPS2WDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RETAWDS), CDPATH//'%RETAWDS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RTOFDALPHA), CDPATH//'%RTOFDALPHA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%REISTHSC), CDPATH//'%REISTHSC'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NSUBST), CDPATH//'%NSUBST'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RFAC_TWO_COEF), CDPATH//'%RFAC_TWO_COEF'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RZC_H), CDPATH//'%RZC_H'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%RZL_INF), CDPATH//'%RZL_INF'

END SUBROUTINE

