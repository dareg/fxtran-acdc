INTEGER*8 FUNCTION SIZE_TEMMP (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TEMMP),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LMYENS, LNEALLNS, LNEPROCN, LNEPTRNS, LNUEMPP

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LNEPROCN = ASSOCIATED (YD%NEPROCN)
IF (LNEPROCN) THEN
  ISIZE = KIND (YD%NEPROCN) * SIZE (YD%NEPROCN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEPROCN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%NUEMP)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NUEMP'
ENDIF
KSIZE = KSIZE + ISIZE
LMYENS = ASSOCIATED (YD%MYENS)
IF (LMYENS) THEN
  ISIZE = KIND (YD%MYENS) * SIZE (YD%MYENS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%MYENS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNUEMPP = ASSOCIATED (YD%NUEMPP)
IF (LNUEMPP) THEN
  ISIZE = KIND (YD%NUEMPP) * SIZE (YD%NUEMPP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NUEMPP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNEALLNS = ASSOCIATED (YD%NEALLNS)
IF (LNEALLNS) THEN
  ISIZE = KIND (YD%NEALLNS) * SIZE (YD%NEALLNS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEALLNS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNEPTRNS = ASSOCIATED (YD%NEPTRNS)
IF (LNEPTRNS) THEN
  ISIZE = KIND (YD%NEPTRNS) * SIZE (YD%NEPTRNS)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NEPTRNS'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

