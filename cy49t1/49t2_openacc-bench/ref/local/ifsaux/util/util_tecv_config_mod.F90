MODULE UTIL_TECV_CONFIG_MOD

USE TYPE_ECV, ONLY : TECV_CONFIG

INTERFACE SAVE
MODULE PROCEDURE SAVE_TECV_CONFIG
END INTERFACE

INTERFACE LOAD
MODULE PROCEDURE LOAD_TECV_CONFIG
END INTERFACE

INTERFACE COPY
MODULE PROCEDURE COPY_TECV_CONFIG
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_TECV_CONFIG
END INTERFACE

     
INTERFACE CRC64
MODULE PROCEDURE CRC64_TECV_CONFIG
END INTERFACE

INTERFACE WIPE
MODULE PROCEDURE WIPE_TECV_CONFIG
END INTERFACE

INTERFACE SIZE
MODULE PROCEDURE SIZE_TECV_CONFIG
END INTERFACE


CONTAINS

SUBROUTINE SAVE_TECV_CONFIG (KLUN, YD)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYRCONFIG
WRITE (KLUN) YD%NECV
LYRCONFIG = ALLOCATED (YD%YRCONFIG)
WRITE (KLUN) LYRCONFIG
IF (LYRCONFIG) THEN
  WRITE (KLUN) LBOUND (YD%YRCONFIG)
  WRITE (KLUN) UBOUND (YD%YRCONFIG)
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    CALL SAVE_TECV_CONFIG_BASE (KLUN, YD%YRCONFIG (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TECV_CONFIG (KLUN, YD)
USE UTIL_TECV_CONFIG_BASE_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TECV_CONFIG), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYRCONFIG
READ (KLUN) YD%NECV
READ (KLUN) LYRCONFIG
IF (LYRCONFIG) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YRCONFIG (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    CALL LOAD_TECV_CONFIG_BASE (KLUN, YD%YRCONFIG (J1))
  ENDDO
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TECV_CONFIG (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRCONFIG

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF

LYRCONFIG = ALLOCATED (YD%YRCONFIG)
IF (LYRCONFIG) THEN
  !$ACC ENTER DATA CREATE (YD%YRCONFIG)
  !$ACC UPDATE DEVICE (YD%YRCONFIG)
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    CALL COPY_TECV_CONFIG_BASE (YD%YRCONFIG (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YRCONFIG)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TECV_CONFIG (YD)
USE UTIL_TECV_CONFIG_BASE_MOD

IMPLICIT NONE
TYPE (TECV_CONFIG), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYRCONFIG

LYRCONFIG = ALLOCATED (YD%YRCONFIG)
IF (LYRCONFIG) THEN
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    CALL HOST_TECV_CONFIG_BASE (YD%YRCONFIG (J1))
  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TECV_CONFIG (YD, KLUN, CDPATH)
USE UTIL_TECV_CONFIG_BASE_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TECV_CONFIG), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYRCONFIG
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NECV), CDPATH//'%NECV'

LYRCONFIG = ALLOCATED (YD%YRCONFIG)
IF (LYRCONFIG) THEN
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TECV_CONFIG_BASE (YD%YRCONFIG (J1), KLUN, CDPATH//'%YRCONFIG'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

SUBROUTINE WIPE_TECV_CONFIG (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRCONFIG

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LYRCONFIG = ALLOCATED (YD%YRCONFIG)
IF (LYRCONFIG) THEN
  !$ACC EXIT DATA DETACH (YD%YRCONFIG)
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    CALL WIPE_TECV_CONFIG_BASE (YD%YRCONFIG (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%YRCONFIG)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_TECV_CONFIG (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TECV_CONFIG_BASE_MOD
IMPLICIT NONE
TYPE (TECV_CONFIG),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
INTEGER :: J1
LOGICAL :: LYRCONFIG

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NECV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NECV'
ENDIF
KSIZE = KSIZE + ISIZE
LYRCONFIG = ALLOCATED (YD%YRCONFIG)
IF (LYRCONFIG) THEN
  JSIZE = 0
  DO J1 = LBOUND (YD%YRCONFIG, 1), UBOUND (YD%YRCONFIG, 1)
    ISIZE = SIZE_TECV_CONFIG_BASE (YD%YRCONFIG (J1), CLPATH//'%YRCONFIG', .FALSE.)
    JSIZE = JSIZE + ISIZE
    KSIZE = KSIZE + ISIZE
  ENDDO
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%YRCONFIG'
  ENDIF
ENDIF
END FUNCTION

END MODULE
