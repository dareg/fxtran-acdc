  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (YD%GMVTNDSI_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(YD%GMVTNDSI_DDH,1):UBOUND(YD%GMVTNDSI_DDH,1), LBOUND(YD%GMVTNDSI_DDH,2):UBOUND(YD%GMVTNDSI_DDH,2), LBOUND(YD%GMVTNDSI_DDH,3):UBOUND(YD%GMVTNDSI_DDH,3), LBOUND(YD%GMVTNDSI_DDH,4):UBOUND(YD%GMVTNDSI_DDH,4)))
  READ (KLUN) ZTMP4
