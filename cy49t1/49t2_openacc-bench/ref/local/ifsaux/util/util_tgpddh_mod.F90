  READ (KLUN) IL4
  READ (KLUN) IU4
  ALLOCATE (YD%GMVTNDSL_DDH (IL4(1):IU4(1), IL4(2):IU4(2), IL4(3):IU4(3), IL4(4):IU4(4)))
  ALLOCATE (ZTMP4(LBOUND(YD%GMVTNDSL_DDH,1):UBOUND(YD%GMVTNDSL_DDH,1), LBOUND(YD%GMVTNDSL_DDH,2):UBOUND(YD%GMVTNDSL_DDH,2), LBOUND(YD%GMVTNDSL_DDH,3):UBOUND(YD%GMVTNDSL_DDH,3), LBOUND(YD%GMVTNDSL_DDH,4):UBOUND(YD%GMVTNDSL_DDH,4)))
