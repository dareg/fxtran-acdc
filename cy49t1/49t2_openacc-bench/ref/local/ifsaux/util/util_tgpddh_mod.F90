INTEGER*8 FUNCTION SIZE_TGPDDH (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TGPDDH),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGFLTNDHD_DDH, LGFLTNDSL_DDH, LGMVTNDHD_DDH, LGMVTNDSI_DDH, LGMVTNDSL_DDH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGMVTNDHD_DDH = ALLOCATED (YD%GMVTNDHD_DDH)
IF (LGMVTNDHD_DDH) THEN
  ISIZE = KIND (YD%GMVTNDHD_DDH) * SIZE (YD%GMVTNDHD_DDH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GMVTNDHD_DDH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LGFLTNDHD_DDH = ALLOCATED (YD%GFLTNDHD_DDH)
