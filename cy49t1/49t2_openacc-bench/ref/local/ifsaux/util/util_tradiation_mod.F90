SUBROUTINE CRC64_TRADIATION (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TRADIATION), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NWEIGHT_UV), CDPATH//'%NWEIGHT_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IBAND_UV), CDPATH//'%IBAND_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%WEIGHT_UV), CDPATH//'%WEIGHT_UV'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NWEIGHT_PAR), CDPATH//'%NWEIGHT_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IBAND_PAR), CDPATH//'%IBAND_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%WEIGHT_PAR), CDPATH//'%WEIGHT_PAR'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%TROP_BG_AER_MASS_EXT), CDPATH//'%TROP_BG_AER_MASS_EXT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%STRAT_BG_AER_MASS_EXT), CDPATH//'%STRAT_BG_AER_MASS_EXT'

END SUBROUTINE

