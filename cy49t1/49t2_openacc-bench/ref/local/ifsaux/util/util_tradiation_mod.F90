SUBROUTINE SAVE_TRADIATION (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TRADIATION), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%NWEIGHT_UV
WRITE (KLUN) YD%IBAND_UV
WRITE (KLUN) YD%WEIGHT_UV
WRITE (KLUN) YD%NWEIGHT_PAR
WRITE (KLUN) YD%IBAND_PAR
WRITE (KLUN) YD%WEIGHT_PAR
WRITE (KLUN) YD%TROP_BG_AER_MASS_EXT
WRITE (KLUN) YD%STRAT_BG_AER_MASS_EXT
END SUBROUTINE

SUBROUTINE LOAD_TRADIATION (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TRADIATION), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%NWEIGHT_UV
READ (KLUN) YD%IBAND_UV
ALLOCATE (ZTMP1(LBOUND(YD%WEIGHT_UV,1):UBOUND(YD%WEIGHT_UV,1)))
READ (KLUN) ZTMP1
YD%WEIGHT_UV = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) YD%NWEIGHT_PAR
READ (KLUN) YD%IBAND_PAR
ALLOCATE (ZTMP1(LBOUND(YD%WEIGHT_PAR,1):UBOUND(YD%WEIGHT_PAR,1)))
READ (KLUN) ZTMP1
YD%WEIGHT_PAR = ZTMP1
DEALLOCATE (ZTMP1)
READ (KLUN) ZTMP0
YD%TROP_BG_AER_MASS_EXT = ZTMP0
READ (KLUN) ZTMP0
YD%STRAT_BG_AER_MASS_EXT = ZTMP0
END SUBROUTINE


