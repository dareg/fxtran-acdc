INTEGER*8 FUNCTION SIZE_TRADIATION (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TRADIATION),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NWEIGHT_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWEIGHT_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%IBAND_UV) * SIZE (YD%IBAND_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IBAND_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%WEIGHT_UV) * SIZE (YD%WEIGHT_UV)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%WEIGHT_UV'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NWEIGHT_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NWEIGHT_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%IBAND_PAR) * SIZE (YD%IBAND_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%IBAND_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%WEIGHT_PAR) * SIZE (YD%WEIGHT_PAR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%WEIGHT_PAR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%TROP_BG_AER_MASS_EXT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TROP_BG_AER_MASS_EXT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%STRAT_BG_AER_MASS_EXT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%STRAT_BG_AER_MASS_EXT'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

