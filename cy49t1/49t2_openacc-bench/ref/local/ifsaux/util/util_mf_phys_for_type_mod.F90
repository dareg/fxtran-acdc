MODULE UTIL_MF_PHYS_FOR_TYPE_MOD

USE MF_PHYS_TYPE_MOD, ONLY : MF_PHYS_FOR_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_MF_PHYS_FOR_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_MF_PHYS_FOR_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_MF_PHYS_FOR_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_MF_PHYS_FOR_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_FOR_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_Q, LF_T, LF_U, LF_V

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_U = ASSOCIATED (YD%F_U)
IF (LF_U .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_U)
  !$ACC ENTER DATA ATTACH (YD%F_U)
ENDIF

LF_V = ASSOCIATED (YD%F_V)
IF (LF_V .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_V)
  !$ACC ENTER DATA ATTACH (YD%F_V)
ENDIF

LF_T = ASSOCIATED (YD%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_T)
  !$ACC ENTER DATA ATTACH (YD%F_T)
ENDIF

LF_Q = ASSOCIATED (YD%F_Q)
IF (LF_Q .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (YD%F_Q)
  !$ACC ENTER DATA ATTACH (YD%F_Q)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_MF_PHYS_FOR_TYPE (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (MF_PHYS_FOR_TYPE), TARGET :: YD
LOGICAL :: LF_Q, LF_T, LF_U, LF_V
LF_U = ASSOCIATED (YD%F_U)
IF (LF_U) THEN
  CALL HOST_FIELD_3RB (YD%F_U)
ENDIF

LF_V = ASSOCIATED (YD%F_V)
IF (LF_V) THEN
  CALL HOST_FIELD_3RB (YD%F_V)
ENDIF

LF_T = ASSOCIATED (YD%F_T)
IF (LF_T) THEN
  CALL HOST_FIELD_3RB (YD%F_T)
ENDIF

LF_Q = ASSOCIATED (YD%F_Q)
IF (LF_Q) THEN
  CALL HOST_FIELD_3RB (YD%F_Q)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_MF_PHYS_FOR_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_FOR_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_Q, LF_T, LF_U, LF_V

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_U = ASSOCIATED (YD%F_U)
IF (LF_U .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_U)
ENDIF

LF_V = ASSOCIATED (YD%F_V)
IF (LF_V .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_V)
ENDIF

LF_T = ASSOCIATED (YD%F_T)
IF (LF_T .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_T)
ENDIF

LF_Q = ASSOCIATED (YD%F_Q)
IF (LF_Q .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (YD%F_Q)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
