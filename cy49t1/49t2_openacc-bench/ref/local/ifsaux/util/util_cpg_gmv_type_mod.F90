MODULE UTIL_CPG_GMV_TYPE_MOD

USE CPG_GMV_TYPE_MOD, ONLY : CPG_GMV_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_GMV_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_GMV_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_GMV_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_GMV_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_CPG_GMV_3D_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_GMV_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC ENTER DATA CREATE (SELF%ZVIEW)
  !$ACC UPDATE DEVICE (SELF%ZVIEW)
  !$ACC ENTER DATA ATTACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC ENTER DATA CREATE (SELF%ZDATA)
  !$ACC UPDATE DEVICE (SELF%ZDATA)
  !$ACC ENTER DATA ATTACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_DATA)
  !$ACC ENTER DATA ATTACH (SELF%F_DATA)
ENDIF

CALL COPY_CPG_GMV_3D_TYPE (SELF%U, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (SELF%V, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (SELF%T, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (SELF%SPD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (SELF%SVD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (SELF%NHX, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGMV = ALLOCATED (SELF%GMV)
IF (LGMV) THEN
  !$ACC ENTER DATA CREATE (SELF%GMV)
  !$ACC UPDATE DEVICE (SELF%GMV)
  DO J1 = LBOUND (SELF%GMV, 1), UBOUND (SELF%GMV, 1)
    CALL COPY_CPG_GMV_3D_TYPE (SELF%GMV (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%GMV)
ENDIF

END SUBROUTINE

