MODULE UTIL_CPG_GMV_TYPE_MOD

USE CPG_GMV_TYPE_MOD, ONLY : CPG_GMV_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_GMV_TYPE
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_GMV_TYPE
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_GMV_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_GMV_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_CPG_GMV_3D_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_GMV_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF

LZVIEW = ASSOCIATED (YD%ZVIEW)
IF (LZVIEW) THEN
  !$ACC ENTER DATA CREATE (YD%ZVIEW)
  !$ACC UPDATE DEVICE (YD%ZVIEW)
  !$ACC ENTER DATA ATTACH (YD%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (YD%ZDATA)
IF (LZDATA) THEN
  !$ACC ENTER DATA CREATE (YD%ZDATA)
  !$ACC UPDATE DEVICE (YD%ZDATA)
  !$ACC ENTER DATA ATTACH (YD%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (YD%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (YD%F_DATA)
  !$ACC ENTER DATA ATTACH (YD%F_DATA)
ENDIF

CALL COPY_CPG_GMV_3D_TYPE (YD%U, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (YD%V, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (YD%T, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (YD%SPD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (YD%SVD, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_CPG_GMV_3D_TYPE (YD%NHX, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGMV = ALLOCATED (YD%GMV)
IF (LGMV) THEN
  !$ACC ENTER DATA CREATE (YD%GMV)
  !$ACC UPDATE DEVICE (YD%GMV)
  DO J1 = LBOUND (YD%GMV, 1), UBOUND (YD%GMV, 1)
    CALL COPY_CPG_GMV_3D_TYPE (YD%GMV (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%GMV)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_CPG_GMV_TYPE (YD)
USE FIELD_UTIL_MODULE
USE UTIL_CPG_GMV_3D_TYPE_MOD

IMPLICIT NONE
TYPE (CPG_GMV_TYPE), TARGET :: YD
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW



LF_DATA = ASSOCIATED (YD%F_DATA)
IF (LF_DATA) THEN
  CALL HOST_FIELD_4RB (YD%F_DATA)
ENDIF

CALL HOST_CPG_GMV_3D_TYPE (YD%U)

CALL HOST_CPG_GMV_3D_TYPE (YD%V)

CALL HOST_CPG_GMV_3D_TYPE (YD%T)

CALL HOST_CPG_GMV_3D_TYPE (YD%SPD)

CALL HOST_CPG_GMV_3D_TYPE (YD%SVD)

CALL HOST_CPG_GMV_3D_TYPE (YD%NHX)

LGMV = ALLOCATED (YD%GMV)
IF (LGMV) THEN
  DO J1 = LBOUND (YD%GMV, 1), UBOUND (YD%GMV, 1)
    CALL HOST_CPG_GMV_3D_TYPE (YD%GMV (J1))
  ENDDO
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_GMV_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_CPG_GMV_3D_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_GMV_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LZVIEW = ASSOCIATED (YD%ZVIEW)
IF (LZVIEW) THEN
  !$ACC EXIT DATA DETACH (YD%ZVIEW)
  !$ACC EXIT DATA DETACH (YD%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (YD%ZDATA)
IF (LZDATA) THEN
  !$ACC EXIT DATA DETACH (YD%ZDATA)
  !$ACC EXIT DATA DETACH (YD%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (YD%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (YD%F_DATA)
ENDIF

CALL WIPE_CPG_GMV_3D_TYPE (YD%U, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (YD%V, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (YD%T, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (YD%SPD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (YD%SVD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (YD%NHX, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGMV = ALLOCATED (YD%GMV)
IF (LGMV) THEN
  !$ACC EXIT DATA DETACH (YD%GMV)
  DO J1 = LBOUND (YD%GMV, 1), UBOUND (YD%GMV, 1)
    CALL WIPE_CPG_GMV_3D_TYPE (YD%GMV (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (YD%GMV)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
