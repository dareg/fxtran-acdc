IF (LF_DATA) THEN
  CALL HOST_FIELD_4RB (SELF%F_DATA)
ENDIF

CALL HOST_CPG_GMV_3D_TYPE (SELF%U)

CALL HOST_CPG_GMV_3D_TYPE (SELF%V)

CALL HOST_CPG_GMV_3D_TYPE (SELF%T)

CALL HOST_CPG_GMV_3D_TYPE (SELF%SPD)

CALL HOST_CPG_GMV_3D_TYPE (SELF%SVD)

CALL HOST_CPG_GMV_3D_TYPE (SELF%NHX)

LGMV = ALLOCATED (SELF%GMV)
IF (LGMV) THEN
  DO J1 = LBOUND (SELF%GMV, 1), UBOUND (SELF%GMV, 1)
    CALL HOST_CPG_GMV_3D_TYPE (SELF%GMV (J1))
  ENDDO
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_CPG_GMV_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_CPG_GMV_3D_TYPE_MOD
IMPLICIT NONE
TYPE (CPG_GMV_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (SELF%F_DATA)
ENDIF

CALL WIPE_CPG_GMV_3D_TYPE (SELF%U, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (SELF%V, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (SELF%T, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (SELF%SPD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (SELF%SVD, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_CPG_GMV_3D_TYPE (SELF%NHX, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGMV = ALLOCATED (SELF%GMV)
IF (LGMV) THEN
  !$ACC EXIT DATA DETACH (SELF%GMV)
  DO J1 = LBOUND (SELF%GMV, 1), UBOUND (SELF%GMV, 1)
    CALL WIPE_CPG_GMV_3D_TYPE (SELF%GMV (J1), LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (SELF%GMV)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
