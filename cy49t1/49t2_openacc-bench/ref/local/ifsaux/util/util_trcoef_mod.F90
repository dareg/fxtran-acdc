  ISIZE = KIND (YD%TRMATSUM) * SIZE (YD%TRMATSUM)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TRMATSUM'
  ENDIF
  KSIZE = KSIZE + ISIZE
