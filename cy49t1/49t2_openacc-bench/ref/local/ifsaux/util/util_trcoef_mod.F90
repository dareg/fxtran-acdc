INTEGER*8 FUNCTION SIZE_TRCOEF (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TRCOEF),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LSOLRAD, LTHERRAD, LTRMATSUM, LTRWEIGHT

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%LRCOEF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LRCOEF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LTLADDIA)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LTLADDIA'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LGLOBRAD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LGLOBRAD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NG3SR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NG3SR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NGMTR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGMTR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLATWR)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLATWR'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLATRD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLATRD'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLENGSRB)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLENGSRB'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%NLENGTRB)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NLENGTRB'
ENDIF
KSIZE = KSIZE + ISIZE
LSOLRAD = ALLOCATED (YD%SOLRAD)
IF (LSOLRAD) THEN
  ISIZE = KIND (YD%SOLRAD) * SIZE (YD%SOLRAD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SOLRAD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTHERRAD = ALLOCATED (YD%THERRAD)
IF (LTHERRAD) THEN
  ISIZE = KIND (YD%THERRAD) * SIZE (YD%THERRAD)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%THERRAD'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTRWEIGHT = ALLOCATED (YD%TRWEIGHT)
IF (LTRWEIGHT) THEN
  ISIZE = KIND (YD%TRWEIGHT) * SIZE (YD%TRWEIGHT)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TRWEIGHT'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTRMATSUM = ALLOCATED (YD%TRMATSUM)
