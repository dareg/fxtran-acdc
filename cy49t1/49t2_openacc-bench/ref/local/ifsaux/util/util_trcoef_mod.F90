SUBROUTINE LOAD_TRCOEF (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TRCOEF), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1), IL2(2), IU2(2)
LOGICAL :: LSOLRAD, LTHERRAD, LTRMATSUM, LTRWEIGHT
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) YD%LRCOEF
READ (KLUN) YD%LTLADDIA
READ (KLUN) YD%LGLOBRAD
READ (KLUN) YD%NG3SR
READ (KLUN) YD%NGMTR
READ (KLUN) YD%NLATWR
READ (KLUN) YD%NLATRD
READ (KLUN) YD%NLENGSRB
READ (KLUN) YD%NLENGTRB
READ (KLUN) LSOLRAD
IF (LSOLRAD) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SOLRAD (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%SOLRAD,1):UBOUND(YD%SOLRAD,1)))
  READ (KLUN) ZTMP1
  YD%SOLRAD = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LTHERRAD
IF (LTHERRAD) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%THERRAD (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%THERRAD,1):UBOUND(YD%THERRAD,1), LBOUND(YD%THERRAD,2):UBOUND(YD%THERRAD,2)))
  READ (KLUN) ZTMP2
  YD%THERRAD = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTRWEIGHT
IF (LTRWEIGHT) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%TRWEIGHT (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%TRWEIGHT,1):UBOUND(YD%TRWEIGHT,1), LBOUND(YD%TRWEIGHT,2):UBOUND(YD%TRWEIGHT,2)))
  READ (KLUN) ZTMP2
  YD%TRWEIGHT = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTRMATSUM
IF (LTRMATSUM) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%TRMATSUM (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%TRMATSUM,1):UBOUND(YD%TRMATSUM,1), LBOUND(YD%TRMATSUM,2):UBOUND(YD%TRMATSUM,2)))
  READ (KLUN) ZTMP2
  YD%TRMATSUM = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
END SUBROUTINE


