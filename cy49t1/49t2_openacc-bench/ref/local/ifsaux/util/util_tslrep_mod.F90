INTEGER*8 FUNCTION SIZE_TSLREP (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TSLREP),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LLADCORE, LNADCORE, LNADMAP, LRSASIGN

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NGPTOTAD)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NGPTOTAD'
ENDIF
KSIZE = KSIZE + ISIZE
LNADMAP = ALLOCATED (YD%NADMAP)
IF (LNADMAP) THEN
  ISIZE = KIND (YD%NADMAP) * SIZE (YD%NADMAP)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NADMAP'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LNADCORE = ALLOCATED (YD%NADCORE)
IF (LNADCORE) THEN
  ISIZE = KIND (YD%NADCORE) * SIZE (YD%NADCORE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%NADCORE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LLADCORE = ALLOCATED (YD%LADCORE)
IF (LLADCORE) THEN
  ISIZE = KIND (YD%LADCORE) * SIZE (YD%LADCORE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%LADCORE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSASIGN = ALLOCATED (YD%RSASIGN)
IF (LRSASIGN) THEN
  ISIZE = KIND (YD%RSASIGN) * SIZE (YD%RSASIGN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSASIGN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

