INTEGER*8 FUNCTION SIZE_TCSGEOM (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCSGEOM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRCOLON = ASSOCIATED (YD%RCOLON)
IF (LRCOLON) THEN
  ISIZE = KIND (YD%RCOLON) * SIZE (YD%RCOLON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RCOLON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSILON = ASSOCIATED (YD%RSILON)
IF (LRSILON) THEN
  ISIZE = KIND (YD%RSILON) * SIZE (YD%RSILON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSILON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRINDX = ASSOCIATED (YD%RINDX)
IF (LRINDX) THEN
  ISIZE = KIND (YD%RINDX) * SIZE (YD%RINDX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RINDX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRINDY = ASSOCIATED (YD%RINDY)
IF (LRINDY) THEN
  ISIZE = KIND (YD%RINDY) * SIZE (YD%RINDY)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RINDY'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRATATH = ASSOCIATED (YD%RATATH)
IF (LRATATH) THEN
  ISIZE = KIND (YD%RATATH) * SIZE (YD%RATATH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RATATH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRATATX = ASSOCIATED (YD%RATATX)
