SUBROUTINE LOAD_TCSGEOM (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCSGEOM), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) LRCOLON
IF (LRCOLON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RCOLON (IL1(1):IU1(1)))
  READ (KLUN) YD%RCOLON
ELSE
  NULLIFY (YD%RCOLON)
ENDIF
READ (KLUN) LRSILON
IF (LRSILON) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RSILON (IL1(1):IU1(1)))
  READ (KLUN) YD%RSILON
ELSE
  NULLIFY (YD%RSILON)
ENDIF
READ (KLUN) LRINDX
IF (LRINDX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RINDX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RINDX,1):UBOUND(YD%RINDX,1)))
  READ (KLUN) ZTMP1
  YD%RINDX = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%RINDX)
ENDIF
READ (KLUN) LRINDY
IF (LRINDY) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RINDY (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RINDY,1):UBOUND(YD%RINDY,1)))
  READ (KLUN) ZTMP1
  YD%RINDY = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%RINDY)
ENDIF
READ (KLUN) LRATATH
IF (LRATATH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RATATH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RATATH,1):UBOUND(YD%RATATH,1)))
  READ (KLUN) ZTMP1
  YD%RATATH = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%RATATH)
ENDIF
READ (KLUN) LRATATX
IF (LRATATX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%RATATX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%RATATX,1):UBOUND(YD%RATATX,1)))
  READ (KLUN) ZTMP1
  YD%RATATX = ZTMP1
  DEALLOCATE (ZTMP1)
ELSE
  NULLIFY (YD%RATATX)
ENDIF
END SUBROUTINE


