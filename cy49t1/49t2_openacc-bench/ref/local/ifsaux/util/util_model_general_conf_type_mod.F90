CALL WIPE_TMODERR (YD%YRMODERR, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TSPPT_CONFIG (YD%YRSPPT_CONFIG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TSPP_CONFIG (YD%YRSPP_CONFIG, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_MODEL_GENERAL_CONF_TYPE (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_GEOMETRY_MOD
USE UTIL_TDIMF_MOD
USE UTIL_TECVDIM_MOD
USE UTIL_TMODERR_MOD
USE UTIL_TRIP_MOD
USE UTIL_TSPPT_CONFIG_MOD
USE UTIL_TSPP_CONFIG_MOD
USE UTIL_TYPE_GFLD_MOD
IMPLICIT NONE
TYPE (MODEL_GENERAL_CONF_TYPE),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGEOM

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LGEOM = ASSOCIATED (YD%GEOM)
IF (LGEOM) THEN
  JSIZE = 0
  ISIZE = SIZE_GEOMETRY (YD%GEOM, CLPATH//'%GEOM', .FALSE.)
  JSIZE = JSIZE + ISIZE
  KSIZE = KSIZE + ISIZE
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%GEOM'
  ENDIF
ENDIF
JSIZE = 0
ISIZE = SIZE_TDIMF (YD%YRDIMF, CLPATH//'%YRDIMF', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIMF'
ENDIF
JSIZE = 0
ISIZE = SIZE_TYPE_GFLD (YD%YGFL, CLPATH//'%YGFL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YGFL'
ENDIF
JSIZE = 0
ISIZE = SIZE_TRIP (YD%YRRIP, CLPATH//'%YRRIP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRRIP'
ENDIF
JSIZE = 0
ISIZE = SIZE_TECVDIM (YD%YRDIMECV, CLPATH//'%YRDIMECV', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIMECV'
ENDIF
JSIZE = 0
ISIZE = SIZE_TMODERR (YD%YRMODERR, CLPATH//'%YRMODERR', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMODERR'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSPPT_CONFIG (YD%YRSPPT_CONFIG, CLPATH//'%YRSPPT_CONFIG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSPPT_CONFIG'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSPP_CONFIG (YD%YRSPP_CONFIG, CLPATH//'%YRSPP_CONFIG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSPP_CONFIG'
ENDIF
END FUNCTION

