SUBROUTINE COPY_MODEL_GENERAL_CONF_TYPE (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_GEOMETRY_MOD
USE UTIL_TDIMF_MOD
USE UTIL_TECVDIM_MOD
USE UTIL_TMODERR_MOD
USE UTIL_TRIP_MOD
USE UTIL_TSPPT_CONFIG_MOD
USE UTIL_TSPP_CONFIG_MOD
USE UTIL_TYPE_GFLD_MOD
IMPLICIT NONE
TYPE (MODEL_GENERAL_CONF_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LGEOM

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LGEOM = ASSOCIATED (SELF%GEOM)
IF (LGEOM) THEN
  !$ACC ENTER DATA CREATE (SELF%GEOM)
  !$ACC UPDATE DEVICE (SELF%GEOM)
  CALL COPY_GEOMETRY (SELF%GEOM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%GEOM)
ENDIF

CALL COPY_TDIMF (SELF%YRDIMF, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TYPE_GFLD (SELF%YGFL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TRIP (SELF%YRRIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TECVDIM (SELF%YRDIMECV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TMODERR (SELF%YRMODERR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSPPT_CONFIG (SELF%YRSPPT_CONFIG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSPP_CONFIG (SELF%YRSPP_CONFIG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

