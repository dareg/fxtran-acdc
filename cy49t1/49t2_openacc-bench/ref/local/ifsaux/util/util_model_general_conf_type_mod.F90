CALL LOAD_TRIP (KLUN, YD%YRRIP)
CALL LOAD_TECVDIM (KLUN, YD%YRDIMECV)
CALL LOAD_TMODERR (KLUN, YD%YRMODERR)
CALL LOAD_TSPPT_CONFIG (KLUN, YD%YRSPPT_CONFIG)
CALL LOAD_TSPP_CONFIG (KLUN, YD%YRSPP_CONFIG)
END SUBROUTINE


SUBROUTINE COPY_MODEL_GENERAL_CONF_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_GEOMETRY_MOD
USE UTIL_TDIMF_MOD
USE UTIL_TECVDIM_MOD
USE UTIL_TMODERR_MOD
USE UTIL_TRIP_MOD
USE UTIL_TSPPT_CONFIG_MOD
USE UTIL_TSPP_CONFIG_MOD
USE UTIL_TYPE_GFLD_MOD
IMPLICIT NONE
TYPE (MODEL_GENERAL_CONF_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LGEOM

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LGEOM = ASSOCIATED (YD%GEOM)
IF (LGEOM) THEN
  !$ACC ENTER DATA CREATE (YD%GEOM)
  !$ACC UPDATE DEVICE (YD%GEOM)
  CALL COPY_GEOMETRY (YD%GEOM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%GEOM)
ENDIF

CALL COPY_TDIMF (YD%YRDIMF, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TYPE_GFLD (YD%YGFL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TRIP (YD%YRRIP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TECVDIM (YD%YRDIMECV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TMODERR (YD%YRMODERR, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSPPT_CONFIG (YD%YRSPPT_CONFIG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSPP_CONFIG (YD%YRSPP_CONFIG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

