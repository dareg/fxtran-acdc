END INTERFACE


CONTAINS

SUBROUTINE SAVE_MODEL_CHEM_TYPE (KLUN, YD)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (MODEL_CHEM_TYPE), INTENT (IN), TARGET :: YD

CALL SAVE_TOZO (KLUN, YD%YROZO)
CALL SAVE_TCHEM (KLUN, YD%YRCHEM)
CALL SAVE_TCOMPO (KLUN, YD%YRCOMPO)
END SUBROUTINE

SUBROUTINE LOAD_MODEL_CHEM_TYPE (KLUN, YD)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (MODEL_CHEM_TYPE), INTENT (OUT), TARGET :: YD

CALL LOAD_TOZO (KLUN, YD%YROZO)
CALL LOAD_TCHEM (KLUN, YD%YRCHEM)
CALL LOAD_TCOMPO (KLUN, YD%YRCOMPO)
END SUBROUTINE


SUBROUTINE COPY_MODEL_CHEM_TYPE (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
CALL COPY_TOZO (YD%YROZO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCHEM (YD%YRCHEM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCOMPO (YD%YRCOMPO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

SUBROUTINE HOST_MODEL_CHEM_TYPE (YD)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD

IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE), TARGET :: YD

CALL HOST_TOZO (YD%YROZO)

CALL HOST_TCHEM (YD%YRCHEM)

CALL HOST_TCOMPO (YD%YRCOMPO)

END SUBROUTINE

     

SUBROUTINE CRC64_MODEL_CHEM_TYPE (YD, KLUN, CDPATH)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

CLIND = ''
CALL CRC64_TOZO (YD%YROZO, KLUN, CDPATH//'%YROZO'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TCHEM (YD%YRCHEM, KLUN, CDPATH//'%YRCHEM'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TCOMPO (YD%YRCOMPO, KLUN, CDPATH//'%YRCOMPO'//TRIM(CLIND))

END SUBROUTINE

SUBROUTINE WIPE_MODEL_CHEM_TYPE (YD, LDDELETED, LDFIELDAPI)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
CALL WIPE_TOZO (YD%YROZO, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TCHEM (YD%YRCHEM, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL WIPE_TCOMPO (YD%YRCOMPO, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE

INTEGER*8 FUNCTION SIZE_MODEL_CHEM_TYPE (YD, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TOZO (YD%YROZO, CLPATH//'%YROZO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YROZO'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCHEM (YD%YRCHEM, CLPATH//'%YRCHEM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCHEM'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCOMPO (YD%YRCOMPO, CLPATH//'%YRCOMPO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCOMPO'
ENDIF
END FUNCTION

