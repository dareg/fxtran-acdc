MODULE UTIL_MF_PHYS_NEXT_STATE_2D_TYPE_MOD

USE MF_PHYS_NEXT_STATE_TYPE_MOD, ONLY : MF_PHYS_NEXT_STATE_2D_TYPE



INTERFACE COPY
MODULE PROCEDURE COPY_MF_PHYS_NEXT_STATE_2D_TYPE
END INTERFACE


     

INTERFACE WIPE
MODULE PROCEDURE WIPE_MF_PHYS_NEXT_STATE_2D_TYPE
END INTERFACE



CONTAINS





SUBROUTINE COPY_MF_PHYS_NEXT_STATE_2D_TYPE (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_NEXT_STATE_2D_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P)
  !$ACC ENTER DATA ATTACH (YD%F_P)
ENDIF

END SUBROUTINE



     



SUBROUTINE WIPE_MF_PHYS_NEXT_STATE_2D_TYPE (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (MF_PHYS_NEXT_STATE_2D_TYPE), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
