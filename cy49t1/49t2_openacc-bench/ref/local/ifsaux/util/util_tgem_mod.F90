READ (KLUN) ZTMP0
YD%R4JP = ZTMP0
READ (KLUN) ZTMP0
YD%RC2P1 = ZTMP0
READ (KLUN) ZTMP0
YD%RC2M1 = ZTMP0
READ (KLUN) ZTMP0
YD%RCOR0 = ZTMP0
READ (KLUN) ZTMP0
YD%RCOR1 = ZTMP0
