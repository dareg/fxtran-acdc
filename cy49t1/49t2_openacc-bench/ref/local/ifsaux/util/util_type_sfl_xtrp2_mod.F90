  ENDDO
ENDIF

END SUBROUTINE

     

SUBROUTINE CRC64_TYPE_SFL_XTRP2 (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_2D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_XTRP2), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYX2
LYX2 = ASSOCIATED (SELF%YX2)
IF (LYX2) THEN
  DO J1 = LBOUND (SELF%YX2, 1), UBOUND (SELF%YX2, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_2D (SELF%YX2 (J1), KLUN, CDPATH//'%YX2'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

