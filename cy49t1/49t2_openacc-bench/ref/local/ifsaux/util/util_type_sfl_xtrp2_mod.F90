END INTERFACE



CONTAINS

SUBROUTINE SAVE_TYPE_SFL_XTRP2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_XTRP2), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYX2
LYX2 = ASSOCIATED (SELF%YX2)
WRITE (KLUN) LYX2
IF (LYX2) THEN
  WRITE (KLUN) LBOUND (SELF%YX2)
  WRITE (KLUN) UBOUND (SELF%YX2)
  DO J1 = LBOUND (SELF%YX2, 1), UBOUND (SELF%YX2, 1)
    CALL SAVE_TYPE_SURF_MTL_2D (SELF%YX2 (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_XTRP2 (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_XTRP2), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYX2
READ (KLUN) LYX2
IF (LYX2) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YX2 (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YX2, 1), UBOUND (SELF%YX2, 1)
    CALL LOAD_TYPE_SURF_MTL_2D (SELF%YX2 (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YX2)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_XTRP2 (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_2D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_XTRP2), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYX2

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LYX2 = ASSOCIATED (SELF%YX2)
IF (LYX2) THEN
  !$ACC ENTER DATA CREATE (SELF%YX2)
  !$ACC UPDATE DEVICE (SELF%YX2)
  DO J1 = LBOUND (SELF%YX2, 1), UBOUND (SELF%YX2, 1)
    CALL COPY_TYPE_SURF_MTL_2D (SELF%YX2 (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YX2)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_TYPE_SFL_XTRP2 (SELF)
USE UTIL_TYPE_SURF_MTL_2D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_XTRP2), TARGET :: SELF
