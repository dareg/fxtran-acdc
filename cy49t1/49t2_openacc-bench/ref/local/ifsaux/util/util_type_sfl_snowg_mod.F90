  ALLOCATE (YD%YT)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YT)
ELSE
  NULLIFY (YD%YT)
ENDIF
READ (KLUN) LYW
IF (LYW) THEN
  ALLOCATE (YD%YW)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YW)
