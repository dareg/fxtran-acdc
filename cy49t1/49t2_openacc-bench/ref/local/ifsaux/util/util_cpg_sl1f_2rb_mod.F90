MODULE UTIL_CPG_SL1F_2RB_MOD

USE CPG_SL1_TYPE_MOD, ONLY : CPG_SL1F_2RB



INTERFACE COPY
MODULE PROCEDURE COPY_CPG_SL1F_2RB
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_CPG_SL1F_2RB
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_CPG_SL1F_2RB
END INTERFACE



CONTAINS





SUBROUTINE COPY_CPG_SL1F_2RB (YD, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL1F_2RB), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (YD%F_P)
  !$ACC ENTER DATA ATTACH (YD%F_P)
ENDIF




END SUBROUTINE

SUBROUTINE HOST_CPG_SL1F_2RB (YD)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
TYPE (CPG_SL1F_2RB), TARGET :: YD
LOGICAL :: LF_P
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P) THEN
  CALL HOST_FIELD_2RB (YD%F_P)
ENDIF




END SUBROUTINE

     



SUBROUTINE WIPE_CPG_SL1F_2RB (YD, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
TYPE (CPG_SL1F_2RB), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_P

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LF_P = ASSOCIATED (YD%F_P)
IF (LF_P .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (YD%F_P)
ENDIF




LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (YD)
ENDIF
END SUBROUTINE



END MODULE
