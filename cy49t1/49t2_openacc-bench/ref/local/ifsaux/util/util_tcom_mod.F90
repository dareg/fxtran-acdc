INTEGER*8 FUNCTION SIZE_TCOM (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCOM),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%NVCOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%NVCOM'
ENDIF
KSIZE = KSIZE + ISIZE
LOMLDTH = ALLOCATED (YD%OMLDTH)
IF (LOMLDTH) THEN
  ISIZE = KIND (YD%OMLDTH) * SIZE (YD%OMLDTH)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%OMLDTH'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LGTTLIN = ALLOCATED (YD%GTTLIN)
IF (LGTTLIN) THEN
  ISIZE = KIND (YD%GTTLIN) * SIZE (YD%GTTLIN)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%GTTLIN'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSSTPRE = ALLOCATED (YD%SSTPRE)
IF (LSSTPRE) THEN
  ISIZE = KIND (YD%SSTPRE) * SIZE (YD%SSTPRE)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SSTPRE'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LSSTMSK = ALLOCATED (YD%SSTMSK)
IF (LSSTMSK) THEN
  ISIZE = KIND (YD%SSTMSK) * SIZE (YD%SSTMSK)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%SSTMSK'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%LOMLDTH)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LOMLDTH'
ENDIF
KSIZE = KSIZE + ISIZE
LTRAFLX = ALLOCATED (YD%TRAFLX)
IF (LTRAFLX) THEN
  ISIZE = KIND (YD%TRAFLX) * SIZE (YD%TRAFLX)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TRAFLX'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
END FUNCTION

