SUBROUTINE LOAD_TCOM (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCOM), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LGTTLIN, LOMLDTH, LSSTMSK, LSSTPRE, LTRAFLX
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%NVCOM
READ (KLUN) LOMLDTH
IF (LOMLDTH) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%OMLDTH (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%OMLDTH,1):UBOUND(YD%OMLDTH,1)))
  READ (KLUN) ZTMP1
  YD%OMLDTH = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LGTTLIN
IF (LGTTLIN) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%GTTLIN (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%GTTLIN,1):UBOUND(YD%GTTLIN,1)))
  READ (KLUN) ZTMP1
  YD%GTTLIN = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LSSTPRE
IF (LSSTPRE) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SSTPRE (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%SSTPRE,1):UBOUND(YD%SSTPRE,1)))
  READ (KLUN) ZTMP1
  YD%SSTPRE = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) LSSTMSK
IF (LSSTMSK) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%SSTMSK (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%SSTMSK,1):UBOUND(YD%SSTMSK,1)))
  READ (KLUN) ZTMP1
  YD%SSTMSK = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
READ (KLUN) YD%LOMLDTH
READ (KLUN) LTRAFLX
IF (LTRAFLX) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%TRAFLX (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%TRAFLX,1):UBOUND(YD%TRAFLX,1)))
  READ (KLUN) ZTMP1
  YD%TRAFLX = ZTMP1
  DEALLOCATE (ZTMP1)
ENDIF
END SUBROUTINE


