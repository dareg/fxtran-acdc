INTEGER*8 FUNCTION SIZE_ECV_CONTAINER (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (ECV_CONTAINER),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRECV1D, LRGPECV2D, LRGPECV3D, LRLSM, LRSPECV2D, LRSPECV3D

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRECV1D = ALLOCATED (YD%RECV1D)
IF (LRECV1D) THEN
  ISIZE = KIND (YD%RECV1D) * SIZE (YD%RECV1D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RECV1D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSPECV2D = ALLOCATED (YD%RSPECV2D)
IF (LRSPECV2D) THEN
  ISIZE = KIND (YD%RSPECV2D) * SIZE (YD%RSPECV2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSPECV2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRGPECV2D = ALLOCATED (YD%RGPECV2D)
IF (LRGPECV2D) THEN
  ISIZE = KIND (YD%RGPECV2D) * SIZE (YD%RGPECV2D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RGPECV2D'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSPECV3D = ALLOCATED (YD%RSPECV3D)
IF (LRSPECV3D) THEN
  ISIZE = KIND (YD%RSPECV3D) * SIZE (YD%RSPECV3D)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RSPECV3D'
