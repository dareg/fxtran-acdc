SUBROUTINE CRC64_TYPE_SFL_SOILB (SELF, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL
LYT = ASSOCIATED (SELF%YT)
IF (LYT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YT, KLUN, CDPATH//'%YT'//TRIM(CLIND))
ENDIF

LYQ = ASSOCIATED (SELF%YQ)
IF (LYQ) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YQ, KLUN, CDPATH//'%YQ'//TRIM(CLIND))
ENDIF

LYTL = ASSOCIATED (SELF%YTL)
IF (LYTL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (SELF%YTL, KLUN, CDPATH//'%YTL'//TRIM(CLIND))
ENDIF

LYSB = ASSOCIATED (SELF%YSB)
IF (LYSB) THEN
  DO J1 = LBOUND (SELF%YSB, 1), UBOUND (SELF%YSB, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (SELF%YSB (J1), KLUN, CDPATH//'%YSB'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

