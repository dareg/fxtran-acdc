SUBROUTINE CRC64_TYPE_SFL_SOILB (YD, KLUN, CDPATH)
USE UTIL_TYPE_SURF_MTL_3D_MOD
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL
LYT = ASSOCIATED (YD%YT)
IF (LYT) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YT, KLUN, CDPATH//'%YT'//TRIM(CLIND))
ENDIF

LYQ = ASSOCIATED (YD%YQ)
IF (LYQ) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YQ, KLUN, CDPATH//'%YQ'//TRIM(CLIND))
ENDIF

LYTL = ASSOCIATED (YD%YTL)
IF (LYTL) THEN
  CLIND = ''
  CALL CRC64_TYPE_SURF_MTL_3D (YD%YTL, KLUN, CDPATH//'%YTL'//TRIM(CLIND))
ENDIF

LYSB = ASSOCIATED (YD%YSB)
IF (LYSB) THEN
  DO J1 = LBOUND (YD%YSB, 1), UBOUND (YD%YSB, 1)
    WRITE (CLIND, '("(",I0,")")') J1
    CALL CRC64_TYPE_SURF_MTL_3D (YD%YSB (J1), KLUN, CDPATH//'%YSB'//TRIM(CLIND))
  ENDDO
ENDIF

END SUBROUTINE

