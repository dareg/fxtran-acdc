SUBROUTINE SAVE_TYPE_SFL_SOILB (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_SOILB), INTENT (IN), TARGET :: YD
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL
LYT = ASSOCIATED (YD%YT)
WRITE (KLUN) LYT
IF (LYT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YT)
ENDIF
LYQ = ASSOCIATED (YD%YQ)
WRITE (KLUN) LYQ
IF (LYQ) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YQ)
ENDIF
LYTL = ASSOCIATED (YD%YTL)
WRITE (KLUN) LYTL
IF (LYTL) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YTL)
ENDIF
LYSB = ASSOCIATED (YD%YSB)
WRITE (KLUN) LYSB
IF (LYSB) THEN
  WRITE (KLUN) LBOUND (YD%YSB)
  WRITE (KLUN) UBOUND (YD%YSB)
  DO J1 = LBOUND (YD%YSB, 1), UBOUND (YD%YSB, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (KLUN, YD%YSB (J1))
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_SOILB (KLUN, YD)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_SOILB), INTENT (OUT), TARGET :: YD
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYQ, LYSB, LYT, LYTL
READ (KLUN) LYT
IF (LYT) THEN
  ALLOCATE (YD%YT)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YT)
ELSE
  NULLIFY (YD%YT)
ENDIF
READ (KLUN) LYQ
IF (LYQ) THEN
  ALLOCATE (YD%YQ)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YQ)
ELSE
  NULLIFY (YD%YQ)
ENDIF
READ (KLUN) LYTL
IF (LYTL) THEN
  ALLOCATE (YD%YTL)
  CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YTL)
ELSE
  NULLIFY (YD%YTL)
ENDIF
READ (KLUN) LYSB
IF (LYSB) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%YSB (IL1(1):IU1(1)))
  DO J1 = LBOUND (YD%YSB, 1), UBOUND (YD%YSB, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (KLUN, YD%YSB (J1))
  ENDDO
ELSE
  NULLIFY (YD%YSB)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_SOILB (YD, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF
LYT = ASSOCIATED (YD%YT)
IF (LYT) THEN
  !$ACC ENTER DATA CREATE (YD%YT)
  !$ACC UPDATE DEVICE (YD%YT)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YT)
ENDIF

LYQ = ASSOCIATED (YD%YQ)
IF (LYQ) THEN
  !$ACC ENTER DATA CREATE (YD%YQ)
  !$ACC UPDATE DEVICE (YD%YQ)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YQ, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YQ)
ENDIF

LYTL = ASSOCIATED (YD%YTL)
IF (LYTL) THEN
  !$ACC ENTER DATA CREATE (YD%YTL)
  !$ACC UPDATE DEVICE (YD%YTL)
  CALL COPY_TYPE_SURF_MTL_3D (YD%YTL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (YD%YTL)
ENDIF

LYSB = ASSOCIATED (YD%YSB)
IF (LYSB) THEN
  !$ACC ENTER DATA CREATE (YD%YSB)
  !$ACC UPDATE DEVICE (YD%YSB)
  DO J1 = LBOUND (YD%YSB, 1), UBOUND (YD%YSB, 1)
    CALL COPY_TYPE_SURF_MTL_3D (YD%YSB (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (YD%YSB)
ENDIF

END SUBROUTINE

