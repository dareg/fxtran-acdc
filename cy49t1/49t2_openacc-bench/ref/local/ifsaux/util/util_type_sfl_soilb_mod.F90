SUBROUTINE SAVE_TYPE_SFL_SOILB (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL
LYT = ASSOCIATED (SELF%YT)
WRITE (KLUN) LYT
IF (LYT) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YT, KLUN)
ENDIF
LYQ = ASSOCIATED (SELF%YQ)
WRITE (KLUN) LYQ
IF (LYQ) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YQ, KLUN)
ENDIF
LYTL = ASSOCIATED (SELF%YTL)
WRITE (KLUN) LYTL
IF (LYTL) THEN
  CALL SAVE_TYPE_SURF_MTL_3D (SELF%YTL, KLUN)
ENDIF
LYSB = ASSOCIATED (SELF%YSB)
WRITE (KLUN) LYSB
IF (LYSB) THEN
  WRITE (KLUN) LBOUND (SELF%YSB)
  WRITE (KLUN) UBOUND (SELF%YSB)
  DO J1 = LBOUND (SELF%YSB, 1), UBOUND (SELF%YSB, 1)
    CALL SAVE_TYPE_SURF_MTL_3D (SELF%YSB (J1), KLUN)
  ENDDO
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_SOILB (SELF, KLUN)
USE UTIL_TYPE_SURF_MTL_3D_MOD

IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYQ, LYSB, LYT, LYTL
READ (KLUN) LYT
IF (LYT) THEN
  ALLOCATE (SELF%YT)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YT, KLUN)
ELSE
  NULLIFY (SELF%YT)
ENDIF
READ (KLUN) LYQ
IF (LYQ) THEN
  ALLOCATE (SELF%YQ)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YQ, KLUN)
ELSE
  NULLIFY (SELF%YQ)
ENDIF
READ (KLUN) LYTL
IF (LYTL) THEN
  ALLOCATE (SELF%YTL)
  CALL LOAD_TYPE_SURF_MTL_3D (SELF%YTL, KLUN)
ELSE
  NULLIFY (SELF%YTL)
ENDIF
READ (KLUN) LYSB
IF (LYSB) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YSB (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YSB, 1), UBOUND (SELF%YSB, 1)
    CALL LOAD_TYPE_SURF_MTL_3D (SELF%YSB (J1), KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YSB)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_SOILB (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TYPE_SURF_MTL_3D_MOD
IMPLICIT NONE
TYPE (TYPE_SFL_SOILB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYQ, LYSB, LYT, LYTL

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LYT = ASSOCIATED (SELF%YT)
IF (LYT) THEN
  !$ACC ENTER DATA CREATE (SELF%YT)
  !$ACC UPDATE DEVICE (SELF%YT)
  CALL COPY_TYPE_SURF_MTL_3D (SELF%YT, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YT)
ENDIF

LYQ = ASSOCIATED (SELF%YQ)
IF (LYQ) THEN
  !$ACC ENTER DATA CREATE (SELF%YQ)
  !$ACC UPDATE DEVICE (SELF%YQ)
  CALL COPY_TYPE_SURF_MTL_3D (SELF%YQ, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YQ)
ENDIF

LYTL = ASSOCIATED (SELF%YTL)
IF (LYTL) THEN
  !$ACC ENTER DATA CREATE (SELF%YTL)
  !$ACC UPDATE DEVICE (SELF%YTL)
  CALL COPY_TYPE_SURF_MTL_3D (SELF%YTL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YTL)
ENDIF

LYSB = ASSOCIATED (SELF%YSB)
IF (LYSB) THEN
  !$ACC ENTER DATA CREATE (SELF%YSB)
  !$ACC UPDATE DEVICE (SELF%YSB)
  DO J1 = LBOUND (SELF%YSB, 1), UBOUND (SELF%YSB, 1)
    CALL COPY_TYPE_SURF_MTL_3D (SELF%YSB (J1), LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YSB)
ENDIF

END SUBROUTINE

