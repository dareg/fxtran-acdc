MODULE UTIL_SURFACE_VIEW_GROUP_VCLIK_MOD

USE SURFACE_VIEWS_DIAGNOSTIC_MODULE, ONLY : SURFACE_VIEW_GROUP_VCLIK



INTERFACE COPY
MODULE PROCEDURE COPY_SURFACE_VIEW_GROUP_VCLIK
END INTERFACE

INTERFACE HOST
MODULE PROCEDURE HOST_SURFACE_VIEW_GROUP_VCLIK
END INTERFACE

     

INTERFACE WIPE
MODULE PROCEDURE WIPE_SURFACE_VIEW_GROUP_VCLIK
END INTERFACE



CONTAINS





SUBROUTINE COPY_SURFACE_VIEW_GROUP_VCLIK (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_SURFACE_VARIABLE_GROUP_VCLIK_MOD
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIK), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP, LF_UDGRO, LVARIABLE_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC ENTER DATA CREATE (SELF%VARIABLE_GROUP)
  !$ACC UPDATE DEVICE (SELF%VARIABLE_GROUP)
  CALL COPY_SURFACE_VARIABLE_GROUP_VCLIK (SELF%VARIABLE_GROUP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%VARIABLE_GROUP)
ENDIF

LF_UDGRO = ASSOCIATED (SELF%F_UDGRO)
IF (LF_UDGRO .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_UDGRO)
  !$ACC ENTER DATA ATTACH (SELF%F_UDGRO)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_GROUP)
  !$ACC ENTER DATA ATTACH (SELF%F_GROUP)
ENDIF

END SUBROUTINE

SUBROUTINE HOST_SURFACE_VIEW_GROUP_VCLIK (SELF)
USE FIELD_UTIL_MODULE
USE UTIL_SURFACE_VARIABLE_GROUP_VCLIK_MOD

IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIK), TARGET :: SELF
LOGICAL :: LF_GROUP, LF_UDGRO, LVARIABLE_GROUP
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  CALL HOST_SURFACE_VARIABLE_GROUP_VCLIK (SELF%VARIABLE_GROUP)
ENDIF

LF_UDGRO = ASSOCIATED (SELF%F_UDGRO)
IF (LF_UDGRO) THEN
  CALL HOST_FIELD_2RB (SELF%F_UDGRO)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

     



SUBROUTINE WIPE_SURFACE_VIEW_GROUP_VCLIK (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
USE UTIL_SURFACE_VARIABLE_GROUP_VCLIK_MOD
IMPLICIT NONE
TYPE (SURFACE_VIEW_GROUP_VCLIK), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP, LF_UDGRO, LVARIABLE_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
  CALL WIPE_SURFACE_VARIABLE_GROUP_VCLIK (SELF%VARIABLE_GROUP, LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
ENDIF

LF_UDGRO = ASSOCIATED (SELF%F_UDGRO)
IF (LF_UDGRO .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_UDGRO)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE



END MODULE
