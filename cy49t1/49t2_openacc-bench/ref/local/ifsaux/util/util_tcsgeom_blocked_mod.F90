INTEGER*8 FUNCTION SIZE_TCSGEOM_BLOCKED (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TCSGEOM_BLOCKED),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LRCOLON = ASSOCIATED (YD%RCOLON)
IF (LRCOLON) THEN
  ISIZE = KIND (YD%RCOLON) * SIZE (YD%RCOLON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%RCOLON'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LRSILON = ASSOCIATED (YD%RSILON)
IF (LRSILON) THEN
  ISIZE = KIND (YD%RSILON) * SIZE (YD%RSILON)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
