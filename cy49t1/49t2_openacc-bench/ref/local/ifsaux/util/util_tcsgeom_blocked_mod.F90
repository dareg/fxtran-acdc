SUBROUTINE LOAD_TCSGEOM_BLOCKED (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TCSGEOM_BLOCKED), INTENT (OUT), TARGET :: YD
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LRATATH, LRATATX, LRCOLON, LRINDX, LRINDY, LRSILON
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LRCOLON
IF (LRCOLON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RCOLON (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RCOLON
ELSE
  NULLIFY (YD%RCOLON)
ENDIF
READ (KLUN) LRSILON
IF (LRSILON) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RSILON (IL2(1):IU2(1), IL2(2):IU2(2)))
  READ (KLUN) YD%RSILON
ELSE
  NULLIFY (YD%RSILON)
ENDIF
READ (KLUN) LRINDX
IF (LRINDX) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RINDX (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RINDX,1):UBOUND(YD%RINDX,1), LBOUND(YD%RINDX,2):UBOUND(YD%RINDX,2)))
  READ (KLUN) ZTMP2
  YD%RINDX = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (YD%RINDX)
ENDIF
READ (KLUN) LRINDY
IF (LRINDY) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RINDY (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RINDY,1):UBOUND(YD%RINDY,1), LBOUND(YD%RINDY,2):UBOUND(YD%RINDY,2)))
  READ (KLUN) ZTMP2
  YD%RINDY = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (YD%RINDY)
ENDIF
READ (KLUN) LRATATH
IF (LRATATH) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RATATH (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RATATH,1):UBOUND(YD%RATATH,1), LBOUND(YD%RATATH,2):UBOUND(YD%RATATH,2)))
  READ (KLUN) ZTMP2
  YD%RATATH = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (YD%RATATH)
ENDIF
READ (KLUN) LRATATX
IF (LRATATX) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%RATATX (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%RATATX,1):UBOUND(YD%RATATX,1), LBOUND(YD%RATATX,2):UBOUND(YD%RATATX,2)))
  READ (KLUN) ZTMP2
  YD%RATATX = ZTMP2
  DEALLOCATE (ZTMP2)
ELSE
  NULLIFY (YD%RATATX)
ENDIF
END SUBROUTINE


