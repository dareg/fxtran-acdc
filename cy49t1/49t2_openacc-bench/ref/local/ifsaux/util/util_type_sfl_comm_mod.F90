SUBROUTINE SAVE_TYPE_SFL_COMM (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_COMM), INTENT (IN), TARGET :: YD

WRITE (KLUN) YD%IGRBCODE
WRITE (KLUN) YD%L_OK
WRITE (KLUN) YD%CNAME
WRITE (KLUN) YD%IFLDNUM
WRITE (KLUN) YD%VALUE
WRITE (KLUN) YD%IPTRSURF
WRITE (KLUN) YD%ICODES
WRITE (KLUN) YD%ICODESML
WRITE (KLUN) YD%ICOUNT
WRITE (KLUN) YD%ICOUNTML
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_COMM (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TYPE_SFL_COMM), INTENT (OUT), TARGET :: YD
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) YD%IGRBCODE
READ (KLUN) YD%L_OK
READ (KLUN) YD%CNAME
READ (KLUN) YD%IFLDNUM
READ (KLUN) ZTMP0
YD%VALUE = ZTMP0
READ (KLUN) YD%IPTRSURF
READ (KLUN) YD%ICODES
READ (KLUN) YD%ICODESML
READ (KLUN) YD%ICOUNT
READ (KLUN) YD%ICOUNTML
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_COMM (YD, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), INTENT (IN), TARGET :: YD
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (YD)
  !$ACC UPDATE DEVICE (YD)
ENDIF










END SUBROUTINE

