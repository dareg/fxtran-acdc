SUBROUTINE CRC64_TYPE_SFL_COMM (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%L_OK), CDPATH//'%L_OK'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IFLDNUM), CDPATH//'%IFLDNUM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%VALUE), CDPATH//'%VALUE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%IPTRSURF), CDPATH//'%IPTRSURF'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ICODES), CDPATH//'%ICODES'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ICODESML), CDPATH//'%ICODESML'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ICOUNT), CDPATH//'%ICOUNT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%ICOUNTML), CDPATH//'%ICOUNTML'

END SUBROUTINE

