SUBROUTINE CRC64_TYPE_SFL_COMM (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IGRBCODE), CDPATH//'%IGRBCODE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%L_OK), CDPATH//'%L_OK'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%CNAME), CDPATH//'%CNAME'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IFLDNUM), CDPATH//'%IFLDNUM'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%VALUE), CDPATH//'%VALUE'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%IPTRSURF), CDPATH//'%IPTRSURF'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ICODES), CDPATH//'%ICODES'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ICODESML), CDPATH//'%ICODESML'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ICOUNT), CDPATH//'%ICOUNT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%ICOUNTML), CDPATH//'%ICOUNTML'

END SUBROUTINE

