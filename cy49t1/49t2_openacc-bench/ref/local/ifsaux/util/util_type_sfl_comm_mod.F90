SUBROUTINE SAVE_TYPE_SFL_COMM (SELF, KLUN)

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%IGRBCODE
WRITE (KLUN) SELF%L_OK
WRITE (KLUN) SELF%CNAME
WRITE (KLUN) SELF%IFLDNUM
WRITE (KLUN) SELF%VALUE
WRITE (KLUN) SELF%IPTRSURF
WRITE (KLUN) SELF%ICODES
WRITE (KLUN) SELF%ICODESML
WRITE (KLUN) SELF%ICOUNT
WRITE (KLUN) SELF%ICOUNTML
END SUBROUTINE

SUBROUTINE LOAD_TYPE_SFL_COMM (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
REAL(KIND=JPRD) :: ZTMP0
READ (KLUN) SELF%IGRBCODE
READ (KLUN) SELF%L_OK
READ (KLUN) SELF%CNAME
READ (KLUN) SELF%IFLDNUM
READ (KLUN) ZTMP0
SELF%VALUE = ZTMP0
READ (KLUN) SELF%IPTRSURF
READ (KLUN) SELF%ICODES
READ (KLUN) SELF%ICODESML
READ (KLUN) SELF%ICOUNT
READ (KLUN) SELF%ICOUNTML
END SUBROUTINE


SUBROUTINE COPY_TYPE_SFL_COMM (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
TYPE (TYPE_SFL_COMM), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

END SUBROUTINE

