  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (YD%XSW_BANDS (IL1(1):IU1(1)))
  ALLOCATE (ZTMP1(LBOUND(YD%XSW_BANDS,1):UBOUND(YD%XSW_BANDS,1)))
