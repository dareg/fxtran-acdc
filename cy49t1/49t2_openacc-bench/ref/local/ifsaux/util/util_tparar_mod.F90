READ (KLUN) YD%LOTOWNC
READ (KLUN) YD%LKOGAN
READ (KLUN) YD%LMODICEDEP
READ (KLUN) YD%LICERAD
READ (KLUN) ZTMP0
YD%RADGR = ZTMP0
READ (KLUN) ZTMP0
YD%RADSN = ZTMP0
READ (KLUN) YD%LLCRIT
READ (KLUN) YD%LTOTPREC
READ (KLUN) YD%LTOTPRECL
READ (KLUN) YD%LDUALMF
READ (KLUN) YD%LSCAWAREMF
READ (KLUN) ZTMP0
YD%XVMAXTHP = ZTMP0
READ (KLUN) ZTMP0
YD%XVMAXTHM = ZTMP0
READ (KLUN) YD%LSQUALL
READ (KLUN) YD%NREFROI1
READ (KLUN) YD%NREFROI2
READ (KLUN) ZTMP0
YD%VSQUALL = ZTMP0
