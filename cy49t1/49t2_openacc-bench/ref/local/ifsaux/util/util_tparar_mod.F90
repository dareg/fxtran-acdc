READ (KLUN) YD%NSV
READ (KLUN) YD%NSWB_MNH
READ (KLUN) YD%NGPAR
READ (KLUN) YD%MINPRR
READ (KLUN) YD%MACPRR
READ (KLUN) YD%MINPRS
