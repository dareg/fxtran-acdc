TYPE (TPARAR), INTENT (OUT), TARGET :: YD
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LXSW_BANDS
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP1 (:)
READ (KLUN) YD%CMICRO
READ (KLUN) YD%CTURB
CALL LOAD_PHYEX_t (KLUN, YD%PHYEX)
READ (KLUN) YD%NRR
READ (KLUN) YD%NRRL
