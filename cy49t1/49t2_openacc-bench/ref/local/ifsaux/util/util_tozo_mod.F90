SUBROUTINE SAVE_TOZO (KLUN, YD)

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TOZO), INTENT (IN), TARGET :: YD
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL
LTOZ2DG = ALLOCATED (YD%TOZ2DG)
