INTEGER*8 FUNCTION SIZE_TOZO (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (TOZO),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
LTOZ2DG = ALLOCATED (YD%TOZ2DG)
IF (LTOZ2DG) THEN
  ISIZE = KIND (YD%TOZ2DG) * SIZE (YD%TOZ2DG)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ2DG'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTOZ2DL = ALLOCATED (YD%TOZ2DL)
IF (LTOZ2DL) THEN
  ISIZE = KIND (YD%TOZ2DL) * SIZE (YD%TOZ2DL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ2DL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
LTOZ3DBL = ALLOCATED (YD%TOZ3DBL)
IF (LTOZ3DBL) THEN
  ISIZE = KIND (YD%TOZ3DBL) * SIZE (YD%TOZ3DBL)
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
    WRITE (*, *) TRIM (CLPATH)//'%TOZ3DBL'
  ENDIF
  KSIZE = KSIZE + ISIZE
ENDIF
ISIZE = KIND (YD%TEECL2)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%TEECL2'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

