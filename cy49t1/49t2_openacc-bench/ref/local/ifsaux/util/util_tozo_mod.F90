SUBROUTINE LOAD_TOZO (KLUN, YD)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
INTEGER, INTENT (IN) :: KLUN
TYPE (TOZO), INTENT (OUT), TARGET :: YD
INTEGER :: IL2(2), IU2(2)
LOGICAL :: LTOZ2DG, LTOZ2DL, LTOZ3DBL
REAL(KIND=JPRD) :: ZTMP0
REAL(KIND=JPRD), ALLOCATABLE :: ZTMP2 (:,:)
READ (KLUN) LTOZ2DG
IF (LTOZ2DG) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%TOZ2DG (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%TOZ2DG,1):UBOUND(YD%TOZ2DG,1), LBOUND(YD%TOZ2DG,2):UBOUND(YD%TOZ2DG,2)))
  READ (KLUN) ZTMP2
  YD%TOZ2DG = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTOZ2DL
IF (LTOZ2DL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%TOZ2DL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%TOZ2DL,1):UBOUND(YD%TOZ2DL,1), LBOUND(YD%TOZ2DL,2):UBOUND(YD%TOZ2DL,2)))
  READ (KLUN) ZTMP2
  YD%TOZ2DL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) LTOZ3DBL
IF (LTOZ3DBL) THEN
  READ (KLUN) IL2
  READ (KLUN) IU2
  ALLOCATE (YD%TOZ3DBL (IL2(1):IU2(1), IL2(2):IU2(2)))
  ALLOCATE (ZTMP2(LBOUND(YD%TOZ3DBL,1):UBOUND(YD%TOZ3DBL,1), LBOUND(YD%TOZ3DBL,2):UBOUND(YD%TOZ3DBL,2)))
  READ (KLUN) ZTMP2
  YD%TOZ3DBL = ZTMP2
  DEALLOCATE (ZTMP2)
ENDIF
READ (KLUN) ZTMP0
YD%TEECL2 = ZTMP0
END SUBROUTINE


