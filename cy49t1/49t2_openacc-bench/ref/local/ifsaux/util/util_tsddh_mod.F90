SUBROUTINE CRC64_TSDDH (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (TSDDH), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NHDQLNVA), CDPATH//'%NHDQLNVA'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NHDQLNTD), CDPATH//'%NHDQLNTD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NHDQLNFD), CDPATH//'%NHDQLNFD'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%NHDQLNFP), CDPATH//'%NHDQLNFP'

