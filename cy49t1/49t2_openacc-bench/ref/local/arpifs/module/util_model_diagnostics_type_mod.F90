FUNCTION SIZE_MODEL_DIAGNOSTICS_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCDDH_MOD
USE UTIL_TGPDDH_MOD
USE UTIL_TLDDH_MOD
USE UTIL_TMDDH_MOD
USE UTIL_TPADDH_MOD
USE UTIL_TSDDH_MOD
USE UTIL_TSPDDH_MOD
USE UTIL_TTDDH_MOD
IMPLICIT NONE
TYPE (MODEL_DIAGNOSTICS_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TCDDH (SELF%YRCDDH, CLPATH//'%YRCDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TLDDH (SELF%YRLDDH, CLPATH//'%YRLDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRLDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TMDDH (SELF%YRMDDH, CLPATH//'%YRMDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSDDH (SELF%YRSDDH, CLPATH//'%YRSDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TTDDH (SELF%YRTDDH, CLPATH//'%YRTDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRTDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TGPDDH (SELF%YRGPDDH, CLPATH//'%YRGPDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRGPDDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TPADDH (SELF%YRPADDH, CLPATH//'%YRPADDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRPADDH'
ENDIF
JSIZE = 0
ISIZE = SIZE_TSPDDH (SELF%YRSPDDH, CLPATH//'%YRSPDDH', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSPDDH'
ENDIF
END FUNCTION

