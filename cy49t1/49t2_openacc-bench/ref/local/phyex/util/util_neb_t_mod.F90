SUBROUTINE CRC64_NEB_T (YD, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
TYPE (NEB_T), TARGET :: YD
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%XTMINMIX), CDPATH//'%XTMINMIX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%XTMAXMIX), CDPATH//'%XTMAXMIX'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LHGT_QS), CDPATH//'%LHGT_QS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CFRAC_ICE_ADJUST), CDPATH//'%CFRAC_ICE_ADJUST'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CFRAC_ICE_SHALLOW_MF), CDPATH//'%CFRAC_ICE_SHALLOW_MF'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%VSIGQSAT), CDPATH//'%VSIGQSAT'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CCONDENS), CDPATH//'%CCONDENS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%CLAMBDA3), CDPATH//'%CLAMBDA3'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSTATNW), CDPATH//'%LSTATNW'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSIGMAS), CDPATH//'%LSIGMAS'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (YD%LSUBG_COND), CDPATH//'%LSUBG_COND'

END SUBROUTINE

