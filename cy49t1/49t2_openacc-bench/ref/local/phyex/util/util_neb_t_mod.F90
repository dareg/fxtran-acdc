INTEGER*8 FUNCTION SIZE_NEB_T (YD, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
TYPE (NEB_T),     INTENT (IN), TARGET :: YD
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%XTMINMIX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XTMINMIX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%XTMAXMIX)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XTMAXMIX'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LHGT_QS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LHGT_QS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CFRAC_ICE_ADJUST) * LEN (YD%CFRAC_ICE_ADJUST)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CFRAC_ICE_ADJUST'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CFRAC_ICE_SHALLOW_MF) * LEN (YD%CFRAC_ICE_SHALLOW_MF)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CFRAC_ICE_SHALLOW_MF'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%VSIGQSAT)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%VSIGQSAT'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CCONDENS) * LEN (YD%CCONDENS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CCONDENS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%CLAMBDA3) * LEN (YD%CLAMBDA3)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%CLAMBDA3'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSTATNW)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSTATNW'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSIGMAS)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSIGMAS'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%LSUBG_COND)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LSUBG_COND'
ENDIF
KSIZE = KSIZE + ISIZE
END FUNCTION

