LOGICAL :: LXDISS, LXDYP, LXLEM, LXSSRFL_C, LXSSTFL_C, LXSSUFL_C, LXSSVFL_C, LXTHP, LXTR

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (YD%XIMPL)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XIMPL'
ENDIF
KSIZE = KSIZE + ISIZE
ISIZE = KIND (YD%XTKEMIN)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%XTKEMIN'
ENDIF
KSIZE = KSIZE + ISIZE
