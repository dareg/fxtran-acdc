SUBROUTINE LARCINB_NQMGMV (YDCPG_SL1, KASLB1, YDML_DYN, YDGEOMETRY, YDCPG_BNDS, PLSCAW, PDP, KLH0)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1F_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_SL1F_TYPE), INTENT (IN)::YDCPG_SL1
INTEGER (KIND=JPIM), INTENT (IN)::KASLB1
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (IN)::PLSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTLSCAW%NDIM)
REAL (KIND=JPRB), INTENT (OUT)::PDP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM), INTENT (IN)::KLH0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, 0:3)
#include "laidli.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LARCINB_NQMGMV', 0, ZHOOK_HANDLE)

IF (YDML_DYN%YRDYNA%LCOMAD_SP) THEN
  CALL LAIDLI (KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV&
  &%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLAMAD&
  &), PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLOMAD+1), KLH0 (:,:, 1:2), YDCPG_SL1%SP9%P, PDP)
ELSE
  CALL LAIDLI (KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV&
  &%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLAT&
  &), PLSCAW (:,:, YDML_DYN%YYTLSCAW%M_WDLO+1), KLH0 (:,:, 1:2), YDCPG_SL1%SP9%P, PDP)
ENDIF

IF (LHOOK) CALL DR_HOOK ('LARCINB_NQMGMV', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
