SUBROUTINE LAPINEB_ROTUVGEO_1 (YDVARS, YDMODEL, YDA_PCCO, YDGEOMETRY, YDCPG_BNDS, PP, PQ)
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3IM_ARRAY, FIELD_4IM_ARRAY, FIELD_3RB_ARRAY, FIELD_4RB_ARRAY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE TYPE_MODEL,ONLY:MODEL
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (INOUT)::PP (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (INOUT)::PQ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "rotuvgeo.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUVGEO_1', 0, ZHOOK_HANDLE)
CALL ROTUVGEO (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
&%KFDIA, YDVARS%GEOMETRY%GNORDM%P, YDVARS%GEOMETRY%GNORDL%P, YDA_PCCO%P (:,:, YDMODEL%YRML_DYN&
&%YYTCCO%M_RQX), YDA_PCCO%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RQY), PP, PQ, LDIR=.TRUE.)
IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUVGEO_1', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
