SUBROUTINE LAPINEB_CVGQ (YDVARS, JFLD, YDCPG_GFL9, YDGEOMETRY, YDCPG_BNDS)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_GFL_TYPE_MOD,ONLY:CPG_GFL_TYPE, CPG_GFLSL_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
TYPE (CPG_GFL_TYPE), INTENT (INOUT)::YDCPG_GFL9
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_CVGQ', 0, ZHOOK_HANDLE)

IF ((YDVARS%GFL_PTR (JFLD)%YCOMP%CNAME=='CVGQ       &
  &     ').AND.YDVARS%GFL_PTR (JFLD)%YCOMP%LGP) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%GFL_PTR (JFLD)%T1 (JROF, JLEV)=YDCPG_GFL9%GFL (JFLD)%P (JROF, JLEV)-YDVARS%Q%T0 (JROF, JLEV)
    ENDDO

  ENDDO

ELSEIF (.NOT.YDVARS%GFL_PTR (JFLD)%YCOMP%LMGRID) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDVARS%GFL_PTR (JFLD)%T1 (JROF, JLEV)=YDVARS%GFL_PTR (JFLD&
      &)%T1 (JROF, JLEV)+YDCPG_GFL9%GFL (JFLD)%P (JROF, JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('LAPINEB_CVGQ', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
