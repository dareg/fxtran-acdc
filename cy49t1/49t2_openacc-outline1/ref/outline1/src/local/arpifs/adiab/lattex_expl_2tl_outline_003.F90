    &%P (JROF, JLEV)+YDVARS%GFL_PTR (JGFL)%T0 (JROF, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('LATTEX_EXPL_2TL_OUTLINE_003', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
