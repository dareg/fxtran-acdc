SUBROUTINE GPRCP_EXPL_OUTLINE_004 (LDCP, PCP, PR, YDCPG_OPTS, YDCST, YDCPG_BNDS)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE YOMCST,ONLY:TCST
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDCP
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PCP (:,:)
REAL (KIND=JPRB), INTENT (INOUT), POINTER::PR (:,:)
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (TCST), INTENT (IN)::YDCST
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
INTEGER (KIND=JPIM)::JLEV
INTEGER (KIND=JPIM)::JLON
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_004', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    PR (JLON, JLEV)=YDCST%RD+PR (JLON, JLEV)
  ENDDO

ENDDO


IF (LDCP) THEN

  DO JLEV=1, YDCPG_OPTS%KFLEVG

    DO JLON=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      PCP (JLON, JLEV)=YDCST%RCPD+PCP (JLON, JLEV)
    ENDDO

  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('GPRCP_EXPL_OUTLINE_004', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
