  CALL COPY_FIELD_2RB (SELF%F_PBLH)
  !$ACC ENTER DATA ATTACH (SELF%F_PBLH)
ENDIF

LF_SPSH = ASSOCIATED (SELF%F_SPSH)
IF (LF_SPSH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SPSH)
  !$ACC ENTER DATA ATTACH (SELF%F_SPSH)
ENDIF

LF_QSH = ASSOCIATED (SELF%F_QSH)
IF (LF_QSH .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_QSH)
  !$ACC ENTER DATA ATTACH (SELF%F_QSH)
ENDIF

