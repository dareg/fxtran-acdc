IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VCLIH), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_BCCH, LF_EVA, LF_GROUP, LF_PBLH, LF_PCL, LF_PCN, LF_PSL, LF_PSN, LF_QSH, LF_SCCH
