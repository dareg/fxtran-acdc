MODULE SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_SATSIM (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_SATSIM), TARGET :: SELF
LOGICAL :: LF_CLBT, LF_CSBT, LF_GROUP, LVARIABLE_GROUP
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  CALL SELF%VARIABLE_GROUP%ACDC_HOST ()
ENDIF

LF_CLBT = ASSOCIATED (SELF%F_CLBT)
IF (LF_CLBT) THEN
  CALL HOST_FIELD_3RB (SELF%F_CLBT)
ENDIF

LF_CSBT = ASSOCIATED (SELF%F_CSBT)
IF (LF_CSBT) THEN
  CALL HOST_FIELD_3RB (SELF%F_CSBT)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_4RB (SELF%F_GROUP)
ENDIF

