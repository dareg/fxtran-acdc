  PWMEAN (JLON)=MAX (1.E-2_JPRB, PWMEAN (JLON)/MAX (1.0_JPRB, ZDPMEAN ))
  PWMEAN (JLON)=SQRT (2.0_JPRB*PWMEAN (JLON))
ENDIF




