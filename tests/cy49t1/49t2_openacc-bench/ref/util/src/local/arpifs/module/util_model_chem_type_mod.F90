FUNCTION SIZE_MODEL_CHEM_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)
USE UTIL_TCHEM_MOD
USE UTIL_TCOMPO_MOD
USE UTIL_TOZO_MOD
IMPLICIT NONE
TYPE (MODEL_CHEM_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
JSIZE = 0
ISIZE = SIZE_TOZO (SELF%YROZO, CLPATH//'%YROZO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YROZO'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCHEM (SELF%YRCHEM, CLPATH//'%YRCHEM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCHEM'
ENDIF
JSIZE = 0
ISIZE = SIZE_TCOMPO (SELF%YRCOMPO, CLPATH//'%YRCOMPO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCOMPO'
ENDIF
END FUNCTION

