SUBROUTINE SAVE_GEOMETRY (SELF, KLUN)
USE UTIL_TCSGLEG_MOD
USE UTIL_TDIMV_MOD
USE UTIL_TDIM_MOD
USE UTIL_TEDIM_MOD
USE UTIL_TEGEO_MOD
USE UTIL_TEGSL_MOD
USE UTIL_TELBC_GEO_MOD
USE UTIL_TEMMP_MOD
USE UTIL_TGEM_MOD
USE UTIL_TLAP_MOD
USE UTIL_TLEP_MOD
USE UTIL_TMP_MOD
USE UTIL_TOROG_BLOCKED_MOD
USE UTIL_TSPGEOM_MOD
USE UTIL_TSTA_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVERTICAL_GEOM_MOD
IMPLICIT NONE
TYPE (GEOMETRY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRELBC_GEO
WRITE (KLUN) SELF%LNHX_GEOM
CALL SAVE_TVERTICAL_GEOM (SELF%YRVERT_GEOM, KLUN)
CALL SAVE_TSTA (SELF%YRSTA, KLUN)
CALL SAVE_TLAP (SELF%YRLAP, KLUN)
CALL SAVE_TCSGLEG (SELF%YRCSGLEG, KLUN)
CALL SAVE_TDIM (SELF%YRDIM, KLUN)
CALL SAVE_TDIMV (SELF%YRDIMV, KLUN)
CALL SAVE_TMP (SELF%YRMP, KLUN)
CALL SAVE_TGEM (SELF%YRGEM, KLUN)
WRITE (KLUN) SELF%AD_GEOM
CALL SAVE_TOROG_BLOCKED (SELF%YROROG_B, KLUN)
CALL SAVE_TSPGEOM (SELF%YSPGEOM, KLUN)
CALL SAVE_TVAB (SELF%YVABIO, KLUN)
CALL SAVE_TEDIM (SELF%YREDIM, KLUN)
CALL SAVE_TEGEO (SELF%YREGEO, KLUN)
CALL SAVE_TEMMP (SELF%YREMP, KLUN)
CALL SAVE_TLEP (SELF%YRELAP, KLUN)
CALL SAVE_TEGSL (SELF%YREGSL, KLUN)
LYRELBC_GEO = ASSOCIATED (SELF%YRELBC_GEO)
WRITE (KLUN) LYRELBC_GEO
IF (LYRELBC_GEO) THEN
  CALL SAVE_TELBC_GEO (SELF%YRELBC_GEO, KLUN)
ENDIF
END SUBROUTINE

SUBROUTINE LOAD_GEOMETRY (SELF, KLUN)
USE UTIL_TCSGLEG_MOD
USE UTIL_TDIMV_MOD
USE UTIL_TDIM_MOD
USE UTIL_TEDIM_MOD
USE UTIL_TEGEO_MOD
USE UTIL_TEGSL_MOD
USE UTIL_TELBC_GEO_MOD
USE UTIL_TEMMP_MOD
USE UTIL_TGEM_MOD
USE UTIL_TLAP_MOD
USE UTIL_TLEP_MOD
USE UTIL_TMP_MOD
USE UTIL_TOROG_BLOCKED_MOD
USE UTIL_TSPGEOM_MOD
USE UTIL_TSTA_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVERTICAL_GEOM_MOD

IMPLICIT NONE
TYPE (GEOMETRY), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRELBC_GEO
READ (KLUN) SELF%LNHX_GEOM
CALL LOAD_TVERTICAL_GEOM (SELF%YRVERT_GEOM, KLUN)
CALL LOAD_TSTA (SELF%YRSTA, KLUN)
CALL LOAD_TLAP (SELF%YRLAP, KLUN)
CALL LOAD_TCSGLEG (SELF%YRCSGLEG, KLUN)
CALL LOAD_TDIM (SELF%YRDIM, KLUN)
CALL LOAD_TDIMV (SELF%YRDIMV, KLUN)
CALL LOAD_TMP (SELF%YRMP, KLUN)
CALL LOAD_TGEM (SELF%YRGEM, KLUN)
NULLIFY (SELF%YRCSGEOM)
NULLIFY (SELF%YRGSGEOM)
READ (KLUN) SELF%AD_GEOM
NULLIFY (SELF%YRCSGEOMAD)
NULLIFY (SELF%YRGSGEOMAD)
CALL LOAD_TOROG_BLOCKED (SELF%YROROG_B, KLUN)
CALL LOAD_TSPGEOM (SELF%YSPGEOM, KLUN)
CALL LOAD_TVAB (SELF%YVABIO, KLUN)
CALL LOAD_TEDIM (SELF%YREDIM, KLUN)
CALL LOAD_TEGEO (SELF%YREGEO, KLUN)
CALL LOAD_TEMMP (SELF%YREMP, KLUN)
CALL LOAD_TLEP (SELF%YRELAP, KLUN)
CALL LOAD_TEGSL (SELF%YREGSL, KLUN)
READ (KLUN) LYRELBC_GEO
IF (LYRELBC_GEO) THEN
  ALLOCATE (SELF%YRELBC_GEO)
  CALL LOAD_TELBC_GEO (SELF%YRELBC_GEO, KLUN)
ELSE
  NULLIFY (SELF%YRELBC_GEO)
ENDIF
END SUBROUTINE


SUBROUTINE COPY_GEOMETRY (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_TCSGLEG_MOD
USE UTIL_TDIMV_MOD
USE UTIL_TDIM_MOD
USE UTIL_TEDIM_MOD
USE UTIL_TEGEO_MOD
USE UTIL_TEGSL_MOD
USE UTIL_TELBC_GEO_MOD
USE UTIL_TEMMP_MOD
USE UTIL_TGEM_MOD
USE UTIL_TLAP_MOD
USE UTIL_TLEP_MOD
USE UTIL_TMP_MOD
USE UTIL_TOROG_BLOCKED_MOD
USE UTIL_TSPGEOM_MOD
USE UTIL_TSTA_MOD
USE UTIL_TVAB_MOD
USE UTIL_TVERTICAL_GEOM_MOD
IMPLICIT NONE
TYPE (GEOMETRY), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LYRELBC_GEO

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF

CALL COPY_TVERTICAL_GEOM (SELF%YRVERT_GEOM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSTA (SELF%YRSTA, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TLAP (SELF%YRLAP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TCSGLEG (SELF%YRCSGLEG, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TDIM (SELF%YRDIM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TDIMV (SELF%YRDIMV, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TMP (SELF%YRMP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TGEM (SELF%YRGEM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TOROG_BLOCKED (SELF%YROROG_B, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TSPGEOM (SELF%YSPGEOM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TVAB (SELF%YVABIO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEDIM (SELF%YREDIM, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEGEO (SELF%YREGEO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEMMP (SELF%YREMP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TLEP (SELF%YRELAP, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_TEGSL (SELF%YREGSL, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LYRELBC_GEO = ASSOCIATED (SELF%YRELBC_GEO)
IF (LYRELBC_GEO) THEN
  !$ACC ENTER DATA CREATE (SELF%YRELBC_GEO)
  !$ACC UPDATE DEVICE (SELF%YRELBC_GEO)
  CALL COPY_TELBC_GEO (SELF%YRELBC_GEO, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%YRELBC_GEO)
ENDIF

END SUBROUTINE

