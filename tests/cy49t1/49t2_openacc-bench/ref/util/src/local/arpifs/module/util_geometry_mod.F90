CALL CRC64_TVERTICAL_GEOM (SELF%YRVERT_GEOM, KLUN, CDPATH//'%YRVERT_GEOM'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TSTA (SELF%YRSTA, KLUN, CDPATH//'%YRSTA'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TLAP (SELF%YRLAP, KLUN, CDPATH//'%YRLAP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TCSGLEG (SELF%YRCSGLEG, KLUN, CDPATH//'%YRCSGLEG'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TDIM (SELF%YRDIM, KLUN, CDPATH//'%YRDIM'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TDIMV (SELF%YRDIMV, KLUN, CDPATH//'%YRDIMV'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TMP (SELF%YRMP, KLUN, CDPATH//'%YRMP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TGEM (SELF%YRGEM, KLUN, CDPATH//'%YRGEM'//TRIM(CLIND))

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%AD_GEOM), CDPATH//'%AD_GEOM'

CLIND = ''
CALL CRC64_TOROG_BLOCKED (SELF%YROROG_B, KLUN, CDPATH//'%YROROG_B'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TSPGEOM (SELF%YSPGEOM, KLUN, CDPATH//'%YSPGEOM'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TVAB (SELF%YVABIO, KLUN, CDPATH//'%YVABIO'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TEDIM (SELF%YREDIM, KLUN, CDPATH//'%YREDIM'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TEGEO (SELF%YREGEO, KLUN, CDPATH//'%YREGEO'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TEMMP (SELF%YREMP, KLUN, CDPATH//'%YREMP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TLEP (SELF%YRELAP, KLUN, CDPATH//'%YRELAP'//TRIM(CLIND))

CLIND = ''
CALL CRC64_TEGSL (SELF%YREGSL, KLUN, CDPATH//'%YREGSL'//TRIM(CLIND))

LYRELBC_GEO = ASSOCIATED (SELF%YRELBC_GEO)
IF (LYRELBC_GEO) THEN
  CLIND = ''
  CALL CRC64_TELBC_GEO (SELF%YRELBC_GEO, KLUN, CDPATH//'%YRELBC_GEO'//TRIM(CLIND))
ENDIF

