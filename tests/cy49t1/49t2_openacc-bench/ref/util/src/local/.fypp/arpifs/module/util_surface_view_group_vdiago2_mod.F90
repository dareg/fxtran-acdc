IF (LF_VSTRC) THEN
  CALL HOST_FIELD_2RB (SELF%F_VSTRC)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

     



