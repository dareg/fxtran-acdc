SUBROUTINE COPY_ECPHYS_VARIABLES (SELF, LDCREATED, LDFIELDAPI)
USE UTIL_VARIABLE_3RB_MOD
IMPLICIT NONE
TYPE (ECPHYS_VARIABLES), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL COPY_VARIABLE_3RB (SELF%USTRTI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%VSTRTI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%AHFSTI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%EVAPTI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL COPY_VARIABLE_3RB (SELF%TSKTI, LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

