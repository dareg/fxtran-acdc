SUBMODULE (MODD_PHYEX) MODD_PHYEX_PHYEX_T_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE SAVE_PHYEX_T (SELF, KLUN)

IMPLICIT NONE
CLASS (PHYEX_T), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

CALL SELF%CST%SAVE (KLUN)
CALL SELF%PARAM_ICEN%SAVE (KLUN)
CALL SELF%RAIN_ICE_DESCRN%SAVE (KLUN)
CALL SELF%RAIN_ICE_PARAMN%SAVE (KLUN)
CALL SELF%CLOUDPARN%SAVE (KLUN)
CALL SELF%PARAM_MFSHALLN%SAVE (KLUN)
CALL SELF%CSTURB%SAVE (KLUN)
CALL SELF%TURBN%SAVE (KLUN)
CALL SELF%NEBN%SAVE (KLUN)
CALL SELF%PARAM_LIMA%SAVE (KLUN)
CALL SELF%PARAM_LIMA_WARM%SAVE (KLUN)
CALL SELF%PARAM_LIMA_COLD%SAVE (KLUN)
CALL SELF%PARAM_LIMA_MIXED%SAVE (KLUN)
CALL SELF%TNSV%SAVE (KLUN)
CALL SELF%MISC%SAVE (KLUN)
END SUBROUTINE

END SUBMODULE
