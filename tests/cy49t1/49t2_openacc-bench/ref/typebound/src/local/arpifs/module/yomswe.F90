MODULE YOMSWE

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB

IMPLICIT NONE

SAVE

!     ------------------------------------------------------------------

!*    Shallow-water equation Williamson et al tests

! ALPHASWE  : Angle ALPHA of the axis of the experiment and the axis of the Earth
! GMUCENSWE : sine  of latitude of the axis of the experiment

TYPE TSWE

REAL(KIND=JPRB) :: ALPHASWE
REAL(KIND=JPRB) :: GMUCENSWE

CONTAINS
PROCEDURE :: COPY => COPY_TSWE
PROCEDURE :: CRC64 => CRC64_TSWE
PROCEDURE :: HOST => HOST_TSWE
PROCEDURE :: LOAD => LOAD_TSWE
PROCEDURE :: SAVE => SAVE_TSWE
PROCEDURE :: SIZE => SIZE_TSWE
PROCEDURE :: WIPE => WIPE_TSWE
END TYPE

!     ------------------------------------------------------------------
INTERFACE

MODULE SUBROUTINE COPY_TSWE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSWE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TSWE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TSWE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TSWE (SELF)

IMPLICIT NONE
CLASS (TSWE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TSWE (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TSWE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TSWE (SELF, KLUN)

IMPLICIT NONE
CLASS (TSWE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TSWE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TSWE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TSWE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSWE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOMSWE
