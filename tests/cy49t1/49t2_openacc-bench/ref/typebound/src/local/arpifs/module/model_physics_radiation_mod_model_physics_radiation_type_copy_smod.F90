SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE COPY_MODEL_PHYSICS_RADIATION_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LYRAD_GSGEOM

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
CALL SELF%YRERAD%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRESWRT%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YREOVLP%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRENEUR%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRELWRAD%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YREAERD%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YREAERATM%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YREUVRAD%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRERDI%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YREMCICA%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRRCOEF%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRTRC%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%RADGRID%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LYRAD_GSGEOM = ASSOCIATED (SELF%YRAD_GSGEOM)
IF (LYRAD_GSGEOM) THEN
  !$ACC ENTER DATA CREATE (SELF%YRAD_GSGEOM)
  !$ACC UPDATE DEVICE (SELF%YRAD_GSGEOM)
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    CALL SELF%YRAD_GSGEOM (J1)%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC ENTER DATA ATTACH (SELF%YRAD_GSGEOM)
ENDIF

CALL SELF%YRERIP%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRADIATION%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRRI%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%YRRO%COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)

END SUBROUTINE

END SUBMODULE
