SUBMODULE (MODEL_ATMOS_OCEAN_COUPLING_MOD) MODEL_ATMOS_OCEAN_COUPLING_MOD_MODEL_ATMOS_OCEAN_COUPLING_TYPE_WIPE_SMOD

