MODULE YOE_MCICA

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRD, JPIM, JPRB

IMPLICIT NONE

SAVE
!------------------------------------------------------------------------------

TYPE :: TEMCICA

REAL(KIND=JPRB) :: XCW(1000,140)
REAL(KIND=JPRD) :: XCW_D(1000,140)
INTEGER(KIND=JPIM) :: NMCI1, NMCI2
!----------------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: COPY => COPY_TEMCICA
PROCEDURE :: CRC64 => CRC64_TEMCICA
PROCEDURE :: HOST => HOST_TEMCICA
PROCEDURE :: LOAD => LOAD_TEMCICA
PROCEDURE :: SAVE => SAVE_TEMCICA
PROCEDURE :: SIZE => SIZE_TEMCICA
PROCEDURE :: WIPE => WIPE_TEMCICA
END TYPE TEMCICA
!============================================================================

!! TYPE(TEMCICA), POINTER :: YREMCICA => NULL()

!------------------------------------------------------------------------------
! Array (with associated dimension)s used in the Raisanen et al. (2004) cloud generator

! NMcI1 : INTEGER : 1st dimension of the XCW array 
!                   (= maximum number of increments in units of cumulative propability)
! NMcI2 : INTEGER : 2nd dimension of the XCW array (= number of g-points in RRTM_LW)
! XCW   : REAL    : resulting array, precomputed in su_mcica.F90
! XCW_D : REAL    : double prec array used for reading files
!------------------------------------------------------------------------------

INTERFACE

MODULE SUBROUTINE COPY_TEMCICA (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEMCICA), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TEMCICA (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TEMCICA), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TEMCICA (SELF)

IMPLICIT NONE
CLASS (TEMCICA), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TEMCICA (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TEMCICA), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TEMCICA (SELF, KLUN)

IMPLICIT NONE
CLASS (TEMCICA), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TEMCICA (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEMCICA),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TEMCICA (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEMCICA), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
IMPLICIT NONE
CLASS(TEMCICA), INTENT(IN) :: SELF
INTEGER       , INTENT(IN) :: KDEPTH
INTEGER       , INTENT(IN) :: KOUTNO

INTEGER :: IDEPTHLOC

IDEPTHLOC = KDEPTH+2

WRITE(KOUTNO,*) REPEAT(' ',KDEPTH   ) // 'model%yrml_phy_rad%yremcica : '
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'XCW SUM = ', SUM(SELF%XCW)
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'XCW_D SUM = ', SUM(SELF%XCW_D)
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NMCI1 = ', SELF%NMCI1
WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NMCI2 = ', SELF%NMCI2

END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOE_MCICA
