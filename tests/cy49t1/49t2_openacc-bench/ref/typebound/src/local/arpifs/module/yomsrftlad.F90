MODULE YOMSRFTLAD

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB,JPIM

IMPLICIT NONE

SAVE

! ------ SKIN TEMPERATURE FOR LINEARIZED PHYSICS

! GPSURF  : BUFFER FOR PERTURBATION OF TOP LAYER SURF. FIELDS 
!           (SKIN TEMPERATURE,...)
! NGSKIN  : NUMBER OF TOP SOIL FIELDS FOR PERTURBATION

! LREGSF  : .TRUE. if the regularization for SURF computation is used

TYPE :: TSRFTLAD
REAL(KIND=JPRB),ALLOCATABLE :: GPTSKIN0(:,:,:)
REAL(KIND=JPRB),ALLOCATABLE :: GPTSKIN9(:,:,:)

INTEGER(KIND=JPIM) :: NGSKIN

LOGICAL :: LREGSF
!----------------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: COPY => COPY_TSRFTLAD
PROCEDURE :: CRC64 => CRC64_TSRFTLAD
PROCEDURE :: HOST => HOST_TSRFTLAD
PROCEDURE :: LOAD => LOAD_TSRFTLAD
PROCEDURE :: SAVE => SAVE_TSRFTLAD
PROCEDURE :: SIZE => SIZE_TSRFTLAD
PROCEDURE :: WIPE => WIPE_TSRFTLAD
END TYPE TSRFTLAD
!============================================================================

!!TYPE(TSRFTLAD), POINTER :: YRSRFTLAD => NULL()

!     ------------------------------------------------------------------

INTERFACE

MODULE SUBROUTINE COPY_TSRFTLAD (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSRFTLAD), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TSRFTLAD (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TSRFTLAD), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TSRFTLAD (SELF)

IMPLICIT NONE
CLASS (TSRFTLAD), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TSRFTLAD (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TSRFTLAD), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TSRFTLAD (SELF, KLUN)

IMPLICIT NONE
CLASS (TSRFTLAD), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TSRFTLAD (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TSRFTLAD),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TSRFTLAD (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TSRFTLAD), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(TSRFTLAD), INTENT(IN) :: SELF
  INTEGER        , INTENT(IN) :: KDEPTH
  INTEGER        , INTENT(IN) :: KOUTNO

  INTEGER :: IDEPTHLOC

  IDEPTHLOC = KDEPTH+2
  
  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH   ) // 'model%yrml_phy_slin%yrephli : '
  IF (ALLOCATED(SELF%GPTSKIN0)) WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'GPTSKIN0 allocated of shape ', &
 &        SHAPE(SELF%GPTSKIN0),' and sum ',SUM(SELF%GPTSKIN0)
  IF (ALLOCATED(SELF%GPTSKIN9)) WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'GPTSKIN9 allocated of shape ', &
 &        SHAPE(SELF%GPTSKIN9),' and sum ',SUM(SELF%GPTSKIN9)
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'NGSKIN = ', SELF%NGSKIN
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LREGSF = ', SELF%LREGSF
 
END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOMSRFTLAD
