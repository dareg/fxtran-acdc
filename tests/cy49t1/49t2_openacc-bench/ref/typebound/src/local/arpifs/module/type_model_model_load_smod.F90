SUBMODULE (TYPE_MODEL) TYPE_MODEL_MODEL_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE LOAD_MODEL (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LYRCST
READ (KLUN) SELF%LINEAR_MODEL
CALL SELF%YRML_GCONF%LOAD (KLUN)
CALL SELF%YRML_AOC%LOAD (KLUN)
CALL SELF%YRML_DYN%LOAD (KLUN)
CALL SELF%YRML_PHY_G%LOAD (KLUN)
CALL SELF%YRML_PHY_EC%LOAD (KLUN)
CALL SELF%YRML_PHY_SLIN%LOAD (KLUN)
CALL SELF%YRML_PHY_AER%LOAD (KLUN)
CALL SELF%YRML_PHY_RAD%LOAD (KLUN)
CALL SELF%YRML_PHY_STOCH%LOAD (KLUN)
CALL SELF%YRML_PHY_MF%LOAD (KLUN)
CALL SELF%YRML_CHEM%LOAD (KLUN)
CALL SELF%YRML_DIAG%LOAD (KLUN)
CALL SELF%YREWCOU%LOAD (KLUN)
READ (KLUN) LYRCST
IF (LYRCST) THEN
  ALLOCATE (SELF%YRCST)
  CALL SELF%YRCST%LOAD (KLUN)
ELSE
  NULLIFY (SELF%YRCST)
ENDIF
CALL SELF%YRML_LBC%LOAD (KLUN)
READ (KLUN) SELF%MOBJECT_ID
READ (KLUN) SELF%COBJECT_ID
END SUBROUTINE


END SUBMODULE
