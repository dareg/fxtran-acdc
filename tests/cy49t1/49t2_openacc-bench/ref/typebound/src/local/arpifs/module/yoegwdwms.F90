MODULE YOEGWDWMS

!$ACDC methods 


USE PARKIND1  ,ONLY : JPRB

IMPLICIT NONE

SAVE

! ------  CONTROLS FOR SIMPLIFIED NON-OROGRAPHIC GRAVITY WAVE SCHEME

! LREGNOGWD: .TRUE. if the regularization for non-orographic GWD is used

TYPE :: TEGWDWMS
LOGICAL :: LREGWWMS
!----------------------------------------------------------------------------
CONTAINS
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
PROCEDURE :: COPY => COPY_TEGWDWMS
PROCEDURE :: CRC64 => CRC64_TEGWDWMS
PROCEDURE :: HOST => HOST_TEGWDWMS
PROCEDURE :: LOAD => LOAD_TEGWDWMS
PROCEDURE :: SAVE => SAVE_TEGWDWMS
PROCEDURE :: SIZE => SIZE_TEGWDWMS
PROCEDURE :: WIPE => WIPE_TEGWDWMS
END TYPE TEGWDWMS
!============================================================================

!!TYPE(TEGWDWMS), POINTER :: YREGWDWMS => NULL()

!     --------------------------------------------------------------------
INTERFACE

MODULE SUBROUTINE COPY_TEGWDWMS (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEGWDWMS), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TEGWDWMS (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TEGWDWMS), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TEGWDWMS (SELF)

IMPLICIT NONE
CLASS (TEGWDWMS), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TEGWDWMS (SELF, KLUN)

IMPLICIT NONE
CLASS (TEGWDWMS), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TEGWDWMS (SELF, KLUN)

IMPLICIT NONE
CLASS (TEGWDWMS), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TEGWDWMS (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TEGWDWMS),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TEGWDWMS (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TEGWDWMS), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS

SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(TEGWDWMS), INTENT(IN) :: SELF
  INTEGER        , INTENT(IN) :: KDEPTH
  INTEGER        , INTENT(IN) :: KOUTNO

  INTEGER :: IDEPTHLOC

  IDEPTHLOC = KDEPTH+2
  
  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH)    // 'model%yrml_phy_slin%yregwdwms : '
  WRITE(KOUTNO,*) REPEAT(' ',IDEPTHLOC) // 'LREGWWMS = ', SELF%LREGWWMS

END SUBROUTINE PRINT_CONFIGURATION

END MODULE YOEGWDWMS
