MODULE MODEL_PHYSICS_SIMPLINEAR_MOD

!$ACDC methods 

  USE YOEPHLI    , ONLY : TEPHLI
  USE YOMCUMFS   , ONLY : TCUMFS
  USE YOEGWDWMS  , ONLY : TEGWDWMS
  USE YOECUMF2   , ONLY : TECUMF2
  USE YOPHLC     , ONLY : TPHLC
  USE YOPHNC     , ONLY : TPHNC
  USE YOMNCL     , ONLY : TNCL
  USE YOMSRFTLAD , ONLY : TSRFTLAD
  USE YOMSPHYHIST, ONLY : SPHYS_HIST_TYPE
  IMPLICIT NONE

  TYPE MODEL_PHYSICS_SIMPLINEAR_TYPE

  TYPE(TEPHLI)   :: YREPHLI   !! constants for lin-phys
  TYPE(TCUMFS)   :: YRCUMFS   !! simplified convection scheme
  TYPE(TEGWDWMS) :: YREGWDWMS !! simplified non-oro g-w scheme
  TYPE(TECUMF2)  :: YRECUMF2  !! new linearized cumulus mass flux
  TYPE(TPHLC)    :: YRPHLC    !! simplified phys switches
  TYPE(TPHNC)    :: YRPHNC    !! switches for t-dt trajectory & phys
  TYPE(TNCL)     :: YRNCL     !! simplified cloud characteristics
  TYPE(TSRFTLAD) :: YRSRFTLAD !! lin phys skin temp
  TYPE(SPHYS_HIST_TYPE), POINTER :: GPHIST(:) => NULL()  !! trajectory “memory”
  !---------------------------------------------------------------------
  
CONTAINS
  
  PROCEDURE, PASS :: PRINT => PRINT_CONFIGURATION 
                                                       !! across timesteps
  PROCEDURE :: COPY => COPY_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: CRC64 => CRC64_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: HOST => HOST_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: LOAD => LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: SAVE => SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: SIZE => SIZE_MODEL_PHYSICS_SIMPLINEAR_TYPE
PROCEDURE :: WIPE => WIPE_MODEL_PHYSICS_SIMPLINEAR_TYPE
END TYPE MODEL_PHYSICS_SIMPLINEAR_TYPE
  !======================================================================

  INTERFACE

MODULE SUBROUTINE COPY_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_MODEL_PHYSICS_SIMPLINEAR_TYPE (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

CONTAINS 

  SUBROUTINE PRINT_CONFIGURATION(SELF, KDEPTH, KOUTNO)
  IMPLICIT NONE
  CLASS(MODEL_PHYSICS_SIMPLINEAR_TYPE), INTENT(IN) :: SELF
  INTEGER                             , INTENT(IN) :: KDEPTH
  INTEGER                             , INTENT(IN) :: KOUTNO

  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH) // 'model%yrml_phy_slin : '
  CALL SELF%YREPHLI%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRCUMFS%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YREGWDWMS%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRECUMF2%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRPHLC%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRPHNC%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRNCL%PRINT(KDEPTH+2, KOUTNO)
  CALL SELF%YRSRFTLAD%PRINT(KDEPTH+2, KOUTNO)
  WRITE(KOUTNO,*) REPEAT(' ',KDEPTH+2) // 'model%yrml_phy_slin%gphist not printed as it is trajectory storage'

  END SUBROUTINE PRINT_CONFIGURATION

END MODULE MODEL_PHYSICS_SIMPLINEAR_MOD
