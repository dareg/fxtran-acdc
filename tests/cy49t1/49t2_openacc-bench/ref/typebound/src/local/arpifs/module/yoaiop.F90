MODULE YOAIOP

!$ACDC methods 


USE PARKIND1, ONLY : JPIM, JPRB

IMPLICIT NONE

SAVE

!     --------------------------------------------------------------------
!*    *YOAIOP* - reading aerosol inherent optical properties from file
!     L. Rontu 15 July 2018
!     --------------------------------------------------------------------

TYPE TAIOP

INTEGER(KIND=JPIM) :: NAEROMR, NWAVSW, NWAVLW, NRELHU

REAL(KIND=JPRB), ALLOCATABLE :: RSWAME(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RLWAME(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RSWAAS(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RLWAAS(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RSWASS(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RLWASS(:,:,:)
REAL(KIND=JPRB), ALLOCATABLE :: RSWWL(:), RLWWL(:), RSUNFR(:), RRH(:), RLWWEI(:)

CONTAINS
PROCEDURE :: COPY => COPY_TAIOP
PROCEDURE :: CRC64 => CRC64_TAIOP
PROCEDURE :: HOST => HOST_TAIOP
PROCEDURE :: LOAD => LOAD_TAIOP
PROCEDURE :: SAVE => SAVE_TAIOP
PROCEDURE :: SIZE => SIZE_TAIOP
PROCEDURE :: WIPE => WIPE_TAIOP
END TYPE TAIOP

!     --------------------------------------------------------------------

INTERFACE

MODULE SUBROUTINE COPY_TAIOP (SELF, LDCREATED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TAIOP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
END SUBROUTINE

MODULE SUBROUTINE CRC64_TAIOP (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (TAIOP), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
END SUBROUTINE

MODULE SUBROUTINE HOST_TAIOP (SELF)

IMPLICIT NONE
CLASS (TAIOP), TARGET :: SELF
END SUBROUTINE

MODULE SUBROUTINE LOAD_TAIOP (SELF, KLUN)
USE PARKIND1, ONLY : JPRD

IMPLICIT NONE
CLASS (TAIOP), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE SUBROUTINE SAVE_TAIOP (SELF, KLUN)

IMPLICIT NONE
CLASS (TAIOP), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
END SUBROUTINE

MODULE FUNCTION SIZE_TAIOP (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (TAIOP),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
END FUNCTION

MODULE SUBROUTINE WIPE_TAIOP (SELF, LDDELETED, LDFIELDAPI)

IMPLICIT NONE
CLASS (TAIOP), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
END SUBROUTINE


END INTERFACE

END MODULE YOAIOP
