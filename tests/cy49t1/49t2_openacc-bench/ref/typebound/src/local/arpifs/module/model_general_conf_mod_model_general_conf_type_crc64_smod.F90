SUBMODULE (MODEL_GENERAL_CONF_MOD) MODEL_GENERAL_CONF_MOD_MODEL_GENERAL_CONF_TYPE_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE CRC64_MODEL_GENERAL_CONF_TYPE (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (MODEL_GENERAL_CONF_TYPE), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
LOGICAL :: LGEOM
LGEOM = ASSOCIATED (SELF%GEOM)
IF (LGEOM) THEN
  CLIND = ''
  CALL SELF%GEOM%CRC64 (KLUN, CDPATH//'%GEOM'//TRIM(CLIND))
ENDIF

CLIND = ''
CALL SELF%YRDIMF%CRC64 (KLUN, CDPATH//'%YRDIMF'//TRIM(CLIND))

CLIND = ''
CALL SELF%YGFL%CRC64 (KLUN, CDPATH//'%YGFL'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRRIP%CRC64 (KLUN, CDPATH//'%YRRIP'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRDIMECV%CRC64 (KLUN, CDPATH//'%YRDIMECV'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRMODERR%CRC64 (KLUN, CDPATH//'%YRMODERR'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRSPPT_CONFIG%CRC64 (KLUN, CDPATH//'%YRSPPT_CONFIG'//TRIM(CLIND))

CLIND = ''
CALL SELF%YRSPP_CONFIG%CRC64 (KLUN, CDPATH//'%YRSPP_CONFIG'//TRIM(CLIND))

END SUBROUTINE

END SUBMODULE
