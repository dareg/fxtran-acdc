SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_HOST_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE HOST_MODEL_PHYSICS_RADIATION_TYPE (SELF)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), TARGET :: SELF
INTEGER :: J1
LOGICAL :: LYRAD_GSGEOM
CALL SELF%YRERAD%HOST ()

CALL SELF%YRESWRT%HOST ()

CALL SELF%YREOVLP%HOST ()

CALL SELF%YRENEUR%HOST ()

CALL SELF%YRELWRAD%HOST ()

CALL SELF%YREAERD%HOST ()

CALL SELF%YREAERATM%HOST ()

CALL SELF%YREUVRAD%HOST ()

CALL SELF%YRERDI%HOST ()

CALL SELF%YREMCICA%HOST ()

CALL SELF%YRRCOEF%HOST ()

CALL SELF%YRTRC%HOST ()

CALL SELF%RADGRID%HOST ()

LYRAD_GSGEOM = ASSOCIATED (SELF%YRAD_GSGEOM)
IF (LYRAD_GSGEOM) THEN
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    CALL SELF%YRAD_GSGEOM (J1)%HOST ()
  ENDDO
ENDIF

CALL SELF%YRERIP%HOST ()

CALL SELF%YRADIATION%HOST ()

CALL SELF%YRRI%HOST ()

CALL SELF%YRRO%HOST ()

END SUBROUTINE

END SUBMODULE
