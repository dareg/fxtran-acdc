SUBMODULE (YOMVERT) YOMVERT_TVERTICAL_GEOM_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE SAVE_TVERTICAL_GEOM (SELF, KLUN)

IMPLICIT NONE
CLASS (TVERTICAL_GEOM), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN

WRITE (KLUN) SELF%LNONHYD_GEOM
CALL SELF%YRVAB%SAVE (KLUN)
CALL SELF%YRVETA%SAVE (KLUN)
CALL SELF%YRVFE%SAVE (KLUN)
CALL SELF%YRCVER%SAVE (KLUN)
END SUBROUTINE

END SUBMODULE
