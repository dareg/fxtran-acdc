SUBMODULE (INTDYN_MOD) INTDYN_MOD_THWIND_CRC64_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE CRC64_THWIND (SELF, KLUN, CDPATH)
USE CRC64_INTRINSIC, ONLY : FCRC64 => CRC64

IMPLICIT NONE
CLASS (THWIND), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
CHARACTER(LEN=*), INTENT (IN) :: CDPATH
CHARACTER(LEN=128) :: CLIND
WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_UH), CDPATH//'%M_UH'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_VH), CDPATH//'%M_VH'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%M_WWI), CDPATH//'%M_WWI'

WRITE (KLUN, '(Z16.16," ",A)') FCRC64 (SELF%NDIM), CDPATH//'%NDIM'

END SUBROUTINE

END SUBMODULE
