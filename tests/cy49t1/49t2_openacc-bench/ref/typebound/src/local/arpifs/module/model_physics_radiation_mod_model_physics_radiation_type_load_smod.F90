SUBMODULE (MODEL_PHYSICS_RADIATION_MOD) MODEL_PHYSICS_RADIATION_MOD_MODEL_PHYSICS_RADIATION_TYPE_LOAD_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE LOAD_MODEL_PHYSICS_RADIATION_TYPE (SELF, KLUN)

IMPLICIT NONE
CLASS (MODEL_PHYSICS_RADIATION_TYPE), INTENT (OUT), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
INTEGER :: J1
INTEGER :: IL1(1), IU1(1)
LOGICAL :: LYRAD_GSGEOM
CALL SELF%YRERAD%LOAD (KLUN)
CALL SELF%YRESWRT%LOAD (KLUN)
CALL SELF%YREOVLP%LOAD (KLUN)
CALL SELF%YRENEUR%LOAD (KLUN)
CALL SELF%YRELWRAD%LOAD (KLUN)
CALL SELF%YREAERD%LOAD (KLUN)
CALL SELF%YREAERATM%LOAD (KLUN)
CALL SELF%YREUVRAD%LOAD (KLUN)
CALL SELF%YRERDI%LOAD (KLUN)
CALL SELF%YREMCICA%LOAD (KLUN)
CALL SELF%YRRCOEF%LOAD (KLUN)
CALL SELF%YRTRC%LOAD (KLUN)
CALL SELF%RADGRID%LOAD (KLUN)
READ (KLUN) LYRAD_GSGEOM
IF (LYRAD_GSGEOM) THEN
  READ (KLUN) IL1
  READ (KLUN) IU1
  ALLOCATE (SELF%YRAD_GSGEOM (IL1(1):IU1(1)))
  DO J1 = LBOUND (SELF%YRAD_GSGEOM, 1), UBOUND (SELF%YRAD_GSGEOM, 1)
    CALL SELF%YRAD_GSGEOM (J1)%LOAD (KLUN)
  ENDDO
ELSE
  NULLIFY (SELF%YRAD_GSGEOM)
ENDIF
CALL SELF%YRERIP%LOAD (KLUN)
CALL SELF%YRADIATION%LOAD (KLUN)
CALL SELF%YRRI%LOAD (KLUN)
CALL SELF%YRRO%LOAD (KLUN)
END SUBROUTINE


END SUBMODULE
