SUBMODULE (TYPE_GEOMETRY) TYPE_GEOMETRY_GEOMETRY_SIZE_SMOD

IMPLICIT NONE

CONTAINS

MODULE FUNCTION SIZE_GEOMETRY (SELF, CDPATH, LDPRINT) RESULT (KSIZE)

IMPLICIT NONE
CLASS (GEOMETRY),     INTENT (IN), TARGET :: SELF
CHARACTER(LEN=*), INTENT (IN), OPTIONAL :: CDPATH
LOGICAL,          INTENT (IN), OPTIONAL :: LDPRINT
INTEGER*8 :: KSIZE
INTEGER*8 :: ISIZE, JSIZE
LOGICAL :: LLPRINT
CHARACTER(LEN=128) :: CLPATH
LOGICAL :: LYRELBC_GEO

LLPRINT = .FALSE.
IF (PRESENT (LDPRINT)) THEN
  LLPRINT = LDPRINT
ENDIF
CLPATH=''
IF (PRESENT (CDPATH)) THEN
  CLPATH = CDPATH
ENDIF
KSIZE = 0
ISIZE = KIND (SELF%LNHX_GEOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%LNHX_GEOM'
ENDIF
KSIZE = KSIZE + ISIZE
JSIZE = 0
ISIZE = SELF%YRVERT_GEOM%SIZE (CLPATH//'%YRVERT_GEOM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRVERT_GEOM'
ENDIF
JSIZE = 0
ISIZE = SELF%YRSTA%SIZE (CLPATH//'%YRSTA', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRSTA'
ENDIF
JSIZE = 0
ISIZE = SELF%YRLAP%SIZE (CLPATH//'%YRLAP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRLAP'
ENDIF
JSIZE = 0
ISIZE = SELF%YRCSGLEG%SIZE (CLPATH//'%YRCSGLEG', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRCSGLEG'
ENDIF
JSIZE = 0
ISIZE = SELF%YRDIM%SIZE (CLPATH//'%YRDIM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIM'
ENDIF
JSIZE = 0
ISIZE = SELF%YRDIMV%SIZE (CLPATH//'%YRDIMV', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRDIMV'
ENDIF
JSIZE = 0
ISIZE = SELF%YRMP%SIZE (CLPATH//'%YRMP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRMP'
ENDIF
JSIZE = 0
ISIZE = SELF%YRGEM%SIZE (CLPATH//'%YRGEM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRGEM'
ENDIF
ISIZE = KIND (SELF%AD_GEOM)
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') ISIZE
  WRITE (*, *) TRIM (CLPATH)//'%AD_GEOM'
ENDIF
KSIZE = KSIZE + ISIZE
JSIZE = 0
ISIZE = SELF%YROROG_B%SIZE (CLPATH//'%YROROG_B', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YROROG_B'
ENDIF
JSIZE = 0
ISIZE = SELF%YSPGEOM%SIZE (CLPATH//'%YSPGEOM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YSPGEOM'
ENDIF
JSIZE = 0
ISIZE = SELF%YVABIO%SIZE (CLPATH//'%YVABIO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YVABIO'
ENDIF
JSIZE = 0
ISIZE = SELF%YREDIM%SIZE (CLPATH//'%YREDIM', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREDIM'
ENDIF
JSIZE = 0
ISIZE = SELF%YREGEO%SIZE (CLPATH//'%YREGEO', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREGEO'
ENDIF
JSIZE = 0
ISIZE = SELF%YREMP%SIZE (CLPATH//'%YREMP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREMP'
ENDIF
JSIZE = 0
ISIZE = SELF%YRELAP%SIZE (CLPATH//'%YRELAP', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YRELAP'
ENDIF
JSIZE = 0
ISIZE = SELF%YREGSL%SIZE (CLPATH//'%YREGSL', .FALSE.)
JSIZE = JSIZE + ISIZE
KSIZE = KSIZE + ISIZE
IF (LLPRINT) THEN
  WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
  WRITE (*, *) TRIM (CLPATH)//'%YREGSL'
ENDIF
LYRELBC_GEO = ASSOCIATED (SELF%YRELBC_GEO)
IF (LYRELBC_GEO) THEN
  JSIZE = 0
  ISIZE = SELF%YRELBC_GEO%SIZE (CLPATH//'%YRELBC_GEO', .FALSE.)
  JSIZE = JSIZE + ISIZE
  KSIZE = KSIZE + ISIZE
  IF (LLPRINT) THEN
    WRITE (*, '(I10," ")', ADVANCE='NO') JSIZE
    WRITE (*, *) TRIM (CLPATH)//'%YRELBC_GEO'
  ENDIF
ENDIF
END FUNCTION

END SUBMODULE
