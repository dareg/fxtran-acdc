MODULE SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VEXTRDI (SELF)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VEXTRDI), TARGET :: SELF
LOGICAL :: LF_GROUP, LF_XEDR, LVARIABLE_GROUP
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  CALL SELF%VARIABLE_GROUP%ACDC_HOST ()
ENDIF

LF_XEDR = ASSOCIATED (SELF%F_XEDR)
IF (LF_XEDR) THEN
  CALL HOST_FIELD_3RB (SELF%F_XEDR)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_4RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

