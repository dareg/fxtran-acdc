  !$ACC EXIT DATA DETACH (SELF%VCHEMFLXO)
  DO J1 = LBOUND (SELF%VCHEMFLXO, 1), UBOUND (SELF%VCHEMFLXO, 1)
    CALL SELF%VCHEMFLXO (J1)%WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
