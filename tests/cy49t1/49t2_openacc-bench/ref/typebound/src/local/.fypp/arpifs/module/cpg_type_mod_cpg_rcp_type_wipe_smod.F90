SUBMODULE (CPG_TYPE_MOD) CPG_TYPE_MOD_CPG_RCP_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE WIPE_CPG_RCP_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_RCP_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CP, LF_DATA, LF_KAP, LF_R, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (SELF%F_DATA)
ENDIF

LF_CP = ASSOCIATED (SELF%F_CP)
IF (LF_CP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_CP)
ENDIF

LF_R = ASSOCIATED (SELF%F_R)
IF (LF_R .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_R)
ENDIF

LF_KAP = ASSOCIATED (SELF%F_KAP)
IF (LF_KAP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_KAP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
