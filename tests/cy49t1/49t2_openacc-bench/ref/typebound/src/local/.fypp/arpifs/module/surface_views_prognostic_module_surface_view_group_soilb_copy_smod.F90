SUBMODULE (SURFACE_VIEWS_PROGNOSTIC_MODULE) SURFACE_VIEWS_PROGNOSTIC_MODULE_SURFACE_VIEW_GROUP_SOILB_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_SOILB (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_SOILB), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_GROUP, LF_Q_T0, LF_Q_T1, LF_Q_T9, LF_TL_T0, LF_TL_T1, LF_TL_T9, LF_T_T0, LF_T_T1, LF_T_T9
LOGICAL :: LVARIABLE_GROUP

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC ENTER DATA CREATE (SELF%VARIABLE_GROUP)
  !$ACC UPDATE DEVICE (SELF%VARIABLE_GROUP)
  CALL SELF%VARIABLE_GROUP%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%VARIABLE_GROUP)
ENDIF

LF_T_T0 = ASSOCIATED (SELF%F_T_T0)
IF (LF_T_T0 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_T_T0)
  !$ACC ENTER DATA ATTACH (SELF%F_T_T0)
ENDIF

LF_T_T1 = ASSOCIATED (SELF%F_T_T1)
IF (LF_T_T1 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_T_T1)
  !$ACC ENTER DATA ATTACH (SELF%F_T_T1)
ENDIF

LF_T_T9 = ASSOCIATED (SELF%F_T_T9)
IF (LF_T_T9 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_T_T9)
  !$ACC ENTER DATA ATTACH (SELF%F_T_T9)
ENDIF

LF_Q_T0 = ASSOCIATED (SELF%F_Q_T0)
IF (LF_Q_T0 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_Q_T0)
  !$ACC ENTER DATA ATTACH (SELF%F_Q_T0)
ENDIF

LF_Q_T1 = ASSOCIATED (SELF%F_Q_T1)
IF (LF_Q_T1 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_Q_T1)
  !$ACC ENTER DATA ATTACH (SELF%F_Q_T1)
ENDIF

LF_Q_T9 = ASSOCIATED (SELF%F_Q_T9)
IF (LF_Q_T9 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_Q_T9)
  !$ACC ENTER DATA ATTACH (SELF%F_Q_T9)
ENDIF

LF_TL_T0 = ASSOCIATED (SELF%F_TL_T0)
IF (LF_TL_T0 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TL_T0)
  !$ACC ENTER DATA ATTACH (SELF%F_TL_T0)
ENDIF

LF_TL_T1 = ASSOCIATED (SELF%F_TL_T1)
IF (LF_TL_T1 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TL_T1)
  !$ACC ENTER DATA ATTACH (SELF%F_TL_T1)
ENDIF

LF_TL_T9 = ASSOCIATED (SELF%F_TL_T9)
IF (LF_TL_T9 .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_TL_T9)
  !$ACC ENTER DATA ATTACH (SELF%F_TL_T9)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_4RB (SELF%F_GROUP)
  !$ACC ENTER DATA ATTACH (SELF%F_GROUP)
ENDIF

END SUBROUTINE

END SUBMODULE
