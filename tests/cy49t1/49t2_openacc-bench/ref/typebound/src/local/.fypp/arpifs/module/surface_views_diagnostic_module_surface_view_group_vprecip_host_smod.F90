MODULE SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VPRECIP (SELF)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VPRECIP), TARGET :: SELF
LOGICAL :: LF_GROUP, LF_PRECIP, LVARIABLE_GROUP
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  CALL SELF%VARIABLE_GROUP%ACDC_HOST ()
ENDIF

LF_PRECIP = ASSOCIATED (SELF%F_PRECIP)
IF (LF_PRECIP) THEN
  CALL HOST_FIELD_3RB (SELF%F_PRECIP)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_4RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

END SUBMODULE
