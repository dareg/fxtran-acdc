        Z_YDVARS_SGRTL_T0 => GET_HOST_DATA_RDONLY (YDVARS%SGRTL%FT0)
        Z_YDVARS_SGRTL_T9 => GET_HOST_DATA_RDWR (YDVARS%SGRTL%FT9)
