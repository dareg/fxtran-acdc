SUBMODULE (SURFACE_VIEWS_DIAGNOSTIC_MODULE) SURFACE_VIEWS_DIAGNOSTIC_MODULE_SURFACE_VIEW_GROUP_WAVES_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_WAVES (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_WAVES), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_CHAR, LF_CHARHQ, LF_GROUP, LF_PHIOC, LF_TAUOCX, LF_TAUOCY, LF_USTOKES, LF_VSTOKES, LF_WSEMEAN, LF_WSFMEAN
LOGICAL :: LVARIABLE_GROUP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
  CALL SELF%VARIABLE_GROUP%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
ENDIF

LF_CHAR = ASSOCIATED (SELF%F_CHAR)
IF (LF_CHAR .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_CHAR)
ENDIF

LF_CHARHQ = ASSOCIATED (SELF%F_CHARHQ)
IF (LF_CHARHQ .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_CHARHQ)
ENDIF

LF_USTOKES = ASSOCIATED (SELF%F_USTOKES)
IF (LF_USTOKES .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_USTOKES)
ENDIF

LF_VSTOKES = ASSOCIATED (SELF%F_VSTOKES)
IF (LF_VSTOKES .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_VSTOKES)
ENDIF

LF_TAUOCX = ASSOCIATED (SELF%F_TAUOCX)
IF (LF_TAUOCX .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_TAUOCX)
ENDIF

LF_TAUOCY = ASSOCIATED (SELF%F_TAUOCY)
IF (LF_TAUOCY .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_TAUOCY)
ENDIF

LF_PHIOC = ASSOCIATED (SELF%F_PHIOC)
IF (LF_PHIOC .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_PHIOC)
ENDIF

LF_WSEMEAN = ASSOCIATED (SELF%F_WSEMEAN)
IF (LF_WSEMEAN .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_WSEMEAN)
ENDIF

LF_WSFMEAN = ASSOCIATED (SELF%F_WSFMEAN)
IF (LF_WSFMEAN .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_WSFMEAN)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
