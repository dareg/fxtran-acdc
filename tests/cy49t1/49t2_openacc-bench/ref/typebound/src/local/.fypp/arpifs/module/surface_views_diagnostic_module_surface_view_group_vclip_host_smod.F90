MODULE SUBROUTINE ACDC_HOST_SURFACE_VIEW_GROUP_VCLIP (SELF)
USE FIELD_UTIL_MODULE

IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VCLIP), TARGET :: SELF
LOGICAL :: LF_GROUP, LF_TPC, LF_WPC, LVARIABLE_GROUP
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  CALL SELF%VARIABLE_GROUP%ACDC_HOST ()
ENDIF

LF_TPC = ASSOCIATED (SELF%F_TPC)
IF (LF_TPC) THEN
  CALL HOST_FIELD_2RB (SELF%F_TPC)
ENDIF

LF_WPC = ASSOCIATED (SELF%F_WPC)
IF (LF_WPC) THEN
  CALL HOST_FIELD_2RB (SELF%F_WPC)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP) THEN
  CALL HOST_FIELD_3RB (SELF%F_GROUP)
ENDIF

END SUBROUTINE

