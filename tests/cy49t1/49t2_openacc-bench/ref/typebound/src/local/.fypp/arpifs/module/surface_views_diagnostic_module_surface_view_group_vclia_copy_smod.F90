SUBMODULE (SURFACE_VIEWS_DIAGNOSTIC_MODULE) SURFACE_VIEWS_DIAGNOSTIC_MODULE_SURFACE_VIEW_GROUP_VCLIA_COPY_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_COPY_SURFACE_VIEW_GROUP_VCLIA (SELF, LDCREATED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VCLIA), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDCREATED, LDFIELDAPI
LOGICAL :: LLCREATED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_DES, LF_GROUP, LF_LAN, LF_SEA, LF_SOO, LF_SUL, LF_VOL, LVARIABLE_GROUP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LLCREATED = .FALSE.
IF (PRESENT (LDCREATED)) THEN
  LLCREATED = LDCREATED
ENDIF
IF (.NOT. LLCREATED) THEN
  !$ACC ENTER DATA CREATE (SELF)
  !$ACC UPDATE DEVICE (SELF)
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC ENTER DATA CREATE (SELF%VARIABLE_GROUP)
  !$ACC UPDATE DEVICE (SELF%VARIABLE_GROUP)
  CALL SELF%VARIABLE_GROUP%ACDC_COPY (LDCREATED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC ENTER DATA ATTACH (SELF%VARIABLE_GROUP)
ENDIF

LF_SEA = ASSOCIATED (SELF%F_SEA)
IF (LF_SEA .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SEA)
  !$ACC ENTER DATA ATTACH (SELF%F_SEA)
ENDIF

LF_LAN = ASSOCIATED (SELF%F_LAN)
IF (LF_LAN .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_LAN)
  !$ACC ENTER DATA ATTACH (SELF%F_LAN)
ENDIF

LF_SOO = ASSOCIATED (SELF%F_SOO)
IF (LF_SOO .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SOO)
  !$ACC ENTER DATA ATTACH (SELF%F_SOO)
ENDIF

LF_DES = ASSOCIATED (SELF%F_DES)
IF (LF_DES .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_DES)
  !$ACC ENTER DATA ATTACH (SELF%F_DES)
ENDIF

LF_SUL = ASSOCIATED (SELF%F_SUL)
IF (LF_SUL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_SUL)
  !$ACC ENTER DATA ATTACH (SELF%F_SUL)
ENDIF

LF_VOL = ASSOCIATED (SELF%F_VOL)
IF (LF_VOL .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_2RB (SELF%F_VOL)
  !$ACC ENTER DATA ATTACH (SELF%F_VOL)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL COPY_FIELD_3RB (SELF%F_GROUP)
  !$ACC ENTER DATA ATTACH (SELF%F_GROUP)
ENDIF

END SUBROUTINE

END SUBMODULE
