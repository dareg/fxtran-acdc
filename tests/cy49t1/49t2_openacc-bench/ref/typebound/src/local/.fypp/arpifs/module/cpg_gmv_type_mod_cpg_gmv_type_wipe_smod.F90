SUBMODULE (CPG_GMV_TYPE_MOD) CPG_GMV_TYPE_MOD_CPG_GMV_TYPE_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_CPG_GMV_TYPE (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (CPG_GMV_TYPE), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
INTEGER :: J1
LOGICAL :: LF_DATA, LGMV, LZDATA, LZVIEW

LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF

LZVIEW = ASSOCIATED (SELF%ZVIEW)
IF (LZVIEW) THEN
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
  !$ACC EXIT DATA DETACH (SELF%ZVIEW)
ENDIF

LZDATA = ASSOCIATED (SELF%ZDATA)
IF (LZDATA) THEN
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
  !$ACC EXIT DATA DETACH (SELF%ZDATA)
ENDIF

LF_DATA = ASSOCIATED (SELF%F_DATA)
IF (LF_DATA .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_4RB (SELF%F_DATA)
ENDIF

CALL SELF%U%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%V%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%T%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%SPD%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%SVD%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

CALL SELF%NHX%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)

LGMV = ALLOCATED (SELF%GMV)
IF (LGMV) THEN
  !$ACC EXIT DATA DETACH (SELF%GMV)
  DO J1 = LBOUND (SELF%GMV, 1), UBOUND (SELF%GMV, 1)
    CALL SELF%GMV (J1)%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  ENDDO
  !$ACC EXIT DATA DETACH (SELF%GMV)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
