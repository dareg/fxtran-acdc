SUBMODULE (SURFACE_VIEWS_DIAGNOSTIC_MODULE) SURFACE_VIEWS_DIAGNOSTIC_MODULE_SURFACE_VIEW_GROUP_VCLIN_WIPE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE ACDC_WIPE_SURFACE_VIEW_GROUP_VCLIN (SELF, LDDELETED, LDFIELDAPI)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (SURFACE_VIEW_GROUP_VCLIN), INTENT (IN), TARGET :: SELF
LOGICAL, OPTIONAL, INTENT (IN) :: LDDELETED, LDFIELDAPI
LOGICAL :: LLDELETED
LOGICAL :: LLFIELDAPI
LOGICAL :: LF_ACCPR, LF_ACCPR5, LF_ACPR, LF_BAS, LF_GROUP, LF_TOP, LVARIABLE_GROUP
LLFIELDAPI = .FALSE.
IF (PRESENT (LDFIELDAPI)) THEN
  LLFIELDAPI = LDFIELDAPI
ENDIF
LVARIABLE_GROUP = ASSOCIATED (SELF%VARIABLE_GROUP)
IF (LVARIABLE_GROUP) THEN
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
  CALL SELF%VARIABLE_GROUP%ACDC_WIPE (LDDELETED=.TRUE., LDFIELDAPI=LDFIELDAPI)
  !$ACC EXIT DATA DETACH (SELF%VARIABLE_GROUP)
ENDIF

LF_TOP = ASSOCIATED (SELF%F_TOP)
IF (LF_TOP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_TOP)
ENDIF

LF_BAS = ASSOCIATED (SELF%F_BAS)
IF (LF_BAS .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_BAS)
ENDIF

LF_ACPR = ASSOCIATED (SELF%F_ACPR)
IF (LF_ACPR .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_ACPR)
ENDIF

LF_ACCPR = ASSOCIATED (SELF%F_ACCPR)
IF (LF_ACCPR .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_ACCPR)
ENDIF

LF_ACCPR5 = ASSOCIATED (SELF%F_ACCPR5)
IF (LF_ACCPR5 .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_2RB (SELF%F_ACCPR5)
ENDIF

LF_GROUP = ASSOCIATED (SELF%F_GROUP)
IF (LF_GROUP .AND. LLFIELDAPI) THEN
  CALL WIPE_FIELD_3RB (SELF%F_GROUP)
ENDIF

LLDELETED = .FALSE.
IF (PRESENT (LDDELETED)) THEN
  LLDELETED = LDDELETED
ENDIF
IF (.NOT. LLDELETED) THEN
  !$ACC EXIT DATA DELETE (SELF)
ENDIF
END SUBROUTINE

END SUBMODULE
