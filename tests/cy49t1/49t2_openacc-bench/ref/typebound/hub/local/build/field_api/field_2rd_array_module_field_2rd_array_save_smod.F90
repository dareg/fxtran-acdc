SUBMODULE (FIELD_2RD_ARRAY_MODULE) FIELD_2RD_ARRAY_MODULE_FIELD_2RD_ARRAY_SAVE_SMOD

IMPLICIT NONE

CONTAINS

MODULE SUBROUTINE SAVE_FIELD_2RD_ARRAY (SELF, KLUN)
USE FIELD_UTIL_MODULE
IMPLICIT NONE
CLASS (FIELD_2RD_ARRAY), INTENT (IN), TARGET :: SELF
INTEGER, INTENT (IN) :: KLUN
LOGICAL :: LF_P, LP_FIELD
LF_P = ASSOCIATED (SELF%F_P)
WRITE (KLUN) LF_P
IF (LF_P) THEN
  CALL SAVE_FIELD_2RD (SELF%F_P, KLUN)
ENDIF
LP_FIELD = ASSOCIATED (SELF%P_FIELD)
WRITE (KLUN) LP_FIELD
IF (LP_FIELD) THEN
  WRITE (KLUN) LBOUND (SELF%P_FIELD)
  WRITE (KLUN) UBOUND (SELF%P_FIELD)
  WRITE (KLUN) SELF%P_FIELD
ENDIF
END SUBROUTINE

END SUBMODULE
