SUBROUTINE CTVTOT_EXPL_OUTLINE_000 (YDVARS, YDCST, YDGEOMETRY, YDCPG_BNDS, PR)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE YOMCST,ONLY:TCST
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (TCST), INTENT (IN)::YDCST
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (INOUT)::PR (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CTVTOT_EXPL_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDVARS%T%T0 (JROF, JLEV)=YDCST%RD*YDVARS%T%T0 (JROF, JLEV)/PR (JROF, JLEV)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('CTVTOT_EXPL_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
