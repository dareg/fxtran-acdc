SUBROUTINE CPG_ZEROPB2 (YDCPG_OPTS, YDCPG_SL2, YDCPG_BNDS)
USE CPG_SL2_TYPE_MOD,ONLY:CPG_SL2_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_SL2_TYPE), INTENT (INOUT)::YDCPG_SL2
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('CPG_ZEROPB2', 0, ZHOOK_HANDLE)
CALL YDCPG_SL2%SET (YDCPG_OPTS, YDCPG_BNDS, 0._JPRB)
IF (LHOOK) CALL DR_HOOK ('CPG_ZEROPB2', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
