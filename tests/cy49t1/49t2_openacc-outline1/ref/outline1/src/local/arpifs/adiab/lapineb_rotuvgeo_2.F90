SUBROUTINE LAPINEB_ROTUVGEO_2 (YDVARS, YDCPG_GMV9, YDA_PCCO, YDMODEL, YDGEOMETRY, YDCPG_BNDS)
USE FIELD_ARRAY_MODULE,ONLY:FIELD_3IM_ARRAY, FIELD_4IM_ARRAY, FIELD_3RB_ARRAY, FIELD_4RB_ARRAY
USE TYPE_MODEL,ONLY:MODEL
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_GMV_TYPE_MOD,ONLY:CPG_GMV_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMV9
TYPE (FIELD_4RB_ARRAY), INTENT (IN)::YDA_PCCO
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
#include "rotuv.intfb.h"
#include "rotuvgeo.intfb.h"
REAL (KIND=JPRB)::ZQ2 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB)::ZP2 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUVGEO_2', 0, ZHOOK_HANDLE)
CALL ROTUVGEO (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
&%KFDIA, YDVARS%GEOMETRY%GNORDM%P, YDVARS%GEOMETRY%GNORDL%P, YDA_PCCO%P (:,:, YDMODEL%YRML_DYN&
&%YYTCCO%M_RQX), YDA_PCCO%P (:,:, YDMODEL%YRML_DYN%YYTCCO%M_RQY), ZP2, ZQ2, LDIR=.TRUE.)
CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_GMV9&
&%U%P, YDCPG_GMV9%V%P, ZP2, ZQ2, YDVARS%U%T1, YDVARS%V%T1, LDIR=.FALSE., LDADD=.TRUE.)

IF (YDMODEL%YRML_DYN%YRDYNA%LPC_CHEAP.AND.(YDMODEL%YRML_DYN%YRDYN%NCURRENT_ITER==0)) THEN
  CALL ROTUV (YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS&
  &%KIDIA, YDCPG_BNDS%KFDIA, YDCPG_GMV9%U%P, YDCPG_GMV9%V%P, ZP2, ZQ2, YDVARS&
  &%PCF_U%PC_PH, YDVARS%PCF_V%PC_PH, LDIR=.FALSE., LDADD=.TRUE.)
ENDIF

IF (LHOOK) CALL DR_HOOK ('LAPINEB_ROTUVGEO_2', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
