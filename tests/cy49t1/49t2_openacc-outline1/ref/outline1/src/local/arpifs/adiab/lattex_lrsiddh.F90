SUBROUTINE LATTEX_LRSIDDH (YDCPG_TNDSI_DDH, YDCPG_SL1, YDML_DYN, YDGEOMETRY, YDCPG_BNDS)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_DDH_TND_TYPE_MOD,ONLY:CPG_TNDSI_DDH_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_TNDSI_DDH_TYPE), INTENT (INOUT)::YDCPG_TNDSI_DDH
TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LATTEX_LRSIDDH', 0, ZHOOK_HANDLE)
YDCPG_TNDSI_DDH%U%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=0.0_JPRB
YDCPG_TNDSI_DDH%V%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=0.0_JPRB
YDCPG_TNDSI_DDH%T%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=0.0_JPRB

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG
  YDCPG_SL1%U9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=0.0_JPRB
  YDCPG_SL1%V9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=0.0_JPRB
  YDCPG_SL1%T9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=0.0_JPRB
ENDDO


IF (YDML_DYN%YRDYNA%LNHDYN) THEN
  YDCPG_TNDSI_DDH%PD%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=0.0_JPRB
  YDCPG_TNDSI_DDH%VD%T0 (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=0.0_JPRB
ENDIF


IF (YDML_DYN%YRDYNA%LNHDYN) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG
    YDCPG_SL1%PD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=0.0_JPRB
    YDCPG_SL1%VD9_SI%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, JLEV)=0.0_JPRB
  ENDDO

ENDIF

IF (LHOOK) CALL DR_HOOK ('LATTEX_LRSIDDH', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
