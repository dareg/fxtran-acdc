SUBROUTINE APL_ARPEGE_PRECIPS (YDMF_PHYS_SURF, YDCPG_OPTS&
&, YDMODEL, YDCPG_BNDS, PPRC_DPRECIPS2, PPRC_DPRECIPS)
USE MF_PHYS_SURFACE_TYPE_MOD,ONLY:MF_PHYS_SURF_TYPE
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE TYPE_MODEL,ONLY:MODEL
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (MF_PHYS_SURF_TYPE), INTENT (INOUT)::YDMF_PHYS_SURF
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (MODEL), INTENT (IN)::YDMODEL
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (INOUT)::PPRC_DPRECIPS2 (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%NDTPREC2)
REAL (KIND=JPRB), INTENT (INOUT)::PPRC_DPRECIPS (YDCPG_OPTS%KLON, 1:YDCPG_OPTS%NDTPREC)
#include "mf_phys_precips.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_PRECIPS', 0, ZHOOK_HANDLE)
CALL MF_PHYS_PRECIPS (YDCPG_BNDS, YDCPG_OPTS, PPRC_DPRECIPS, PPRC_DPRECIPS2,&
& YDMF_PHYS_SURF%GSD_XP%PPRECIP, YDMF_PHYS_SURF%GSD_XP2%PPRECIP2, YDMODEL)
IF (LHOOK) CALL DR_HOOK ('APL_ARPEGE_PRECIPS', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
