SUBROUTINE LARCINB_LAITRE_GFL (LDMOM, YDVARS, YDCPG_SL1, JFLD, KHVI, YDCPG_GFLF, KASLB1,&
& YDML_DYN, YDML_GCONF, KWKGFL, YDGEOMETRY, YDCPG_BNDS, PLSCAW, PRSCAW, KL0, KNOWENO)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1F_TYPE
USE CPG_GFL_TYPE_MOD,ONLY:CPG_GFL_TYPE, CPG_GFLSL_TYPE
USE MODEL_GENERAL_CONF_MOD,ONLY:MODEL_GENERAL_CONF_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDMOM
TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
TYPE (CPG_SL1F_TYPE), INTENT (IN)::YDCPG_SL1
INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
INTEGER (KIND=JPIM), INTENT (IN)::KHVI
TYPE (CPG_GFL_TYPE), INTENT (INOUT)::YDCPG_GFLF
INTEGER (KIND=JPIM), INTENT (IN)::KASLB1
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (MODEL_GENERAL_CONF_TYPE), INTENT (IN)::YDML_GCONF
INTEGER (KIND=JPIM), INTENT (INOUT)::KWKGFL
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (IN)::PLSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTLSCAW%NDIM)
REAL (KIND=JPRB), INTENT (IN)::PRSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTRSCAW%NDIM)
INTEGER (KIND=JPIM), INTENT (IN)::KL0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, 0:3)
INTEGER (KIND=JPIM), INTENT (IN)::KNOWENO (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "laitre_gfl.intfb.h"
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LARCINB_LAITRE_GFL', 0, ZHOOK_HANDLE)
CALL LAITRE_GFL (YDGEOMETRY, YDML_GCONF%YGFL, YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS&
&%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN&
&, KHVI, KWKGFL, YDVARS%GFL_PTR (JFLD)%YCOMP%CSLINT, YDVARS%GFL_PTR (JFLD)%YCOMP%WENO_ALPHA, LDMOM&
&, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%GFL (JFLD)%P, YDCPG_GFLF%GFL (JFLD)%P)
IF (LHOOK) CALL DR_HOOK ('LARCINB_LAITRE_GFL', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
