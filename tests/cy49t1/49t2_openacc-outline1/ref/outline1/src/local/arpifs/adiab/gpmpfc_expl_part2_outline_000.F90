SUBROUTINE GPMPFC_EXPL_PART2_OUTLINE_000 (YDVARS, JFLD, YDCPG_OPTS, YDCPG_BNDS, PGM)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM), INTENT (INOUT)::JFLD
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (IN)::PGM (YDCPG_OPTS%KLON)
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPMPFC_EXPL_PART2_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDVARS%GFL_PTR (JFLD)%DM (JROF, JLEV)=YDVARS%GFL_PTR (JFLD)%DM (JROF, JLEV)*PGM (JROF)
    YDVARS%GFL_PTR (JFLD)%DL (JROF, JLEV)=YDVARS%GFL_PTR (JFLD)%DL (JROF, JLEV)*PGM (JROF)
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('GPMPFC_EXPL_PART2_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
