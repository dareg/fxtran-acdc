SUBROUTINE LATTEX_EXPL_VSPLTRANS_OUTLINE_000 (YDCPG_SL1, JGFL, YDML_DYN, YDGEOMETRY, YDCPG_BNDS)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1B_TYPE
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (CPG_SL1B_TYPE), INTENT (INOUT)::YDCPG_SL1
INTEGER (KIND=JPIM), INTENT (INOUT)::JGFL
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
#include "vspltrans.intfb.h"
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LATTEX_EXPL_VSPLTRANS_OUTLINE_000', 0, ZHOOK_HANDLE)

DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDCPG_SL1%GFL (JGFL)%P_SPF (JROF, JLEV)=YDCPG_SL1%GFL (JGFL)%P_F (JROF, JLEV)
  ENDDO

ENDDO

CALL VSPLTRANS (YDML_DYN%YRSLINT%YRVSPLIP, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS&
&%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA&
&, YDGEOMETRY%YRDIMV%NFLEN, YDCPG_SL1%GFL (JGFL)%P_SPF)
IF (LHOOK) CALL DR_HOOK ('LATTEX_EXPL_VSPLTRANS_OUTLINE_000', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
