SUBROUTINE LARCINB_LRHS_CURV_F (LDMOM, LDHOI2, LDSLPHY, LD_LIN, LD2TLFF1, LDNOSLHD, YDCPG_SL1&
&, KWKM, KWK, KASLB1, YDCPG_GMVF_SI, YDGEOMETRY, YDCPG_GMVF, YDCPG_DDH_TND, YDCPG_GMVF_NL,&
& YDLDDH, YDML_DYN, YDCPG_BNDS, PUPF, PLSCAW, PVPF, PUFZ, KNOWENO, PRSCAW, KL0, PVFZ)
USE CPG_DDH_TND_TYPE_MOD,ONLY:CPG_DDH_TND_TYPE
USE CPG_SL1_TYPE_MOD,ONLY:CPG_SL1F_TYPE
USE CPG_GMV_TYPE_MOD,ONLY:CPG_GMV_TYPE
USE GEOMETRY_MOD,ONLY:GEOMETRY
USE PARKIND1,ONLY:JPIM, JPRB
USE YOMLDDH,ONLY:TLDDH
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_OPTS_TYPE, CPG_BNDS_TYPE
USE MODEL_DYNAMICS_MOD,ONLY:MODEL_DYNAMICS_TYPE
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

LOGICAL, INTENT (INOUT)::LDMOM
LOGICAL, INTENT (INOUT)::LDHOI2
LOGICAL, INTENT (IN)::LDSLPHY
LOGICAL, INTENT (INOUT)::LD_LIN
LOGICAL, INTENT (IN)::LD2TLFF1
LOGICAL, INTENT (INOUT)::LDNOSLHD
TYPE (CPG_SL1F_TYPE), INTENT (IN)::YDCPG_SL1
INTEGER (KIND=JPIM), INTENT (INOUT)::KWKM
INTEGER (KIND=JPIM), INTENT (INOUT)::KWK
INTEGER (KIND=JPIM), INTENT (IN)::KASLB1
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMVF_SI
TYPE (GEOMETRY), INTENT (IN)::YDGEOMETRY
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMVF
TYPE (CPG_DDH_TND_TYPE), INTENT (INOUT)::YDCPG_DDH_TND
TYPE (CPG_GMV_TYPE), INTENT (INOUT)::YDCPG_GMVF_NL
TYPE (TLDDH), INTENT (IN)::YDLDDH
TYPE (MODEL_DYNAMICS_TYPE), INTENT (IN)::YDML_DYN
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
REAL (KIND=JPRB), INTENT (OUT)::PUPF (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PLSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTLSCAW%NDIM)
REAL (KIND=JPRB), INTENT (OUT)::PVPF (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (OUT)::PUFZ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
INTEGER (KIND=JPIM), INTENT (IN)::KNOWENO (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
REAL (KIND=JPRB), INTENT (IN)::PRSCAW (YDGEOMETRY%YRDIM%NPROMA&
&, YDGEOMETRY%YRDIMV%NFLEVG, YDML_DYN%YYTRSCAW%NDIM)
INTEGER (KIND=JPIM), INTENT (IN)::KL0 (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, 0:3)
REAL (KIND=JPRB), INTENT (OUT)::PVFZ (YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG)
#include "laitlif.intfb.h"
#include "laitre_gmv.intfb.h"
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('LARCINB_LRHS_CURV_F', 0, ZHOOK_HANDLE)
LDMOM=.TRUE.
CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
&%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN&
&, KWKM, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W, YDML_DYN%YRDYNA%LSLHD_W, YDML_DYN%YRDYN&
&%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN%LQMW, YDML_DYN%YRDYN&
&%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%U9%P, YDCPG_GMVF%U%P)

IF (YDLDDH%LRSLDDH) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDCPG_DDH_TND%YSL%U%P (JROF, JLEV)=YDCPG_DDH_TND%YSL%U%P (JROF, JLEV)+YDCPG_GMVF%U%P (JROF, JLEV)
    ENDDO

  ENDDO

ENDIF


IF ((YDML_DYN%YRDYN%NSPLTHOI/=0).AND.(YDML_DYN%YRDYN%NWLAG<4)) THEN

  IF (YDML_DYN%YRDYN%NSPLTHOI==1) THEN
    LDHOI2=LDNOSLHD
    KWK=YDML_DYN%YRDYN%NSLDIMK
  ELSE
    LDHOI2=YDML_DYN%YRDYNA%LSLHD_W
    KWK=KWKM
  ENDIF

  CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KWK, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W&
  &, LDHOI2, YDML_DYN%YRDYN%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN%LQMW, YDML_DYN&
  &%YRDYN%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%UF9%P, YDCPG_GMVF%U%P, LDADD=.TRUE.)
ENDIF


IF (YDML_DYN%YRDYN%NWLAG>=3) THEN

  IF (LD2TLFF1) THEN
    CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KWKM, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W&
    &, YDML_DYN%YRDYNA%LSLHD_W, YDML_DYN%YRDYN%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN&
    &%LQMW, YDML_DYN%YRDYN%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%UR9%P, PUFZ)
  ELSE
    PUFZ (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=YDCPG_GMVF&
    &%U%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
  ENDIF

  CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
  &%LCOMAD_W, PLSCAW, YDCPG_SL1%U0%P, YDCPG_GMVF%U%P, LDADD=.TRUE.)

  IF (YDLDDH%LRSIDDH) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA%LCOMAD_W&
    &, PLSCAW, YDCPG_SL1%U9_SI%P, YDCPG_DDH_TND%YSI%U%T0, LDADD=.TRUE.)
  ENDIF


  IF (YDML_DYN%YRDYNA%LSLINL.AND.YDML_DYN%YRDYN%NCURRENT_ITER==0) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
    &%LCOMAD_W, PLSCAW, YDCPG_SL1%U9_SI%P, YDCPG_GMVF_SI%U%P)
  ENDIF


  IF (YDML_DYN%YRDYNA%LSETTLS.AND.YDML_DYN%YRDYNA%LPC_CHEAP.AND.YDML_DYN%YRDYN%NCURRENT_ITER==0) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
    &%LCOMAD_W, PLSCAW, YDCPG_SL1%U9_NL%P, YDCPG_GMVF_NL%U%P)
  ENDIF

ENDIF


IF (LDSLPHY) THEN
  CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, YDCPG_BNDS&
  &%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA%LCOMAD_W, PLSCAW, YDCPG_SL1%UP9%P, PUPF)
ENDIF

LDMOM=.TRUE.
CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS&
&%KFDIA, YDGEOMETRY%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN&
&, KWKM, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W, YDML_DYN%YRDYNA%LSLHD_W, YDML_DYN%YRDYN&
&%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN%LQMW, YDML_DYN%YRDYN&
&%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%V9%P, YDCPG_GMVF%V%P)

IF (YDLDDH%LRSLDDH) THEN

  DO JLEV=1, YDGEOMETRY%YRDIMV%NFLEVG

    DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
      YDCPG_DDH_TND%YSL%V%P (JROF, JLEV)=YDCPG_DDH_TND%YSL%V%P (JROF, JLEV)+YDCPG_GMVF%V%P (JROF, JLEV)
    ENDDO

  ENDDO

ENDIF


IF ((YDML_DYN%YRDYN%NSPLTHOI/=0).AND.(YDML_DYN%YRDYN%NWLAG<4)) THEN

  IF (YDML_DYN%YRDYN%NSPLTHOI==1) THEN
    LDHOI2=LDNOSLHD
    KWK=YDML_DYN%YRDYN%NSLDIMK
  ELSE
    LDHOI2=YDML_DYN%YRDYNA%LSLHD_W
    KWK=KWKM
  ENDIF

  CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KWK, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W&
  &, LDHOI2, YDML_DYN%YRDYN%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN%LQMW, YDML_DYN&
  &%YRDYN%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%VF9%P, YDCPG_GMVF%V%P, LDADD=.TRUE.)
ENDIF


IF (YDML_DYN%YRDYN%NWLAG>=3) THEN

  IF (LD2TLFF1) THEN
    CALL LAITRE_GMV (YDML_DYN, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDGEOMETRY%YRDIMV%NFLSA, YDGEOMETRY%YRDIMV%NFLEN, KWKM, LD_LIN, YDML_DYN%YRDYNA%LCOMAD_W&
    &, YDML_DYN%YRDYNA%LSLHD_W, YDML_DYN%YRDYN%LVWENO_W, YDML_DYN%YRDYN%WENO_ALPHA_W, LDMOM, YDML_DYN%YRDYN&
    &%LQMW, YDML_DYN%YRDYN%LQMHW, KNOWENO, KL0, PLSCAW, PRSCAW, YDCPG_SL1%VR9%P, PVFZ)
  ELSE
    PVFZ (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)=YDCPG_GMVF&
    &%V%P (YDCPG_BNDS%KIDIA:YDCPG_BNDS%KFDIA, 1:YDGEOMETRY%YRDIMV%NFLEVG)
  ENDIF

  CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
  &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
  &%LCOMAD_W, PLSCAW, YDCPG_SL1%V0%P, YDCPG_GMVF%V%P, LDADD=.TRUE.)

  IF (YDLDDH%LRSIDDH) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA%LCOMAD_W&
    &, PLSCAW, YDCPG_SL1%V9_SI%P, YDCPG_DDH_TND%YSI%V%T0, LDADD=.TRUE.)
  ENDIF


  IF (YDML_DYN%YRDYNA%LSLINL.AND.YDML_DYN%YRDYN%NCURRENT_ITER==0) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
    &%LCOMAD_W, PLSCAW, YDCPG_SL1%V9_SI%P, YDCPG_GMVF_SI%V%P)
  ENDIF


  IF (YDML_DYN%YRDYNA%LSETTLS.AND.YDML_DYN%YRDYNA%LPC_CHEAP.AND.YDML_DYN%YRDYN%NCURRENT_ITER==0) THEN
    CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY&
    &%YRDIMV%NFLEVG, YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA&
    &%LCOMAD_W, PLSCAW, YDCPG_SL1%V9_NL%P, YDCPG_GMVF_NL%V%P)
  ENDIF

ENDIF


IF (LDSLPHY) THEN
  CALL LAITLIF (YDML_DYN%YYTLSCAW, KASLB1, YDGEOMETRY%YRDIM%NPROMA, YDGEOMETRY%YRDIMV%NFLEVG, YDCPG_BNDS&
  &%KIDIA, YDCPG_BNDS%KFDIA, KL0, YDML_DYN%YRDYNA%LCOMAD_W, PLSCAW, YDCPG_SL1%VP9%P, PVPF)
ENDIF

IF (LHOOK) CALL DR_HOOK ('LARCINB_LRHS_CURV_F', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
