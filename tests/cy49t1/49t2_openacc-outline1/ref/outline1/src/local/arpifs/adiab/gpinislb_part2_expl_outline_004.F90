SUBROUTINE GPINISLB_PART2_EXPL_OUTLINE_004 (YDVARS, JGFL, YDCPG_OPTS, YDCPG_BNDS)
USE PARKIND1,ONLY:JPIM, JPRB
USE CPG_OPTS_TYPE_MOD,ONLY:CPG_BNDS_TYPE, CPG_OPTS_TYPE
USE FIELD_VARIABLES_MOD,ONLY:FIELD_VARIABLES
USE YOMHOOK,ONLY:LHOOK, JPHOOK, DR_HOOK

IMPLICIT NONE

TYPE (FIELD_VARIABLES), INTENT (INOUT)::YDVARS
INTEGER (KIND=JPIM), INTENT (INOUT)::JGFL
TYPE (CPG_OPTS_TYPE), INTENT (IN)::YDCPG_OPTS
TYPE (CPG_BNDS_TYPE), INTENT (IN)::YDCPG_BNDS
INTEGER (KIND=JPIM)::JROF
INTEGER (KIND=JPIM)::JLEV
REAL (KIND=JPHOOK)::ZHOOK_HANDLE
IF (LHOOK) CALL DR_HOOK ('GPINISLB_PART2_EXPL_OUTLINE_004', 0, ZHOOK_HANDLE)

DO JLEV=1, YDCPG_OPTS%KFLEVG

  DO JROF=YDCPG_BNDS%KIDIA, YDCPG_BNDS%KFDIA
    YDVARS%GFL_PTR (JGFL)%T1 (JROF, JLEV)=YDVARS%GFL_PTR (JGFL)%T9 (JROF, JLEV)*YDCPG_OPTS%ZTE
  ENDDO

ENDDO

IF (LHOOK) CALL DR_HOOK ('GPINISLB_PART2_EXPL_OUTLINE_004', 1, ZHOOK_HANDLE)
ENDSUBROUTINE
