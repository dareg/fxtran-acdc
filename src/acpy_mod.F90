MODULE ACPY_MOD

IMPLICIT NONE

INTEGER, PARAMETER :: JPRM = 4, JPRD = 8, JPIM = 4

INTERFACE ACPY
  MODULE PROCEDURE ACPY_RD_1D
  MODULE PROCEDURE ACPY_RD_2D
  MODULE PROCEDURE ACPY_RM_1D
  MODULE PROCEDURE ACPY_RM_2D
END INTERFACE

PUBLIC :: ACPY

CONTAINS

SUBROUTINE ACPY_RD_1D (JLON, PL, PR)

!$acc routine (ACPY_RD_1D) seq

REAL (KIND=JPRD) :: PL (:,:), PR (:,:)
INTEGER (KIND=JPIM) :: JLON

PL (JLON, :) = PR (JLON, :)

END SUBROUTINE

SUBROUTINE ACPY_RD_2D (JLON, PL, PR)

!$acc routine (ACPY_RD_2D) seq

REAL (KIND=JPRD) :: PL (:,:,:), PR (:,:,:)
INTEGER (KIND=JPIM) :: JLON

PL (JLON, :, :) = PR (JLON, :, :)

END SUBROUTINE

SUBROUTINE ACPY_RM_1D (JLON, PL, PR)

!$acc routine (ACPY_RM_1D) seq

REAL (KIND=JPRM) :: PL (:,:), PR (:,:)
INTEGER (KIND=JPIM) :: JLON

PL (JLON, :) = PR (JLON, :)

END SUBROUTINE

SUBROUTINE ACPY_RM_2D (JLON, PL, PR)

!$acc routine (ACPY_RM_2D) seq

REAL (KIND=JPRM) :: PL (:,:,:), PR (:,:,:)
INTEGER (KIND=JPIM) :: JLON

PL (JLON, :, :) = PR (JLON, :, :)

END SUBROUTINE

END MODULE

